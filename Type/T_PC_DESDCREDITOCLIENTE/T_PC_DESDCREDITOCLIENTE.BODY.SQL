CREATE OR REPLACE TYPE BODY T_PC_DESDCREDITOCLIENTE AS

 CONSTRUCTOR FUNCTION T_PC_DESDCREDITOCLIENTE(PMATRICULA NUMBER, PCODROTINA NUMBER) RETURN SELF AS RESULT
 IS
 BEGIN
   SELF.CODFUNCLANC := PMATRICULA;
   SELF.DTLANC      := TRUNC(SYSDATE);
   SELF.CODROTINA   := PCODROTINA;
   RETURN;
 END;

 MEMBER PROCEDURE LIMPAR
 IS
 BEGIN
   SELF.CODIGOORIG  := NULL;
   SELF.CODIGODEST  := NULL;
   SELF.DTLANC      := TRUNC(SYSDATE);
 END LIMPAR;

 MEMBER PROCEDURE INSERIR
 IS
 BEGIN
   INSERT INTO PCDESDCRECLI
    (CODIGOORIG,
     CODIGODEST,
     DTLANC,
     CODFUNCLANC,
     CODROTINA)
   VALUES
    (SELF.CODIGOORIG,
     SELF.CODIGODEST,
     TRUNC(SYSDATE),
     SELF.CODFUNCLANC,
     SELF.CODROTINA);
 END INSERIR;

END;