CREATE OR REPLACE TYPE BODY T_PC_PARAMETROS AS

  MEMBER FUNCTION getVALORNUM (PNOMEPARAM VARCHAR2, PFILIAL VARCHAR2)
  RETURN NUMBER
  IS
  vretorno  NUMBER;
  BEGIN

    SELECT TO_NUMBER(VALOR)
      INTO vretorno
      FROM PCPARAMFILIAL
     WHERE NOME LIKE PNOMEPARAM
       AND CODFILIAL = NVL(PFILIAL,'99');

    RETURN vretorno;
  
  EXCEPTION
   WHEN NO_DATA_FOUND THEN
     RETURN NULL; 
  END;
  
  MEMBER FUNCTION getVALORNUMNVL (PNOMEPARAM VARCHAR2, PFILIAL VARCHAR2, PVALORSENULO NUMBER)
  RETURN NUMBER
  IS
  vnVALORTEMP  NUMBER;
  BEGIN

    SELECT NVL(TO_NUMBER(VALOR), PVALORSENULO)
      INTO vnVALORTEMP
      FROM PCPARAMFILIAL
     WHERE NOME = PNOMEPARAM
       AND CODFILIAL = PFILIAL;

    RETURN vnVALORTEMP;

  EXCEPTION
    WHEN INVALID_NUMBER THEN

    BEGIN
      SELECT NVL(TO_NUMBER(translate(VALOR,',.','.,')), PVALORSENULO)
        INTO vnVALORTEMP
        FROM PCPARAMFILIAL
       WHERE NOME = PNOMEPARAM
         AND CODFILIAL = PFILIAL;

      RETURN vnVALORTEMP;

    EXCEPTION
      WHEN INVALID_NUMBER THEN
        RAISE_APPLICATION_ERROR(-20000, 'NÚMERO INVÁLIDO ATRIBUÍDO AO PARÂMETRO: "' || PNOMEPARAM || '". VERIFIQUE O SEPARADOR DECIMAL');
    WHEN OTHERS THEN
      RETURN PVALORSENULO;
    END;

    WHEN OTHERS THEN

      RETURN PVALORSENULO;
  END;

  MEMBER FUNCTION getVALORALFA (PNOMEPARAM VARCHAR2, PFILIAL VARCHAR2)
  RETURN VARCHAR2
  IS
  vretorno  VARCHAR2(100);
  BEGIN

    SELECT VALOR
      INTO vretorno
      FROM PCPARAMFILIAL
     WHERE NOME LIKE PNOMEPARAM
       AND CODFILIAL = NVL(PFILIAL,'99');

    RETURN vretorno;
    
  EXCEPTION
   WHEN NO_DATA_FOUND THEN
     RETURN NULL;
  END;
  
  MEMBER FUNCTION getVALORALFANVL (PNOMEPARAM VARCHAR2, PFILIAL VARCHAR2, PVALORSENULO VARCHAR2)
  RETURN VARCHAR2
  IS
  vretorno  VARCHAR2(100);
  BEGIN

    SELECT NVL(VALOR,PVALORSENULO)
      INTO vretorno
      FROM PCPARAMFILIAL
     WHERE NOME LIKE PNOMEPARAM
       AND CODFILIAL = NVL(PFILIAL,'99');

    RETURN vretorno;
  
  EXCEPTION
   WHEN NO_DATA_FOUND THEN
     RETURN NULL;
  END;

  MEMBER FUNCTION getVALORDT (PNOMEPARAM VARCHAR2, PFILIAL VARCHAR2)
  RETURN DATE
  IS
  vretorno  DATE;
  BEGIN

    SELECT VALOR
      INTO vretorno
      FROM PCPARAMFILIAL
     WHERE NOME LIKE PNOMEPARAM
       AND CODFILIAL = NVL(PFILIAL,'99');

    RETURN TO_DATE(vretorno,'DD/MM/RRRR');
  
  EXCEPTION
   WHEN NO_DATA_FOUND THEN
     RETURN NULL;
  END;
  
  MEMBER FUNCTION getVALORDTNVL (PNOMEPARAM VARCHAR2, PFILIAL VARCHAR2, PVALORSENULO DATE)
  RETURN DATE
  IS
  vretorno  DATE;
  BEGIN

    SELECT NVL(VALOR,PVALORSENULO)
      INTO vretorno
      FROM PCPARAMFILIAL
     WHERE NOME LIKE PNOMEPARAM
       AND CODFILIAL = NVL(PFILIAL,'99');

    RETURN TO_DATE(vretorno,'DD/MM/RRRR');
  
  EXCEPTION
   WHEN NO_DATA_FOUND THEN
     RETURN NULL;
  END;

END;
