CREATE OR REPLACE TYPE BODY T_PC_PARAMETROS AS

  MEMBER FUNCTION getVALORNUM (PNOMEPARAM VARCHAR2, PFILIAL VARCHAR2)
  RETURN NUMBER
  IS
  vretorno  NUMBER;
  BEGIN

    SELECT TO_NUMBER(VALOR)
      INTO vretorno
      FROM PCPARAMFILIAL
     WHERE NOME LIKE PNOMEPARAM
       AND CODFILIAL = NVL(PFILIAL,'99');

    RETURN vretorno;
  
  EXCEPTION
   WHEN NO_DATA_FOUND THEN
     RETURN NULL; 
  END;
  
