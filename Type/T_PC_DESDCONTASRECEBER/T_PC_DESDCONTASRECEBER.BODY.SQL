CREATE OR REPLACE TYPE BODY T_PC_DESDCONTASRECEBER AS

 CONSTRUCTOR FUNCTION T_PC_DESDCONTASRECEBER(PMATRICULA NUMBER, PCODROTINA NUMBER) RETURN SELF AS RESULT
 IS
 BEGIN
   SELF.CODFUNCCXMOT := PMATRICULA;
   SELF.DTLANC       := TRUNC(SYSDATE);
   SELF.CODROTINA    := PCODROTINA;
   RETURN;
 END;

 MEMBER PROCEDURE LIMPAR
 IS
 BEGIN
   SELF.NUMTRANSVENDADEST := NULL;
   SELF.PRESTDEST         := NULL;
   SELF.CODCLIDEST        := NULL;
   SELF.NUMTRANSVENDAORIG := NULL;
   SELF.PRESTORIG         := NULL;
   SELF.CODCLIORIG        := NULL;
 END LIMPAR;

 MEMBER PROCEDURE INSERIR
 IS
 BEGIN
   INSERT INTO PCDESD
    (NUMTRANSVENDADEST,
     PRESTDEST,
     CODCLIDEST,
     NUMTRANSVENDAORIG,
     PRESTORIG,
     CODCLIORIG,
     DTLANC,
     CODFUNCCXMOT,
     CODROTINA)
   VALUES
    (SELF.NUMTRANSVENDADEST,
     SELF.PRESTDEST,
     SELF.CODCLIDEST,
     SELF.NUMTRANSVENDAORIG,
     SELF.PRESTORIG,
     SELF.CODCLIORIG,
     TRUNC(SYSDATE),
     SELF.CODFUNCCXMOT,
     SELF.CODROTINA);
 END INSERIR;

 END;