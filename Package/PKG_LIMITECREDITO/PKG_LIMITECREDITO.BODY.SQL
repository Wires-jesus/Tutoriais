CREATE OR REPLACE PACKAGE BODY PKG_LIMITECREDITO IS

  FUNCTION RETORNA_CLIENTE_PRINCIPAL(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    CLIENTE_PRINCIPAL NUMBER;
  BEGIN
    CLIENTE_PRINCIPAL := 0;

    SELECT NVL(PCCLIENT.CODCLIPRINC, 0)
      INTO CLIENTE_PRINCIPAL
      FROM PCCLIENT
     WHERE PCCLIENT.CODCLI = CODIGOCLIENTE;

    IF CLIENTE_PRINCIPAL > 0 THEN
      RETURN CLIENTE_PRINCIPAL;
    ELSE
      RETURN CODIGOCLIENTE;
    END IF;
  END;

  FUNCTION RETORNA_LIMITE_CREDITO_CLIENTE(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    LIMITE_CREDITO    NUMBER;
    CLIENTE_PRINCIPAL NUMBER;
  BEGIN
    LIMITE_CREDITO := 0;

    IF PARAMETROS.getVALORALFANVL('SOMACREDITOCLIPRINCIPAL', '99', 'N') = 'S' THEN
      CLIENTE_PRINCIPAL := RETORNA_CLIENTE_PRINCIPAL(CODIGOCLIENTE);

      SELECT SUM(LIMCRED)
        INTO LIMITE_CREDITO
        FROM PCCLIENT
       WHERE PCCLIENT.CODCLI IN
             (SELECT CODCLI
                FROM PCCLIENT
               WHERE CODCLIPRINC = CLIENTE_PRINCIPAL
              UNION ALL
              SELECT CODCLI
                FROM PCCLIENT
               WHERE CODCLI = CLIENTE_PRINCIPAL
                 AND CODCLIPRINC IS NULL);
    ELSE
      SELECT SUM(LIMCRED)
        INTO LIMITE_CREDITO
        FROM PCCLIENT
       WHERE PCCLIENT.CODCLI = CODIGOCLIENTE;
    END IF;

    RETURN LIMITE_CREDITO;
  END;

  FUNCTION CONSUMIDOR_FINAL(CODIGOCLIENTE NUMBER) RETURN BOOLEAN IS
    CONSUMIDOR_FINAL  BOOLEAN;
    CLIENTE_DESCRICAO VARCHAR2(60);
  BEGIN
    BEGIN
      SELECT TRIM(UPPER(CLIENTE)) CLIENTE
        INTO CLIENTE_DESCRICAO
        FROM PCCLIENT
       WHERE CODCLI = CODIGOCLIENTE
         AND CLIENTE LIKE TRIM(UPPER('%CONSUMIDOR%'));
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        CLIENTE_DESCRICAO := 'CLIENTE NORMAL';
    END;

    CONSUMIDOR_FINAL := CODIGOCLIENTE = 1 OR
                        CLIENTE_DESCRICAO LIKE TRIM(UPPER('%CONSUMIDOR%'));

    RETURN CONSUMIDOR_FINAL;
  END;

  FUNCTION RETORNA_LIMITE_CREDITO_EXTRA(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    TOTAL_LIMITE_EXTRA      NUMBER;
    PERCENTUAL_LIMITE_EXTRA NUMBER;
    LIMITE_CREDITO_CLIENTE  NUMBER;
  BEGIN
    TOTAL_LIMITE_EXTRA := 0;

    LIMITE_CREDITO_CLIENTE := RETORNA_LIMITE_CREDITO_CLIENTE(CODIGOCLIENTE);

    PERCENTUAL_LIMITE_EXTRA := PARAMETROS.getVALORNUMNVL('CON_PEREXCEDELIMCRED',
                                                         '99',
                                                         0);

    TOTAL_LIMITE_EXTRA := LIMITE_CREDITO_CLIENTE *
                          (PERCENTUAL_LIMITE_EXTRA / 100);

    RETURN TOTAL_LIMITE_EXTRA;
  END;

  FUNCTION RETORNA_CONTAS_RECEBER_ABERTO(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    TOTAL_CONTAS_RECEBER NUMBER;
    V_SQL                VARCHAR2(4000);
    CLIENTE_PRINCIPAL    NUMBER;
  BEGIN
    TOTAL_CONTAS_RECEBER := 0;

    IF NOT CONSUMIDOR_FINAL(CODIGOCLIENTE) THEN
      V_SQL := ' SELECT NVL(SUM(NVL(VALOR, 0)), 0) VALOR
                      FROM PCPREST, PCCOB
                     WHERE PCPREST.CODCOB = PCCOB.CODCOB
                       AND NVL(PCCOB.VALIDALIMITECREDITO, ''S'') = ''S''
                       AND DTPAG IS NULL
                       AND PCPREST.CODCOB NOT IN
                           (SELECT CODCOB FROM PCCOB WHERE COBSUPPLIERCARD = ''S'')
                       AND PCPREST.CODCOB <> ''SUPP'' ';

      IF PARAMETROS.getVALORALFANVL('USABNFLIMITECREDITO', '99', 'S') = 'N' THEN
        V_SQL := V_SQL ||
                 'AND PCPREST.CODCOB NOT IN (''BNF'',''BNFT'',''BNTR'',''BNFR'', ''BNRP'')';
      END IF;

      IF PARAMETROS.getVALORALFANVL('SOMACREDITOCLIPRINCIPAL', '99', 'N') = 'S' THEN
        CLIENTE_PRINCIPAL := RETORNA_CLIENTE_PRINCIPAL(CODIGOCLIENTE);

        V_SQL := V_SQL || 'AND PCPREST.CODCLI IN
                     (SELECT CODCLI
                        FROM PCCLIENT
                       WHERE CODCLIPRINC = ' ||
                 CLIENTE_PRINCIPAL ||
                 ' UNION ALL
                        SELECT CODCLI
                          FROM PCCLIENT
                         WHERE CODCLI = ' ||
                 CLIENTE_PRINCIPAL || ' AND CODCLIPRINC IS NULL)';
      ELSE
        V_SQL := V_SQL || 'AND CODCLI = ' || CODIGOCLIENTE;
      END IF;

      IF PARAMETROS.getVALORALFANVL('UTILIZALIMITECREDITOCPF', '99', 'N') = 'S' THEN
        V_SQL := V_SQL || 'AND NVL(CHEQUETERCEIRO, ''N'') = ''N'' ';
      END IF;

      V_SQL := V_SQL || ' UNION ALL
                    SELECT NVL(SUM(NVL(VALOR, 0)), 0) VALOR
                      FROM PCPRESTPREFAT, PCCOB
                     WHERE PCPRESTPREFAT.CODCOB = PCCOB.CODCOB
                       AND NVL(PCCOB.VALIDALIMITECREDITO, ''S'') = ''S''
                       AND DTPAG IS NULL
                       AND PCPRESTPREFAT.CODCOB NOT IN
                           (SELECT CODCOB FROM PCCOB WHERE COBSUPPLIERCARD = ''S'')
                       AND PCPRESTPREFAT.CODCOB <> ''SUPP'' ';

      IF PARAMETROS.getVALORALFANVL('USABNFLIMITECREDITO', '99', 'S') = 'N' THEN
        V_SQL := V_SQL ||
                 'AND PCPRESTPREFAT.CODCOB NOT IN (''BNF'',''BNFT'',''BNTR'',''BNFR'', ''BNRP'')';
      END IF;

      IF PARAMETROS.getVALORALFANVL('SOMACREDITOCLIPRINCIPAL', '99', 'N') = 'S' THEN
        CLIENTE_PRINCIPAL := RETORNA_CLIENTE_PRINCIPAL(CODIGOCLIENTE);

        V_SQL := V_SQL || 'AND PCPRESTPREFAT.CODCLI IN
                     (SELECT CODCLI
                        FROM PCCLIENT
                       WHERE CODCLIPRINC = ' ||
                 CLIENTE_PRINCIPAL ||
                 ' UNION ALL
                        SELECT CODCLI
                          FROM PCCLIENT
                         WHERE CODCLI = ' ||
                 CLIENTE_PRINCIPAL || ' AND CODCLIPRINC IS NULL)';
      ELSE
        V_SQL := V_SQL || 'AND CODCLI = ' || CODIGOCLIENTE;
      END IF;

      IF PARAMETROS.getVALORALFANVL('UTILIZALIMITECREDITOCPF', '99', 'N') = 'S' THEN
        V_SQL := V_SQL || 'AND NVL(CHEQUETERCEIRO, ''N'') = ''N'' ';
      END IF;

      V_SQL := 'SELECT SUM(NVL(VALOR, 0)) FROM (' || V_SQL || ')';
      BEGIN
        EXECUTE IMMEDIATE V_SQL
          INTO TOTAL_CONTAS_RECEBER;
      EXCEPTION
        WHEN NO_DATA_FOUND THEN
          TOTAL_CONTAS_RECEBER := 0;
      END;

    END IF;

    RETURN TOTAL_CONTAS_RECEBER;
  END;

  FUNCTION RETORNA_PEDIDOS_FATURAR(CODIGOCLIENTE     NUMBER,
                                   NUMEROPEDIDOATUAL NUMBER DEFAULT 0)
    RETURN NUMBER IS
    TOTAL_PEDIDOS_FATURAR NUMBER;
    V_SQL                 VARCHAR2(4000);
    CLIENTE_PRINCIPAL     NUMBER;
  BEGIN
    TOTAL_PEDIDOS_FATURAR := 0;

    IF NOT CONSUMIDOR_FINAL(CODIGOCLIENTE) THEN

      V_SQL := 'SELECT NVL(SUM(ROUND(NVL (VLATEND, 0)';

      IF PARAMETROS.getVALORALFANVL('USABNFLIMITECREDITO', 99, 'S') = 'S' THEN
        V_SQL := V_SQL || ' + NVL(PCPEDC.VLBONIFIC, 0)';
      END IF;

      V_SQL := V_SQL ||
               ' - CASE WHEN ((PCPLPAG.TIPOENTRADA = 3) AND (PCPLPAG.DESCENTLIMCREDCLI = ''S'')) THEN PCPEDC.VLENTRADA ELSE 0 END ,2)), 0)  VLATEND
                          FROM PCPEDC,PCPLPAG, PCCOB
                         WHERE PCPEDC.CODCOB = PCCOB.CODCOB
                           AND NVL(PCCOB.VALIDALIMITECREDITO, ''S'') = ''S''
                           AND DTCANCEL IS NULL
                           AND PCPEDC.CONDVENDA NOT IN (8, 13)
                           AND PCPLPAG.CODPLPAG = PCPEDC.CODPLPAG';

      IF PARAMETROS.getVALORALFANVL('CON_ACEITAVENDABLOQ', '99', 'S') = 'N' THEN
        V_SQL := V_SQL || ' AND POSICAO IN (''L'', ''M'', ''B'') ';
      ELSE
        V_SQL := V_SQL || ' AND POSICAO IN (''L'', ''M'') ';
      END IF;

      V_SQL := V_SQL ||
               ' AND PCPEDC.CODCOB NOT IN (SELECT CODCOB
                                                      FROM PCCOB where COBSUPPLIERCARD = ''S'')
                         AND PCPEDC.CODCOB <> ''SUPP'' ';

      IF PARAMETROS.getVALORALFANVL('CON_VERIFICALIMCREDCODCOBD', '99', 'N') = 'N' THEN
        V_SQL := V_SQL ||
                 ' AND PCPEDC.CODCOB NOT IN (''D'', ''DH'', ''CAR'')
                            AND PCPEDC.CODCOB NOT IN (SELECT CODCOB
                                                         FROM PCCOB where CARTAO = ''S'') ';

      END IF;

      IF PARAMETROS.getVALORALFANVL('SOMACREDITOCLIPRINCIPAL', '99', 'N') = 'S' THEN
        CLIENTE_PRINCIPAL := RETORNA_CLIENTE_PRINCIPAL(CODIGOCLIENTE);

        V_SQL := V_SQL || 'AND PCPEDC.CODCLI IN
                     (SELECT CODCLI
                        FROM PCCLIENT
                       WHERE CODCLIPRINC = ' ||
                 CLIENTE_PRINCIPAL ||
                 ' UNION ALL
                        SELECT CODCLI
                          FROM PCCLIENT
                         WHERE CODCLI = ' ||
                 CLIENTE_PRINCIPAL || ' AND CODCLIPRINC IS NULL)';
      ELSE
        V_SQL := V_SQL || 'AND CODCLI = ' || CODIGOCLIENTE;
      END IF;

      IF NUMEROPEDIDOATUAL > 0 THEN
        V_SQL := V_SQL || ' AND NUMPED <> ' || NUMEROPEDIDOATUAL;
      END IF;

      BEGIN
        EXECUTE IMMEDIATE V_SQL
          INTO TOTAL_PEDIDOS_FATURAR;
      EXCEPTION
        WHEN NO_DATA_FOUND THEN
          TOTAL_PEDIDOS_FATURAR := 0;
      END;

    END IF;

    RETURN TOTAL_PEDIDOS_FATURAR;

  END;

  FUNCTION RETORNA_TOTAL_CHEQUE(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    TOTAL_CHEQUE      NUMBER;
    V_SQL             VARCHAR2(4000);
    CLIENTE_PRINCIPAL NUMBER;
  BEGIN
    TOTAL_CHEQUE := 0;

    IF NOT CONSUMIDOR_FINAL(CODIGOCLIENTE) THEN
      V_SQL := ' WITH DADOS AS
                 (SELECT VALOR, ';

      IF PARAMETROS.getVALORALFANVL('CON_CHECARDIASUTEISTITPGNLIB', '99', 'N') = 'S' THEN
        V_SQL := V_SQL ||
                 ' FNC_DTFINAL(PCPREST.DTPAG,
                                        (SELECT NVL(PCCOB.NUMDIASLIBERACAOCREDITO, 0)
                                           FROM PCCOB
                                          WHERE PCCOB.CODCOB = PCPREST.CODCOB),
                                        ''S'') DATA_LIBERACAO_CREDITO  ';
      ELSE
        V_SQL := V_SQL ||
                 ' PCPREST.DTPAG +
                            (SELECT NVL(PCCOB.NUMDIASLIBERACAOCREDITO, 0)
                               FROM PCCOB
                              WHERE PCCOB.CODCOB = PCPREST.CODCOB) DATA_LIBERACAO_CREDITO ';

      END IF;

      V_SQL := V_SQL ||
               '   FROM PCPREST, PCCOB
                           WHERE PCPREST.CODCOB = PCCOB.CODCOB
                             AND NVL(PCCOB.VALIDALIMITECREDITO, ''S'') = ''S''
                             AND PCPREST.CODCOB <> ''DESD''
                             AND PCPREST.CODCOB <> ''SUPP''
                             AND PCPREST.DTPAG IS NOT NULL
                             AND NVL(CHEQUETERCEIRO, ''N'') = ''N''
                             AND PCPREST.DTVENC >= (SELECT MIN(DTVENC) FROM PCPREST)
                             AND PCPREST.DTCANCEL IS NULL
                             AND PCPREST.CODCOB NOT IN (SELECT CODCOB
                                                          FROM PCCOB where COBSUPPLIERCARD = ''S'') ';

      IF PARAMETROS.getVALORALFANVL('SOMACREDITOCLIPRINCIPAL', '99', 'N') = 'S' THEN
        CLIENTE_PRINCIPAL := RETORNA_CLIENTE_PRINCIPAL(CODIGOCLIENTE);

        V_SQL := V_SQL || 'AND PCPREST.CODCLI IN
                     (SELECT CODCLI
                        FROM PCCLIENT
                       WHERE CODCLIPRINC = ' ||
                 CLIENTE_PRINCIPAL ||
                 ' UNION ALL
                        SELECT CODCLI
                          FROM PCCLIENT
                         WHERE CODCLI = ' ||
                 CLIENTE_PRINCIPAL || ' AND CODCLIPRINC IS NULL)';
      ELSE
        V_SQL := V_SQL || 'AND CODCLI = ' || CODIGOCLIENTE;
      END IF;

      V_SQL := V_SQL ||
               '  )
                  SELECT NVL(SUM(VALOR), 0) VLCHEQUE
                            FROM DADOS
                           WHERE DADOS.DATA_LIBERACAO_CREDITO > TRUNC(SYSDATE) ';
      BEGIN
        EXECUTE IMMEDIATE V_SQL
          INTO TOTAL_CHEQUE;
      EXCEPTION
        WHEN NO_DATA_FOUND THEN
          TOTAL_CHEQUE := 0;
      END;
    END IF;

    RETURN TOTAL_CHEQUE;
  END;

  FUNCTION RETORNA_DIFERENCA_TIT_TERCEIRO(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    DIFERENCA_TITULO_TERCEIRO NUMBER;
    V_SQL                     VARCHAR2(4000);
    LIMITE_CREDITO_CPF        NUMBER;
    DIFERENCA                 NUMBER;
  BEGIN
    DIFERENCA_TITULO_TERCEIRO := 0;
    LIMITE_CREDITO_CPF        := 0;

    SELECT LIMCREDCPF
      INTO LIMITE_CREDITO_CPF
      FROM PCCLIENT
     WHERE CODCLI = CODIGOCLIENTE;

    FOR DADOS IN (SELECT CGCCPFCH
                    FROM PCPREST
                   WHERE CODCLI = CODIGOCLIENTE
                     AND DTPAG IS NULL
                     AND CODCOB <> 'SUPP'
                     AND NVL(CHEQUETERCEIRO, 'N') = 'S'
                   GROUP BY CGCCPFCH) LOOP

      SELECT GREATEST((SUM(NVL(VALOR, 0)) - LIMITE_CREDITO_CPF), 0) DIFERENCA
        INTO DIFERENCA
        FROM PCPREST
       WHERE CGCCPFCH = DADOS.CGCCPFCH
         AND DTPAG IS NULL
         AND CODCOB <> 'SUPP';

      DIFERENCA_TITULO_TERCEIRO := DIFERENCA_TITULO_TERCEIRO + DIFERENCA;

    END LOOP;

    RETURN DIFERENCA_TITULO_TERCEIRO;
  END;

  FUNCTION RETORNA_LIM_CREDITO_PARCEIRO(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    TOTAL_LIMITE_PARCEIRO NUMBER;

  BEGIN
    TOTAL_LIMITE_PARCEIRO := 0;

    BEGIN
      SELECT NVL(LIMITECREDDISPONSUPPLI, 0) LIMITECREDSUPPLI
        INTO TOTAL_LIMITE_PARCEIRO
        FROM PCSUPPLICLIENTE
       WHERE CODCLI = CODIGOCLIENTE;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        TOTAL_LIMITE_PARCEIRO := 0;
    END;

    RETURN TOTAL_LIMITE_PARCEIRO;
  END;

  FUNCTION RETORNA_CONTASRECEBER_PARCEIRO(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    TOTAL_CONTASRECEBER_PARCEIRO NUMBER;
    V_SQL                        VARCHAR2(4000);
    CLIENTE_PRINCIPAL            NUMBER;
  BEGIN
    TOTAL_CONTASRECEBER_PARCEIRO := 0;

    IF NOT CONSUMIDOR_FINAL(CODIGOCLIENTE) THEN
      V_SQL := ' SELECT NVL(SUM(NVL(VALOR,0)), 0) VALOR
                 FROM PCPREST, PCCOB
                WHERE DTPAG IS NULL
                AND PCPREST.CODCOB = PCCOB.CODCOB
                AND NVL(PCCOB.VALIDALIMITECREDITO, ''S'') = ''S''
                AND NVL(PCCOB.COBSUPPLIERCARD, ''N'') = ''S''
                  AND PCPREST.CODCOB <> ''SUPP'' ';

      IF PARAMETROS.getVALORALFANVL('SOMACREDITOCLIPRINCIPAL', '99', 'N') = 'S' THEN
        CLIENTE_PRINCIPAL := RETORNA_CLIENTE_PRINCIPAL(CODIGOCLIENTE);

        V_SQL := V_SQL || 'AND PCPREST.CODCLI IN
                     (SELECT CODCLI
                        FROM PCCLIENT
                       WHERE CODCLIPRINC = ' ||
                 CLIENTE_PRINCIPAL ||
                 ' UNION ALL
                        SELECT CODCLI
                          FROM PCCLIENT
                         WHERE CODCLI = ' ||
                 CLIENTE_PRINCIPAL || ' AND CODCLIPRINC IS NULL)';
      ELSE
        V_SQL := V_SQL || 'AND CODCLI = ' || CODIGOCLIENTE;
      END IF;

      EXECUTE IMMEDIATE V_SQL
        INTO TOTAL_CONTASRECEBER_PARCEIRO;
    END IF;

    RETURN TOTAL_CONTASRECEBER_PARCEIRO;
  END;

  FUNCTION RETORNA_PEDIDOS_COB_PARCEIRO(CODIGOCLIENTE NUMBER) RETURN NUMBER IS
    TOTAL_PEDIDOS_COB_PARCEIRO NUMBER;
  BEGIN
    TOTAL_PEDIDOS_COB_PARCEIRO := 0;

    IF NOT CONSUMIDOR_FINAL(CODIGOCLIENTE) THEN

      BEGIN
        SELECT SUM(ROUND(NVL(VLATEND, 0) - CASE
                           WHEN ((PCPLPAG.TIPOENTRADA = 3) AND
                                (PCPLPAG.DESCENTLIMCREDCLI = 'S')) THEN
                            PCPEDC.VLENTRADA
                           ELSE
                            0
                         END,
                         2)) VLATEND
          INTO TOTAL_PEDIDOS_COB_PARCEIRO
          FROM PCPEDC, PCPLPAG, PCCOB
         WHERE DTCANCEL IS NULL
           AND PCPLPAG.CODPLPAG = PCPEDC.CODPLPAG
           AND PCPEDC.CODCOB = PCCOB.CODCOB
           AND NVL(PCCOB.COBSUPPLIERCARD, 'N') = 'S'
           AND NVL(PCCOB.VALIDALIMITECREDITO, 'S') = 'S'
           AND POSICAO IN ('L', 'M')
           AND PCPEDC.CODCOB <> 'SUPP'
           AND CODCLI = CODIGOCLIENTE;
      EXCEPTION
        WHEN NO_DATA_FOUND THEN
          TOTAL_PEDIDOS_COB_PARCEIRO := 0;
      END;

    END IF;

    RETURN TOTAL_PEDIDOS_COB_PARCEIRO;
  END;

  FUNCTION RETORNA_LIMITE_CRED_DISPONIVEL(CODIGOCLIENTE          NUMBER,
                                          UTILIZALIMITEEXTRA     BOOLEAN DEFAULT TRUE,
                                          UTILIZALIMITEPARCEIROS BOOLEAN DEFAULT FALSE,
                                          NUMEROPEDIDOATUAL      NUMBER DEFAULT 0)
    RETURN NUMBER IS
    LIMITE_CREDITO NUMBER;
  BEGIN
    LIMITE_CREDITO := 0;

    IF NOT UTILIZALIMITEPARCEIROS THEN
      LIMITE_CREDITO := RETORNA_LIMITE_CREDITO_CLIENTE(CODIGOCLIENTE);

      IF UTILIZALIMITEEXTRA THEN
        LIMITE_CREDITO := LIMITE_CREDITO +
                          RETORNA_LIMITE_CREDITO_EXTRA(CODIGOCLIENTE);
      END IF;

      LIMITE_CREDITO := LIMITE_CREDITO -
                        RETORNA_CONTAS_RECEBER_ABERTO(CODIGOCLIENTE);

      LIMITE_CREDITO := LIMITE_CREDITO -
                        RETORNA_PEDIDOS_FATURAR(CODIGOCLIENTE,
                                                NUMEROPEDIDOATUAL);

      LIMITE_CREDITO := LIMITE_CREDITO -
                        RETORNA_TOTAL_CHEQUE(CODIGOCLIENTE);

      IF PARAMETROS.getVALORALFANVL('CON_USALIMCREDCPF', '99', 'N') = 'S' THEN
        LIMITE_CREDITO := LIMITE_CREDITO -
                          RETORNA_DIFERENCA_TIT_TERCEIRO(CODIGOCLIENTE);
      END IF;

    ELSE
      LIMITE_CREDITO := RETORNA_LIM_CREDITO_PARCEIRO(CODIGOCLIENTE);

      LIMITE_CREDITO := LIMITE_CREDITO -
                        RETORNA_CONTASRECEBER_PARCEIRO(CODIGOCLIENTE);

      LIMITE_CREDITO := LIMITE_CREDITO -
                        RETORNA_PEDIDOS_COB_PARCEIRO(CODIGOCLIENTE);
    END IF;

    RETURN LIMITE_CREDITO;
  END;

  FUNCTION RETORNA_LIMITE_CREDITO_SAZONAL(CODIGOCLIENTE NUMBER,
                                          CODIGORCA     NUMBER) RETURN NUMBER IS
    LIMITE_CREDITO_SAZONAL NUMBER;
    PERCENTUAL_AUMENTO     NUMBER;
  BEGIN
    LIMITE_CREDITO_SAZONAL := 0;
    PERCENTUAL_AUMENTO     := 0;

    BEGIN
      SELECT NVL(PERCAUMENTO, 0)
        INTO PERCENTUAL_AUMENTO
        FROM PCLIMCRED
       WHERE PCLIMCRED.CODDCLI = CODIGOCLIENTE
         AND TRUNC(SYSDATE) BETWEEN PCLIMCRED.DTINICIO AND PCLIMCRED.DTFIM
         AND ((PCLIMCRED.CODUSUR IS NULL) OR
             (PCLIMCRED.CODUSUR = CODIGORCA));
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        PERCENTUAL_AUMENTO := 0;
    END;

    IF PERCENTUAL_AUMENTO > 0 THEN
      LIMITE_CREDITO_SAZONAL := RETORNA_LIMITE_CREDITO_CLIENTE(CODIGOCLIENTE) *
                                (PERCENTUAL_AUMENTO / 100);
    END IF;

    RETURN LIMITE_CREDITO_SAZONAL;
  END;

END;

