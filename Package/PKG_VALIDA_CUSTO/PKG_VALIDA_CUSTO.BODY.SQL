       RET_CUSTO_VENDA.CUSTOFISCAL_VENDA := RET_CUSTO_NOTATRANSF.CUSTOFISCAL_DEV;
      END IF;  

   END IF;
 END IF;

 IF VSCODFILIAL <> vcCODFILIALRETIRA AND (NVL(RET_CUSTO_VENDA.CUSTOREAL_VENDA,0)      = 0 OR
                                         NVL(RET_CUSTO_VENDA.CUSTOREP_VENDA,0)       = 0 OR
