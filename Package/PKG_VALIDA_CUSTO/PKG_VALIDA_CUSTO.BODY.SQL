  /************************************************************************************************************************
  ------------------------------------------------------ Alterações -------------------------------------------------------
  Data           Autor             Solicitação         Descrição
  -------------------------------------------------------------------------------------------------------------------------
                                   0                   FUNÇÃO PARA BUSCAR OS CUSTOS DA NOTA DE VENDA
  ************************************************************************************************************************/
  FUNCTION BUSCA_CUSTO_VENDA(P_EXECUCAO IN TP_EXECUCAO)
   RETURN TP_RETORNO_CUSTO_VENDA IS

    RET_CUSTO_VENDA              TP_RETORNO_CUSTO_VENDA;
    vsCODFILIAL                  PCMOV.CODFILIAL%TYPE;
    vsCODFILIALNF                PCMOV.CODFILIALNF%TYPE;
  vcCODFILIALRETIRA            PCMOV.CODFILIALRETIRA%TYPE;                            
    vnTRANSACAO_TRANSF           PCMOV.NUMTRANSVENDA%TYPE;
    RET_CUSTO_NOTATRANSF         PKG_VALIDA_CUSTO.TP_RETORNO_CUSTO_DEV;
  RET_CUSTO_NOTATRANSF_RETIRA  PKG_VALIDA_CUSTO.TP_RETORNO_CUSTO_DEV;                                  
    ENT_TRANSF                   PKG_VALIDA_CUSTO.TP_EXECUCAO;

  BEGIN

BEGIN
    SELECT CODFILIAL,
           CODFILIALNF,
           CODFILIALRETIRA              
      INTO vsCODFILIAL,
           vsCODFILIALNF,
           vcCODFILIALRETIRA              
      FROM PCMOV
     WHERE PCMOV.NUMTRANSVENDA = vnNUMTRANSVENDA
       AND PCMOV.CODPROD = P_EXECUCAO.VCODPROD
       AND PCMOV.NUMSEQ = vnNUMSEQ_NOTAVENDA
       AND NVL(PCMOV.QTCONT,0) + NVL(PCMOV.QT,0) > 0;
 EXCEPTION
   WHEN NO_DATA_FOUND THEN
   vsCODFILIAL   := P_EXECUCAO.VCODFILIAL;
   vsCODFILIALNF := P_EXECUCAO.VCODFILIAL;
 END;

    BEGIN
    SELECT DTMOVLOG
          ,CODPROD
          ,PCMOVCOMPLE.CUSTOREALEST
          ,PCMOV.CUSTOREAL
          ,PCMOV.CUSTOREP
          ,PCMOV.CUSTOFINEST
          ,PCMOV.CUSTOFIN
          ,PCMOV.CUSTOCONT
          ,PCMOV.CUSTOREALSEMST
          ,PCMOVCOMPLE.CUSTOFINSEMST
          ,PCMOVCOMPLE.CUSTOREALLIQ
          ,PCMOVCOMPLE.ESTBONIFIC
          ,PCMOV.TIPOMERC
          ,PCMOVCOMPLE.CUSTOFISCAL 
      INTO RET_CUSTO_VENDA.VDATA_VENDA
          ,RET_CUSTO_VENDA.CODPROD
          ,RET_CUSTO_VENDA.CUSTOREAL_VENDA
          ,RET_CUSTO_VENDA.CUSTOREALCMV_VENDA
          ,RET_CUSTO_VENDA.CUSTOREP_VENDA
          ,RET_CUSTO_VENDA.CUSTOFIN_VENDA
          ,RET_CUSTO_VENDA.CUSTOFINCMV_VENDA
          ,RET_CUSTO_VENDA.CUSTOCONT_VENDA
          ,RET_CUSTO_VENDA.CUSTOREALSEMST_VENDA
          ,RET_CUSTO_VENDA.CUSTOFINSEMST_VENDA
          ,RET_CUSTO_VENDA.CUSTOREALLIQ_VENDA
          ,RET_CUSTO_VENDA.vsESTBONIFIC
          ,RET_CUSTO_VENDA.vsTIPOMERC
          ,RET_CUSTO_VENDA.CUSTOFISCAL_VENDA
      FROM PCMOV, PCMOVCOMPLE
     WHERE PCMOV.NUMTRANSITEM = PCMOVCOMPLE.NUMTRANSITEM
       AND NUMTRANSVENDA = vnNUMTRANSVENDA
       AND (NVL(PCMOV.QT,0) > 0 OR NVL(PCMOV.QTCONT,0) > 0)
       AND CODPROD = P_EXECUCAO.VCODPROD
       AND PCMOV.NUMSEQ = vnNUMSEQ_NOTAVENDA
       AND NVL(PCMOV.QTCONT,0) + NVL(PCMOV.QT,0) > 0;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RET_CUSTO_VENDA.VDATA_VENDA := NULL;
        RET_CUSTO_VENDA.CODPROD := 0;
        RET_CUSTO_VENDA.CUSTOREAL_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOREALCMV_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOREP_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOFIN_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOFINCMV_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOCONT_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOREALSEMST_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOFINSEMST_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOREALLIQ_VENDA := 0;
        RET_CUSTO_VENDA.CUSTOFISCAL_VENDA := 0;
    END;

    IF VSCODFILIAL <> VSCODFILIALNF AND (NVL(RET_CUSTO_VENDA.CUSTOREAL_VENDA,0)      = 0 OR
                                         NVL(RET_CUSTO_VENDA.CUSTOREP_VENDA,0)       = 0 OR
                                         NVL(RET_CUSTO_VENDA.CUSTOFIN_VENDA,0)       = 0 OR
                                         NVL(RET_CUSTO_VENDA.CUSTOCONT_VENDA,0)      = 0 OR
                                         NVL(RET_CUSTO_VENDA.CUSTOREALSEMST_VENDA,0) = 0 OR
                                         NVL(RET_CUSTO_VENDA.CUSTOFINSEMST_VENDA,0)  = 0 OR
                                         NVL(RET_CUSTO_VENDA.CUSTOREALLIQ_VENDA,0)   = 0 OR
                                         NVL(RET_CUSTO_VENDA.CUSTOFISCAL_VENDA,0)   = 0)
      THEN

    IF NVL(RET_CUSTO_VENDA.CUSTOREAL_VENDA, 0)   = 0  AND
       NVL(RET_CUSTO_VENDA.CUSTOREALCMV_VENDA,0) > 0  AND
       NVL(RET_CUSTO_VENDA.CUSTOFINCMV_VENDA,0)  > 0  AND
       NVL(RET_CUSTO_VENDA.CUSTOFIN_VENDA,0)     > 0
    THEN
      RET_CUSTO_VENDA.CUSTOREAL_VENDA := (RET_CUSTO_VENDA.CUSTOREALCMV_VENDA -
                                         (RET_CUSTO_VENDA.CUSTOFINCMV_VENDA  -
                                          RET_CUSTO_VENDA.CUSTOFIN_VENDA));
    END IF;
  BEGIN
      SELECT MAX(NUMTRANSVENDATRANSF)
        INTO vnTRANSACAO_TRANSF
        FROM PCTRANSFVIRT
       WHERE PCTRANSFVIRT.NUMPED = (SELECT NUMPED
                                      FROM PCNFSAID
                                     WHERE NUMTRANSVENDA = vnNUMTRANSVENDA)
         AND PCTRANSFVIRT.NUMCAR = (SELECT NUMCAR
                                      FROM PCNFSAID
                                     WHERE NUMTRANSVENDA = vnNUMTRANSVENDA)
         AND CODPROD = P_EXECUCAO.VCODPROD
         AND DTCANCEL IS NULL;
   EXCEPTION
     WHEN NO_DATA_FOUND THEN
       vnTRANSACAO_TRANSF := vnNUMTRANSVENDA;
   END;

   BEGIN
     SELECT DTMOVLOG
          ,CODPROD
          ,DECODE(NVL(RET_CUSTO_VENDA.CUSTOREAL_VENDA,0),0,PCMOVCOMPLE.CUSTOREALEST,RET_CUSTO_VENDA.CUSTOREAL_VENDA)
          ,DECODE(NVL(RET_CUSTO_VENDA.CUSTOREP_VENDA,0),0,PCMOV.CUSTOREP,RET_CUSTO_VENDA.CUSTOREP_VENDA)
          ,DECODE(NVL(RET_CUSTO_VENDA.CUSTOFIN_VENDA,0),0,PCMOV.CUSTOFINEST,RET_CUSTO_VENDA.CUSTOFIN_VENDA)
          ,DECODE(NVL(RET_CUSTO_VENDA.CUSTOCONT_VENDA,0),0,PCMOV.CUSTOCONT,RET_CUSTO_VENDA.CUSTOCONT_VENDA)
