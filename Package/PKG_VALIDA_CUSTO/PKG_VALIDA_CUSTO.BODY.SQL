CREATE OR REPLACE PACKAGE BODY PKG_VALIDA_CUSTO IS
  /************************************************************************************************************************
  ------------------------------------------------------ Alterações -------------------------------------------------------
  Data           Autor             Solicitação         Descrição
  -------------------------------------------------------------------------------------------------------------------------
                                   0                   FUNÇÃO PARA IDENTIFICAR QUAL CUSTO SERÁ UTILIZADO NA DEVOLUÇÃO
  ************************************************************************************************************************/
  vnNUMTRANSVENDA         PCMOV.NUMTRANSVENDA%TYPE;
  vnNUMSEQ_NOTAVENDA      PCMOV.NUMSEQ%TYPE;
  vnCOUNT_CONDVENDA       NUMBER(10);

  FUNCTION BUSCA_CUSTO_DEV(P_EXECUCAO IN TP_EXECUCAO)
    RETURN TP_RETORNO_CUSTO_DEV IS
    VCUSTO_VENDA            TP_RETORNO_CUSTO_VENDA;
    VCUSTO_ENTRADA          TP_RETORNO_CUSTO_ENTRADA;
    VCUSTO_HISTEST          TP_RETORNO_CUSTO_HISTEST;
    VCUSTO_DEV              TP_RETORNO_CUSTO_DEV;
    VCUSTO_ANT              TP_RETORNO_CUSTO_ANT;
    VNACRESCUSTOREAL        NUMBER;
    VGUID                   VARCHAR2(37) := NULL;
    VSVER                   VARCHAR2(15);
    VSORIGEM_CUSTOREAL      VARCHAR2(12) := NULL;
    VSORIGEM_CUSTOREP       VARCHAR2(12) := NULL;
    VSORIGEM_CUSTOFIN       VARCHAR2(12) := NULL;
    VSORIGEM_CUSTOCONT      VARCHAR2(12) := NULL;
    VSORIGEM_CUSTOREALSEMST VARCHAR2(12) := NULL;
    VSORIGEM_CUSTOFINSEMST  VARCHAR2(12) := NULL;
    VSORIGEM_CUSTOREALLIQ   VARCHAR2(12) := NULL;
    VSORIGEM_CUSTOFISCAL    VARCHAR2(12) := NULL;
   -- JSON                     pljson;

  BEGIN

    VCUSTO_DEV.CUSTOREAL_DEV      := NULL;
    VCUSTO_DEV.CUSTOFIN_DEV       := NULL;
    VCUSTO_DEV.CUSTOCONT_DEV      := NULL;
    VCUSTO_DEV.CUSTOREP_DEV       := NULL;
    VCUSTO_DEV.CUSTOREALSEMST_DEV := NULL;
    VCUSTO_DEV.CUSTOFINSEMST_DEV  := NULL;
    VCUSTO_DEV.CUSTOREALLIQ_DEV   := NULL;
    VCUSTO_DEV.CUSTOFISCAL_DEV    := NULL;

    VSVER := PKG_VALIDA_CUSTO.VERSAO;

    VCUSTO_ANT := PKG_VALIDA_CUSTO.BUSCA_CUSTO_ANT(P_EXECUCAO);

    VCUSTO_DEV.VQTESTANT          := VCUSTO_ANT.vnQTEST_ANT;
    VCUSTO_DEV.VQTESTGERANT       := VCUSTO_ANT.vnQTESTGER_ANT;
    VCUSTO_DEV.VCUSTOREALANT      := VCUSTO_ANT.vnCUSTOREAL_ANT;
    VCUSTO_DEV.VCUSTOREPANT       := VCUSTO_ANT.vnCUSTOREP_ANT;
    VCUSTO_DEV.VCUSTOFINANT       := VCUSTO_ANT.vnCUSTOFIN_ANT;
    VCUSTO_DEV.VCUSTOCONTANT      := VCUSTO_ANT.vnCUSTOCONT_ANT;
    VCUSTO_DEV.VCUSTOREALSEMSTANT := VCUSTO_ANT.vnCUSTOREALSEMST_ANT;
    VCUSTO_DEV.VCUSTOFINSEMSTANT  := VCUSTO_ANT.vnCUSTOFINSEMST_ANT;
    VCUSTO_DEV.VCUSTOREALLIQANT   := VCUSTO_ANT.vnCUSTOREALLIQ_ANT ;
    VCUSTO_DEV.VCUSTOFISCALANT    := VCUSTO_ANT.vnCUSTOFISCAL_ANT ;

/* ALIMENTANDO AS VARIÁVEIS COM O NUMTRANSVENDA E NUMSEQ PARA TRATAR O PROCESSO DE DEVOLUÇÃO DE VENDA MANIFESTO*/

    vnNUMTRANSVENDA := P_EXECUCAO.TRANSACAO_SAID;
    vnNUMSEQ_NOTAVENDA := P_EXECUCAO.VNUMSEQ;

/* BUSCA ULTIMA NOTA DO CARREGAMENTO, PARA O PROCESSO DE DEVOLUÇÃO DA VENDA MANIFESTO (1332)*/
 IF NVL(P_EXECUCAO.vnNUMCAR,0) <> 0 THEN
   SELECT COUNT(1)
     INTO vnCOUNT_CONDVENDA
     FROM PCMOV,
          PCNFSAID
    WHERE PCMOV.NUMTRANSVENDA = PCNFSAID.NUMTRANSVENDA
      AND PCMOV.NUMCAR = P_EXECUCAO.vnNUMCAR
      AND PCMOV.CODPROD = P_EXECUCAO.VCODPROD
      AND PCNFSAID.CONDVENDA = 13
      AND NVL(PCMOV.QTCONT,0) + NVL(PCMOV.QT,0) > 0;


  IF NVL(vnCOUNT_CONDVENDA ,0) > 0 THEN

    SELECT NUMTRANSVENDA,
           NUMSEQ
      INTO VNNUMTRANSVENDA,
           VNNUMSEQ_NOTAVENDA
      FROM (SELECT NUMTRANSVENDA, NUMSEQ
              FROM PCMOV
             WHERE NUMCAR = P_EXECUCAO.VNNUMCAR
               AND CODPROD = P_EXECUCAO.VCODPROD
               AND NUMTRANSVENDA IS NOT NULL
               AND PCMOV.DTCANCEL IS NULL
          ORDER BY NUMTRANSVENDA DESC)
     WHERE ROWNUM = 1;

  END IF;
END IF;

/*BUSCA PARAMETRO DE ACRESCIMO DO CUSTO REAL*/
    SELECT NVL(PERCACRESCIMOCUSTOREAL, 0)
      INTO VNACRESCUSTOREAL
      FROM PCCONSUM;


/*CHAMADA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    VCUSTO_VENDA := PKG_VALIDA_CUSTO.BUSCA_CUSTO_VENDA(P_EXECUCAO);

/*ENCONTRA O CSUTO REAL ATRAVÉS DO CMV DO CUSTO FINANCEIRO*/
    IF NVL(VCUSTO_VENDA.CUSTOREAL_VENDA, 0) = 0
    THEN
      VCUSTO_VENDA.CUSTOREAL_VENDA := (VCUSTO_VENDA.CUSTOREALCMV_VENDA -
                                      (VCUSTO_VENDA.CUSTOFINCMV_VENDA -
                                      VCUSTO_VENDA.CUSTOFIN_VENDA));
    END IF;

/*CASO O CUSTOREALLIQ SEJA ZERO OU NULL E NAO HOUVER PARAMETRO DE ACRESCIMO DO CUSTOREAL, O CUSTOREALILIQ DEVERÁ SER IGUAL AO CUSTOREAL*/
    IF VNACRESCUSTOREAL = 0 AND NVL(VCUSTO_VENDA.CUSTOREALLIQ_VENDA, 0) = 0
    THEN
      VCUSTO_VENDA.CUSTOREALLIQ_VENDA := VCUSTO_VENDA.CUSTOREAL_VENDA;
    END IF;

/*VALIDAÇÃO DO CUSTOREAL ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    IF NVL(VCUSTO_VENDA.CUSTOREAL_VENDA, 0) > 0
    THEN
      VSORIGEM_CUSTOREAL       := 'VENDA';
      VCUSTO_DEV.CUSTOREAL_DEV := VCUSTO_VENDA.CUSTOREAL_VENDA;
      VCUSTO_DEV.vsMENS_CUSTOREAL := 'S';
    END IF;

/*VALIDAÇÃO DO CUSTOFIN ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    IF NVL(VCUSTO_VENDA.CUSTOFIN_VENDA, 0) > 0
    THEN
      VSORIGEM_CUSTOFIN       := 'VENDA';
      VCUSTO_DEV.CUSTOFIN_DEV := VCUSTO_VENDA.CUSTOFIN_VENDA;
      VCUSTO_DEV.vsMENS_CUSTOFIN := 'S';
    END IF;

/*VALIDAÇÃO DO CUSTOCONT ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    IF NVL(VCUSTO_VENDA.CUSTOCONT_VENDA, 0) > 0 OR 
      (NVL(VCUSTO_VENDA.CUSTOCONT_VENDA, 0) = 0 AND NVL(VCUSTO_VENDA.vsESTBONIFIC,'N') = 'S') OR 
      (GREATEST(NVL(VCUSTO_VENDA.CUSTOCONT_VENDA, 0),0) = 0 AND NVL(VCUSTO_VENDA.vsTIPOMERC,'XX') = 'CB')
    THEN
      VSORIGEM_CUSTOCONT       := 'VENDA';
      VCUSTO_DEV.CUSTOCONT_DEV := VCUSTO_VENDA.CUSTOCONT_VENDA;
      VCUSTO_DEV.vdDT_ORIGEM   := VCUSTO_VENDA.VDATA_VENDA;
      VCUSTO_DEV.vsMENS_CUSTOCONT := 'S';
    END IF;
    
/*VALIDAÇÃO DO CUSTOFISCAL ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    IF NVL(VCUSTO_VENDA.CUSTOFISCAL_VENDA, 0) > 0 OR 
      (NVL(VCUSTO_VENDA.CUSTOFISCAL_VENDA, 0) = 0 AND NVL(VCUSTO_VENDA.vsESTBONIFIC,'N') = 'S') OR 
      (GREATEST(NVL(VCUSTO_VENDA.CUSTOFISCAL_VENDA, 0),0) = 0 AND NVL(VCUSTO_VENDA.vsTIPOMERC,'XX') = 'CB')
    THEN
      VSORIGEM_CUSTOFISCAL     := 'VENDA';
      VCUSTO_DEV.CUSTOFISCAL_DEV := VCUSTO_VENDA.CUSTOFISCAL_VENDA;
      VCUSTO_DEV.vdDT_ORIGEM   := VCUSTO_VENDA.VDATA_VENDA;
      VCUSTO_DEV.vsMENS_CUSTOFISCAL := 'S';
    END IF;    

/*VALIDAÇÃO DO CUSTOREP ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    IF NVL(VCUSTO_VENDA.CUSTOREP_VENDA, 0) > 0
    THEN
      VSORIGEM_CUSTOREP       := 'VENDA';
      VCUSTO_DEV.CUSTOREP_DEV := VCUSTO_VENDA.CUSTOREP_VENDA;
      VCUSTO_DEV.vsMENS_CUSTOREP := 'S';
    END IF;

/*VALIDAÇÃO DO CUSTOREALSEMST ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    IF NVL(VCUSTO_VENDA.CUSTOREALSEMST_VENDA, 0) > 0
    THEN
      VSORIGEM_CUSTOREALSEMST       := 'VENDA';
      VCUSTO_DEV.CUSTOREALSEMST_DEV := VCUSTO_VENDA.CUSTOREALSEMST_VENDA;
      VCUSTO_DEV.vsMENS_CUSTOREALSEMST := 'S';
    END IF;

/*VALIDAÇÃO DO CUSTOFINSEMST ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    IF NVL(VCUSTO_VENDA.CUSTOFINSEMST_VENDA, 0) > 0
    THEN
      VSORIGEM_CUSTOFINSEMST       := 'VENDA';
      VCUSTO_DEV.CUSTOFINSEMST_DEV := VCUSTO_VENDA.CUSTOFINSEMST_VENDA;
      VCUSTO_DEV.vsMENS_CUSTOFINSEMST := 'S';
    END IF;

/*VALIDAÇÃO DO CUSTOREALLIQ ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA VENDA*/
    IF NVL(VCUSTO_VENDA.CUSTOREALLIQ_VENDA, 0) > 0
    THEN
      VSORIGEM_CUSTOREALLIQ       := 'VENDA';
      VCUSTO_DEV.CUSTOREALLIQ_DEV := VCUSTO_VENDA.CUSTOREALLIQ_VENDA;
      VCUSTO_DEV.vsMENS_CUSTOREALLIQ := 'S';
    END IF;

/*CASO A VENDA NAO TENHA ENCONTRADO ALGUM CUSTO, DEVERA CHAMAR A FUNCAO DE BUSCA DOS CUSTOS DA ENTRADA*/
    IF VSORIGEM_CUSTOREAL IS NULL OR VSORIGEM_CUSTOREP IS NULL OR
       VSORIGEM_CUSTOFIN IS NULL OR VSORIGEM_CUSTOCONT IS NULL OR
       VSORIGEM_CUSTOREALSEMST IS NULL OR VSORIGEM_CUSTOFINSEMST IS NULL OR
       VSORIGEM_CUSTOREALLIQ IS NULL OR
       VSORIGEM_CUSTOFISCAL IS NULL
    THEN
/*CHAMADA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      BEGIN
        VCUSTO_ENTRADA := PKG_VALIDA_CUSTO.BUSCA_CUSTO_ENTRADA(P_EXECUCAO);
      EXCEPTION
        WHEN OTHERS THEN
          VCUSTO_DEV.VMENSAGEM := 'Erro ao buscar custo da última entrada. Mensagem Original: ' ||
                                  SQLCODE || ' - ' || SQLERRM;
          RETURN VCUSTO_DEV;
      END;

/*CASO O CUSTOREALLIQ SEJA ZERO OU NULL E NAO HOUVER PARAMETRO DE ACRESCIMO DO CUSTOREAL, O CUSTOREALILIQ DEVERÁ SER IGUAL AO CUSTOREAL*/
      IF VNACRESCUSTOREAL = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOREALLIQ_ENTRADA, 0) = 0
      THEN
        VCUSTO_ENTRADA.CUSTOREALLIQ_ENTRADA := VCUSTO_ENTRADA.CUSTOREAL_ENTRADA;
      END IF;

/*VALIDAÇÃO DO CUSTOREAL ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      IF NVL(VCUSTO_DEV.CUSTOREAL_DEV, 0) = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOREAL_ENTRADA, 0) > 0
      THEN
        VSORIGEM_CUSTOREAL       := 'ENTRADA';
        VCUSTO_DEV.CUSTOREAL_DEV := VCUSTO_ENTRADA.CUSTOREAL_ENTRADA;
        VCUSTO_DEV.vsMENS_CUSTOREAL := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOFIN ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      IF NVL(VCUSTO_DEV.CUSTOFIN_DEV, 0) = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOFIN_ENTRADA, 0) > 0
      THEN
        VSORIGEM_CUSTOFIN       := 'ENTRADA';
        VCUSTO_DEV.CUSTOFIN_DEV := VCUSTO_ENTRADA.CUSTOFIN_ENTRADA;
        VCUSTO_DEV.vsMENS_CUSTOFIN := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOCONT ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      IF NVL(VCUSTO_DEV.CUSTOCONT_DEV, 0) = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOCONT_ENTRADA, 0) > 0 OR (NVL(VCUSTO_ENTRADA.CUSTOCONT_ENTRADA,0) = 0 
                                                      AND VCUSTO_ENTRADA.CODOPER = 'EB' 
                                                      AND (VCUSTO_ENTRADA.vsESTBONIFIC IS NULL OR VCUSTO_ENTRADA.vsESTBONIFIC = 'S'))

      THEN
        VSORIGEM_CUSTOCONT       := 'ENTRADA';
        VCUSTO_DEV.CUSTOCONT_DEV := VCUSTO_ENTRADA.CUSTOCONT_ENTRADA;
        VCUSTO_DEV.vdDT_ORIGEM := VCUSTO_ENTRADA.VDATA_ENTRADA; --DATA ORIGEM DO CUSTO CORRETO PARA O AUDITOR
        VCUSTO_DEV.vsMENS_CUSTOCONT := 'S';
      END IF;
      
/*VALIDAÇÃO DO CUSTOFISCAL ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      IF NVL(VCUSTO_DEV.CUSTOFISCAL_DEV, 0) = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOFISCAL_ENTRADA, 0) > 0 OR (NVL(VCUSTO_ENTRADA.CUSTOFISCAL_ENTRADA,0) = 0 
                                                      AND VCUSTO_ENTRADA.CODOPER = 'EB' 
                                                      AND (VCUSTO_ENTRADA.vsESTBONIFIC IS NULL OR VCUSTO_ENTRADA.vsESTBONIFIC = 'S'))

      THEN
        VSORIGEM_CUSTOFISCAL       := 'ENTRADA';
        VCUSTO_DEV.CUSTOFISCAL_DEV := VCUSTO_ENTRADA.CUSTOFISCAL_ENTRADA;
        VCUSTO_DEV.vdDT_ORIGEM := VCUSTO_ENTRADA.VDATA_ENTRADA; --DATA ORIGEM DO CUSTO CORRETO PARA O AUDITOR
        VCUSTO_DEV.vsMENS_CUSTOFISCAL := 'S';
      END IF;      
      

/*VALIDAÇÃO DO CUSTOREP ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      IF NVL(VCUSTO_DEV.CUSTOREP_DEV, 0) = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOREP_ENTRADA, 0) > 0
      THEN
        VSORIGEM_CUSTOREP       := 'ENTRADA';
        VCUSTO_DEV.CUSTOREP_DEV := VCUSTO_ENTRADA.CUSTOREP_ENTRADA;
        VCUSTO_DEV.vsMENS_CUSTOREP := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOREALSEMST ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      IF NVL(VCUSTO_DEV.CUSTOREALSEMST_DEV, 0) = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOREALSEMST_ENTRADA, 0) > 0
      THEN
        VSORIGEM_CUSTOREALSEMST       := 'ENTRADA';
        VCUSTO_DEV.CUSTOREALSEMST_DEV := VCUSTO_ENTRADA.CUSTOREALSEMST_ENTRADA;
        VCUSTO_DEV.vsMENS_CUSTOREALSEMST := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOFINSEMST ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      IF NVL(VCUSTO_DEV.CUSTOFINSEMST_DEV, 0) = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOFINSEMST_ENTRADA, 0) > 0
      THEN
        VSORIGEM_CUSTOFINSEMST       := 'ENTRADA';
        VCUSTO_DEV.CUSTOFINSEMST_DEV := VCUSTO_ENTRADA.CUSTOFINSEMST_ENTRADA;
        VCUSTO_DEV.vsMENS_CUSTOFINSEMST := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOREALLIQ ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA ENTRADA*/
      IF NVL(VCUSTO_DEV.CUSTOREALLIQ_DEV, 0) = 0 AND
         NVL(VCUSTO_ENTRADA.CUSTOREALLIQ_ENTRADA, 0) > 0
      THEN
        VSORIGEM_CUSTOREALLIQ       := 'ENTRADA';
        VCUSTO_DEV.CUSTOREALLIQ_DEV := VCUSTO_ENTRADA.CUSTOREALLIQ_ENTRADA;
        VCUSTO_DEV.vsMENS_CUSTOREALLIQ := 'S';

      END IF;

    END IF;

/*CASO A VENDA NAO TENHA ENCONTRADO ALGUM CUSTO, DEVERA CHAMAR A FUNCAO DE BUSCA DOS CUSTOS NA PCHISEST*/
    IF VSORIGEM_CUSTOREAL IS NULL OR VSORIGEM_CUSTOREP IS NULL OR
       VSORIGEM_CUSTOFIN IS NULL OR VSORIGEM_CUSTOCONT IS NULL OR
       VSORIGEM_CUSTOREALSEMST IS NULL OR VSORIGEM_CUSTOFINSEMST IS NULL OR
       VSORIGEM_CUSTOREALLIQ IS NULL OR
       VSORIGEM_CUSTOFISCAL IS NULL
    THEN
/*CHAMADA FUNÇÃO DE BUSCA DOS CUSTOS NA PCHISTEST*/
      BEGIN
        VCUSTO_HISTEST := PKG_VALIDA_CUSTO.BUSCA_CUSTO_HISTEST(P_EXECUCAO);
      EXCEPTION
        WHEN OTHERS THEN
          VCUSTO_DEV.VMENSAGEM := 'Erro buscar custos da PCHISTEST. Mensagem Original: ' ||
                                  SQLCODE || ' - ' || SQLERRM;
          RETURN VCUSTO_DEV;
      END;
/*CASO O CUSTOREALLIQ SEJA ZERO OU NULL E NAO HOUVER PARAMETRO DE ACRESCIMO DO CUSTOREAL, O CUSTOREALILIQ DEVERÁ SER IGUAL AO CUSTOREAL*/
      IF VNACRESCUSTOREAL = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOREALLIQ_HISTEST, 0) = 0
      THEN
        VCUSTO_HISTEST.CUSTOREALLIQ_HISTEST := VCUSTO_HISTEST.CUSTOREAL_HISTEST;
      END IF;

/*VALIDAÇÃO DO CUSTOREAL ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA PCHISTEST*/
      IF NVL(VCUSTO_DEV.CUSTOREAL_DEV, 0) = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOREAL_HISTEST, 0) > 0
      THEN
        VSORIGEM_CUSTOREAL       := 'PCHISTEST';
        VCUSTO_DEV.CUSTOREAL_DEV := VCUSTO_HISTEST.CUSTOREAL_HISTEST;
        VCUSTO_DEV.vsMENS_CUSTOREAL := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOFIN ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA PCHISTEST*/
      IF NVL(VCUSTO_DEV.CUSTOFIN_DEV, 0) = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOFIN_HISTEST, 0) > 0
      THEN
        VSORIGEM_CUSTOFIN       := 'PCHISTEST';
        VCUSTO_DEV.CUSTOFIN_DEV := VCUSTO_HISTEST.CUSTOFIN_HISTEST;
        VCUSTO_DEV.vsMENS_CUSTOFIN := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOCONT ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA PCHISTEST*/
      IF NVL(VCUSTO_DEV.CUSTOCONT_DEV, 0) = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOCONT_HISTEST, 0) > 0
      THEN
        VSORIGEM_CUSTOCONT       := 'PCHISTEST';
        VCUSTO_DEV.CUSTOCONT_DEV := VCUSTO_HISTEST.CUSTOCONT_HISTEST;
        VCUSTO_DEV.vdDT_ORIGEM := VCUSTO_HISTEST.VDATA_HISTEST; --DATA ORIGEM DO CUSTO CORRETO PARA O AUDITOR
        VCUSTO_DEV.vsMENS_CUSTOCONT := 'S';
      END IF;
      
/*VALIDAÇÃO DO CUSTOFISCAL ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA PCHISTEST*/
      IF NVL(VCUSTO_DEV.CUSTOFISCAL_DEV, 0) = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOFISCAL_HISTEST, 0) > 0
      THEN
        VSORIGEM_CUSTOFISCAL       := 'PCHISTEST';
        VCUSTO_DEV.CUSTOFISCAL_DEV := VCUSTO_HISTEST.CUSTOFISCAL_HISTEST;
        VCUSTO_DEV.vdDT_ORIGEM := VCUSTO_HISTEST.VDATA_HISTEST; --DATA ORIGEM DO CUSTO CORRETO PARA O AUDITOR
        VCUSTO_DEV.vsMENS_CUSTOFISCAL := 'S';
      END IF;      

/*VALIDAÇÃO DO CUSTOREP ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA PCHISTEST*/
      IF NVL(VCUSTO_DEV.CUSTOREP_DEV, 0) = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOREP_HISTEST, 0) > 0
      THEN
        VSORIGEM_CUSTOREP       := 'PCHISTEST';
        VCUSTO_DEV.CUSTOREP_DEV := VCUSTO_HISTEST.CUSTOREP_HISTEST;
        VCUSTO_DEV.vsMENS_CUSTOREP := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOREALSEMST ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA PCHISTEST*/
      IF NVL(VCUSTO_DEV.CUSTOREALSEMST_DEV, 0) = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOREALSEMST_HISTEST, 0) > 0
      THEN
        VSORIGEM_CUSTOREALSEMST       := 'PCHISTEST';
        VCUSTO_DEV.CUSTOREALSEMST_DEV := VCUSTO_HISTEST.CUSTOREALSEMST_HISTEST;
        VCUSTO_DEV.vsMENS_CUSTOREALSEMST := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOFINSEMST ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA PCHISTEST*/
      IF NVL(VCUSTO_DEV.CUSTOFINSEMST_DEV, 0) = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOFINSEMST_HISTEST, 0) > 0
      THEN
        VSORIGEM_CUSTOFINSEMST       := 'PCHISTEST';
        VCUSTO_DEV.CUSTOFINSEMST_DEV := VCUSTO_HISTEST.CUSTOFINSEMST_HISTEST;
        VCUSTO_DEV.vsMENS_CUSTOFINSEMST := 'S';

      END IF;

/*VALIDAÇÃO DO CUSTOREALLIQ ENCONTRADO NA FUNÇÃO DE BUSCA DOS CUSTOS DA PCHISTEST*/
      IF NVL(VCUSTO_DEV.CUSTOREALLIQ_DEV, 0) = 0 AND
         NVL(VCUSTO_HISTEST.CUSTOREALLIQ_HISTEST, 0) > 0
      THEN
        VSORIGEM_CUSTOREALLIQ       := 'PCHISTEST';
        VCUSTO_DEV.CUSTOREALLIQ_DEV := VCUSTO_HISTEST.CUSTOREALLIQ_HISTEST;
        VCUSTO_DEV.vsMENS_CUSTOREALLIQ := 'S';

      END IF;

    END IF;

/*CASO TENHA PASSADO POR TODAS AS BUSCAS E NÃO TENHA ENCONTRADO O CUSTOFINSEMST, VERIFICARÁ SE O CUSTOREAL É IGUAL AO CUSTOFIN, PARA IGUALAR O CUSTOREALSEMST AO CUSTOFINSEMST*/
    IF NVL(VCUSTO_VENDA.CUSTOFINSEMST_VENDA,0) > 0 AND
       NVL(VCUSTO_VENDA.CUSTOREAL_VENDA,0) = NVL(VCUSTO_VENDA.CUSTOFIN_VENDA,0) AND
       NVL(VCUSTO_VENDA.CUSTOREALSEMST_VENDA,0) = 0 AND VSORIGEM_CUSTOREALSEMST IS NULL
    THEN
       VSORIGEM_CUSTOREALSEMST      := '+VENDA+';
       VCUSTO_DEV.CUSTOREALSEMST_DEV := VCUSTO_VENDA.CUSTOFINSEMST_VENDA;
       VCUSTO_DEV.vsMENS_CUSTOREALSEMST := 'S';
    END IF;

/*CASO TENHA PASSADO POR TODAS AS BUSCAS E NÃO TENHA ENCONTRADO O CUSTOREALSEMST, VERIFICARÁ SE O CUSTOREAL É IGUAL AO CUSTOFIN, PARA IGUALAR O CUSTOFINSEMST AO CUSTOREALSEMST */
     IF NVL(VCUSTO_VENDA.CUSTOREALSEMST_VENDA,0) > 0 AND
       NVL(VCUSTO_VENDA.CUSTOREAL_VENDA,0)= NVL(VCUSTO_VENDA.CUSTOFIN_VENDA,0) AND
       NVL(VCUSTO_VENDA.CUSTOFINSEMST_VENDA,0) = 0 AND VSORIGEM_CUSTOFINSEMST IS NULL
    THEN
       VSORIGEM_CUSTOFINSEMST       := '+VENDA+';
       VCUSTO_DEV.CUSTOFINSEMST_DEV := VCUSTO_VENDA.CUSTOREALSEMST_VENDA;
       VCUSTO_DEV.vsMENS_CUSTOFINSEMST := 'S';

    END IF;

