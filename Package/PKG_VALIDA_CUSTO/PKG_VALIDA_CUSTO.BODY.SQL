    VDDTMOVLOG        PCMOV.DTMOVLOG%TYPE;
    VSCODFILIAL       PCMOV.CODFILIAL%TYPE;
    vnTRANSACAODEV    PCMOV.NUMTRANSENT%TYPE;

  BEGIN
    BEGIN
    IF vnNUMTRANSVENDA IS NOT NULL AND vnNUMTRANSVENDA <> 0 THEN  
        SELECT MAX(PCESTCOM.NUMTRANSENT), DTMOVLOG, NVL(CODFILIALNF, CODFILIAL)
          INTO vnTRANSACAODEV,VDDTMOVLOG, VSCODFILIAL
          FROM PCMOV, PCESTCOM
         WHERE PCMOV.NUMTRANSVENDA = PCESTCOM.NUMTRANSVENDA
           AND PCMOV.NUMTRANSVENDA = vnNUMTRANSVENDA
           AND PCMOV.CODPROD = P_EXECUCAO.VCODPROD
           AND PCMOV.NUMSEQ = vnNUMSEQ_NOTAVENDA
           AND NVL(PCMOV.QTCONT,0) + NVL(PCMOV.QT,0) > 0
           GROUP BY DTMOVLOG
                  , NVL(CODFILIALNF, CODFILIAL);
     ELSE
        SELECT PCMOV.NUMTRANSENT, DTMOVLOG, NVL(CODFILIALNF, CODFILIAL)
            INTO vnTRANSACAODEV,VDDTMOVLOG, VSCODFILIAL
            FROM PCMOV 
            WHERE PCMOV.ROWID = P_EXECUCAO.ROWID_PCMOV_DEVOLUCAO;
     END IF;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        VDDTMOVLOG := P_EXECUCAO.VDTDEV;
        VSCODFILIAL := P_EXECUCAO.VCODFILIAL;
