      END IF;
    IF NVL(RET_CUSTO_VENDA.CUSTOFISCAL_VENDA,0)   = 0 THEN
       RET_CUSTO_VENDA.CUSTOFISCAL_VENDA := RET_CUSTO_NOTATRANSF_RETIRA.CUSTOFISCAL_DEV;
      END IF;  
      

  END IF;
    RETURN RET_CUSTO_VENDA;

  END BUSCA_CUSTO_VENDA;

