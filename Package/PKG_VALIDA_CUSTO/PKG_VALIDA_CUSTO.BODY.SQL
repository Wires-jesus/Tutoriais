    END;
  END IF;
    RETURN RET_CUSTO_ENTRADA;

  END BUSCA_CUSTO_ENTRADA;

  /************************************************************************************************************************
  ------------------------------------------------------ Alterações -------------------------------------------------------
  Data           Autor             Solicitação         Descrição
  -------------------------------------------------------------------------------------------------------------------------
                                   0                   FUNÇÃO PARA BUSCAR OS CUSTOS DA PCHISTEST ANTERIOR A NOTA DE VENDA
  ************************************************************************************************************************/
  FUNCTION BUSCA_CUSTO_HISTEST(P_EXECUCAO IN TP_EXECUCAO)
    RETURN TP_RETORNO_CUSTO_HISTEST IS

    RET_CUSTO_HISTEST TP_RETORNO_CUSTO_HISTEST;
    VDDTMOVLOG        PCMOV.DTMOVLOG%TYPE;
    VSCODFILIAL       PCMOV.CODFILIAL%TYPE;
    VDDTMOV           PCMOV.DTMOVLOG%TYPE;

  BEGIN
    BEGIN
    IF vnNUMTRANSVENDA IS NOT NULL AND vnNUMTRANSVENDA <> 0 THEN  
       SELECT TRUNC(NVL(DTMOVLOG,DTMOV)), NVL(CODFILIALNF, CODFILIAL)
            INTO VDDTMOV, VSCODFILIAL
            FROM PCMOV
           WHERE PCMOV.NUMTRANSVENDA = vnNUMTRANSVENDA
             AND PCMOV.CODPROD = P_EXECUCAO.VCODPROD
             AND PCMOV.NUMSEQ = vnNUMSEQ_NOTAVENDA
             AND NVL(PCMOV.QTCONT,0) + NVL(PCMOV.QT,0) > 0;
     ELSE
        SELECT TRUNC(NVL(DTMOVLOG,DTMOV)), NVL(CODFILIALNF, CODFILIAL)
            INTO VDDTMOV, VSCODFILIAL
            FROM PCMOV 
            WHERE PCMOV.ROWID = P_EXECUCAO.ROWID_PCMOV_DEVOLUCAO;
    END IF; 
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        VDDTMOV := TRUNC(P_EXECUCAO.VDTDEV);
        VSCODFILIAL := P_EXECUCAO.VCODFILIAL;
     END;
    VDDTMOVLOG := VDDTMOV - 1;
     BEGIN

      WITH DADOS AS
       (SELECT TO_DATE(DATA, 'DD/MM/YYYY HH24:MI:SS') DATA
              ,M.CODPROD
              ,NVL(H.CUSTOREAL, 0) CUSTOREAL
              ,NVL(H.CUSTOREP, 0) CUSTOREP
              ,NVL(H.CUSTOFIN, 0) CUSTOFIN
              ,NVL(H.CUSTOCONT, 0) CUSTOCONT
              ,NVL(H.CUSTOREALSEMST, 0) CUSTOREALSEMST
              ,NVL(H.CUSTOFINSEMST, 0) CUSTOFINSEMST
              ,NVL(H.CUSTOREALLIQ, 0) CUSTOREALLIQ
              ,ROW_NUMBER() OVER(ORDER BY H.DATA DESC) RS
              ,NVL(H.CUSTOFISCAL, 0) CUSTOFISCAL
          FROM PCMOV M, PCHISTEST H
         WHERE M.NUMTRANSVENDA = vnNUMTRANSVENDA
           AND M.CODPROD = H.CODPROD
           AND NVL(M.CODFILIALNF, M.CODFILIAL) = H.CODFILIAL
           AND M.CODPROD = P_EXECUCAO.VCODPROD
           AND TRUNC(H.DATA) BETWEEN VDDTMOVLOG - 90 AND VDDTMOVLOG
           AND NVL(M.QTCONT,0) + NVL(M.QT,0) > 0
         ORDER BY H.DATA DESC)
      SELECT DATA
            ,CODPROD
            ,CUSTOREAL
            ,CUSTOREP
            ,CUSTOFIN
            ,CUSTOCONT
            ,CUSTOREALSEMST
            ,CUSTOFINSEMST
            ,CUSTOREALLIQ
            ,CUSTOFISCAL
        INTO RET_CUSTO_HISTEST.VDATA_HISTEST
            ,RET_CUSTO_HISTEST.CODPROD
            ,RET_CUSTO_HISTEST.CUSTOREAL_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOREP_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOFIN_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOCONT_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOREALSEMST_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOFINSEMST_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOREALLIQ_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOFISCAL_HISTEST
        FROM DADOS
       WHERE DADOS.RS = 1
       ORDER BY DADOS.RS;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        BEGIN
          WITH DADOS AS
       (SELECT TO_DATE(DATA, 'DD/MM/YYYY HH24:MI:SS') DATA
              ,H.CODPROD
              ,NVL(H.CUSTOREAL, 0) CUSTOREAL
              ,NVL(H.CUSTOREP, 0) CUSTOREP
              ,NVL(H.CUSTOFIN, 0) CUSTOFIN
              ,NVL(H.CUSTOCONT, 0) CUSTOCONT
              ,NVL(H.CUSTOREALSEMST, 0) CUSTOREALSEMST
              ,NVL(H.CUSTOFINSEMST, 0) CUSTOFINSEMST
              ,NVL(H.CUSTOREALLIQ, 0) CUSTOREALLIQ
              ,ROW_NUMBER() OVER(ORDER BY H.DATA DESC) RS
              ,NVL(H.CUSTOFISCAL, 0) CUSTOFISCAL
          FROM PCHISTEST H
         WHERE H.CODPROD = P_EXECUCAO.VCODPROD
           AND H.CODFILIAL = VSCODFILIAL
           AND TRUNC(H.DATA) BETWEEN VDDTMOVLOG - 90 AND VDDTMOVLOG
         ORDER BY H.DATA DESC)
      SELECT DATA
            ,CODPROD
            ,CUSTOREAL
            ,CUSTOREP
            ,CUSTOFIN
            ,CUSTOCONT
            ,CUSTOREALSEMST
            ,CUSTOFINSEMST
            ,CUSTOREALLIQ
            ,CUSTOFISCAL
        INTO RET_CUSTO_HISTEST.VDATA_HISTEST
            ,RET_CUSTO_HISTEST.CODPROD
            ,RET_CUSTO_HISTEST.CUSTOREAL_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOREP_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOFIN_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOCONT_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOREALSEMST_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOFINSEMST_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOREALLIQ_HISTEST
            ,RET_CUSTO_HISTEST.CUSTOFISCAL_HISTEST
        FROM DADOS
       WHERE DADOS.RS = 1
       ORDER BY DADOS.RS;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RET_CUSTO_HISTEST.CODPROD                := P_EXECUCAO.VCODPROD;
        RET_CUSTO_HISTEST.CUSTOREAL_HISTEST      := 0;
        RET_CUSTO_HISTEST.CUSTOREP_HISTEST       := 0;
        RET_CUSTO_HISTEST.CUSTOFIN_HISTEST       := 0;
        RET_CUSTO_HISTEST.CUSTOCONT_HISTEST      := 0;
        RET_CUSTO_HISTEST.CUSTOREALSEMST_HISTEST := 0;
        RET_CUSTO_HISTEST.CUSTOFINSEMST_HISTEST  := 0;
        RET_CUSTO_HISTEST.CUSTOREALLIQ_HISTEST   := 0;
        RET_CUSTO_HISTEST.CUSTOFISCAL_HISTEST    := 0;
      WHEN OTHERS THEN
        RET_CUSTO_HISTEST := NULL;
        RAISE;
    END;
   END;

    RETURN RET_CUSTO_HISTEST;
  END BUSCA_CUSTO_HISTEST;

  FUNCTION BUSCA_CUSTO_ANT(P_EXECUCAO IN TP_EXECUCAO)
    RETURN TP_RETORNO_CUSTO_ANT IS

  RET_CUSTO_ANT                 TP_RETORNO_CUSTO_ANT;

  BEGIN

    BEGIN
        SELECT PCEST.QTEST
            ,PCEST.QTESTGER
            ,PCEST.CUSTOREAL
            ,PCEST.CUSTOREP
            ,PCEST.CUSTOFIN
            ,PCEST.CUSTOCONT
            ,PCEST.CUSTOREALSEMST
            ,PCEST.CUSTOFINSEMST
            ,PCEST.CUSTOREALLIQ
            ,PCEST.CUSTOFISCAL
        INTO RET_CUSTO_ANT.vnQTEST_ANT
            ,RET_CUSTO_ANT.vnQTESTGER_ANT
            ,RET_CUSTO_ANT.vnCUSTOREAL_ANT
            ,RET_CUSTO_ANT.vnCUSTOREP_ANT
            ,RET_CUSTO_ANT.vnCUSTOFIN_ANT
            ,RET_CUSTO_ANT.vnCUSTOCONT_ANT
            ,RET_CUSTO_ANT.vnCUSTOREALSEMST_ANT
            ,RET_CUSTO_ANT.vNCUSTOFINSEMST_ANT
            ,RET_CUSTO_ANT.vnCUSTOREALLIQ_ANT
            ,RET_CUSTO_ANT.vnCUSTOFISCAL_ANT
        FROM PCEST
       WHERE PCEST.CODPROD = P_EXECUCAO.VCODPROD
         AND PCEST.CODFILIAL = P_EXECUCAO.VCODFILIAL;
     EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RET_CUSTO_ANT.VMENSAGEM := 'Os Custos anteriores não foram encontrados. Favor verificar parâmetros passados.';
        RETURN RET_CUSTO_ANT;
      WHEN OTHERS THEN
        RET_CUSTO_ANT.VMENSAGEM := 'Erro ao buscar Custos anteriores' ||
                                CHR(13) || 'Msg. Original: ' || SQLCODE ||
                                ' - ' || SQLERRM;
   END;
 RET_CUSTO_ANT.VMENSAGEM := 'OK';

 RETURN RET_CUSTO_ANT;
END BUSCA_CUSTO_ANT;

