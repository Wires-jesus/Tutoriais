        RET_CUSTO_ENTRADA.CUSTOREALSEMST_ENTRADA   := 0;
        RET_CUSTO_ENTRADA.CUSTOFINSEMST_ENTRADA    := 0;
        RET_CUSTO_ENTRADA.CUSTOREALLIQ_ENTRADA     := 0;
        RET_CUSTO_ENTRADA.NUMERO_TRANSACAO_ENTRADA := 0;
        RET_CUSTO_ENTRADA.CODOPER                  := 'XX';
        RET_CUSTO_ENTRADA.vsESTBONIFIC              := 'X';
        RET_CUSTO_ENTRADA.CUSTOFISCAL_ENTRADA        := 0;
      WHEN OTHERS THEN
