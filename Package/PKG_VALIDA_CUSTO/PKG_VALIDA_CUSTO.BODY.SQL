               NVL(PCNFENT.CODCONTFOR, PCCONSUM.CODCONTFOR) OR
               (PCNFENT.TIPODESCARGA IN ('S', '6', '7', '8', 'T')) OR (PCMOV.CODOPER = 'EP'))
