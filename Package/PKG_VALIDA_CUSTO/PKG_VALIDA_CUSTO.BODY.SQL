      ENT_TRANSF.VCODPROD              := P_EXECUCAO.VCODPROD;
      ENT_TRANSF.VDTDEV                := SYSDATE;
      ENT_TRANSF.VCHAMADA              := 'CALLTRANSF';
      ENT_TRANSF.VCODFILIAL            := VSCODFILIAL;
      ENT_TRANSF.VNUMSEQ               := vnNUMSEQ_NOTAVENDA;

      RET_CUSTO_NOTATRANSF := PKG_VALIDA_CUSTO.BUSCA_CUSTO_DEV(ENT_TRANSF);

    IF NVL(RET_CUSTO_VENDA.CUSTOREAL_VENDA,0)      = 0 THEN
        RET_CUSTO_VENDA.CUSTOREAL_VENDA := RET_CUSTO_NOTATRANSF.CUSTOREAL_DEV;
