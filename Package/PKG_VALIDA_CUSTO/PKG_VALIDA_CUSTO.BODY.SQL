               (PCMOV.STATUS = 'B' AND PCNFENT.TIPODESCARGA = 'I' ))
           AND NVL(PCNFENT.TIPODESCARGA,'X') NOT IN ('F', 'H', 'G', 'D','P')
           AND NVL(PCNFENT.FINALIDADENFE, 'O') <> 'C'
           AND PCMOV.NUMTRANSENT NOT IN (NVL(vnTRANSACAODEV,0))
           AND NVL(PCMOV.QTCONT,0) + NVL(PCMOV.QT,0) > 0
         ORDER BY DTMOVLOG DESC)
      SELECT DTMOVLOG
            ,CODPROD
            ,CUSTOREAL
            ,CUSTOREP
            ,CUSTOFIN
            ,CUSTOCONT
            ,CUSTOREALSEMST
            ,CUSTOFINSEMST
            ,CUSTOREALLIQ
            ,NUMTRANSENT
            ,CUSTOULTENTCONT
            ,DTENT
            ,QTCONT
            ,QTESTANT
            ,CODOPER
            ,ESTBONIFIC
