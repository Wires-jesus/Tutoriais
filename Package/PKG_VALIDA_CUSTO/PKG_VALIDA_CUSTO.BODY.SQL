        RET_CUSTO_VENDA.CUSTOREP_VENDA := RET_CUSTO_NOTATRANSF.CUSTOREP_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOFIN_VENDA,0)       = 0 THEN
        RET_CUSTO_VENDA.CUSTOFIN_VENDA := RET_CUSTO_NOTATRANSF.CUSTOFIN_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOCONT_VENDA,0)      = 0 THEN
       RET_CUSTO_VENDA.CUSTOCONT_VENDA := RET_CUSTO_NOTATRANSF.CUSTOCONT_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOREALSEMST_VENDA,0) = 0 THEN
       RET_CUSTO_VENDA.CUSTOREALSEMST_VENDA := RET_CUSTO_NOTATRANSF.CUSTOREALSEMST_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOFINSEMST_VENDA,0)  = 0 THEN
       RET_CUSTO_VENDA.CUSTOFINSEMST_VENDA := RET_CUSTO_NOTATRANSF.CUSTOFINSEMST_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOREALLIQ_VENDA,0)   = 0 THEN
       RET_CUSTO_VENDA.CUSTOREALLIQ_VENDA := RET_CUSTO_NOTATRANSF.CUSTOREALLIQ_DEV;
      END IF;
      
