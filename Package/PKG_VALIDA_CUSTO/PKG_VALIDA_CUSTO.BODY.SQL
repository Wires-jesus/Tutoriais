       RET_CUSTO_VENDA.CUSTOCONT_VENDA := RET_CUSTO_NOTATRANSF_RETIRA.CUSTOCONT_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOREALSEMST_VENDA,0) = 0 THEN
       RET_CUSTO_VENDA.CUSTOREALSEMST_VENDA := RET_CUSTO_NOTATRANSF_RETIRA.CUSTOREALSEMST_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOFINSEMST_VENDA,0)  = 0 THEN
       RET_CUSTO_VENDA.CUSTOFINSEMST_VENDA := RET_CUSTO_NOTATRANSF_RETIRA.CUSTOFINSEMST_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOREALLIQ_VENDA,0)   = 0 THEN
