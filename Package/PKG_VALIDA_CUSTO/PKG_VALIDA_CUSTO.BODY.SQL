    IF NVL(RET_CUSTO_VENDA.CUSTOREAL_VENDA,0)      = 0 THEN
        RET_CUSTO_VENDA.CUSTOREAL_VENDA := RET_CUSTO_NOTATRANSF_RETIRA.CUSTOREAL_DEV;
      END IF;

    IF NVL(RET_CUSTO_VENDA.CUSTOREP_VENDA,0)       = 0 THEN
        RET_CUSTO_VENDA.CUSTOREP_VENDA := RET_CUSTO_NOTATRANSF_RETIRA.CUSTOREP_DEV;
      END IF;

