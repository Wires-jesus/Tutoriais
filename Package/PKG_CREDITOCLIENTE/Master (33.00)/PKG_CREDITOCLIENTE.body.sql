  /*
  7. "FNC_ATUALIZABAIXACREDITOVAREJO"
  FUNÇÃO CRIADA PARA SER UTILIZADA PELA EQUIPE DO VAREJO QUE POSSUI SUAS PARTICULARIDADES.
  NESSE MOMENTO IRA ATUALIZAR DADOS DO CREDITO REFERENTE A BAIXA FEITA NA FUNÇÃO "FNC_BAIXADECREDITOVAREJO"
  */
  FUNCTION FNC_ATUALIZABAIXACREDITOVAREJO(pfCODIGOCREDITO           IN NUMBER,
                                          pfCODIGOROTINA            IN NUMBER,
                                          pfMATRICULAUSUARIO        IN NUMBER
                                    )
  RETURN BOOLEAN IS
    vsPREST          VARCHAR2(2);
    vfNUMTRANSVENDA  NUMBER(10);
    voCREDITOCLIENTE T_PC_CREDITOCLIENTE;
  BEGIN
      --VALIDACOES DOS PARAMETROS DE ENTRADA
      IF (pfCODIGOCREDITO <= 0) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM CODIGO DE CREDITO VALIDO!');
      END IF;
      --BUSCANDO O CREDITO INFORMADO
      voCREDITOCLIENTE         := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voCREDITOCLIENTE.BUSCARCREDITO(pfCODIGOCREDITO);

      IF NOT (voCREDITOCLIENTE.DTESTORNO IS NULL)  THEN
        RAISE_APPLICATION_ERROR(-20016,'ESSE CREDITO ESTA ESTORNADO. FAVOR INFORMAR UM CREDITO VALIDO!');
      END IF;

      IF (voCREDITOCLIENTE.DTDESCONTO IS NULL)  THEN
        RAISE_APPLICATION_ERROR(-20016,'ESSE CREDITO NAO ESTA BAIXADO. FAVOR INFORMAR UM CREDITO VALIDO BAIXADO!');
      END IF;

      INSERELOG('FNC_ATUALIZABAIXACREDITOVAREJO', '99', 0, 0, 0, 0, 0, 0, 'Inicio', pfCODIGOCREDITO, 0, pfCODIGOROTINA);

      BEGIN --BUSCANDO INFORMACOES DO TITULO PARA ATUALIZAR A PCCRECLI
         SELECT PRESTECF, NUMTRANSVENDA
         INTO vsPREST, vfNUMTRANSVENDA
         FROM PCPRESTECF
         WHERE CODCOB = 'CRED'
           AND NUMPEDECF = voCREDITOCLIENTE.NUMPEDECF
           AND CODFILIAL = voCREDITOCLIENTE.CODFILIALDESC
           AND NUMCHECKOUT = voCREDITOCLIENTE.NUMCAIXA
           AND ROWNUM = 1;
      EXCEPTION WHEN NO_DATA_FOUND THEN
         RAISE_APPLICATION_ERROR(-20016,'NAO FOI POSSIVEL LOCALIZAR O TITULO NA PCPREST!');
      END;

      --ATUALIZANDO O CREDITO!
      voCREDITOCLIENTE.NUMTRANSVENDADESC := vfNUMTRANSVENDA;
      voCREDITOCLIENTE.PRESTRESTCLI      := vsPREST;
      voCREDITOCLIENTE.NUMNOTADESC       := voCREDITOCLIENTE.NUMCUPOM;
      voCREDITOCLIENTE.DTLANC            := voCREDITOCLIENTE.DTLANC;
      voCREDITOCLIENTE.BAIXAR;

      INSERELOG('FNC_ATUALIZABAIXACREDITOVAREJO', '99', 0, 0, 0, 0, 0, 0, 'Fim', pfCODIGOCREDITO, 0, pfCODIGOROTINA);

      RETURN TRUE;
    EXCEPTION WHEN OTHERS THEN
      RAISE_APPLICATION_ERROR(-20016,
                             CHR(13) ||
                             'NAO FOI POSSIVEL REALIZAR O PROCESSO ATUALIZAÇÃO DE BAIXA DE CREDITO DO VAREJO!'
                             ||CHR(13)||'MENSAGEM.: '|| SQLERRM);
      RETURN FALSE;
  END FNC_ATUALIZABAIXACREDITOVAREJO;

