  /*
  9. FNC_CANCELARESTORNOCREDITOVAREJO
  FUNCAO CRIADA PARA FAZER O CANCELAMENTO DO ESTORNO REALIZADO POR BAIXA PARCIAL NO VAREJO.
  EX: CASO TENHA UM CREDITO DE 100,00 E FACA A BAIXA DE 80,00 FICARA 20,00 EM ABERTO.
  SE O CODIGO INFORMADO FOR DO CREDITO DE 100,00 QUE JA ENCONTRA-SE ESTORNADO, O SISTEMA IRA
  APAGAR OS CREDITOS DE 80,00 E 20,00 VOLTANDO 100,00 EM ABERTO.
  */
  FUNCTION FNC_CANCELARESTORCREDITOVAREJO(pfCODIGOCREDITO IN NUMBER, pUltimoCredito IN VARCHAR2,
           pNUMCUPOM IN NUMBER, pfCODIGOROTINA IN NUMBER, pfMATRICULAUSUARIO IN NUMBER, pCODIGODEST IN NUMBER,
           pNUMPEDECF IN NUMBER, pNUMSERIEEQUIP IN VARCHAR2, pNUMCAIXA IN NUMBER)
  RETURN BOOLEAN IS
     vsCODIGOS  VARCHAR2(100);
     V_SQL      VARCHAR2(100);
     V_CONTADOR NUMBER(2);
     V_ROWS_FOUND BOOLEAN := FALSE;
     vGerarRegistros VARCHAR2(1);
  BEGIN
     --VALIDACOES DOS PARAMETROS DE ENTRADA
     IF (pfCODIGOCREDITO <= 0) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM CODIGO DE CREDITO VALIDO!');
     END IF;
     --UTILIZANDO ESSA VARIAVEL PORQUE O CREDITO PODE TER SIDO DESDOBRADO VÁRIAS VEZES E ASSIM PODE CANCELANDO OS OUTROS DESDOBRAMENTOS.
     vGerarRegistros := 'S';
     vsCODIGOS := pfCODIGOCREDITO;
     --LOCALIZANDO OS DESDOBRAMENTOS PARA FAZER A EXCLUSAO.
     IF pUltimoCredito = 'S' THEN
        <<VOLTA_REGISTRO>>
        FOR DADOS IN (SELECT ROWNUM,
                             D.CODIGODEST,
                             D.CODIGOORIG,
                             C.NUMCRED,
                             C.NUMCUPOM
                      FROM PCDESDCRECLI D, PCCRECLI C
                      WHERE D.CODIGODEST = C.CODIGO
                        AND D.CODIGOORIG IN (vsCODIGOS)
                      ORDER BY ROWNUM)
        LOOP
           V_ROWS_FOUND := TRUE;
           --LIMPANDO A LISTA LOGO NO PRIMEIRO REGISTRO
           IF DADOS.ROWNUM = 1 THEN
              vsCODIGOS := '';
           END IF;
           --EXCLUINDO PCCRECLI - CREDITO
           DELETE FROM PCCRECLI C WHERE C.CODIGO = DADOS.CODIGODEST;
           --EXCLUINDO TODOS OS CUPONS QUE NÃO SÃO DO CRÉDITO PRINCIPAL, CASO EXISTA MAIS DE UM CRÉDITO USADO
           DELETE FROM PCCRECLI C WHERE C.CODIGO <> DADOS.CODIGOORIG AND C.NUMCUPOM = pNUMCUPOM AND C.NUMCRED = DADOS.NUMCRED;
           --EXCLUINDO PCDESDCRECLI - RASTREAMENTO DO DESDOBRAMENTO
           DELETE FROM PCDESDCRECLI D WHERE D.CODIGOORIG = DADOS.CODIGOORIG AND D.CODIGODEST = DADOS.CODIGODEST;
           --VERIFICANDO SE O CREDITO FOI REDESDOBRADO
           V_SQL := 'SELECT COUNT(*) CONTADOR FROM PCDESDCRECLI WHERE CODIGOORIG = :CODIGOORIG ';
           EXECUTE IMMEDIATE V_SQL INTO V_CONTADOR USING DADOS.CODIGODEST;

           IF (V_CONTADOR > 0) THEN
              IF (LENGTH(TRIM(vsCODIGOS))) > 0 THEN
                 vsCODIGOS := vsCODIGOS ||','|| DADOS.CODIGODEST;
              ELSE
                 vsCODIGOS := DADOS.CODIGODEST;
              END IF;
           END IF;
        END LOOP;
        -- Caso algum código de crédito tenha ficado sem excluir, irá voltar para o início do <<VOLTA_REGISTRO>>
        IF ((LENGTH(TRIM(vsCODIGOS)) > 0) AND (V_ROWS_FOUND = TRUE)) THEN
           V_ROWS_FOUND := FALSE;
           GOTO VOLTA_REGISTRO;
        END IF;
        -- Caso não exista o registro na tabela PCCRECLI e ainda exista na tabela PCDESDCRECLI, deve excluir o que está na PCDESDCRECLI, pois não existe mais o vínculo entre elas.
        IF ((LENGTH(TRIM(vsCODIGOS)) > 0) AND (V_ROWS_FOUND = FALSE)) THEN
           DELETE FROM PCDESDCRECLI D WHERE D.CODIGOORIG IN (vsCODIGOS) AND NOT EXISTS (SELECT 1 FROM PCCRECLI WHERE CODIGO IN (vsCODIGOS));
        END IF;
     ELSE
        FOR DADOS IN (SELECT ROWNUM,
                             D.CODIGODEST,
                             D.CODIGOORIG,
                             C.NUMCRED,
                             C.NUMCUPOM
                      FROM PCDESDCRECLI D, PCCRECLI C
                      WHERE D.CODIGODEST = C.CODIGO
                        AND D.CODIGOORIG IN (vsCODIGOS)
                      ORDER BY ROWNUM)
        LOOP
           IF vGerarRegistros = 'S' THEN
              vGerarRegistros := 'N';

              INSERIRREGISTROSVAREJO(pCODIGODEST, pfCODIGOROTINA, pfMATRICULAUSUARIO);
           END IF;
        END LOOP;
     END IF;

     IF pUltimoCredito = 'S' THEN
        --REABRINDO O CREDITO
        UPDATE PCCRECLI SET DTDESCONTO        = NULL,
                            CODFILIALDESC     = NULL,
                            NUMNOTADESC       = NULL,
                            SERIEDESC         = NULL,
                            NUMTRANSVENDADESC = NULL,
                            NUMCRED           = NULL,
                            DTESTORNO         = NULL,
                            NUMCUPOM          = NULL
        WHERE CODIGO = pfCODIGOCREDITO
          AND NVL(VALOR,0) > 0;
     END IF;

     -- NECESSÁRIO PARA RETORNAR O CRÉDITO QUE NÃO HOUVE DESDOBRAMENTO.
     IF pNUMPEDECF > 0 AND vGerarRegistros = 'S' THEN
        FOR CRED IN (SELECT CC.CODIGO
                     FROM PCCRECLI CC
                     WHERE CC.NUMCUPOM = pNUMCUPOM
                       AND CC.NUMPEDECF = pNUMPEDECF
                       AND CC.NUMSERIEEQUIP = pNUMSERIEEQUIP
                       AND CC.NUMCAIXA = pNUMCAIXA
                       AND CC.DTDESCONTO IS NOT NULL
                       AND TRUNC(CC.DTDESCONTO) >= (TRUNC(SYSDATE) - 5)
                       AND CC.VALOR > 0
                       AND CC.DTESTORNO IS NULL
                       AND NOT EXISTS (SELECT 1 FROM PCDESDCRECLI DC WHERE DC.CODIGOORIG = CC.CODIGO AND NVL(VALOR,0) > 0))
        LOOP
           --REABRINDO O CREDITO
           UPDATE PCCRECLI SET DTDESCONTO        = NULL,
                               CODFILIALDESC     = NULL,
                               NUMNOTADESC       = NULL,
                               SERIEDESC         = NULL,
                               NUMTRANSVENDADESC = NULL,
                               NUMCRED           = NULL,
                               DTESTORNO         = NULL,
                               NUMCUPOM          = NULL
           WHERE CODIGO = CRED.CODIGO
             AND NVL(VALOR,0) > 0;
        END LOOP;
     END IF;

     RETURN TRUE;
  EXCEPTION WHEN OTHERS THEN
     RAISE_APPLICATION_ERROR(-20016,
                             CHR(13) ||
                             'NAO FOI POSSIVEL REALIZAR O PROCESSO DE CANCELAMENTO DE ESTORNO DE CREDITO!'
                             ||CHR(13)||'MENSAGEM.: '|| SQLERRM);
     RETURN FALSE;
  END FNC_CANCELARESTORCREDITOVAREJO;

