  /*
  8. "FNC_ESTORNARBAIXACREDITOVAREJO"
  FUNÇÃO CRIADA PARA SER UTILIZADA PELA EQUIPE DO VAREJO QUE POSSUI SUAS PARTICULARIDADES.
  FARA O ESTORNO DO CREDITO BAIXADO PELO VAREJO FAZENDO CONFORME CRITERIOS PASSADOS PELO VAREJO
  */
FUNCTION FNC_ESTORNARBAIXACREDITOVAREJO(pfNUMPEDECF               IN NUMBER,
                                          psNUMSERIEEQUIP           IN VARCHAR2,
                                          pfNUMCAIXA                IN NUMBER,
                                          pfNUMCUPOM                IN NUMBER,
                                          psHISTORICO               IN VARCHAR2,
                                          pfCODIGOROTINA            IN NUMBER,
                                          pfMATRICULAUSUARIO        IN NUMBER,                                          
                                          psCODIGOSCREDITOGERADO    IN OUT VARCHAR2,
                                          psCAIXAVENDAABERTA        IN VARCHAR2 DEFAULT 'N')
  RETURN VARCHAR2 IS
     vnQtCreditos NUMBER;
     vsCODIGOSCREDITOGERADO varchar2(4000);
     vCODIGOESTORNO PCCRECLI.CODIGO%TYPE;
     vUltimoCredito VARCHAR2(1);
     vCancelouPrimeiroCredito VARCHAR2(1);
     vCodigoCredito PCCRECLI.CODIGO%TYPE;
     vExisteDesdobramento NUMBER(10);
  BEGIN
     --VALIDACOES DOS PARAMETROS DE ENTRADA
     IF (pfNUMPEDECF <= 0) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM NUMERO DE PEDIDO ECF VALIDO!');
     END IF;

     INSERELOG('FNC_ESTORNARBAIXACREDITOVAREJO', '99', 0, 0, pfNUMCUPOM, 0, 0, 0, 'Inicio', 0, 0, pfCODIGOROTINA);

     IF (TRIM(psNUMSERIEEQUIP) = '') OR (psNUMSERIEEQUIP IS NULL) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM NUMERO DE SERIE DE EQUIPAMENTO VALIDO!');
     END IF;

     IF (pfNUMCAIXA <= 0) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM NUMERO DE CAIXA VALIDO!');
     END IF;
     
     vCancelouPrimeiroCredito := 'S';
     vnQtCreditos := 0;
     
     -- BUSCANDO CREDITOS UTILIZADOS NA VENDA
     FOR CREDITOS IN (SELECT DISTINCT D.CODIGOORIG CODIGO,
                             (SELECT MAX(CL.CODIGODEST)
                              FROM PCDESDCRECLI CL
                              WHERE CL.CODIGOORIG = C.CODIGO) CODIGODEST,
                             (SELECT MIN(CL.CODIGODEST)
                              FROM PCDESDCRECLI CL
                              WHERE CL.CODIGOORIG = C.CODIGO) CODIGODESTPRI
                      FROM PCDESDCRECLI D,
                           PCCRECLI C
                      WHERE D.CODIGOORIG = C.CODIGO
                        AND C.NUMCUPOM = pfNUMCUPOM
                        AND C.NUMPEDECF = PFNUMPEDECF
                        AND C.NUMSERIEEQUIP = PSNUMSERIEEQUIP
                        AND C.NUMCAIXA = PFNUMCAIXA
                        AND C.DTDESCONTO IS NOT NULL
                        AND TRUNC(C.DTDESCONTO) >= (TRUNC(SYSDATE) - 5)
                        AND C.VALOR > 0
                        AND C.DTESTORNO IS NULL
                      ORDER BY D.CODIGOORIG)
     LOOP
        vCancelouPrimeiroCredito := 'N';

        BEGIN
           vUltimoCredito := 'N';

           SELECT 'S'
           INTO vUltimoCredito
           FROM PCCRECLI CC
           WHERE CC.CODIGO = CREDITOS.CODIGODEST
             AND CC.DTDESCONTO IS NULL;
        EXCEPTION WHEN OTHERS THEN
           vUltimoCredito := 'N';
        END;

        VCODIGOESTORNO := CREDITOS.CODIGO;
        vnQtCreditos := vnQtCreditos + 1;
        -- ESTORNA O CRÉDITO
        IF PKG_CREDITOCLIENTE.FNC_CANCELARESTORCREDITOVAREJO(vCODIGOESTORNO, vUltimoCredito, pfNUMCUPOM, pfCODIGOROTINA, pfMATRICULAUSUARIO, CREDITOS.CODIGODESTPRI, pfNUMPEDECF, psNUMSERIEEQUIP, pfNUMCAIXA) THEN
           IF (vsCODIGOSCREDITOGERADO = '') OR (vsCODIGOSCREDITOGERADO IS NULL) THEN
              vsCODIGOSCREDITOGERADO := vCODIGOESTORNO;
           ELSE
              vsCODIGOSCREDITOGERADO := vsCODIGOSCREDITOGERADO || ',' || vCODIGOESTORNO;
           END IF;
        END IF;
     END LOOP;

     -- NECESSÁRIO PARA RETORNAR O CRÉDITO QUE NÃO HOUVE DESDOBRAMENTO.
     IF vCancelouPrimeiroCredito = 'S' AND PFNUMPEDECF > 0 THEN
        FOR CRED IN (SELECT CC.CODIGO,
                            CC.NUMCUPOM,
                            CC.NUMPEDECF,
                            CC.NUMSERIEEQUIP,
                            CC.NUMCAIXA
                     FROM PCCRECLI CC
                     WHERE CC.NUMCUPOM = pfNUMCUPOM
                       AND CC.DTDESCONTO IS NOT NULL
                       AND CC.NUMPEDECF = PFNUMPEDECF
                       AND CC.NUMSERIEEQUIP = PSNUMSERIEEQUIP
                       AND CC.NUMCAIXA = PFNUMCAIXA
                       AND TRUNC(CC.DTDESCONTO) >= (TRUNC(SYSDATE) - 5)
                       AND CC.VALOR > 0
                       AND CC.DTESTORNO IS NULL
                       AND NOT EXISTS (SELECT 1 FROM PCDESDCRECLI DC WHERE DC.CODIGOORIG = CC.CODIGO))
        LOOP
           vnQtCreditos := vnQtCreditos + 1;
           vCodigoCredito := CRED.CODIGO;
           
           IF (psCAIXAVENDAABERTA = 'S') THEN           
              BEGIN
                SELECT NVL(D.CODIGOORIG, 0) INTO vCodigoCredito
                  FROM pccrecli CC, PCDESDCRECLI D
                 WHERE CC.CODIGO = CRED.CODIGO
                   AND CC.CODIGO = D.CODIGODEST
                   AND EXISTS (SELECT 1
                          FROM PCCRECLI C
                         WHERE C.CODIGO = D.CODIGOORIG
                           AND C.NUMCUPOM = CRED.NUMCUPOM
                           AND C.DTDESCONTO IS NOT NULL
                           AND C.NUMPEDECF = CRED.NUMPEDECF
                           AND C.NUMSERIEEQUIP = CRED.NUMSERIEEQUIP
                           AND C.NUMCAIXA = CRED.NUMCAIXA);
              EXCEPTION WHEN OTHERS THEN
                vCodigoCredito := CRED.CODIGO;
              END;                         
                  
                DELETE FROM PCCRECLI C
                 WHERE C.CODIGO IN
                       (SELECT DESD.CODIGODEST
                          FROM PCDESDCRECLI DESD
                         WHERE DESD.CODIGOORIG = vCodigoCredito);
                
                DELETE FROM PCCRECLI C
                 WHERE C.NUMPEDECF             = CRED.NUMPEDECF
                   AND C.NUMCUPOM              = CRED.NUMCUPOM
                   AND C.NUMSERIEEQUIP         = CRED.NUMSERIEEQUIP
                   AND C.NUMCAIXA              = CRED.NUMCAIXA
                   AND C.VALOR                 < 0;
                
                 DELETE FROM PCDESDCRECLI D
                 WHERE D.CODIGOORIG = vCodigoCredito;
           END IF;  
           
           --REABRINDO O CREDITO
           UPDATE PCCRECLI SET DTDESCONTO        = NULL,
                               CODFILIALDESC     = NULL,
                               NUMNOTADESC       = NULL,
                               SERIEDESC         = NULL,
                               NUMTRANSVENDADESC = NULL,
                               NUMCRED           = NULL,
                               DTESTORNO         = NULL,
                               NUMCUPOM          = NULL,
                               NUMPEDECF         = NULL
           WHERE CODIGO = vCodigoCredito
             AND NVL(VALOR,0) > 0;

           IF (vsCODIGOSCREDITOGERADO = '') OR (vsCODIGOSCREDITOGERADO IS NULL) THEN
              vsCODIGOSCREDITOGERADO := vCodigoCredito;
           ELSE
              vsCODIGOSCREDITOGERADO := vsCODIGOSCREDITOGERADO || ',' || vCodigoCredito;
           END IF;
        END LOOP;
     END IF;

     IF (vnQtCreditos = 0) THEN
        RAISE_APPLICATION_ERROR(-20016,'NÃO FOI POSSÍVEL LOCALIZAR CRÉDITO(S) BAIXADO(S) PARA ESTORNAR!!');
     END IF;
     -- RETORNA A LISTA DE CRÉDITOS ESTORNADOS
     RETURN vsCODIGOSCREDITOGERADO;

     INSERELOG('FNC_ESTORNARBAIXACREDITOVAREJO', '99', 0, 0, pfNUMCUPOM, 0, 0, 0, 'Fim', 0, 0, pfCODIGOROTINA);
  EXCEPTION WHEN OTHERS THEN
     RAISE_APPLICATION_ERROR(-20016, CHR(13) ||
                             'NAO FOI POSSIVEL REALIZAR O PROCESSO DE ESTORNO DE BAIXA DE CREDITO DO VAREJO!'
                             ||CHR(13)||'MENSAGEM.: '|| SQLERRM);
  END FNC_ESTORNARBAIXACREDITOVAREJO;


