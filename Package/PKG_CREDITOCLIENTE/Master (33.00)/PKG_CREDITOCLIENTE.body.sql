  /*
  10. FNC_CHECKOUT_BAIXA_CREDITO
  FUNCAO DESENVOLVIDA PELA EQUIPE DO VAREJO E PEDIDA PARA INCLUIR NA PACKAGE DE CREDITO DE CLIENTE
  */
  FUNCTION FNC_CHECKOUT_BAIXA_CREDITO(codigo             IN NUMBER,
                                      valorBaixar        IN NUMBER,
                                      numPedEcf          IN NUMBER,
                                      numSerieEquip      IN VARCHAR2,
                                      numCaixa           IN NUMBER,
                                      numCupom           IN NUMBER,
                                      matriculaOperador  IN NUMBER,
                                      filial             IN VARCHAR2,
                                      psHISTORICO        IN VARCHAR2
  )
  RETURN VARCHAR2  IS
    v_retorno            LISTA_PC_CREDITOCLIENTE;
    v_retorno_string     VARCHAR2(4000);
    v_codigos_retornados  VARCHAR2(4000);
    handle_exception EXCEPTION;
    pragma exception_init(handle_exception,-20016);
  BEGIN
    v_retorno := pkg_creditocliente.fnc_baixadecreditovarejo(codigo,
                                                             valorBaixar,
                                                             numPedEcf,
                                                             numSerieEquip,
                                                             numCaixa,
                                                             numCupom,
                                                             psHISTORICO,
                                                             2075,
                                                             matriculaOperador,
                                                             filial,
                                                             v_codigos_retornados);
    IF v_codigos_retornados IS NOT NULL THEN
      v_retorno_string := v_codigos_retornados;
    ELSE
      v_retorno_string := 'NAO FORAM GERADOS NOVOS CREDITOS';
    END IF;

    RETURN v_retorno_string;
  EXCEPTION WHEN handle_exception THEN
        RETURN 'ERRO AO BAIXAR CREDITO - CODIGO: '|| codigo || ' Motivo: ' || SUBSTR(SQLERRM, 1, 200);
  END FNC_CHECKOUT_BAIXA_CREDITO;


FUNCTION FNC_CANCELARCREDITOCLIENTE(psFILIAL IN VARCHAR2, 
                                    pfNUMTRANSVENDA IN NUMBER, 
                                    pfVALOR IN NUMBER, 
                                    pfCODIGOROTINA IN NUMBER, 
                                    pfMATRICULAUSUARIO IN NUMBER, 
                                    pfNUMTRANSENT IN NUMBER DEFAULT 0)
RETURN NUMBER IS
   vfNUMCRED NUMBER(10);
   voCREDITOCLIENTE T_PC_CREDITOCLIENTE;
   vMENSAGEM VARCHAR2(1);
BEGIN
   --CRIANDO OBJETO DE PERSISTENCIA COM PCCRECLI
   voCREDITOCLIENTE := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
   --OBTENDO O "NUMCRED" CODIGO IDENTIFICADOR PARA RELACIONAMENTO DE REGISTROS NA BAIXA
   IF NVL(voCREDITOCLIENTE.NUMCRED,0) > 0 THEN
      vfNUMCRED:= voCREDITOCLIENTE.NUMCRED;
   ELSE
      SELECT DFSEQ_PCCRECLI_NUMCRED.NEXTVAL INTO vfNUMCRED FROM DUAL;
   END IF;
   --inicio
   --BUSCANDO OS CREDITOS DO CLIENTE INFORMADO
   FOR LISTACREDITOS IN (SELECT C.CODCLI,
                                C.DTLANC,
                                C.CODFILIAL,
                                C.ORIGEM,
                                C.NUMNOTA,
                                NVL(C.NUMTRANSVENDA, 0) NUMTRANSVENDA,
                                C.NUMTRANSENTDEVCLI,
                                C.VALOR,
                                NVL(C.NUMTRANS, 0) NUMTRANS,
                                NVL(C.NUMLANC, 0) NUMLANC,
                                C.SERIE,
                                C.HISTORICO,
                                C.NUMPED,
                                C.DTEMISSAOTITULO,
                                C.CODIGO,
                                C.CODPLPAG,
                                C.SITUACAO
                         FROM PCCRECLI C
                         WHERE C.DTDESCONTO IS NULL
                           AND C.DTESTORNO IS NULL
                           AND ((PARAMFILIAL.OBTERCOMOVARCHAR2('USACREDCLITODASFILIAIS') = 'S') OR (C.CODFILIAL = psFILIAL))
                           AND NVL(C.NUMTRANSVENDA,0) = pfNUMTRANSVENDA
                           AND NVL(C.NUMTRANSENTDEVCLI,0) = pfNUMTRANSENT
                         ORDER BY C.VALOR)
   LOOP
      vMENSAGEM := 'S';
      IF LISTACREDITOS.VALOR < pfVALOR THEN
         vMENSAGEM := 'N';
         
         RAISE_APPLICATION_ERROR(-20016, CHR(13) ||
                                 'NAO FOI POSSIVEL REALIZAR O PROCESSO DE CANCELAMENTO DO CRÉDITO DO CLIENTE!'||CHR(13)||
                                 '- VALOR INFORMADO É MAIOR QUE O VALOR DO CRÉDITO DO CLIENTE:'||CHR(13)||
                                 '  VALOR DO CRÉDITO = '|| LISTACREDITOS.VALOR|| ', VALOR INFORMADO PARA A FUNÇÃO = '||pfVALOR||CHR(13)||
                                 'MENSAGEM.: '|| SQLERRM);
      END IF;
      --ATRIBUO UMA VEZ VALOR AO OBJETO. E FAÇO AS MODIFICACOES COM BASE NA OPERACAO QUE DESEJAR.
      voCREDITOCLIENTE.LIMPAR;
      voCREDITOCLIENTE.DTLANC            := LISTACREDITOS.DTLANC;
      voCREDITOCLIENTE.DTEMISSAOTITULO   := LISTACREDITOS.DTEMISSAOTITULO;
      voCREDITOCLIENTE.CODIGO            := LISTACREDITOS.CODIGO;
      voCREDITOCLIENTE.CODCLI            := LISTACREDITOS.CODCLI;
      voCREDITOCLIENTE.CODFILIAL         := LISTACREDITOS.CODFILIAL;
      voCREDITOCLIENTE.CODFILIALDESC     := LISTACREDITOS.CODFILIAL;
      voCREDITOCLIENTE.NUMTRANS          := LISTACREDITOS.NUMTRANS;
      voCREDITOCLIENTE.NUMLANC           := LISTACREDITOS.NUMLANC;
      voCREDITOCLIENTE.NUMNOTA           := LISTACREDITOS.NUMNOTA;
      voCREDITOCLIENTE.NUMNOTADESC       := LISTACREDITOS.NUMNOTA;
      voCREDITOCLIENTE.SERIE             := LISTACREDITOS.SERIE;
      voCREDITOCLIENTE.SERIEDESC         := LISTACREDITOS.SERIE;
      voCREDITOCLIENTE.NUMTRANSVENDADESC := pfNUMTRANSVENDA;
      voCREDITOCLIENTE.NUMTRANSVENDA     := LISTACREDITOS.NUMTRANSVENDA;
      voCREDITOCLIENTE.NUMTRANSENTDEVCLI := LISTACREDITOS.NUMTRANSENTDEVCLI;
      voCREDITOCLIENTE.ORIGEM            := LISTACREDITOS.ORIGEM;
      voCREDITOCLIENTE.NUMPED            := LISTACREDITOS.NUMPED;
      voCREDITOCLIENTE.VALOR             := LISTACREDITOS.VALOR;
      voCREDITOCLIENTE.NUMCRED           := vfNUMCRED;
      voCREDITOCLIENTE.CODPLPAG          := LISTACREDITOS.CODPLPAG;
      voCREDITOCLIENTE.SITUACAO          := LISTACREDITOS.SITUACAO;
      voCREDITOCLIENTE.HISTORICO         := LISTACREDITOS.HISTORICO;
      
       IF LISTACREDITOS.VALOR = pfVALOR THEN
         voCREDITOCLIENTE.DTCANCEL := TRUNC(SYSDATE);
       END IF;
      
      voCREDITOCLIENTE.BAIXAR;

      voCREDITOCLIENTE.ESTORNARTOTAL;
      ---CANCELAMENTO PARCIAL---
      IF LISTACREDITOS.VALOR > pfVALOR THEN
         ---Lançando o valor a ser cancelado
         voCREDITOCLIENTE.NUMTRANS := NULL;
         voCREDITOCLIENTE.VALOR := pfVALOR;
         voCREDITOCLIENTE.CODIGO := NULL;
         voCREDITOCLIENTE.DTLANC := voCREDITOCLIENTE.DTLANC;
         voCREDITOCLIENTE.HISTORICO := voCREDITOCLIENTE.HISTORICO;
         voCREDITOCLIENTE.DTCANCEL := NULL;
         voCREDITOCLIENTE.INSERIR;
         voCREDITOCLIENTE.DTCANCEL := TRUNC(SYSDATE);
         voCREDITOCLIENTE.BAIXAR;
         voCREDITOCLIENTE.ESTORNARTOTAL;
         ---Lançando a diferença em aberto
         voCREDITOCLIENTE.DTDESCONTO := NULL;
         voCREDITOCLIENTE.HORA := NULL;
         voCREDITOCLIENTE.MINUTO := NULL;
         voCREDITOCLIENTE.DTESTORNO := NULL;
         voCREDITOCLIENTE.CODFILIALDESC := NULL;
         voCREDITOCLIENTE.NUMTRANS := NULL;
         voCREDITOCLIENTE.NUMNOTADESC := NULL;
         voCREDITOCLIENTE.SERIEDESC := NULL;
         voCREDITOCLIENTE.NUMTRANSVENDADESC := NULL;
         voCREDITOCLIENTE.VALOR := LISTACREDITOS.VALOR - pfVALOR;
         voCREDITOCLIENTE.CODIGO := NULL;
         voCREDITOCLIENTE.SITUACAO := NULL;
         voCREDITOCLIENTE.DTLANC := voCREDITOCLIENTE.DTLANC;
         voCREDITOCLIENTE.HISTORICO := voCREDITOCLIENTE.HISTORICO;
         voCREDITOCLIENTE.DTCANCEL := NULL;
         voCREDITOCLIENTE.INSERIR;
      END IF;
   END LOOP; --FIM DO LOOP DOS CREDITOS
   --fim
   RETURN voCREDITOCLIENTE.CODIGO;
EXCEPTION WHEN OTHERS THEN
   IF vMENSAGEM = 'S' THEN
      RAISE_APPLICATION_ERROR(-20016, CHR(13) ||
                              'NAO FOI POSSIVEL REALIZAR O PROCESSO DE CANCELAMENTO DO CRÉDITO DO CLIENTE!'||CHR(13)||
                              'MENSAGEM.: '|| SQLERRM);
   END IF;

   RETURN 0;
END FNC_CANCELARCREDITOCLIENTE;

