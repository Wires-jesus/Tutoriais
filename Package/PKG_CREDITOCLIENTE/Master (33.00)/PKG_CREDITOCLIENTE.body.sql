  /*
  6. "FNC_BAIXADECREDITOVAREJO"
  FUNÇÃO CRIADA PARA SER UTILIZADA PELA EQUIPE DO VAREJO QUE POSSUI SUAS PARTICULARIDADES.
  */
  FUNCTION FNC_BAIXADECREDITOVAREJO(pfCODIGOCREDITO           IN NUMBER,
                                    pfVALORBAIXAR             IN NUMBER,
                                    pfNUMPEDECF               IN NUMBER,
                                    psNUMSERIEEQUIP           IN VARCHAR2,
                                    pfNUMCAIXA                IN NUMBER,
                                    pfNUMCUPOM                IN NUMBER,
                                    psHISTORICO               IN VARCHAR2,
                                    pfCODIGOROTINA            IN NUMBER,
                                    pfMATRICULAUSUARIO        IN NUMBER,
                                    psCODIGOFILIALDESCONTO    IN VARCHAR2,
                                    psCODIGOSCREDITOGERADO    IN OUT VARCHAR2
                                    )
  RETURN LISTA_PC_CREDITOCLIENTE IS
    voLISTA LISTA_PC_CREDITOCLIENTE;
    voCREDITOCLIENTE               T_PC_CREDITOCLIENTE;
    voCREDITOCLIENTEESTORNADO      T_PC_CREDITOCLIENTE;
    voCREDITOCLIENTEPARCIALBAIXADO T_PC_CREDITOCLIENTE;
    voCREDITOCLIENTEPARCIALABERTO  T_PC_CREDITOCLIENTE;
    voDESDCREDITO                  T_PC_DESDCREDITOCLIENTE;
    vfNUMCRED                      NUMBER(10);
  BEGIN
    --VALIDACOES DOS PARAMETROS DE ENTRADA
    IF (pfCODIGOCREDITO <= 0) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM CODIGO DE CREDITO VALIDO!');
    END IF;

    IF (pfVALORBAIXAR <= 0) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR O VALOR A SER BAIXADO COM CREDITO!');
    END IF;

    IF (pfNUMPEDECF <= 0) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM NUMERO DE PEDIDO ECF VALIDO!');
    END IF;

    IF (TRIM(psNUMSERIEEQUIP) = '') OR (psNUMSERIEEQUIP IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM NUMERO DE SERIE DE EQUIPAMENTO VALIDO!');
    END IF;

    IF (TRIM(psHISTORICO) = '') OR (psHISTORICO IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR O HISTORICO!');
    END IF;

    IF (TRIM(psCODIGOFILIALDESCONTO) = '') OR (psCODIGOFILIALDESCONTO IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR O CODIGO DA FILIAL DE DESCONTO!');
    END IF;
    --BUSCANDO O CREDITO INFORMADO
    voCREDITOCLIENTE := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
    voCREDITOCLIENTE.BUSCARCREDITO(pfCODIGOCREDITO);

    IF (pfVALORBAIXAR > voCREDITOCLIENTE.VALOR)  THEN
      RAISE_APPLICATION_ERROR(-20016,'O VALOR INFORMADO PARA BAIXA É MAIOR QUE O VALOR DO CREDITO!');
    END IF;

    IF NOT (voCREDITOCLIENTE.DTESTORNO IS NULL)  THEN
      RAISE_APPLICATION_ERROR(-20016,'O CREDITO INFORMADO JÁ ENCONTRA-SE ESTORNADO!');
    END IF;

    IF NOT (voCREDITOCLIENTE.DTDESCONTO IS NULL)  THEN
      RAISE_APPLICATION_ERROR(-20016,'O CREDITO INFORMADO JÁ ENCONTRA-SE BAIXADO!');
    END IF;

    IF (voCREDITOCLIENTE.SITUACAO = 'P') THEN
      RAISE_APPLICATION_ERROR(-20016,'O CREDITO INFORMADO ESTA PENDENTE, FAVOR LIBERAR CRÉDITO NA ROTINA 603!');
    END IF;
    --OBTENDO O "NUMCRED" CODIGO IDENTIFICADOR PARA RELACIONAMENTO DE REGISTROS NA BAIXA
    IF NVL(voCREDITOCLIENTE.NUMCRED,0) > 0 THEN
     vfNUMCRED:= voCREDITOCLIENTE.NUMCRED;
    ELSE
     SELECT DFSEQ_PCCRECLI_NUMCRED.NEXTVAL INTO vfNUMCRED FROM DUAL;
    END IF;
    --CRIANDO A LISTA COM OS CREDITOS PARA RETORNO
    voLISTA := LISTA_PC_CREDITOCLIENTE();

    INSERELOG('FNC_BAIXADECREDITOVAREJO', '99', 0, 0, 0, 0, 0, pfVALORBAIXAR, 'Iniciou', vfNUMCRED, 0, pfCODIGOROTINA);
    --INICIANDO PROCESSO DE BAIXA DO CREDITO
    --1. BAIXANDO O CREDITO INTEGRAL
    IF (pfVALORBAIXAR = voCREDITOCLIENTE.VALOR) THEN
      voCREDITOCLIENTE.NUMCRED       := vfNUMCRED;
      voCREDITOCLIENTE.NUMPEDECF     := pfNUMPEDECF;
      voCREDITOCLIENTE.NUMSERIEEQUIP := psNUMSERIEEQUIP;
      voCREDITOCLIENTE.NUMCAIXA      := pfNUMCAIXA;
      voCREDITOCLIENTE.NUMCUPOM      := pfNUMCUPOM;
      voCREDITOCLIENTE.HISTORICO     := psHISTORICO;
      voCREDITOCLIENTE.CODFILIALDESC := psCODIGOFILIALDESCONTO;
      voCREDITOCLIENTE.CODFUNC       := pfMATRICULAUSUARIO;
      voCREDITOCLIENTE.CODROTINA     := pfCODIGOROTINA;
      voCREDITOCLIENTE.CODFUNCCX     := pfMATRICULAUSUARIO;
      voCREDITOCLIENTE.DTLANC        := voCREDITOCLIENTE.DTLANC;
      voCREDITOCLIENTE.BAIXAR;

      INSERELOG('FNC_BAIXADECREDITOVAREJO', '99', 0, 0, 0, 0, voCREDITOCLIENTE.VALOR, pfVALORBAIXAR, 'BaixaTotal', vfNUMCRED, 0, pfCODIGOROTINA);

      voLISTA.EXTEND(1);
      voLISTA(1) := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voLISTA(1) := voCREDITOCLIENTE;
      psCODIGOSCREDITOGERADO := voCREDITOCLIENTE.CODIGO;
    --2. BAIXANDO CREDITO A MENOR
    ELSIF (pfVALORBAIXAR < voCREDITOCLIENTE.VALOR) THEN
      --CRIANDO OBJETO DE DESDOBRAMENTO DE CREDITO
      voDESDCREDITO                  := T_PC_DESDCREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voDESDCREDITO.CODIGOORIG       := voCREDITOCLIENTE.CODIGO;
      voDESDCREDITO.DTLANC           := voCREDITOCLIENTE.DTLANC;
      --INICIALIZANDO OBJETOS DE CREDITO
      voCREDITOCLIENTEESTORNADO      := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voCREDITOCLIENTEESTORNADO      := voCREDITOCLIENTE;
      voCREDITOCLIENTEPARCIALBAIXADO := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voCREDITOCLIENTEPARCIALBAIXADO := voCREDITOCLIENTE;
      voCREDITOCLIENTEPARCIALABERTO  := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voCREDITOCLIENTEPARCIALABERTO  := voCREDITOCLIENTE;
      voCREDITOCLIENTEPARCIALABERTO.CODFILIALDESC := NULL;
      --ESTORNANDO O CREDITO
      voCREDITOCLIENTEESTORNADO.NUMCRED       := vfNUMCRED;
      voCREDITOCLIENTEESTORNADO.NUMPEDECF     := pfNUMPEDECF;
      voCREDITOCLIENTEESTORNADO.NUMSERIEEQUIP := psNUMSERIEEQUIP;
      voCREDITOCLIENTEESTORNADO.NUMCAIXA      := pfNUMCAIXA;
      voCREDITOCLIENTEESTORNADO.NUMCUPOM      := pfNUMCUPOM;
      voCREDITOCLIENTEESTORNADO.HISTORICO     := psHISTORICO;
      voCREDITOCLIENTEESTORNADO.CODFILIALDESC := psCODIGOFILIALDESCONTO;
      voCREDITOCLIENTEESTORNADO.CODFUNC       := pfMATRICULAUSUARIO;
      voCREDITOCLIENTEESTORNADO.CODROTINA     := pfCODIGOROTINA;
      voCREDITOCLIENTEESTORNADO.DTLANC        := voCREDITOCLIENTE.DTLANC;
      voCREDITOCLIENTEESTORNADO.ESTORNARTOTAL;
      --BAIXANDO O VALOR INFORMADO
      voCREDITOCLIENTEPARCIALBAIXADO.CODIGO        := voCREDITOCLIENTEPARCIALBAIXADO.PROXIMOCODIGOCREDITO;
      voCREDITOCLIENTEPARCIALBAIXADO.NUMCRED       := vfNUMCRED;
      voCREDITOCLIENTEPARCIALBAIXADO.VALOR         := pfVALORBAIXAR;
      voCREDITOCLIENTEPARCIALBAIXADO.DTDESCONTO    := TRUNC(SYSDATE);
      voCREDITOCLIENTEPARCIALBAIXADO.CODFUNC       := pfMATRICULAUSUARIO;
      voCREDITOCLIENTEPARCIALBAIXADO.HORA          := TO_CHAR(SYSDATE,'HH24');
      voCREDITOCLIENTEPARCIALBAIXADO.MINUTO        := TO_CHAR(SYSDATE,'MI');
      voCREDITOCLIENTEPARCIALBAIXADO.NUMPEDECF     := pfNUMPEDECF;
      voCREDITOCLIENTEPARCIALBAIXADO.NUMSERIEEQUIP := psNUMSERIEEQUIP;
      voCREDITOCLIENTEPARCIALBAIXADO.NUMCAIXA      := pfNUMCAIXA;
      voCREDITOCLIENTEPARCIALBAIXADO.NUMCUPOM      := pfNUMCUPOM;
      voCREDITOCLIENTEPARCIALBAIXADO.HISTORICO     := psHISTORICO;
      voCREDITOCLIENTEPARCIALBAIXADO.CODFILIALDESC := psCODIGOFILIALDESCONTO;
      voCREDITOCLIENTEPARCIALBAIXADO.CODFUNC       := pfMATRICULAUSUARIO;
      voCREDITOCLIENTEPARCIALBAIXADO.CODROTINA     := pfCODIGOROTINA;
      voCREDITOCLIENTEPARCIALBAIXADO.CODFUNCCX     := pfMATRICULAUSUARIO;
      voCREDITOCLIENTEPARCIALBAIXADO.CODPLPAG      := voCREDITOCLIENTE.CODPLPAG;
      voCREDITOCLIENTEPARCIALBAIXADO.DTVENC        := voCREDITOCLIENTE.DTVENC;
      voCREDITOCLIENTEPARCIALBAIXADO.DTLANC        := voCREDITOCLIENTE.DTLANC;
      voCREDITOCLIENTEPARCIALBAIXADO.INSERIR;
      INSERELOG('FNC_BAIXADECREDITOVAREJO', '99', 0, 0, 0, 0, voCREDITOCLIENTE.VALOR, pfVALORBAIXAR, 'BaixaParcial', vfNUMCRED, 0, pfCODIGOROTINA);
      --GRAVANDO DESDOBRAMENTO
      voDESDCREDITO.CODIGODEST                     := voCREDITOCLIENTEPARCIALBAIXADO.CODIGO;
      voDESDCREDITO.DTLANC                         := voCREDITOCLIENTE.DTLANC;
      voDESDCREDITO.INSERIR;
      --EM ABERTO A DIFERENÇA
      voCREDITOCLIENTEPARCIALABERTO.CODIGO        := voCREDITOCLIENTEPARCIALABERTO.PROXIMOCODIGOCREDITO;
      voCREDITOCLIENTEPARCIALABERTO.NUMCRED       := vfNUMCRED;
      voCREDITOCLIENTEPARCIALABERTO.VALOR         := (voCREDITOCLIENTEPARCIALABERTO.VALOR - pfVALORBAIXAR);
      voCREDITOCLIENTEPARCIALABERTO.HISTORICO     := psHISTORICO;
      voCREDITOCLIENTEPARCIALABERTO.CODFUNC       := pfMATRICULAUSUARIO;
      voCREDITOCLIENTEPARCIALABERTO.CODROTINA     := pfCODIGOROTINA;
      voCREDITOCLIENTEPARCIALABERTO.CODPLPAG      := voCREDITOCLIENTE.CODPLPAG;
      voCREDITOCLIENTEPARCIALABERTO.DTVENC        := voCREDITOCLIENTE.DTVENC;
      voCREDITOCLIENTEPARCIALABERTO.PRESTRESTCLI  := voCREDITOCLIENTE.PRESTRESTCLI;
      voCREDITOCLIENTEPARCIALABERTO.DTLANC        := voCREDITOCLIENTE.DTLANC;
      voCREDITOCLIENTEPARCIALABERTO.INSERIR;
      --GRAVANDO DESDOBRAMENTO
      voDESDCREDITO.CODIGODEST := voCREDITOCLIENTEPARCIALABERTO.CODIGO;
      voDESDCREDITO.DTLANC := voCREDITOCLIENTE.DTLANC;
      voDESDCREDITO.INSERIR;

      voLISTA.EXTEND(2);
      voLISTA(1) := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voLISTA(1) := voCREDITOCLIENTEPARCIALBAIXADO;
      psCODIGOSCREDITOGERADO := voCREDITOCLIENTEPARCIALBAIXADO.CODIGO;

      voLISTA(2) := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voLISTA(2) := voCREDITOCLIENTEPARCIALABERTO;
      psCODIGOSCREDITOGERADO := psCODIGOSCREDITOGERADO ||','|| voCREDITOCLIENTEPARCIALABERTO.CODIGO;
    END IF;

    INSERELOG('FNC_BAIXADECREDITOVAREJO', '99', 0, 0, 0, 0, 0, pfVALORBAIXAR, 'Fim', vfNUMCRED, 0, pfCODIGOROTINA);
    RETURN voLISTA; --RETORNANDO A LISTA COM OS CREDITOS GERADOS!
  END FNC_BAIXADECREDITOVAREJO;

