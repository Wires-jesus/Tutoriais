CREATE OR REPLACE PACKAGE BODY PKG_CREDITOCLIENTE AS
/*
PACKAGE CRIADA PARA MANIPULAR OS PROCESSOS DE INCLUSÃO, BAIXA E ESTORNO DE CREDITOS.
DESENVOLVIDO POR PAULO GONÇALVES EM 15-MAI-2016
*/
  /*
  1. "FNC_BAIXARCREDITOCLIENTE"
  FUNÇÃO CRIADA PARA ATENDER A PACKAGE DE FATURAMENTO DA EQUIPE DE VENDAS.
  ONDE É PASSADO A NOTA, VALOR E CLIENTE. AUTOMATICAMENTE É FEITO AS BAIXAS DOS CREDITOS EM ABERTO.
  */
  FUNCTION FNC_BAIXARCREDITOCLIENTE(psFILIAL           IN VARCHAR2,
                                    pfCODCLI           IN NUMBER,
                                    pfNUMNOTA          IN NUMBER,
                                    pfNUMTRANSVENDA    IN NUMBER,
                                    pfNUMPED           IN NUMBER,
                                    pfVALORNOTA        IN NUMBER,
                                    pfCODIGOROTINA     IN NUMBER,
                                    pfMATRICULAUSUARIO IN NUMBER
                                   ) RETURN NUMBER IS
      vfVALOR NUMBER(14,2);
      vfNUMCRED NUMBER(10);
      voCREDITOCLIENTE T_PC_CREDITOCLIENTE;
      voDESD T_PC_DESDCREDITOCLIENTE;
    BEGIN
      --VALIDANDO PARAMETROS DE ENTRADA!
      IF ((TRIM(psFILIAL) = '') OR (psFILIAL IS NULL)) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UMA FILIAL');
      END IF;

      IF (pfCODCLI <= 0) OR (pfCODCLI IS NULL) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM CODIGO PARA O CLIENTE');
      END IF;

      IF (pfVALORNOTA <= 0) OR (pfVALORNOTA IS NULL) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM VALOR PARA SER BAIXADO O CREDITO');
      END IF;

      vfVALOR := pfVALORNOTA;
      --CRIANDO OBJETO DE PERSISTENCIA COM PCCRECLI
      voCREDITOCLIENTE := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voDESD           := T_PC_DESDCREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      --OBTENDO O "NUMCRED" CODIGO IDENTIFICADOR PARA RELACIONAMENTO DE REGISTROS NA BAIXA
      IF NVL(voCREDITOCLIENTE.NUMCRED,0) > 0 THEN
       vfNUMCRED:= voCREDITOCLIENTE.NUMCRED;
      ELSE
       SELECT DFSEQ_PCCRECLI_NUMCRED.NEXTVAL INTO vfNUMCRED FROM DUAL;
      END IF;

      INSERELOG('FNC_BAIXARCREDITOCLIENTE', psFILIAL, 0, 0, pfNUMTRANSVENDA, 0, 0, pfVALORNOTA, 'Iniciou', vfNUMCRED, pfNUMPED, pfCODIGOROTINA);

      --BUSCANDO OS CREDITOS DO CLIENTE INFORMADO
      FOR LISTACREDITOS IN ( SELECT C.CODCLI,
                                    C.DTLANC,
                                    C.CODFILIAL,
                                    C.ORIGEM,
                                    C.NUMNOTA,
                                    NVL(C.NUMTRANSVENDA, 0) NUMTRANSVENDA,
                                    C.NUMTRANSENTDEVCLI,
                                    C.VALOR,
                                    NVL(C.NUMTRANS, 0) NUMTRANS,
                                    NVL(C.NUMLANC, 0) NUMLANC,
                                    C.SERIE,
                                    C.HISTORICO,
                                    C.NUMPED,
                                    C.DTEMISSAOTITULO,
                                    C.CODIGO,
                                    C.CODPLPAG,
                                    C.NUMERARIO,
                                    C.CODMOVIMENTO
                             FROM PCCRECLI C
                             WHERE C.CODCLI =  pfCODCLI
                             AND   C.DTDESCONTO IS NULL
                             AND   C.DTESTORNO IS NULL
                             AND   C.SITUACAO IS NULL
                             AND   ((PARAMFILIAL.OBTERCOMOVARCHAR2('USACREDCLITODASFILIAIS') = 'S') OR (C.CODFILIAL = psFILIAL))
                             AND   NVL(C.NUMPED,0) = DECODE(NVL(pfNUMPED, 0), 0, 0, NVL(pfNUMPED, 0))
                             ORDER BY C.VALOR)
      LOOP
         --ATRIBUO UMA VEZ VALOR AO OBJETO. E FAÇO AS MODIFICACOES COM BASE NA OPERACAO QUE DESEJAR.
         voCREDITOCLIENTE.LIMPAR;
         voCREDITOCLIENTE.DTLANC            := LISTACREDITOS.DTLANC;
         voCREDITOCLIENTE.DTEMISSAOTITULO   := LISTACREDITOS.DTEMISSAOTITULO;
         voCREDITOCLIENTE.CODIGO            := LISTACREDITOS.CODIGO;
         voCREDITOCLIENTE.CODCLI            := LISTACREDITOS.CODCLI;
         voCREDITOCLIENTE.CODFILIAL         := LISTACREDITOS.CODFILIAL;
         voCREDITOCLIENTE.CODFILIALDESC     := LISTACREDITOS.CODFILIAL;
         voCREDITOCLIENTE.NUMTRANS          := LISTACREDITOS.NUMTRANS;
         voCREDITOCLIENTE.NUMLANC           := LISTACREDITOS.NUMLANC;
         voCREDITOCLIENTE.NUMNOTA           := LISTACREDITOS.NUMNOTA;
         voCREDITOCLIENTE.NUMNOTADESC       := pfNUMNOTA;
         voCREDITOCLIENTE.SERIE             := LISTACREDITOS.SERIE;
         voCREDITOCLIENTE.SERIEDESC         := LISTACREDITOS.SERIE;
         voCREDITOCLIENTE.NUMTRANSVENDADESC := pfNUMTRANSVENDA;
         voCREDITOCLIENTE.NUMTRANSVENDA     := LISTACREDITOS.NUMTRANSVENDA;
         voCREDITOCLIENTE.NUMTRANSENTDEVCLI := LISTACREDITOS.NUMTRANSENTDEVCLI;
         voCREDITOCLIENTE.ORIGEM            := LISTACREDITOS.ORIGEM;
         voCREDITOCLIENTE.NUMPED            := LISTACREDITOS.NUMPED;
         voCREDITOCLIENTE.VALOR             := LISTACREDITOS.VALOR;
         voCREDITOCLIENTE.NUMCRED           := vfNUMCRED;
         voCREDITOCLIENTE.CODPLPAG          := LISTACREDITOS.CODPLPAG;
         voCREDITOCLIENTE.NUMERARIO         := LISTACREDITOS.NUMERARIO;
         voCREDITOCLIENTE.CODMOVIMENTO      := LISTACREDITOS.CODMOVIMENTO;
        -- SE NÃO TEM MAIS NADA. SAIR DO LAÇO!
        IF (vfVALOR <= 0) THEN
          INSERELOG('FNC_BAIXARCREDITOCLIENTE', psFILIAL, 0, 0, pfNUMTRANSVENDA, 0, vfVALOR, pfVALORNOTA, 'Saiu', vfNUMCRED, pfNUMPED, pfCODIGOROTINA);
          EXIT;
        -- SE O VALOR DA NOTA É IGUAL AO VALOR DO CREDITO. BAIXAR APENAS O CREDITO.
        ELSIF ((LISTACREDITOS.VALOR = vfVALOR) OR (LISTACREDITOS.VALOR < vfVALOR))  THEN
          INSERELOG('FNC_BAIXARCREDITOCLIENTE', psFILIAL, 0, 0, pfNUMTRANSVENDA, LISTACREDITOS.VALOR, vfVALOR, pfVALORNOTA, 'BaixaTotal Codigo: '||LISTACREDITOS.CODIGO, vfNUMCRED, pfNUMPED, pfCODIGOROTINA);
          voCREDITOCLIENTE.HISTORICO         := SUBSTR(LISTACREDITOS.HISTORICO || '-' || 'BX NF.: '|| pfNUMNOTA || ' - NUMTRANSVENDA.:  ' ||pfNUMTRANSVENDA, 1, 100);
          voCREDITOCLIENTE.BAIXAR;
          vfVALOR                            := vfVALOR - LISTACREDITOS.VALOR;
        -- O CREDITO É MAIOR QUE O VALOR DA NOTA
        ELSIF (LISTACREDITOS.VALOR > vfVALOR)  THEN
          INSERELOG('FNC_BAIXARCREDITOCLIENTE', psFILIAL, 0, 0, pfNUMTRANSVENDA, vfVALOR, vfVALOR, pfVALORNOTA, 'BaixaParcial Codigo: '||LISTACREDITOS.CODIGO, vfNUMCRED, pfNUMPED, pfCODIGOROTINA);
          --ESTORNAR O ORIGINAL
          voCREDITOCLIENTE.HISTORICO         := SUBSTR(LISTACREDITOS.HISTORICO || '-' || 'DESD BX NF.: '|| pfNUMNOTA || ' - NUMTRANSVENDA.:  ' ||pfNUMTRANSVENDA, 1, 100);
          voCREDITOCLIENTE.ESTORNARTOTAL;
          --GERAR CREDITO QUITADO DO VALOR DA NOTA
          voCREDITOCLIENTE.CODIGO            := voCREDITOCLIENTE.PROXIMOCODIGOCREDITO;
          voCREDITOCLIENTE.HISTORICO         := SUBSTR(LISTACREDITOS.HISTORICO || '-' || 'BX PARC NF.: '|| pfNUMNOTA || ' - NUMTRANSVENDA.:  ' ||pfNUMTRANSVENDA, 1, 100);
          voCREDITOCLIENTE.DTESTORNO         := NULL;
          voCREDITOCLIENTE.DTDESCONTO        := TRUNC(SYSDATE);
          voCREDITOCLIENTE.VALOR             := vfVALOR;
          voCREDITOCLIENTE.INSERIR;
          voDESD.CODIGOORIG                  := LISTACREDITOS.CODIGO;
          voDESD.CODIGODEST                  := voCREDITOCLIENTE.CODIGO;
          voDESD.INSERIR;
          --GERAR CREDITO EM ABERTO COM A DIFERENCA
          voCREDITOCLIENTE.CODIGO            := voCREDITOCLIENTE.PROXIMOCODIGOCREDITO;
          voCREDITOCLIENTE.HISTORICO         := SUBSTR(LISTACREDITOS.HISTORICO || '-' || 'DIF BX PARC NF.: '|| pfNUMNOTA || ' - NUMTRANSVENDA.:  ' ||pfNUMTRANSVENDA, 1, 100);
          voCREDITOCLIENTE.CODFILIALDESC     := NULL;
          voCREDITOCLIENTE.DTESTORNO         := NULL;
          voCREDITOCLIENTE.DTDESCONTO        := NULL;
          voCREDITOCLIENTE.NUMNOTADESC       := NULL;
          voCREDITOCLIENTE.NUMTRANSVENDADESC := NULL;
          voCREDITOCLIENTE.SERIEDESC         := NULL;
          voCREDITOCLIENTE.NUMPED            := NULL;
          voCREDITOCLIENTE.VALOR             := LISTACREDITOS.VALOR - vfVALOR;
          voCREDITOCLIENTE.DTLANC            := LISTACREDITOS.DTLANC;
          voCREDITOCLIENTE.INSERIR;
          voDESD.CODIGOORIG                  := LISTACREDITOS.CODIGO;
          voDESD.CODIGODEST                  := voCREDITOCLIENTE.CODIGO;
          voDESD.DTLANC                      := LISTACREDITOS.DTLANC;
          voDESD.INSERIR;
          vfVALOR                            := vfVALOR - vfVALOR;
        END IF;
      END LOOP; --FIM DO LOOP DOS CREDITOS

      INSERELOG('FNC_BAIXARCREDITOCLIENTE', psFILIAL, 0, 0, pfNUMTRANSVENDA, 0, vfVALOR, pfVALORNOTA, 'Fim', vfNUMCRED, pfNUMPED, pfCODIGOROTINA);

      IF (vfVALOR <= 0) THEN --SE O VALOR FOR NEGATIVO, NÃO DEVE GERAR NOVO TITULO.
         RETURN 0;
      ELSE
         RETURN vfVALOR; --SOBROU VALOR DA NOTA, DEVE SER DESDOBRADO EM TITULO.
      END IF;
   EXCEPTION WHEN OTHERS THEN
     RAISE_APPLICATION_ERROR(-20016,
                             CHR(13) ||
                             'NAO FOI POSSIVEL FAZER A BAIXA AUTOMATICA DE CREDITOS DE CLIENTE!'
                             ||CHR(13)||'MENSAGEM.: '|| SQLERRM);
  END FNC_BAIXARCREDITOCLIENTE;

  ----------------------------------------------------------------------------------------
  /*
  2. "FNC_ESTORNARCREDITOBAIXADO"
  FUNÇÃO CRIADA PARA ATENDER A FUNCAO DE CANCELAMENTO DE NOTA DA EQUIPE DE VENDAS.
  ONDE É PASSADO O NUMTRANSVENDA E AUTOMATICA SERA LOCALIZADO OS CREDITOS BAIXADOS E FEITO O ESTORNO TORNANDO EM ABERTO NOVAMENTE.
  O VALOR RETORNADO É O TOTAL DE CREDITO QUE CONSEGUIU SER ESTORNADO.
  PARA ATENDER A ROTINA 1209:
  **SE TIVER DE ESTORNAR O CREDITO DE UM TITULO ESPECIFICO SERÁ NECESSARIO INFORMAR "pfPRESTACAO" QUE EQUIVALE A BUSCAR PELO TITULO.
  **PARA ESTORNAR AUTOMATICAMENTE A PCPREST BASTA MARCAR O PARAMETRO "pbGERARPCPRESTAUTOMATICAMENTE", LEMBRANDO QUE PRECISA TAMBÉM DO "pfPRESTACAO.
  */
  FUNCTION FNC_ESTORNARCREDITOBAIXADO(pfNUMTRANSVENDA               IN NUMBER,
                                      pfCODIGOROTINA                IN NUMBER,
                                      pfMATRICULAUSUARIO            IN NUMBER,
                                      pfPRESTACAO                   IN VARCHAR2 DEFAULT '0',
                                      pbGERARPCPRESTAUTOMATICAMENTE IN BOOLEAN DEFAULT FALSE

  )
  RETURN NUMBER IS
    vfNUMCRED               NUMBER(10);
    voCREDITOCLIENTE        T_PC_CREDITOCLIENTE;
    voDESD                  T_PC_DESDCREDITOCLIENTE;
    vfTOTALCREDITOESTORNADO NUMBER(14,2);
    voCONTASRECEBER         T_PC_CONTASRECEBER;
  BEGIN

    IF (pfNUMTRANSVENDA <= 0) OR (pfNUMTRANSVENDA IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR O NUMERO DE TRANSACAO DE VENDA (NUMTRANSVENDA) PARA FAZER O ESTORNO DO CREDITO BAIXADO.');
    END IF;

    IF ((pbGERARPCPRESTAUTOMATICAMENTE = TRUE) AND ((pfPRESTACAO IS NULL) OR (TRIM(pfPRESTACAO) = ''))) THEN
      RAISE_APPLICATION_ERROR(-20016,'PARA GERAR TITULOS NO CONTAS A RECEBER AUTOMATICAMENTO SERA NECESSARIO INFORMAR A PRESTACAO DO TITULO.');
    END IF;

    vfTOTALCREDITOESTORNADO := 0;
    --CRIANDO OBJETO DE PERSISTENCIA COM PCCRECLI
    voCREDITOCLIENTE := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
    voDESD           := T_PC_DESDCREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
    --OBTENDO O "NUMCRED" CODIGO IDENTIFICADOR PARA RELACIONAMENTO DE REGISTROS NA BAIXA
    IF NVL(voCREDITOCLIENTE.NUMCRED,0) > 0 THEN
     vfNUMCRED:= voCREDITOCLIENTE.NUMCRED;
    ELSE
     SELECT DFSEQ_PCCRECLI_NUMCRED.NEXTVAL INTO vfNUMCRED FROM DUAL;
    END IF;

    INSERELOG('FNC_ESTORNARCREDITOBAIXADO', '99', 0, 0, pfNUMTRANSVENDA, 0, 0, 0, 'Iniciou', vfNUMCRED, 0, pfCODIGOROTINA);
    --BUSCANDO OS CREDITOS BAIXADOS PARA O NUMTRANSVENDA INFORMADO. FAZER O ESTORNO SOMENTE DOS BAIXADOS!
    FOR LISTACREDITOS IN (SELECT C.CODCLI,
                                 C.DTLANC,
                                 C.CODFILIAL,
                                 C.ORIGEM,
                                 C.NUMNOTA,
                                 NVL(C.NUMTRANSVENDA, 0) NUMTRANSVENDA,
                                 C.NUMTRANSENTDEVCLI,
                                 C.VALOR,
                                 NVL(C.NUMTRANS, 0) NUMTRANS,
                                 NVL(C.NUMLANC, 0) NUMLANC,
                                 C.SERIE,
                                 C.HISTORICO,
                                 C.NUMPED,
                                 C.DTEMISSAOTITULO,
                                 C.CODIGO,
                                 C.DTDESCONTO,
                                 C.CODPLPAG,
                                 C.NUMERARIO,
                                 C.CODMOVIMENTO,
                                 C.NUMTRANSVENDADESC,
                                 C.PRESTRESTCLI
                          FROM PCCRECLI C
                          WHERE C.DTDESCONTO IS NOT NULL
                          AND   C.DTESTORNO IS NULL
                          AND   C.NUMTRANSVENDADESC = pfNUMTRANSVENDA
                          AND   ((C.PRESTRESTCLI = NVL(pfPRESTACAO,'')) OR ( pfPRESTACAO = '0' )) )
    LOOP
      --ATRIBUO UMA VEZ VALOR AO OBJETO. E FAÇO AS MODIFICACOES COM BASE NA OPERACAO QUE DESEJAR.
      voCREDITOCLIENTE.LIMPAR;
      voCREDITOCLIENTE.CODIGO            := LISTACREDITOS.CODIGO;
      voCREDITOCLIENTE.DTLANC            := LISTACREDITOS.DTLANC;
      voCREDITOCLIENTE.DTEMISSAOTITULO   := LISTACREDITOS.DTEMISSAOTITULO;
      voCREDITOCLIENTE.CODCLI            := LISTACREDITOS.CODCLI;
      voCREDITOCLIENTE.CODFILIAL         := LISTACREDITOS.CODFILIAL;
      voCREDITOCLIENTE.NUMTRANS          := LISTACREDITOS.NUMTRANS;
      voCREDITOCLIENTE.NUMLANC           := LISTACREDITOS.NUMLANC;
      voCREDITOCLIENTE.NUMNOTA           := LISTACREDITOS.NUMNOTA;
      voCREDITOCLIENTE.SERIE             := LISTACREDITOS.SERIE;
      voCREDITOCLIENTE.NUMTRANSVENDA     := LISTACREDITOS.NUMTRANSVENDA;
      voCREDITOCLIENTE.NUMTRANSVENDADESC := LISTACREDITOS.NUMTRANSVENDADESC;
      voCREDITOCLIENTE.PRESTRESTCLI      := LISTACREDITOS.PRESTRESTCLI;
      voCREDITOCLIENTE.NUMTRANSENTDEVCLI := LISTACREDITOS.NUMTRANSENTDEVCLI;
      voCREDITOCLIENTE.ORIGEM            := LISTACREDITOS.ORIGEM;
      voCREDITOCLIENTE.NUMPED            := LISTACREDITOS.NUMPED;
      voCREDITOCLIENTE.VALOR             := LISTACREDITOS.VALOR;
      voCREDITOCLIENTE.DTDESCONTO        := LISTACREDITOS.DTDESCONTO;
      voCREDITOCLIENTE.NUMCRED           := vfNUMCRED;
      --ESTORNAR O TITULO
      voCREDITOCLIENTE.HISTORICO         := SUBSTR(LISTACREDITOS.HISTORICO || '-' || 'EST BX NF.: '|| LISTACREDITOS.NUMNOTA || ' - NUMTRANSVENDA.:  ' ||pfNUMTRANSVENDA, 1, 100);
      voCREDITOCLIENTE.ESTORNARTOTAL;
      --CRIAR UM NOVO EM ABERTO IGUAL AO ORIGINAL
      voCREDITOCLIENTE.CODIGO            := voCREDITOCLIENTE.PROXIMOCODIGOCREDITO;
      voCREDITOCLIENTE.HISTORICO         := SUBSTR(LISTACREDITOS.HISTORICO || '-' || 'EST BX NF.: '|| LISTACREDITOS.NUMNOTA || ' - NUMTRANSVENDA.:  ' ||pfNUMTRANSVENDA, 1, 100);
      voCREDITOCLIENTE.CODFILIALDESC     := NULL;
      voCREDITOCLIENTE.DTESTORNO         := NULL;
      voCREDITOCLIENTE.DTDESCONTO        := NULL;
      voCREDITOCLIENTE.NUMNOTADESC       := NULL;
      voCREDITOCLIENTE.NUMTRANSVENDADESC := NULL;
      voCREDITOCLIENTE.PRESTRESTCLI      := NULL;
      voCREDITOCLIENTE.SERIEDESC         := NULL;
      voCREDITOCLIENTE.VALOR             := LISTACREDITOS.VALOR;
      voCREDITOCLIENTE.CODPLPAG          := LISTACREDITOS.CODPLPAG;
      voCREDITOCLIENTE.NUMERARIO         := LISTACREDITOS.NUMERARIO;
      voCREDITOCLIENTE.CODMOVIMENTO      := LISTACREDITOS.CODMOVIMENTO;
      voCREDITOCLIENTE.INSERIR;

      voDESD.CODIGOORIG                  := LISTACREDITOS.CODIGO;
      voDESD.CODIGODEST                  := voCREDITOCLIENTE.CODIGO;
      voDESD.DTLANC                      := LISTACREDITOS.DTLANC;
      voDESD.INSERIR;
      vfTOTALCREDITOESTORNADO            := vfTOTALCREDITOESTORNADO + LISTACREDITOS.VALOR;
      INSERELOG('FNC_ESTORNARCREDITOBAIXADO', LISTACREDITOS.CODFILIAL, 0, 0, pfNUMTRANSVENDA, vfTOTALCREDITOESTORNADO, LISTACREDITOS.VALOR, 0, 'Estornou: ' ||pfNUMTRANSVENDA || ' - ' || NVL(pfPRESTACAO,''), vfNUMCRED, 0, pfCODIGOROTINA);
    END LOOP;

    IF vfTOTALCREDITOESTORNADO <= 0 THEN
      RAISE_APPLICATION_ERROR(-20016,'NÃO FOI POSSÍVEL LOCALIZAR O CRÉDITO VINCULADO COM A TRANSAÇAO NUMTRANSVENDA.:  ' || pfNUMTRANSVENDA || ' - PREST: ' || NVL(pfPRESTACAO,''));
    END IF;

    IF pbGERARPCPRESTAUTOMATICAMENTE THEN
      voCONTASRECEBER := T_PC_CONTASRECEBER(pfMATRICULAUSUARIO);
      voCONTASRECEBER.BUSCARREGISTRO(pfNUMTRANSVENDA, pfPRESTACAO);

      IF voCONTASRECEBER.DTPAG IS NULL THEN
        RAISE_APPLICATION_ERROR(-20016,'O TITULO QUE ESTA TENTANDO ESTORNAR ESTA EM ABERTO.');
      END IF;

      -- GERA O ESTORNO E TITULO EM ABERTO
      voCONTASRECEBER.ESTORNAR;
    END IF;

    INSERELOG('FNC_ESTORNARCREDITOBAIXADO', '99', 0, 0, pfNUMTRANSVENDA, 0, 0, 0, 'Fim', vfNUMCRED, 0, pfCODIGOROTINA);

    RETURN vfTOTALCREDITOESTORNADO;
   EXCEPTION WHEN OTHERS THEN
     RAISE_APPLICATION_ERROR(-20016,
                             CHR(13) ||
                             'NAO FOI POSSIVEL FAZER O ESTORNO DE CREDITO BAIXADO!'
                             ||CHR(13)||'MENSAGEM.: '|| SQLERRM);
  END FNC_ESTORNARCREDITOBAIXADO;

  ----------------------------------------------------------------------------------------
  /*
  3. "FNC_TOTALCREDITODISPONIVEL"
  FUNÇÃO RETORNA O TOTAL DE CREDITO DISPONIVEL PARA BAIXA. QUANDO O NUMPED=0 CONSULTA DOS OS CREDITOS, SENÃO APENAS PARA O NUMERO DE PEDIDO INFORMADO.
  */
  FUNCTION FNC_TOTALCREDITODISPONIVEL(psFILIAL           IN VARCHAR2,
                                      pfCODCLI           IN NUMBER,
                                      pfNUMPED           IN NUMBER DEFAULT 0
                                     )
  RETURN NUMBER IS
    vfTOTALCREDITO NUMBER(14,2);
  BEGIN
    --VALIDANDO PARAMETROS DE ENTRADA!
    IF ((TRIM(psFILIAL) = '') OR (psFILIAL IS NULL)) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UMA FILIAL');
    END IF;

    IF (pfCODCLI <= 0) OR (pfCODCLI IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM CODIGO PARA O CLIENTE');
    END IF;

    SELECT NVL(SUM(C.VALOR),0)
    INTO vfTOTALCREDITO
    FROM PCCRECLI C
    WHERE C.CODCLI =  pfCODCLI
    AND   C.DTDESCONTO IS NULL
    AND   C.DTESTORNO IS NULL
    AND   C.SITUACAO IS NULL
    AND   ((PARAMFILIAL.OBTERCOMOVARCHAR2('USACREDCLITODASFILIAIS') = 'S') OR (C.CODFILIAL = psFILIAL))
    AND   ((C.NUMPED = NVL(pfNUMPED,0)) OR ( 0 = NVL(pfNUMPED,0)));

    RETURN vfTOTALCREDITO;
  END FNC_TOTALCREDITODISPONIVEL;

  ----------------------------------------------------------------------------------------
  /*
  4. "FNC_BAIXAR1CREDITONTITULOS"
  PERMITE BAIXA UM CREDITO COM VÁRIOS TITULOS. PARA CADA TITULO PODE SER FEITO BAIXA PARCIAL.
  */
  FUNCTION FNC_BAIXAR1CREDITONTITULOS(pfCODIGOCREDITO           IN NUMBER,
                                      poLISTA_PC_CONTASRECEBER  IN LISTA_PC_CONTASRECEBER,
                                      pfCODIGOROTINA            IN NUMBER,
                                      pfMATRICULAUSUARIO        IN NUMBER
                                      )
  RETURN BOOLEAN IS
    voCREDITOCLIENTE        T_PC_CREDITOCLIENTE;
    voCREDITOCLIENTEAUX     T_PC_CREDITOCLIENTE;
    voCONTASRECEBER         T_PC_CONTASRECEBER;
    voCONTASRECEBERAUX      T_PC_CONTASRECEBER;
    vfNUMCRED               NUMBER(10);
    vfVALORCREDITO          NUMBER(14,2);
    voDESDCREDITO           T_PC_DESDCREDITOCLIENTE;
    voDESDCONTASRECEBER     T_PC_DESDCONTASRECEBER;
    vbBAIXAPARCIAL          BOOLEAN;
    vfTOTALVALORAPROVEITADO NUMBER(14,2);
    voHISTORICOCREDITO      VARCHAR2(100);
  BEGIN
    --VALIDANDO OS PARAMETROS DE ENTRADA!
    IF (pfCODIGOCREDITO <= 0) OR (pfCODIGOCREDITO IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM CODIGO DO CREDITO');
    END IF;

    IF (poLISTA_PC_CONTASRECEBER IS NULL) OR (poLISTA_PC_CONTASRECEBER.COUNT <= 0) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR OS TITULOS QUE DEVERÃO SER BAIXADOS COM O CREDITO');
    END IF;
    --OBTENDO O "NUMCRED" CODIGO IDENTIFICADOR PARA RELACIONAMENTO DE REGISTROS NA BAIXA
    IF NVL(voCREDITOCLIENTE.NUMCRED,0) > 0 THEN
     vfNUMCRED:= voCREDITOCLIENTE.NUMCRED;
    ELSE
     SELECT DFSEQ_PCCRECLI_NUMCRED.NEXTVAL INTO vfNUMCRED FROM DUAL;
    END IF;
    --BUSCANDO O CREDITO INFORMADO
    voCREDITOCLIENTE         := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
    voCREDITOCLIENTE.BUSCARCREDITO(pfCODIGOCREDITO);
    voCREDITOCLIENTE.NUMCRED := vfNUMCRED;
    voHISTORICOCREDITO       := voCREDITOCLIENTE.HISTORICO;

    IF NOT (voCREDITOCLIENTE.DTESTORNO IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'O CREDITO N. '|| pfCODIGOCREDITO ||' INFORMADO NÃO PODE SER UTILIZADO PORQUE O MESMO ENCONTRA-SE ESTORNADO');
    END IF;

    IF NOT (voCREDITOCLIENTE.DTDESCONTO IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'O CREDITO N. '|| pfCODIGOCREDITO ||' INFORMADO NÃO PODE SER UTILIZADO PORQUE O MESMO ENCONTRA-SE BAIXADO');
    END IF;
    --VARIAVEL DE CONTROLE DO CREDITO APROVEITADO
    vfVALORCREDITO := voCREDITOCLIENTE.VALOR;
    --CRIANDO UM OBJETO DE REFERENCIA AO ORIGINAL PARA FAZER AS PERSISTENCIAS NO BANCO.
    voCREDITOCLIENTEAUX := voCREDITOCLIENTE;
    --CRIANDO OBJETO DE DESDOBRAMENTO DE CREDITO
    voDESDCREDITO := T_PC_DESDCREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
    voDESDCREDITO.CODIGOORIG:= voCREDITOCLIENTE.CODIGO;
    --CRIANDO OBJETO DE DESDOBRAMENTO DE TITULO
    voDESDCONTASRECEBER     := T_PC_DESDCONTASRECEBER(pfMATRICULAUSUARIO, pfCODIGOROTINA);
    --INICIALIZANDO VARIAVEIS
    vfTOTALVALORAPROVEITADO := 0;

    INSERELOG('FNC_BAIXAR1CREDITONTITULOS', '99', 0, 0, 0, 0, 0, 0, 'Iniciou CodigoCredito: '||pfCODIGOCREDITO, 0, 0, pfCODIGOROTINA);

    FOR i IN 1 .. poLISTA_PC_CONTASRECEBER.COUNT
    LOOP
      voCONTASRECEBER := T_PC_CONTASRECEBER(1);
      voCONTASRECEBER := poLISTA_PC_CONTASRECEBER(i);
      -- ACUMULANDO O TOTAL APROVEITADO, PARA VER SE O FINAL O VALOR TOTAL APROVEITADO NÃO PODE SER MAIOR QUE O CREDITO!
      vfTOTALVALORAPROVEITADO := vfTOTALVALORAPROVEITADO + voCONTASRECEBER.VALORAPROVEITADOCREDITO;
      --VALIDACOES DOS PARAMETROS DE ENTRADA
      IF (voCONTASRECEBER.NUMTRANSVENDA <= 0) OR (voCONTASRECEBER.NUMTRANSVENDA IS NULL) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR O NUMERO DE TRANSACAO DE VENDA DO TITULO');
      END IF;

      IF (TRIM(voCONTASRECEBER.PREST) = '') OR (voCONTASRECEBER.PREST IS NULL) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR A PRESTACAO DO TITULO');
      END IF;

      IF (voCONTASRECEBER.VALORAPROVEITADOCREDITO <= 0) OR (voCONTASRECEBER.VALORAPROVEITADOCREDITO IS NULL) THEN
        RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR O VALOR APROVEITADO PARA BAIXA.');
      END IF;

      IF (voCONTASRECEBER.VALORAPROVEITADOCREDITO > voCONTASRECEBER.VALOR) THEN
        RAISE_APPLICATION_ERROR(-20016,'O VALOR APROVEITADO NAO PODE SER MAIOR QUE O VALOR DO TITULO.');
      END IF;
      --1. BAIXA TOTAL. VALOR DO TITULO = VALOR APROVEITADO = VALOR DO CREDITO
      IF ((voCONTASRECEBER.VALOR = voCONTASRECEBER.VALORAPROVEITADOCREDITO) AND
          (vfVALORCREDITO = voCONTASRECEBER.VALORAPROVEITADOCREDITO)) THEN
        --BAIXA NO CONTAS A RECEBER
        voCONTASRECEBER.VPAGO := voCONTASRECEBER.VALOR;
        voCONTASRECEBER.BAIXARCOMCREDITO;
        --BAIXA O CREDITO
        voCREDITOCLIENTE.CODFILIALDESC     := voCONTASRECEBER.CODFILIAL;
        voCREDITOCLIENTE.NUMNOTADESC       := voCONTASRECEBER.DUPLIC;
        voCREDITOCLIENTE.NUMTRANSVENDADESC := voCONTASRECEBER.NUMTRANSVENDA;
        voCREDITOCLIENTE.PRESTRESTCLI      := voCONTASRECEBER.PREST;
        voCREDITOCLIENTE.CODFUNCCX         := voCONTASRECEBER.CODFUNCCHECKOUT;
        voCREDITOCLIENTE.BAIXAR;
        vbBAIXAPARCIAL := FALSE;
        INSERELOG('FNC_BAIXAR1CREDITONTITULOS', '99', 0, 0, voCONTASRECEBER.NUMTRANSVENDA, 0, voCONTASRECEBER.VALOR, voCONTASRECEBER.VPAGO, 'BaixaTotal: '||voCONTASRECEBER.NUMTRANSVENDA || ' - ' ||voCONTASRECEBER.PREST, vfNUMCRED, 0, pfCODIGOROTINA);
      --2. BAIXA PARCIAL. VALOR DO TITULO = VALOR APROVEITADO <> VALOR DO CREDITO
      --EXEMPLO: CREDITO DE $1000 - 1 TITULO DE $600 E OUTRO DE $300 -> BAIXAS OS TITLOS COMO "CRED", DESDOBRA O CREDITO DE $1000, GERA 2 BAIXADOS ($600 E $200) E EM ABERTO DE $100
      ELSIF ((voCONTASRECEBER.VALOR = voCONTASRECEBER.VALORAPROVEITADOCREDITO) AND
             (vfVALORCREDITO <> voCONTASRECEBER.VALORAPROVEITADOCREDITO)) THEN
        --BAIXA NO CONTAS A RECEBER
        voCONTASRECEBER.VPAGO := voCONTASRECEBER.VALOR;
        voCONTASRECEBER.BAIXARCOMCREDITO;
        --INSERIR UM CREDITO BAIXADO COM O VALOR APROVEITADO
        voCREDITOCLIENTEAUX.CODIGO            := voCREDITOCLIENTEAUX.PROXIMOCODIGOCREDITO;
        voCREDITOCLIENTEAUX.HISTORICO         := SUBSTR(voHISTORICOCREDITO || '-' || 'BX NF.: '|| voCONTASRECEBER.DUPLIC || ' - NUMTRANSVENDA.:  ' ||voCONTASRECEBER.NUMTRANSVENDA, 1, 100);
        voCREDITOCLIENTEAUX.DTESTORNO         := NULL;
        voCREDITOCLIENTEAUX.DTDESCONTO        := TRUNC(SYSDATE);
        voCREDITOCLIENTEAUX.VALOR             := voCONTASRECEBER.VALORAPROVEITADOCREDITO;
        voCREDITOCLIENTEAUX.NUMNOTADESC       := voCONTASRECEBER.DUPLIC;
        voCREDITOCLIENTEAUX.NUMTRANSVENDADESC := voCONTASRECEBER.NUMTRANSVENDA;
        voCREDITOCLIENTEAUX.PRESTRESTCLI      := voCONTASRECEBER.PREST;
        voCREDITOCLIENTEAUX.CODFUNCCX         := voCONTASRECEBER.CODFUNCCHECKOUT;
        voCREDITOCLIENTEAUX.CODPLPAG          := voCREDITOCLIENTE.CODPLPAG;
        voCREDITOCLIENTEAUX.DTLANC            := voCREDITOCLIENTE.DTLANC;
        voCREDITOCLIENTEAUX.INSERIR;
        --HISTORICO DE DESDOBRAMENTO
        voDESDCREDITO.CODIGODEST              := voCREDITOCLIENTEAUX.CODIGO;
        voDESDCREDITO.DTLANC                  := voCREDITOCLIENTE.DTLANC;
        voDESDCREDITO.INSERIR;

        vbBAIXAPARCIAL := TRUE;
        INSERELOG('FNC_BAIXAR1CREDITONTITULOS', '99', 0, 0, voCONTASRECEBER.NUMTRANSVENDA, 0, voCONTASRECEBER.VALOR, voCONTASRECEBER.VPAGO, 'Baixa Parcial: '||voCONTASRECEBER.NUMTRANSVENDA || ' - ' ||voCONTASRECEBER.PREST, vfNUMCRED, 0, pfCODIGOROTINA);
      --3. BAIXA PARCIAL. VALOR DO TITULO <> VALOR APROVEITADO <> VALOR DO CREDITO
      --EXEMPLO: TITULO $300 APROVEITADO $200 -> DESD $300, BAIXAR $200CRED, ABERTO $100 e PCCRECLI $200.
      ELSIF ((voCONTASRECEBER.VALOR <> voCONTASRECEBER.VALORAPROVEITADOCREDITO) AND
             (vfVALORCREDITO <> voCONTASRECEBER.VALORAPROVEITADOCREDITO)) THEN
        --BUSCANDO TODAS AS INFORMACOES DO TITULO. POIS NESSA OPCAO É NECESSARIO FAZER A INCLUSÃO DO REGISTRO.
        voCONTASRECEBER.BUSCARREGISTRO(voCONTASRECEBER.NUMTRANSVENDA, voCONTASRECEBER.PREST);
        --PREPARANDO O OBJETO DE DESDOBRAMENTO DE CONTAS A PAGAR... PARA PASSAR APENAS A "PRESTDEST" DEPOIS!
        voDESDCONTASRECEBER.NUMTRANSVENDAORIG := voCONTASRECEBER.NUMTRANSVENDA;
        voDESDCONTASRECEBER.PRESTORIG         := voCONTASRECEBER.PREST;
        voDESDCONTASRECEBER.CODCLIORIG        := voCONTASRECEBER.CODCLI;
        voDESDCONTASRECEBER.NUMTRANSVENDADEST := voCONTASRECEBER.NUMTRANSVENDA;
        voDESDCONTASRECEBER.CODCLIDEST        := voCONTASRECEBER.CODCLI;
        --FAZENDO UM CLONE. POIS UM IRA BAIXAR COMO CREDITO, E O OUTRO IRA LANÇAR A DIFERENÇA EM ABERTO.
        voCONTASRECEBERAUX  := voCONTASRECEBER;
        --DESDOBRANDO O TITULO (COLOCANDO ELE COMO "DESD"
        voCONTASRECEBERAUX.DESDOBRAR;
        --BAIXANDO O TITULO COMO CREDITO
        voCONTASRECEBERAUX.CODCOB       := 'CRED';
        voCONTASRECEBERAUX.PREST        := FNC_GETPROXIMAPREST(voCONTASRECEBER.NUMTRANSVENDA);
        voCONTASRECEBERAUX.VALOR        := voCONTASRECEBER.VALORAPROVEITADOCREDITO;
        voCONTASRECEBERAUX.VALORORIG    := voCONTASRECEBER.VALORAPROVEITADOCREDITO;
        voCONTASRECEBERAUX.VPAGO        := voCONTASRECEBER.VALORAPROVEITADOCREDITO;
        voCONTASRECEBERAUX.DTPAG        := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.DTBAIXA      := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.DTDESD       := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.CODFUNCFECHA := pfMATRICULAUSUARIO;
        voCONTASRECEBERAUX.CODFUNCCXMOT := pfMATRICULAUSUARIO;
        voCONTASRECEBERAUX.INSERIR;
        INSERELOG('FNC_BAIXAR1CREDITONTITULOS', '99', 0, 0, voCONTASRECEBER.NUMTRANSVENDA, 0, voCONTASRECEBER.VALOR, voCONTASRECEBER.VPAGO, 'Baixa Parcial: '||voCONTASRECEBER.NUMTRANSVENDA || ' - ' ||voCONTASRECEBER.PREST, vfNUMCRED, 0, pfCODIGOROTINA);
        --GRAVANDO PCDESD
        voDESDCONTASRECEBER.PRESTDEST   := voCONTASRECEBERAUX.PREST;
        voDESDCONTASRECEBER.INSERIR;
        --GERANDO A DIFERENCA EM ABERTO
        voCONTASRECEBERAUX.CODCOB       := voCONTASRECEBER.CODCOB;
        voCONTASRECEBERAUX.PREST        := FNC_GETPROXIMAPREST(voCONTASRECEBER.NUMTRANSVENDA);
        voCONTASRECEBERAUX.VALOR        := (voCONTASRECEBER.VALOR - voCONTASRECEBER.VALORAPROVEITADOCREDITO);
        voCONTASRECEBERAUX.VALORORIG    := (voCONTASRECEBER.VALOR - voCONTASRECEBER.VALORAPROVEITADOCREDITO);
        voCONTASRECEBERAUX.VPAGO        := NULL;
        voCONTASRECEBERAUX.DTPAG        := NULL;
        voCONTASRECEBERAUX.DTBAIXA      := NULL;
        voCONTASRECEBERAUX.DTDESD       := NULL;
        voCONTASRECEBERAUX.CODFUNCFECHA := NULL;
        voCONTASRECEBERAUX.CODFUNCCXMOT := NULL;
        voCONTASRECEBERAUX.INSERIR;
        --GRAVANDO PCDESD
        voDESDCONTASRECEBER.PRESTDEST   := voCONTASRECEBERAUX.PREST;
        voDESDCONTASRECEBER.INSERIR;
        --INSERIR UM CREDITO BAIXADO COM O VALOR APROVEITADO
        voCREDITOCLIENTEAUX.CODIGO            := voCREDITOCLIENTEAUX.PROXIMOCODIGOCREDITO;
        voCREDITOCLIENTEAUX.HISTORICO         := SUBSTR(voHISTORICOCREDITO || '-' || 'BX PARC NF.: '|| voCONTASRECEBER.DUPLIC || ' - NUMTRANSVENDA.:  ' ||voCONTASRECEBER.NUMTRANSVENDA, 1, 100);
        voCREDITOCLIENTEAUX.DTESTORNO         := NULL;
        voCREDITOCLIENTEAUX.DTDESCONTO        := TRUNC(SYSDATE);
        voCREDITOCLIENTEAUX.VALOR             := voCONTASRECEBER.VALORAPROVEITADOCREDITO;
        voCREDITOCLIENTEAUX.NUMNOTADESC       := voCONTASRECEBER.DUPLIC;
        voCREDITOCLIENTEAUX.NUMTRANSVENDADESC := voCONTASRECEBER.NUMTRANSVENDA;
        voCREDITOCLIENTEAUX.PRESTRESTCLI      := voCONTASRECEBER.PREST;
        voCREDITOCLIENTEAUX.CODFUNCCX         := voCONTASRECEBER.CODFUNCCHECKOUT;
        voCREDITOCLIENTEAUX.CODPLPAG          := voCREDITOCLIENTE.CODPLPAG;
        voCREDITOCLIENTEAUX.INSERIR;
        --HISTORICO DE DESDOBRAMENTO
        voDESDCREDITO.CODIGODEST              := voCREDITOCLIENTEAUX.CODIGO;
        voDESDCREDITO.INSERIR;
        vbBAIXAPARCIAL := FALSE;
      --4. BAIXA PARCIAL. VALOR DO TITULO <> VALOR APROVEITADO <> VALOR DO CREDITO
      --EXEMPLO: TITULO $1500 APROVEITADO $750 CREDITO $750 -> DESD $1500, BAIXAR $750CRED, ABERTO $750 e PCCRECLI $750 BAIXADO.
      ELSIF ((voCONTASRECEBER.VALOR <> voCONTASRECEBER.VALORAPROVEITADOCREDITO) AND
             (vfVALORCREDITO = voCONTASRECEBER.VALORAPROVEITADOCREDITO)) THEN
        --BUSCANDO TODAS AS INFORMACOES DO TITULO. POIS NESSA OPCAO É NECESSARIO FAZER A INCLUSÃO DO REGISTRO.
        voCONTASRECEBER.BUSCARREGISTRO(voCONTASRECEBER.NUMTRANSVENDA, voCONTASRECEBER.PREST);
        --PREPARANDO O OBJETO DE DESDOBRAMENTO DE CONTAS A PAGAR... PARA PASSAR APENAS A "PRESTDEST" DEPOIS!
        voDESDCONTASRECEBER.NUMTRANSVENDAORIG := voCONTASRECEBER.NUMTRANSVENDA;
        voDESDCONTASRECEBER.PRESTORIG         := voCONTASRECEBER.PREST;
        voDESDCONTASRECEBER.CODCLIORIG        := voCONTASRECEBER.CODCLI;
        voDESDCONTASRECEBER.NUMTRANSVENDADEST := voCONTASRECEBER.NUMTRANSVENDA;
        voDESDCONTASRECEBER.CODCLIDEST        := voCONTASRECEBER.CODCLI;
        --FAZENDO UM CLONE. POIS UM IRA BAIXAR COMO CREDITO, E O OUTRO IRA LANÇAR A DIFERENÇA EM ABERTO.
        voCONTASRECEBERAUX  := voCONTASRECEBER;
        --DESDOBRANDO O TITULO (COLOCANDO ELE COMO "DESD"
        voCONTASRECEBERAUX.DESDOBRAR;
        --BAIXANDO O TITULO COMO CREDITO
        voCONTASRECEBERAUX.CODCOB       := 'CRED';
        voCONTASRECEBERAUX.PREST        := FNC_GETPROXIMAPREST(voCONTASRECEBER.NUMTRANSVENDA);
        voCONTASRECEBERAUX.VALOR        := voCONTASRECEBER.VALORAPROVEITADOCREDITO;
        voCONTASRECEBERAUX.VALORORIG    := voCONTASRECEBER.VALORAPROVEITADOCREDITO;
        voCONTASRECEBERAUX.VPAGO        := voCONTASRECEBER.VALORAPROVEITADOCREDITO;
        voCONTASRECEBERAUX.DTPAG        := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.DTBAIXA      := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.DTDESD       := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.CODFUNCFECHA := pfMATRICULAUSUARIO;
        voCONTASRECEBERAUX.CODFUNCCXMOT := pfMATRICULAUSUARIO;
        voCONTASRECEBERAUX.INSERIR;
        INSERELOG('FNC_BAIXAR1CREDITONTITULOS', '99', 0, 0, voCONTASRECEBER.NUMTRANSVENDA, 0, voCONTASRECEBER.VALOR, voCONTASRECEBER.VPAGO, 'Baixa Parcial: '||voCONTASRECEBER.NUMTRANSVENDA || ' - ' ||voCONTASRECEBER.PREST, vfNUMCRED, 0, pfCODIGOROTINA);
        --GRAVANDO PCDESD
        voDESDCONTASRECEBER.PRESTDEST   := voCONTASRECEBERAUX.PREST;
        voDESDCONTASRECEBER.INSERIR;
        --GERANDO A DIFERENCA EM ABERTO
        voCONTASRECEBERAUX.CODCOB       := voCONTASRECEBER.CODCOB;
        voCONTASRECEBERAUX.PREST        := FNC_GETPROXIMAPREST(voCONTASRECEBER.NUMTRANSVENDA);
        voCONTASRECEBERAUX.VALOR        := (voCONTASRECEBER.VALOR - voCONTASRECEBER.VALORAPROVEITADOCREDITO);
        voCONTASRECEBERAUX.VALORORIG    := (voCONTASRECEBER.VALOR - voCONTASRECEBER.VALORAPROVEITADOCREDITO);
        voCONTASRECEBERAUX.VPAGO        := NULL;
        voCONTASRECEBERAUX.DTPAG        := NULL;
        voCONTASRECEBERAUX.DTBAIXA      := NULL;
        voCONTASRECEBERAUX.DTDESD       := NULL;
        voCONTASRECEBERAUX.CODFUNCFECHA := NULL;
        voCONTASRECEBERAUX.CODFUNCCXMOT := NULL;
        voCONTASRECEBERAUX.INSERIR;
        --GRAVANDO PCDESD
        voDESDCONTASRECEBER.PRESTDEST   := voCONTASRECEBERAUX.PREST;
        voDESDCONTASRECEBER.INSERIR;
        --BAIXA O CREDITO
        voCREDITOCLIENTE.CODFILIALDESC     := voCONTASRECEBER.CODFILIAL;
        voCREDITOCLIENTE.NUMNOTADESC       := voCONTASRECEBER.DUPLIC;
        voCREDITOCLIENTE.NUMTRANSVENDADESC := voCONTASRECEBER.NUMTRANSVENDA;
        voCREDITOCLIENTE.PRESTRESTCLI      := voCONTASRECEBER.PREST;
        voCREDITOCLIENTE.DTLANC            := voCREDITOCLIENTE.DTLANC;

        voCREDITOCLIENTE.BAIXAR;
        vbBAIXAPARCIAL := FALSE;
      END IF;
      --DECREMENTANDO O VALOR DO CREDITO, PARA SABER O QUANTO AINDA TEM PARA APROVEITAR DO CREDITO.
      vfVALORCREDITO := vfVALORCREDITO - voCONTASRECEBER.VALORAPROVEITADOCREDITO;
    END LOOP;

    --O TOTAL APROVEITADO (TITULOS) NAO PODE SER MAIOR QUE O CREDITO.
    IF vfTOTALVALORAPROVEITADO > voCREDITOCLIENTE.VALOR THEN
      RAISE_APPLICATION_ERROR(-20016,'O VALOR APROVEITADO TOTAL NAO PODE SER MAIOR QUE O VALOR DO CREDITO.' ||CHR(13) ||
                              'VALOR DO CREDITO: '|| TO_CHAR(voCREDITOCLIENTE.VALOR, '9999999999.99') || CHR(13) ||
                              'VALOR TOTAL APROVEITADO: '|| TO_CHAR(vfTOTALVALORAPROVEITADO, '9999999999.99'));
    END IF;
    --GERAR A SOBRA DO CREDITO EM ABERTO.
    IF (vfVALORCREDITO > 0) THEN
      voCREDITOCLIENTEAUX.LIMPAR;
      voCREDITOCLIENTEAUX            := voCREDITOCLIENTE;
      voCREDITOCLIENTEAUX.CODIGO     := voCREDITOCLIENTEAUX.PROXIMOCODIGOCREDITO;
      voCREDITOCLIENTEAUX.VALOR      := vfVALORCREDITO;
      voCREDITOCLIENTEAUX.CODPLPAG   := voCREDITOCLIENTEAUX.CODPLPAG;
      voCREDITOCLIENTEAUX.DTLANC     := voCREDITOCLIENTE.DTLANC;
      voCREDITOCLIENTEAUX.INSERIR;
      --HISTORICO DE DESDOBRAMENTO
      voDESDCREDITO.CODIGODEST       := voCREDITOCLIENTEAUX.CODIGO;
      voDESDCREDITO.DTLANC           := voCREDITOCLIENTE.DTLANC;
      voDESDCREDITO.INSERIR;
    END IF;
    --SE HOUVE BAIXA PARCIAL. FAZER O ESTORNO DO CREDITO ORIGINAL.
    IF (vbBAIXAPARCIAL) THEN
      voCREDITOCLIENTE.HISTORICO     := SUBSTR(voHISTORICOCREDITO || '-' || 'DESD BX NF.: '|| voCONTASRECEBER.DUPLIC || ' - NUMTRANSVENDA.:  ' ||voCONTASRECEBER.NUMTRANSVENDA, 1, 100);
      voCREDITOCLIENTE.DTLANC        := voCREDITOCLIENTE.DTLANC;
      voCREDITOCLIENTE.ESTORNARTOTAL;
    END IF;

    INSERELOG('FNC_BAIXAR1CREDITONTITULOS', '99', 0, 0, 0, 0, 0, vfVALORCREDITO, 'Fim CodigoCredito: '||pfCODIGOCREDITO, 0, 0, pfCODIGOROTINA);

    RETURN TRUE;
   EXCEPTION WHEN OTHERS THEN
     RAISE_APPLICATION_ERROR(-20016,
                             CHR(13) ||
                             'NAO FOI POSSIVEL REALIZAR O PROCESSO BAIXAR 1 CREDITO COM VARIOS TITULOS!'
                             ||CHR(13)||'MENSAGEM.: '|| SQLERRM);
     RETURN FALSE;
  END FNC_BAIXAR1CREDITONTITULOS;

  ----------------------------------------------------------------------------------------
  /*
  5. "FNC_BAIXAR1TITULONCREDITOS"
  PERMITE BAIXA UM TITULO COM VÁRIOS CREDITOS.
  */
  FUNCTION FNC_BAIXAR1TITULONCREDITOS(poCONTARECEBER            IN OUT T_PC_CONTASRECEBER,
                                      poLISTA_PC_CREDITOCLIENTE IN LISTA_PC_CREDITOCLIENTE,
                                      pfCODIGOROTINA            IN NUMBER,
                                      pfMATRICULAUSUARIO        IN NUMBER)
  RETURN BOOLEAN IS
    vfVALORCONTASRECEBER    NUMBER(14,2);
    voCREDITOCLIENTE        T_PC_CREDITOCLIENTE;
    voCREDITOCLIENTEAUX     T_PC_CREDITOCLIENTE;
    voCONTASRECEBERAUX      T_PC_CONTASRECEBER;
    vfNUMCRED               NUMBER(10);
    voDESDCONTASRECEBER     T_PC_DESDCONTASRECEBER;
    voDESDCREDITO           T_PC_DESDCREDITOCLIENTE;
    vbBAIXAPARCIAL          BOOLEAN;
    voHISTORICOCREDITO      VARCHAR2(100);
  BEGIN
    --VALIDACOES DOS PARAMETROS DE ENTRADA
    IF (poCONTARECEBER.NUMTRANSVENDA <= 0) OR (poCONTARECEBER.NUMTRANSVENDA IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR O NUMERO DE TRANSACAO DE VENDA DO TITULO');
    END IF;

    IF (TRIM(poCONTARECEBER.PREST) = '') OR (poCONTARECEBER.PREST IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR A PRESTACAO DO TITULO');
    END IF;

    IF (poLISTA_PC_CREDITOCLIENTE IS NULL) OR (poLISTA_PC_CREDITOCLIENTE.COUNT <= 0) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR OS CREDITOS QUE DEVERAO SER BAIXADOS O TITULO');
    END IF;
    --BUSCANDO TODAS AS INFORMACOES DO TITULO. POIS NESSA OPCAO É NECESSARIO FAZER A INCLUSÃO DO REGISTRO.
    poCONTARECEBER.BUSCARREGISTRO(poCONTARECEBER.NUMTRANSVENDA, poCONTARECEBER.PREST);

    IF NOT (poCONTARECEBER.DTPAG IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'O TITULO INFORMADO JÁ ESTA BAIXADO');
    END IF;

    --INICIALIZANDO VARIAVEIS
    vfVALORCONTASRECEBER                  := poCONTARECEBER.VALOR;
    --FAZENDO UM CLONE. POIS UM IRA BAIXAR COMO CREDITO, E O OUTRO IRA LANÇAR A DIFERENÇA EM ABERTO.
    voCONTASRECEBERAUX                    := T_PC_CONTASRECEBER(pfMATRICULAUSUARIO);
    voCONTASRECEBERAUX                    := poCONTARECEBER;
    --CRIANDO OBJETO DE DESDOBRAMENTO DE CREDITO
    voDESDCREDITO                         := T_PC_DESDCREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
    --CRIANDO OBJETO DE DESDOBRAMENTO DE TITULO
    voDESDCONTASRECEBER                   := T_PC_DESDCONTASRECEBER(pfMATRICULAUSUARIO, pfCODIGOROTINA);
    voDESDCONTASRECEBER.NUMTRANSVENDAORIG := poCONTARECEBER.NUMTRANSVENDA;
    voDESDCONTASRECEBER.PRESTORIG         := poCONTARECEBER.PREST;
    voDESDCONTASRECEBER.CODCLIORIG        := poCONTARECEBER.CODCLI;
    voDESDCONTASRECEBER.NUMTRANSVENDADEST := poCONTARECEBER.NUMTRANSVENDA;
    voDESDCONTASRECEBER.CODCLIDEST        := poCONTARECEBER.CODCLI;
    --OBTENDO O "NUMCRED" CODIGO IDENTIFICADOR PARA RELACIONAMENTO DE REGISTROS NA BAIXA
    IF NVL(voCREDITOCLIENTE.NUMCRED,0) > 0 THEN
     vfNUMCRED:= voCREDITOCLIENTE.NUMCRED;
    ELSE
     SELECT DFSEQ_PCCRECLI_NUMCRED.NEXTVAL INTO vfNUMCRED FROM DUAL;
    END IF;

    INSERELOG('FNC_BAIXAR1TITULONCREDITOS', '99', 0, 0, 0, 0, 0, 0, 'Inicio', vfNUMCRED, 0, pfCODIGOROTINA);
    --PERCORRENDO TODOS OS CREDITOS
    FOR i IN 1 .. poLISTA_PC_CREDITOCLIENTE.COUNT
    LOOP
      --ATRIBUINDO A LISTA AO OBJETO
      voCREDITOCLIENTE    := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voCREDITOCLIENTE    := poLISTA_PC_CREDITOCLIENTE(i);
      voCREDITOCLIENTE.BUSCARCREDITO(poLISTA_PC_CREDITOCLIENTE(i).CODIGO);
      voCREDITOCLIENTE.NUMCRED := vfNUMCRED;
      voCREDITOCLIENTEAUX := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
      voCREDITOCLIENTEAUX := voCREDITOCLIENTE;
      voHISTORICOCREDITO  := voCREDITOCLIENTE.HISTORICO;

      IF NOT (voCREDITOCLIENTEAUX.DTESTORNO IS NULL) THEN
        RAISE_APPLICATION_ERROR(-20016,'O CREDITO N. '|| voCREDITOCLIENTEAUX.CODIGO ||' INFORMADO NÃO PODE SER UTILIZADO PORQUE O MESMO ENCONTRA-SE ESTORNADO');
      END IF;

      IF NOT (voCREDITOCLIENTEAUX.DTDESCONTO IS NULL) THEN
        RAISE_APPLICATION_ERROR(-20016,'O CREDITO N. '|| voCREDITOCLIENTEAUX.CODIGO ||' INFORMADO NÃO PODE SER UTILIZADO PORQUE O MESMO ENCONTRA-SE BAIXADO');
      END IF;

      voDESDCREDITO.CODIGOORIG :=  voCREDITOCLIENTE.CODIGO;
      --VALOR DO TITULO = CREDITO.
      IF (vfVALORCONTASRECEBER = voCREDITOCLIENTE.VALOR) THEN
        --BAIXAR O CREDITO
        voCREDITOCLIENTE.CODFILIALDESC     := poCONTARECEBER.CODFILIAL;
        voCREDITOCLIENTE.NUMNOTADESC       := poCONTARECEBER.DUPLIC;
        voCREDITOCLIENTE.NUMTRANSVENDADESC := poCONTARECEBER.NUMTRANSVENDA;
        voCREDITOCLIENTE.PRESTRESTCLI      := poCONTARECEBER.PREST;
        voCREDITOCLIENTE.DTLANC            := poLISTA_PC_CREDITOCLIENTE(I).DTLANC;
        voCREDITOCLIENTE.BAIXAR;
        --BAIXAR O TITULO
        poCONTARECEBER.VPAGO               := poCONTARECEBER.VALOR;
        poCONTARECEBER.BAIXARCOMCREDITO;

        vbBAIXAPARCIAL                     := FALSE;
        INSERELOG('FNC_BAIXAR1TITULONCREDITOS', '99', 0, 0, poCONTARECEBER.NUMTRANSVENDA, 0, poCONTARECEBER.VALOR, poCONTARECEBER.VPAGO, 'BaixaTotal: '||poCONTARECEBER.NUMTRANSVENDA || ' - ' ||poCONTARECEBER.PREST, vfNUMCRED, 0, pfCODIGOROTINA);
        EXIT;
      --VALOR DO TITULO MAIOR QUE O CREDITO -> $1000 DE TITULO, SENDO $800 DE CREDITO.
      ELSIF (vfVALORCONTASRECEBER > voCREDITOCLIENTE.VALOR) THEN
        --INSERIR A DIFERENÇA DO TITULO COMO CRED BAIXADO (O DESDOBRAMENTO SERA NO FINAL)
        voCONTASRECEBERAUX.CODCOB       := 'CRED';
        voCONTASRECEBERAUX.PREST        := FNC_GETPROXIMAPREST(poCONTARECEBER.NUMTRANSVENDA);
        voCONTASRECEBERAUX.VALOR        := voCREDITOCLIENTE.VALOR;
        voCONTASRECEBERAUX.VALORORIG    := voCREDITOCLIENTE.VALOR;
        voCONTASRECEBERAUX.VPAGO        := voCREDITOCLIENTE.VALOR;
        voCONTASRECEBERAUX.DTPAG        := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.DTBAIXA      := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.DTDESD       := TRUNC(SYSDATE);
        voCONTASRECEBERAUX.CODFUNCFECHA := pfMATRICULAUSUARIO;
        voCONTASRECEBERAUX.CODFUNCCXMOT := pfMATRICULAUSUARIO;
        voCONTASRECEBERAUX.INSERIR;
        INSERELOG('FNC_BAIXAR1TITULONCREDITOS', '99', 0, 0, poCONTARECEBER.NUMTRANSVENDA, 0, poCONTARECEBER.VALOR, poCONTARECEBER.VPAGO, 'BaixaParcial: '||poCONTARECEBER.NUMTRANSVENDA || ' - ' ||poCONTARECEBER.PREST, vfNUMCRED, 0, pfCODIGOROTINA);
        --GRAVANDO PCDESD
        voDESDCONTASRECEBER.PRESTDEST   := voCONTASRECEBERAUX.PREST;
        voDESDCONTASRECEBER.INSERIR;
        --BAIXAR O CREDITO
        voCREDITOCLIENTE.CODFILIALDESC     := poCONTARECEBER.CODFILIAL;
        voCREDITOCLIENTE.NUMNOTADESC       := poCONTARECEBER.DUPLIC;
        voCREDITOCLIENTE.NUMTRANSVENDADESC := poCONTARECEBER.NUMTRANSVENDA;
        voCREDITOCLIENTE.PRESTRESTCLI      := poCONTARECEBER.PREST;
        voCREDITOCLIENTE.DTLANC            := poLISTA_PC_CREDITOCLIENTE(I).DTLANC;
        voCREDITOCLIENTE.BAIXAR;

        vbBAIXAPARCIAL                     := TRUE;
      --VALOR DO TITULO OU O QUE SOBROU MENOR QUE O CREDITO ->  $700 TITULO, $800 CREDITO
      ELSIF (vfVALORCONTASRECEBER < voCREDITOCLIENTE.VALOR) THEN
        --DESDOBRAR O CREDITO
        voCREDITOCLIENTEAUX.HISTORICO          := SUBSTR(voHISTORICOCREDITO || '-' || 'DESD BX NF.: '|| poCONTARECEBER.DUPLIC || ' - NUMTRANSVENDA.:  ' ||poCONTARECEBER.NUMTRANSVENDA, 1, 100);
        voCREDITOCLIENTEAUX.ESTORNARTOTAL;
        --BAIXAR O CREDITO NO VALOR DO CONTAS A RECEBER
        voCREDITOCLIENTEAUX.CODIGO             := voCREDITOCLIENTEAUX.PROXIMOCODIGOCREDITO;
        voCREDITOCLIENTEAUX.DTESTORNO          := NULL;
        voCREDITOCLIENTEAUX.CODFILIALDESC      := poCONTARECEBER.CODFILIAL;
        voCREDITOCLIENTEAUX.HISTORICO          := SUBSTR(voHISTORICOCREDITO || '-' || 'DESD BX NF.: '|| poCONTARECEBER.DUPLIC || ' - NUMTRANSVENDA.:  ' ||poCONTARECEBER.NUMTRANSVENDA, 1, 100);
        voCREDITOCLIENTEAUX.NUMTRANSVENDADESC  := poCONTARECEBER.NUMTRANSVENDA;
        voCREDITOCLIENTEAUX.NUMNOTADESC        := poCONTARECEBER.DUPLIC;
        voCREDITOCLIENTEAUX.VALOR              := vfVALORCONTASRECEBER;
        voCREDITOCLIENTEAUX.DTDESCONTO         := TRUNC(SYSDATE);
        voCREDITOCLIENTEAUX.PRESTRESTCLI       := poCONTARECEBER.PREST;
        voCREDITOCLIENTEAUX.CODPLPAG           := voCREDITOCLIENTE.CODPLPAG;
        voCREDITOCLIENTE.DTLANC                := voCREDITOCLIENTE.DTLANC;
        voCREDITOCLIENTEAUX.INSERIR;
        --HISTORICO DE DESDOBRAMENTO
        voDESDCREDITO.CODIGODEST               := voCREDITOCLIENTEAUX.CODIGO;
        voDESDCREDITO.DTLANC                   := voCREDITOCLIENTE.DTLANC;
        voDESDCREDITO.INSERIR;
        --LANCAR A DIFERENCA EM ABERTO
        voCREDITOCLIENTEAUX.CODIGO             := voCREDITOCLIENTEAUX.PROXIMOCODIGOCREDITO;
        voCREDITOCLIENTEAUX.DTESTORNO          := NULL;
        voCREDITOCLIENTEAUX.CODFILIALDESC      := NULL;
        voCREDITOCLIENTEAUX.DTDESCONTO         := NULL;
        voCREDITOCLIENTEAUX.HISTORICO          := SUBSTR(voHISTORICOCREDITO || '-' || 'DIF BX NF.: '|| poCONTARECEBER.DUPLIC || ' - NUMTRANSVENDA.:  ' ||poCONTARECEBER.NUMTRANSVENDA, 1, 100);
        voCREDITOCLIENTEAUX.NUMTRANSVENDADESC  := NULL;
        voCREDITOCLIENTEAUX.NUMNOTADESC        := NULL;
        voCREDITOCLIENTEAUX.VALOR              := voCREDITOCLIENTE.VALOR - vfVALORCONTASRECEBER;
        voCREDITOCLIENTEAUX.CODPLPAG           := voCREDITOCLIENTE.CODPLPAG;
        voCREDITOCLIENTEAUX.DTLANC             := voCREDITOCLIENTE.DTLANC;
        voCREDITOCLIENTEAUX.INSERIR;
        --HISTORICO DE DESDOBRAMENTO
        voDESDCREDITO.CODIGODEST               := voCREDITOCLIENTEAUX.CODIGO;
        voDESDCREDITO.DTLANC                   := voCREDITOCLIENTE.DTLANC;
        voDESDCREDITO.INSERIR;
        --VALIDACAO PARA O QUE FAZER COM O TITULO
        IF vfVALORCONTASRECEBER = poCONTARECEBER.VALOR THEN --BAIXA O TITULO
          poCONTARECEBER.VPAGO               := poCONTARECEBER.VALOR;
          poCONTARECEBER.BAIXARCOMCREDITO;
          vbBAIXAPARCIAL                     := FALSE;
          INSERELOG('FNC_BAIXAR1TITULONCREDITOS', '99', 0, 0, poCONTARECEBER.NUMTRANSVENDA, 0, poCONTARECEBER.VALOR, poCONTARECEBER.VPAGO, 'BaixaTotal: '||poCONTARECEBER.NUMTRANSVENDA || ' - ' ||poCONTARECEBER.PREST, vfNUMCRED, 0, pfCODIGOROTINA);
        ELSE --INSERE O TITULO
          voCONTASRECEBERAUX.CODCOBORIG   := poCONTARECEBER.CODCOB;
          voCONTASRECEBERAUX.CODCOB       := 'CRED';
          voCONTASRECEBERAUX.PREST        := FNC_GETPROXIMAPREST(poCONTARECEBER.NUMTRANSVENDA);
          voCONTASRECEBERAUX.VALOR        := vfVALORCONTASRECEBER;
          voCONTASRECEBERAUX.VALORORIG    := vfVALORCONTASRECEBER;
          voCONTASRECEBERAUX.VPAGO        := vfVALORCONTASRECEBER;
          voCONTASRECEBERAUX.DTPAG        := TRUNC(SYSDATE);
          voCONTASRECEBERAUX.DTBAIXA      := TRUNC(SYSDATE);
          voCONTASRECEBERAUX.DTDESD       := TRUNC(SYSDATE);
          voCONTASRECEBERAUX.CODFUNCFECHA := pfMATRICULAUSUARIO;
          voCONTASRECEBERAUX.CODFUNCCXMOT := pfMATRICULAUSUARIO;          
          voCONTASRECEBERAUX.INSERIR;
          --GRAVANDO PCDESD
          voDESDCONTASRECEBER.PRESTDEST   := voCONTASRECEBERAUX.PREST;
          voDESDCONTASRECEBER.INSERIR;

          vbBAIXAPARCIAL                     := TRUE;
          INSERELOG('FNC_BAIXAR1TITULONCREDITOS', '99', 0, 0, poCONTARECEBER.NUMTRANSVENDA, 0, poCONTARECEBER.VALOR, poCONTARECEBER.VPAGO, 'BaixaParcial: '||poCONTARECEBER.NUMTRANSVENDA || ' - ' ||poCONTARECEBER.PREST, vfNUMCRED, 0, pfCODIGOROTINA);
        END IF;
      END IF;

      vfVALORCONTASRECEBER := vfVALORCONTASRECEBER - voCREDITOCLIENTE.VALOR;
    END LOOP;

    INSERELOG('FNC_BAIXAR1TITULONCREDITOS', '99', 0, 0, 0, 0, 0, 0, 'Fim', vfNUMCRED, 0, pfCODIGOROTINA);

    --FIM DA FUNCAO
    IF vbBAIXAPARCIAL THEN
      poCONTARECEBER.DESDOBRAR;

      IF vfVALORCONTASRECEBER > 0 THEN
        poCONTARECEBER.VALOR     := vfVALORCONTASRECEBER;
        poCONTARECEBER.VALORORIG := vfVALORCONTASRECEBER;
        poCONTARECEBER.PREST     := FNC_GETPROXIMAPREST(poCONTARECEBER.NUMTRANSVENDA);
        poCONTARECEBER.INSERIR;
      END IF;
    END IF;
    RETURN TRUE;
  EXCEPTION WHEN OTHERS THEN
      RAISE_APPLICATION_ERROR(-20016,
                             CHR(13) ||
                             'NAO FOI POSSIVEL REALIZAR O PROCESSO BAIXAR 1 TITULO COM VARIOS CREDITOS!'
                             ||CHR(13)||'MENSAGEM.: '|| SQLERRM);
      RETURN FALSE;
  END FNC_BAIXAR1TITULONCREDITOS;

