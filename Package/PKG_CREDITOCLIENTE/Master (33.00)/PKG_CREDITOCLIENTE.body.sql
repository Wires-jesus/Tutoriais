 /*
 11. FNC_CHECKOUT_BAIXA_CREDITO
 FUNCAO DESENVOLVIDA PELA EQUIPE DO VAREJO E PEDIDA PARA INCLUIR NA PACKAGE DE CREDITO DE CLIENTE
 */
PROCEDURE INSERIRREGISTROSVAREJO(pCODIGO IN NUMBER, pfCODIGOROTINA IN NUMBER, pfMATRICULAUSUARIO IN NUMBER)
IS
   voCREDITOCLIENTE T_PC_CREDITOCLIENTE;
   voDESD T_PC_DESDCREDITOCLIENTE;
   vNUMCRED NUMBER(10);
   vEXISTEREGISTRO VARCHAR2(1);
BEGIN
   voCREDITOCLIENTE := T_PC_CREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
   voDESD := T_PC_DESDCREDITOCLIENTE(pfMATRICULAUSUARIO, pfCODIGOROTINA);
   vEXISTEREGISTRO := 'N';

   BEGIN
      voCREDITOCLIENTE.LIMPAR;

      SELECT CR.CODCLI, --01
             CR.DTLANC, --02
             CR.NUMCUPOM, --03
             CR.NUMCRED, --04
             CR.VALOR, --05
             NVL(CR.DTDESCONTO,SYSDATE), --06
             CR.NUMSERIEEQUIP, --08
             CR.CODROTINA, --09
             CR.CODFILIAL, --10
             CR.NUMNOTA, --11
             CR.NUMTRANSVENDA, --12
             CR.SERIE, --13
             CR.CODFILIALDESC, --14
             CR.NUMNOTADESC, --15
             CR.SERIEDESC, --16
             CR.CODFUNC, --17
             CR.HORA, --18
             CR.MINUTO, --19
             CR.ORIGEM, --20
             CR.NUMTRANSVENDADESC, --21
             CR.NUMTRANSENTDEVCLI, --22
             CR.HISTORICO, --23
             CR.CODFUNCLANC, --24
             CR.NUMPED, --25
             CR.NUMERARIO, --26
             CR.CODMOVIMENTO, --27
             CR.NUMLANC, --28
             CR.NUMPEDECF, --29
             CR.NUMCAIXA, --30
             CR.CODFUNCCX, --31
             CR.PRESTRESTCLI, --32
             NVL(CR.DTESTORNO,SYSDATE) --33
      INTO voCREDITOCLIENTE.CODCLI, --01
           voCREDITOCLIENTE.DTLANC, --02
           voCREDITOCLIENTE.NUMCUPOM, --03
           vNUMCRED, --04
           voCREDITOCLIENTE.VALOR, --05
           voCREDITOCLIENTE.DTDESCONTO, --06
           voCREDITOCLIENTE.NUMSERIEEQUIP, --08
           voCREDITOCLIENTE.CODROTINA, --09
           voCREDITOCLIENTE.CODFILIAL, --10
           voCREDITOCLIENTE.NUMNOTA, --11
           voCREDITOCLIENTE.NUMTRANSVENDA, --12
           voCREDITOCLIENTE.SERIE, --13
           voCREDITOCLIENTE.CODFILIALDESC, --14
           voCREDITOCLIENTE.NUMNOTADESC, --15
           voCREDITOCLIENTE.SERIEDESC, --16
           voCREDITOCLIENTE.CODFUNC, --17
           voCREDITOCLIENTE.HORA, --18
           voCREDITOCLIENTE.MINUTO, --19
           voCREDITOCLIENTE.ORIGEM, --20
           voCREDITOCLIENTE.NUMTRANSVENDADESC, --21
           voCREDITOCLIENTE.NUMTRANSENTDEVCLI, --22
           voCREDITOCLIENTE.HISTORICO, --23
           voCREDITOCLIENTE.CODFUNCLANC, --24
           voCREDITOCLIENTE.NUMPED, --25
           voCREDITOCLIENTE.NUMERARIO, --26
           voCREDITOCLIENTE.CODMOVIMENTO, --27
           voCREDITOCLIENTE.NUMLANC, --28
           voCREDITOCLIENTE.NUMPEDECF, --29
           voCREDITOCLIENTE.NUMCAIXA, --30
           voCREDITOCLIENTE.CODFUNCCX, --31
           voCREDITOCLIENTE.PRESTRESTCLI, --32
           voCREDITOCLIENTE.DTESTORNO --33
      FROM PCCRECLI CR
      WHERE CR.CODIGO = pCODIGO;

      vEXISTEREGISTRO := 'S';
   EXCEPTION WHEN OTHERS THEN
      vEXISTEREGISTRO := 'N';
      voCREDITOCLIENTE.LIMPAR;
   END;

   INSERELOG('INSERIRREGISTROSVAREJO', 'PCCRECLI', 0, 0, pCODIGO, 0, 0, 0, 'pCODIGO = '||pCODIGO, 0, 0, pfCODIGOROTINA);

   IF vEXISTEREGISTRO = 'S' THEN
      IF vNUMCRED > 0 THEN
         voCREDITOCLIENTE.NUMCRED := vNUMCRED;
      END IF;
      --Inserindo o débito do que será estornado
      voCREDITOCLIENTE.DTESTORNO := TRUNC(SYSDATE);
      voCREDITOCLIENTE.CODIGO := voCREDITOCLIENTE.PROXIMOCODIGOCREDITO;
      voCREDITOCLIENTE.DTLANC := NVL(voCREDITOCLIENTE.DTLANC, TRUNC(SYSDATE));
      voCREDITOCLIENTE.INSERIR;
      --Inserindo os desdobramentos do retorno do débito
      voDESD.LIMPAR;
      voDESD.CODIGOORIG := pCODIGO;
      voDESD.CODIGODEST := voCREDITOCLIENTE.CODIGO;
      voDESD.DTLANC := NVL(voCREDITOCLIENTE.DTLANC, TRUNC(SYSDATE));
      voDESD.INSERIR;
      --Inserindo o crédito do que será estornado
      voCREDITOCLIENTE.CODIGO := voCREDITOCLIENTE.PROXIMOCODIGOCREDITO;
      voCREDITOCLIENTE.VALOR := voCREDITOCLIENTE.VALOR * -1;
      voCREDITOCLIENTE.DTLANC := NVL(voCREDITOCLIENTE.DTLANC, TRUNC(SYSDATE));
      voCREDITOCLIENTE.INSERIR;
      --Inserindo os desdobramentos do retorno do crédito
      voDESD.CODIGODEST := voCREDITOCLIENTE.CODIGO;
      voDESD.DTLANC := NVL(voCREDITOCLIENTE.DTLANC, TRUNC(SYSDATE));
      voDESD.INSERIR;
      voCREDITOCLIENTE.LIMPAR;
      voDESD.LIMPAR;
      INSERELOG('INSERIRREGISTROSVAREJO', 'PCDESDCRECLI', 0, 0, pCODIGO, 0, 0, 0, 'pCODIGO = '||pCODIGO, 0, 0, pfCODIGOROTINA);
      --Reabrindo o Crédito
      UPDATE PCCRECLI SET DTDESCONTO = NULL,
                          CODFILIALDESC = NULL,
                          NUMNOTADESC = NULL,
                          SERIEDESC = NULL,
                          NUMTRANSVENDADESC = NULL,
                          NUMCRED = NULL,
                          DTESTORNO = NULL,
                          NUMCUPOM = NULL
      WHERE CODIGO = pCODIGO;
   END IF;
END INSERIRREGISTROSVAREJO;
