  /*
  13. "FNC_BUSCACREDITO_CODIGO"
  FUNÇÃO RETORNA O CREDITO DISPONIVEL PARA O CODIGO INFORMADO.
  */
  FUNCTION FNC_BUSCACREDITO_CODIGO( pfCODIGOCREDITO    IN NUMBER,
                                    psFILIAL           IN VARCHAR2
                                    )
  RETURN NUMBER IS
    vfVALORCREDITO NUMBER(14,2);
  BEGIN
    --VALIDANDO PARAMETROS DE ENTRADA!
    IF ((TRIM(psFILIAL) = '') OR (psFILIAL IS NULL)) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UMA FILIAL');
    END IF;

    IF (pfCODIGOCREDITO <= 0) OR (pfCODIGOCREDITO IS NULL) THEN
      RAISE_APPLICATION_ERROR(-20016,'FAVOR INFORMAR UM CODIGO DE CREDITO VALIDO.');
    END IF;

    SELECT C.VALOR
    INTO vfVALORCREDITO
    FROM PCCRECLI C
    WHERE C.CODIGO =  pfCODIGOCREDITO
    AND   C.DTDESCONTO IS NULL
    AND   C.DTESTORNO IS NULL
    AND   C.SITUACAO IS NULL
    AND   ((PARAMFILIAL.OBTERCOMOVARCHAR2('USACREDCLITODASFILIAIS') = 'S') OR (C.CODFILIAL = psFILIAL));

    RETURN vfVALORCREDITO;
  END FNC_BUSCACREDITO_CODIGO;

