CREATE OR REPLACE PACKAGE BODY "PKG_MONITOR_TRIBUTARIO" AS

  FUNCTION GET_NOME_TABELA_FORMATADA(PPREFIXO IN VARCHAR2, PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
    v_tabela VARCHAR2(30);
  BEGIN
    v_tabela := SUBSTR(PTABELA, 3, LENGTH(PTABELA)); -- remove o prefixo PC
    v_tabela := PPREFIXO || v_tabela;
    v_tabela := 'PC' || v_tabela; -- restaura o prefixo PC

    RETURN  SUBSTR(v_tabela, 1, 30);
  END GET_NOME_TABELA_FORMATADA;
  

  FUNCTION GET_NOME_TABELA_TEMPORARIA(PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN GET_NOME_TABELA_FORMATADA('TMPMON', PTABELA);
  END GET_NOME_TABELA_TEMPORARIA;
  

  FUNCTION GET_NOME_TABELA_MONITOR(PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN GET_NOME_TABELA_FORMATADA('MONITOR', PTABELA);
  END GET_NOME_TABELA_MONITOR;


  FUNCTION VERIFICAR_EXISTE_TABELA_TMPMON(PTABELA IN VARCHAR2) RETURN BOOLEAN IS
    v_existe_tabela NUMBER;
  BEGIN
    SELECT COUNT(TABLE_NAME)
    INTO v_existe_tabela
    FROM USER_TABLES
    WHERE TABLE_NAME = PTABELA;

    RETURN v_existe_tabela > 0;
  END VERIFICAR_EXISTE_TABELA_TMPMON;


  PROCEDURE CRIAR_TABELA(PTABELA IN VARCHAR2, PCONSULTA IN VARCHAR2) IS
    v_sql_create VARCHAR2(4000);
  BEGIN
    v_sql_create := 'CREATE TABLE ' || PTABELA || ' NOLOGGING PARALLEL AS ' || PCONSULTA;
    EXECUTE IMMEDIATE v_sql_create;
  END CRIAR_TABELA;
  

  PROCEDURE LIMPAR_TABELA(PTABELA IN VARCHAR2) IS
    v_sql_truncate VARCHAR2(100);
  BEGIN
    v_sql_truncate := 'TRUNCATE TABLE ' || PTABELA;

    BEGIN
      EXECUTE IMMEDIATE v_sql_truncate;
    EXCEPTION

      WHEN OTHERS THEN
        IF SQLCODE != -942 THEN
          RAISE;
        END IF;
    END;
  END LIMPAR_TABELA;
  

  PROCEDURE APAGAR_TABELA(PTABELA IN VARCHAR2) IS
    v_sql_drop VARCHAR2(100);
  BEGIN
    v_sql_drop := 'DROP TABLE ' || PTABELA;

    BEGIN
      EXECUTE IMMEDIATE v_sql_drop;
    EXCEPTION

      WHEN OTHERS THEN
        IF SQLCODE != -942 THEN
          RAISE;
        END IF;
    END;
  END APAGAR_TABELA;
  

  PROCEDURE CRIAR_TABELA_TEMPORARIA(PTABELA IN VARCHAR2, PCONSULTA IN VARCHAR2) IS
  BEGIN
    CRIAR_TABELA(GET_NOME_TABELA_TEMPORARIA(PTABELA), PCONSULTA);
  END CRIAR_TABELA_TEMPORARIA;
  

  PROCEDURE LIMPAR_TABELA_TEMPORARIA(PTABELA IN VARCHAR2) IS
  BEGIN
    LIMPAR_TABELA(GET_NOME_TABELA_TEMPORARIA(PTABELA));
  END LIMPAR_TABELA_TEMPORARIA;
  

  PROCEDURE APAGAR_TABELA_TEMPORARIA(PTABELA IN VARCHAR2) IS
  BEGIN
    APAGAR_TABELA(GET_NOME_TABELA_TEMPORARIA(PTABELA));
  END APAGAR_TABELA_TEMPORARIA;
  

  PROCEDURE CRIAR_TABELA_MONITOR(PTABELA IN VARCHAR2, PCONSULTA IN VARCHAR2) IS
  BEGIN
    CRIAR_TABELA(GET_NOME_TABELA_MONITOR(PTABELA), PCONSULTA);
  END CRIAR_TABELA_MONITOR;
  

  PROCEDURE LIMPAR_TABELA_MONITOR(PTABELA IN VARCHAR2) IS
