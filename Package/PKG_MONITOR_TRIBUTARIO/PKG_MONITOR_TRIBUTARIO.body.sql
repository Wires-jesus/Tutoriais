CREATE OR REPLACE PACKAGE BODY PKG_MONITOR_TRIBUTARIO AS

  PROCEDURE APAGARTABELATEMPORARIA(PTABELA IN VARCHAR2) IS
    V_SQL_DROP     VARCHAR2(2000);
    V_SQL_TRUNCATE VARCHAR2(2000);
  BEGIN
    V_SQL_TRUNCATE := 'TRUNCATE TABLE ' || PTABELA;
    V_SQL_DROP     := 'DROP TABLE ' || PTABELA;
  
    BEGIN
      EXECUTE IMMEDIATE V_SQL_TRUNCATE;
    EXCEPTION
    
      WHEN OTHERS THEN
        IF SQLCODE != -942 THEN
          RAISE;
        END IF;
    END;
  
    BEGIN
      EXECUTE IMMEDIATE V_SQL_DROP;
    EXCEPTION
    
      WHEN OTHERS THEN
        IF SQLCODE != -942 THEN
          RAISE;
        END IF;
    END;
  END APAGARTABELATEMPORARIA;

  PROCEDURE CRIARTABELA_PCNFSAID(PTABELA      IN VARCHAR2,
                                 PDIAANTERIOR IN BOOLEAN) IS
    V_SQL_CREATE VARCHAR2(2000);
  BEGIN
  
    V_SQL_CREATE := 'CREATE TABLE ' || PTABELA || ' AS ' ||
                    GET_VALORES_FATURAMENTO(PDIAANTERIOR);
    EXECUTE IMMEDIATE V_SQL_CREATE;
  END CRIARTABELA_PCNFSAID;

  PROCEDURE CRIARTABELA_CODFISCAL(PTABELA    IN VARCHAR2,
                                  PDIAANTERIOR IN BOOLEAN) IS
    V_SQL_CREATE VARCHAR2(4000);
  BEGIN
    V_SQL_CREATE := 'CREATE TABLE ' || PTABELA || ' AS ' ||
                    GET_NOTAS_CODIGO_FISCAL(PDIAANTERIOR);
    EXECUTE IMMEDIATE V_SQL_CREATE;
  END CRIARTABELA_CODFISCAL;

  PROCEDURE CRIARTABELA_ITENS(PTABELA    IN VARCHAR2,
                              PDIAANTERIOR IN BOOLEAN) IS
    V_SQL_CREATE VARCHAR2(4000);
  BEGIN
    V_SQL_CREATE := 'CREATE TABLE ' || PTABELA || ' AS ' ||
                    GET_VALORES_ITENS(PDIAANTERIOR);
    EXECUTE IMMEDIATE V_SQL_CREATE;
  END CRIARTABELA_ITENS;

  PROCEDURE CRIARTABELA_LIVROSAIDA(PTABELA    IN VARCHAR2,
                                   PDIAANTERIOR IN BOOLEAN) IS
    V_SQL_CREATE VARCHAR2(4000);
  BEGIN
    V_SQL_CREATE := 'CREATE TABLE ' || PTABELA || ' AS ' ||
                    GET_SQL_LIVRO_SAIDA(PDIAANTERIOR);
    EXECUTE IMMEDIATE V_SQL_CREATE;
  END CRIARTABELA_LIVROSAIDA;

  PROCEDURE CRIARTABELA_LIVROENTRADA(PTABELA    IN VARCHAR2,
                                     PDIAANTERIOR IN BOOLEAN) IS
    V_SQL_CREATE VARCHAR2(4000);
  BEGIN
    V_SQL_CREATE := 'CREATE TABLE ' || PTABELA || ' AS ' ||
                    GET_SQL_LIVRO_ENTRADA(PDIAANTERIOR);
    EXECUTE IMMEDIATE V_SQL_CREATE;

  END CRIARTABELA_LIVROENTRADA;

  PROCEDURE CRIARMONITORTRIBUTARIO IS
    C_TABELA_TEMPPCNFSAID     CONSTANT VARCHAR2(30) := 'PCMONITORPCNFSAID';
    C_TABELA_TEMPCODFISCAL    CONSTANT VARCHAR2(30) := 'PCMONITORCODFISCAL';
    C_TABELA_TEMPPCMOV        CONSTANT VARCHAR2(30) := 'PCMONITORPCMOV';
    C_TABELA_TEMPPCNFBASEENT  CONSTANT VARCHAR2(30) := 'PCMONITORPCNFBASEENT';
    C_TABELA_TEMPPCNFBASESAID CONSTANT VARCHAR2(30) := 'PCMONITORPCNFBASESAID';
    V_DIAANTERIOR BOOLEAN;
  BEGIN
    V_DIAANTERIOR := FALSE;
    IF TO_CHAR(SYSDATE, 'HH24MI') < 8 THEN
      V_DIAANTERIOR := TRUE;
    END IF;
  
    APAGARTABELATEMPORARIA(C_TABELA_TEMPPCNFSAID);
    APAGARTABELATEMPORARIA(C_TABELA_TEMPCODFISCAL);
    APAGARTABELATEMPORARIA(C_TABELA_TEMPPCMOV);
    APAGARTABELATEMPORARIA(C_TABELA_TEMPPCNFBASEENT);
    APAGARTABELATEMPORARIA(C_TABELA_TEMPPCNFBASESAID);
  
    CRIARTABELA_PCNFSAID(C_TABELA_TEMPPCNFSAID, V_DIAANTERIOR);
    CRIARTABELA_CODFISCAL(C_TABELA_TEMPCODFISCAL, V_DIAANTERIOR);
    CRIARTABELA_ITENS(C_TABELA_TEMPPCMOV, V_DIAANTERIOR);
    CRIARTABELA_LIVROENTRADA(C_TABELA_TEMPPCNFBASEENT, V_DIAANTERIOR);
    CRIARTABELA_LIVROSAIDA(C_TABELA_TEMPPCNFBASESAID, V_DIAANTERIOR);
  
  END CRIARMONITORTRIBUTARIO;

END PKG_MONITOR_TRIBUTARIO;
