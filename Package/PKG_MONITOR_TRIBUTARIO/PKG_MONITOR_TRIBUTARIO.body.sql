  PROCEDURE PREPARA_AMBIENTE_MONITOR_TRIB IS 
    v_Telemetria T_TELEMETRIA;
    v_AtualizaAmbiente BOOLEAN;
  BEGIN
    LOG('*Preparando o ambiente*');
    v_AtualizaAmbiente := (AMBIENTE_ESTA_PREPARADO = 'S');
    LOG('Ambiente atualizado: ' || CASE WHEN v_AtualizaAmbiente THEN 'S' ELSE 'N' END);

    IF NOT v_AtualizaAmbiente THEN
      CRIAR_ESTRUTURA_CONTROLEPROC;
      ATUALIZAR_DATA_PREPAMBIENTE;
      LIMPAR_DATA_ULTIMA_EXECUCAO;
      LOG('  Data de atualização do ambiente realizada!');
      
      INICIAR_TELEMETRIA_CABECALHO(g_Telemetria, '** MONITOR TRIBUTARIO **');
      INICIAR_TELEMETRIA(v_Telemetria, 'PREPARA_AMBIENTE_MONITOR_TRIB', 0);
      
      LIMPAR_TABELA('PCMONITORTRIBUTARIOLISTATABELA');
      
      LOG('  Gerando estruturas do monitor');
      CRIAR_LISTA_TABELAS_MONITOR_TR;
      CRIAR_ESTRUTURA_TELEMETRIA;
      LOG('  Estruturas do monitor geradas');
      
      GERAR_TABELAS(
        PDATAINICIAL       => SYSDATE,
        PDATAFINAL         => SYSDATE,
        PAMBIENTEPREPARADO => FALSE
      );
      
      FINALIZAR_E_GRAVAR_TELEMETRIA(v_Telemetria);
      FINALIZAR_TELEMETRIA_CABECALHO(g_telemetria);
    END IF;
    LOG('*Ambiente preparado*');
  END PREPARA_AMBIENTE_MONITOR_TRIB;


  PROCEDURE CRIAR_MONITOR_TRIBUTARIO(PDATAINICIAL IN DATE := SYSDATE - 30, PDATAFINAL IN DATE := SYSDATE) IS
    v_Telemetria T_TELEMETRIA;
    v_DataInicial DATE;
    v_DataFinal DATE;
    v_GeraDados BOOLEAN;
  BEGIN
    INICIAR_TELEMETRIA_CABECALHO(v_Telemetria, 'CRIAR_MONITOR_TRIBUTARIO');

    v_DataInicial := NVL(PDATAINICIAL, SYSDATE - 30);
    v_DataFinal   := NVL(PDATAFINAL, SYSDATE);
    v_GeraDados   := MONITOR_TRIBUTARIO_HABILITADO
                     AND DADOS_ESTAO_DESATUALIZADOS
                     AND (AMBIENTE_ESTA_PREPARADO = 'S');

    IF v_GeraDados THEN
        GERAR_TABELAS(
          PDATAINICIAL       => v_DataInicial, 
          PDATAFINAL         => v_DataFinal,
          PAMBIENTEPREPARADO => TRUE 
        );
    END IF;
    
    FINALIZAR_TELEMETRIA_CABECALHO(v_Telemetria);
    FINALIZAR_TELEMETRIA_CABECALHO(g_telemetria);
  END CRIAR_MONITOR_TRIBUTARIO;

  PROCEDURE FINALIZAR_CARGA_MONITOR_TRIBUT IS 
    CR_REFCUR_TABELAS_MONITOR T_REFCUR_TABELA_MONITOR;
    LISTA_TABELAS_MONITOR     T_TABLE_TABELA_MONITOR;
  BEGIN
    IF TEM_DATA_ULTIMA_EXECUCAO = 'S' THEN
       RETURN;
    END IF;  
  
    CR_REFCUR_TABELAS_MONITOR := GET_TABELAS_MONITOR;
    
    LOOP
      FETCH CR_REFCUR_TABELAS_MONITOR BULK COLLECT INTO LISTA_TABELAS_MONITOR;
   
      FOR ITEM IN LISTA_TABELAS_MONITOR.FIRST .. LISTA_TABELAS_MONITOR.LAST LOOP
         APAGAR_TABELA_MONITOR(LISTA_TABELAS_MONITOR(ITEM).TABELAORIGEM);
         
         CRIAR_TABELA_MONITOR(
           LISTA_TABELAS_MONITOR(ITEM).TABELAORIGEM,
           PKG_MONITOR_TRIBUTARIO_CONSULT.GET_VALORES_TABELA_MONITOR(LISTA_TABELAS_MONITOR(ITEM).TABELATEMPORARIA)
         );
         
         CRIAR_INDICES_TABELA(
           LISTA_TABELAS_MONITOR(ITEM).TABELAORIGEM, 
           LISTA_TABELAS_MONITOR(ITEM).INDICE
         );
         
         IF NVL(TRIM(LISTA_TABELAS_MONITOR(ITEM).TABELADEPENDENCIA), 'XX') <> 'XX' THEN
           LIMPAR_TABELA_TEMPORARIA(LISTA_TABELAS_MONITOR(ITEM).TABELADEPENDENCIA);
         END IF;
      END LOOP;
       
      EXIT WHEN LISTA_TABELAS_MONITOR.EXISTS(1) = FALSE;
      EXIT WHEN CR_REFCUR_TABELAS_MONITOR%NOTFOUND;
      EXIT WHEN LISTA_TABELAS_MONITOR.COUNT = 0;
    END LOOP;

    ATUALIZAR_DATA_ULTIMA_EXECUCAO;
  END FINALIZAR_CARGA_MONITOR_TRIBUT;


