  END APAGAR_TABELA_MONITOR;


  PROCEDURE CARREGAR_TABELA_MONITOR(PTABELA IN VARCHAR2) IS
    v_tabela VARCHAR2(30);
  BEGIN
    v_tabela := GET_NOME_TABELA_TEMPORARIA(PTABELA);
    
    IF VERIFICAR_EXISTE_TABELA_TMPMON(v_tabela) THEN
      APAGAR_TABELA_MONITOR(PTABELA);
      CRIAR_TABELA_MONITOR(PTABELA, PKG_MONITOR_TRIBUTARIO_CONSULT.GET_VALORES_TABELA_MONITOR(v_tabela));
    END IF;
  END CARREGAR_TABELA_MONITOR;


  PROCEDURE CRIAR_INDICES_TABELA(PTABELA IN VARCHAR2, PINDICE IN VARCHAR2) IS 
    C_PADRAO_CREATE CONSTANT VARCHAR2(100) := 'CREATE INDEX @INDICE ON @TABELA(@CAMPOS) NOLOGGING TABLESPACE TS_INDICE';
    v_indice VARCHAR2(2000);
    v_tabela VARCHAR(30);
  BEGIN
    v_tabela := GET_NOME_TABELA_MONITOR(PTABELA);
    
    FOR LISTA_INDICE IN (
      WITH INDICE AS (
        SELECT PINDICE IDX
        FROM DUAL
      )
      SELECT 
        ROWNUM NUMERO,
        REGEXP_SUBSTR( 
          IDX,
          '[^;]+',
          1,
          LEVEL
        ) NOME_INDICE
      FROM INDICE
      CONNECT BY LEVEL <= LENGTH(IDX) - LENGTH(REPLACE(IDX, ';')) + 1
    ) LOOP
      IF NVL(TRIM(LISTA_INDICE.NOME_INDICE), 'X') <> 'X' THEN
        v_indice := C_PADRAO_CREATE;
        v_indice := REPLACE(v_indice, '@INDICE', v_tabela || '_IDX' || LISTA_INDICE.NUMERO);
        v_indice := REPLACE(v_indice, '@TABELA', v_tabela);
        v_indice := REPLACE(v_indice, '@CAMPOS', LISTA_INDICE.NOME_INDICE);
      
        EXECUTE IMMEDIATE v_indice; 
      END IF;
    END LOOP;
  
  END CRIAR_INDICES_TABELA;


  PROCEDURE CRIARTABELA_PCNFSAID(PDATA_INICIAL IN DATE, PDATA_FINAL IN DATE) IS
    C_TABELA CONSTANT VARCHAR2(30) := 'PCNFSAID';
  BEGIN
    APAGAR_TABELA_TEMPORARIA(C_TABELA);
    CRIAR_TABELA_TEMPORARIA(C_TABELA, PKG_MONITOR_TRIBUTARIO_CONSULT.GET_VALORES_PCNFSAID(PDATA_INICIAL, PDATA_FINAL));
    CARREGAR_TABELA_MONITOR(C_TABELA);
    CRIAR_INDICES_TABELA(C_TABELA, PKG_MONITOR_TRIBUTARIO_CONSULT.GET_INDICES_PCNFSAID);
  END CRIARTABELA_PCNFSAID;


