CREATE OR REPLACE PACKAGE BODY PKG_MONITOR_TRIBUTARIO AS

  FUNCTION QUOTED(PTEXTO VARCHAR2) RETURN VARCHAR2 IS 
  BEGIN
    RETURN CHR(39) || PTEXTO || CHR(39);
  END QUOTED;
  
  
  FUNCTION QUOTEDDUPLO(PTEXTO VARCHAR2) RETURN VARCHAR2 IS 
  BEGIN
    RETURN CHR(39) || CHR(39) || PTEXTO || CHR(39) || CHR(39);
  END QUOTEDDUPLO;


  FUNCTION MONITOR_TRIBUTARIO_HABILITADO RETURN BOOLEAN IS 
  BEGIN
    RETURN TRUE;
  END MONITOR_TRIBUTARIO_HABILITADO;
  
  PROCEDURE LOG(PTEXTO VARCHAR2) IS
  BEGIN
    DBMS_OUTPUT.put_line(TO_CHAR(SYSDATE, 'DD/MM/YYYY HH24:MI:SS') || ' - ' || PTEXTO);
  END LOG ;

  FUNCTION GET_TABELAS_MONITOR RETURN SYS_REFCURSOR AS 
    v_tabelas SYS_REFCURSOR;
  BEGIN
    OPEN v_tabelas FOR 'SELECT 
                          TABELAORIGEM, 
                          TABELATEMPORARIA, 
                          TABELAMONITOR, 
                          TABELADEPENDENCIA, 
                          INDICE 
                        FROM PCMONITORTRIBUTARIOLISTATABELA';
    RETURN v_tabelas;
  END GET_TABELAS_MONITOR;

  FUNCTION AMBIENTE_ESTA_PREPARADO RETURN VARCHAR2 IS
    v_DataAmbiente DATE;
  BEGIN
    BEGIN
      EXECUTE IMMEDIATE 'SELECT DTPREPARACAOAMBIENTE
                         FROM PCCONTROLEPROCESSAMENTO
                         WHERE OBJETO = ''PKG_MONITOR_TRIBUTARIO'''
      INTO v_DataAmbiente;
    EXCEPTION 
      WHEN OTHERS THEN
        v_DataAmbiente := SYSDATE - 1;
    END;
    
    IF TRUNC(NVL(v_DataAmbiente, SYSDATE - 1)) = TRUNC(SYSDATE) THEN
      RETURN 'S';
    ELSE
      RETURN 'N';
    END IF;
  END AMBIENTE_ESTA_PREPARADO;
  

  PROCEDURE ATUALIZAR_DATA_ULTIMA_EXECUCAO IS
  BEGIN
    EXECUTE IMMEDIATE 'UPDATE PCCONTROLEPROCESSAMENTO SET 
