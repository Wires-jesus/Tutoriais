  PROCEDURE CRIAR_INDICES_TABELA(PTABELA IN VARCHAR2, PSTRINGINDICE IN VARCHAR2, PTABELATEMP IN BOOLEAN := FALSE) IS
    C_PADRAO_CREATE CONSTANT VARCHAR2(100) := 'CREATE INDEX @INDICE ON @TABELA(@CAMPOS) NOLOGGING TABLESPACE TS_INDICE';
    v_ScriptIndice VARCHAR2(2000);
    v_NomeTabela VARCHAR(30);
  BEGIN
    v_NomeTabela := GET_NOME_TABELA_MONITOR(PTABELA);

    IF PTABELATEMP THEN
      v_NomeTabela := GET_NOME_TABELA_TEMPORARIA(PTABELA);
    END IF;

    FOR LISTA_INDICE IN (
      WITH INDICE AS (
        SELECT PSTRINGINDICE STRINGINDICE
        FROM DUAL
      )
      SELECT
        ROWNUM NUMERO,
        REGEXP_SUBSTR( STRINGINDICE, '[^;]+', 1, LEVEL ) NOME_CAMPO_INDICE
      FROM INDICE
      CONNECT BY LEVEL <= LENGTH(STRINGINDICE) - LENGTH(REPLACE(STRINGINDICE, ';')) + 1
    ) LOOP
      IF NVL(TRIM(LISTA_INDICE.NOME_CAMPO_INDICE), 'X') <> 'X' THEN
        v_ScriptIndice := C_PADRAO_CREATE;
        v_ScriptIndice := REPLACE(v_ScriptIndice, '@INDICE', v_NomeTabela || '_IDX' || LISTA_INDICE.NUMERO);
        v_ScriptIndice := REPLACE(v_ScriptIndice, '@TABELA', v_NomeTabela);
        v_ScriptIndice := REPLACE(v_ScriptIndice, '@CAMPOS', LISTA_INDICE.NOME_CAMPO_INDICE);

        EXECUTE IMMEDIATE v_ScriptIndice;
      END IF;
    END LOOP;
  END CRIAR_INDICES_TABELA;

  PROCEDURE PREPARA_TABELA_MONITOR(PTABELA VARCHAR2, PSCRIPTINDICE IN VARCHAR2) IS 
    v_NomeTabelaTemporaria VARCHAR2(30);
    v_NomeTabelaMonitor VARCHAR2(30);
    v_ContinuaPreparacao BOOLEAN;
  BEGIN
    v_NomeTabelaTemporaria := GET_NOME_TABELA_TEMPORARIA(PTABELA);
    v_NomeTabelaMonitor    := GET_NOME_TABELA_MONITOR(PTABELA);
    
    v_ContinuaPreparacao   := EXISTE_TABELA_TMPMON(v_NomeTabelaTemporaria)
                              AND TABELA_MONITOR_DESATUALIZADA(v_NomeTabelaMonitor);

    IF v_ContinuaPreparacao THEN
      APAGAR_TABELA_MONITOR(PTABELA);
      CRIAR_TABELA_MONITOR(PTABELA, GET_CONSULTA_PREPARAR_AMBIENTE(PKG_MONITOR_TRIBUTARIO_CONSULT.GET_VALORES_TABELA_MONITOR(v_NomeTabelaTemporaria)));
      IF NVL(TRIM(PSCRIPTINDICE), 'X') <> 'X' THEN
        CRIAR_INDICES_TABELA(PTABELA, PSCRIPTINDICE);
      END IF;
    END IF;
  END PREPARA_TABELA_MONITOR;

  PROCEDURE CRIATABELA_BASE(PTABELA IN VARCHAR2, PQUERY IN VARCHAR2, PSCRIPTINDICE IN VARCHAR2, PTABELADEPENDENCIA IN VARCHAR2, PAMBIENTEPREPARADO IN BOOLEAN) IS
  BEGIN
    IF NOT PAMBIENTEPREPARADO THEN
      GRAVAR_TABELA_NA_LISTA_TABELAS(PTABELA, PSCRIPTINDICE, PTABELADEPENDENCIA);
      APAGAR_TABELA_TEMPORARIA(PTABELA);
      CRIAR_TABELA_TEMPORARIA(PTABELA, GET_CONSULTA_PREPARAR_AMBIENTE(PQUERY));
      
      IF NVL(TRIM(PTABELADEPENDENCIA), 'XX') <> 'XX' THEN
        CRIAR_INDICES_TABELA(PTABELADEPENDENCIA, PSCRIPTINDICE, PTABELATEMP => TRUE);
      END IF;
    ELSE
      CARREGAR_TABELA_TEMPORARIA(PTABELA, PQUERY);
    END IF;
  END CRIATABELA_BASE;


