  FUNCTION GET_NOME_TABELA_FORMATADA(PPREFIXO IN VARCHAR2, PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
    v_Tabela VARCHAR2(30);
  BEGIN
    v_Tabela := SUBSTR(PTABELA, 3, LENGTH(PTABELA)); -- remove o prefixo PC
    v_Tabela := PPREFIXO || v_Tabela;
    v_Tabela := 'PC' || v_Tabela; -- restaura o prefixo PC

    RETURN  SUBSTR(v_Tabela, 1, 30);
  END GET_NOME_TABELA_FORMATADA;


  FUNCTION GET_CONSULTA_PREPARAR_AMBIENTE(PCONSULTA VARCHAR2) RETURN VARCHAR2 IS 
    v_TemGroupBy BOOLEAN;
  BEGIN
    v_TemGroupBy := INSTR(UPPER(PCONSULTA), 'GROUP BY') > 0;
  
    IF INSTR(UPPER(PCONSULTA), ' WHERE ') = 0 THEN
        IF v_TemGroupBy THEN
          RETURN REPLACE(PCONSULTA, ' WHERE ', ' WHERE ROWNUM = 0 ');
        ELSE
           RETURN PCONSULTA || ' WHERE ROWNUM = 0';
        END IF;
    ELSE
      IF v_TemGroupBy THEN
        RETURN REPLACE(PCONSULTA, ' WHERE ' , ' WHERE ROWNUM = 0 AND ');
      ELSE
        RETURN PCONSULTA || ' AND ROWNUM = 0';
      END IF;
    END IF;
  END GET_CONSULTA_PREPARAR_AMBIENTE;


  FUNCTION EXISTE_TABELA_TMPMON(PTABELA IN VARCHAR2) RETURN BOOLEAN IS
    v_ExisteTabela NUMBER;
  BEGIN
    SELECT COUNT(TABLE_NAME)
    INTO v_ExisteTabela
    FROM USER_TABLES
    WHERE TABLE_NAME = PTABELA;

    RETURN NVL(v_ExisteTabela,0) > 0;
  END EXISTE_TABELA_TMPMON;
  

  FUNCTION GET_TAMANHO_TABELA_EM_MB(PTABELA IN VARCHAR2) RETURN NUMBER IS 
    v_Tamanho NUMBER;
  BEGIN
    SELECT SUM(BYTES)/POWER(1024,2) 
    INTO v_Tamanho
    FROM USER_SEGMENTS 
    WHERE SEGMENT_NAME = PTABELA;  
    
    RETURN v_Tamanho;
  END GET_TAMANHO_TABELA_EM_MB; 

  
  FUNCTION GET_QUANTIDADE_REGISTROS_TAB(PTABELA IN VARCHAR2) RETURN NUMBER IS 
    C_COUNT_TABELA CONSTANT VARCHAR(100) := 'SELECT COUNT(1) FROM @TABELA';
    v_Query VARCHAR(100);
    v_QuantidadeRegistros NUMBER;
  BEGIN
    v_Query := REPLACE(C_COUNT_TABELA, '@TABELA', PTABELA);
    EXECUTE IMMEDIATE v_Query INTO v_QuantidadeRegistros;
    
    RETURN v_QuantidadeRegistros;
  END GET_QUANTIDADE_REGISTROS_TAB;


  FUNCTION GET_ESPACO_LIVRE_TABLESPACE_MB(PTABLESPACE IN VARCHAR2 := 'TS_DADOS') RETURN NUMBER IS 
    v_Tamanho NUMBER;
  BEGIN
    SELECT SUM(BYTES)/POWER(1024,2)
    INTO v_Tamanho
    FROM USER_FREE_SPACE
    WHERE TABLESPACE_NAME = PTABLESPACE;
    
    RETURN v_Tamanho;
  END GET_ESPACO_LIVRE_TABLESPACE_MB;


  FUNCTION GET_ESPACO_USADO_TABLESPACE(PTABLESPACE IN VARCHAR2 := 'TS_DADOS') RETURN NUMBER IS 
    v_Tamanho NUMBER;
  BEGIN
    SELECT SUM(BYTES)/POWER(1024,2) 
    INTO v_Tamanho
    FROM USER_SEGMENTS 
    WHERE TABLESPACE_NAME = PTABLESPACE;
    
    RETURN v_Tamanho;
  END GET_ESPACO_USADO_TABLESPACE;


  FUNCTION GET_NOME_TABELA_TEMPORARIA(PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN GET_NOME_TABELA_FORMATADA('TMPMON', PTABELA);
  END GET_NOME_TABELA_TEMPORARIA;


  FUNCTION GET_NOME_TABELA_MONITOR(PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN GET_NOME_TABELA_FORMATADA('MONITOR', PTABELA);
  END GET_NOME_TABELA_MONITOR;


  FUNCTION EXISTE_TABELA_MONITOR(PTABELA VARCHAR2, PDATA DATE) RETURN BOOLEAN IS 
    v_QuantidadeTabela NUMBER;
    v_NomeTabela VARCHAR2(30);
  BEGIN
    v_NomeTabela := GET_NOME_TABELA_MONITOR(PTABELA);
  
    SELECT COUNT(1)
    INTO v_QuantidadeTabela
    FROM USER_OBJECTS
    WHERE OBJECT_TYPE = 'TABLE'
          AND OBJECT_NAME = v_NomeTabela
          AND TRUNC(CREATED) >= TRUNC(PDATA);
  
    RETURN v_QuantidadeTabela > 0;
  END EXISTE_TABELA_MONITOR;
  

  FUNCTION GET_VERSAO_OBJETO(PNOMEOBJETO IN VARCHAR2) RETURN VARCHAR2 IS
    v_Versao VARCHAR2(15);
  BEGIN
    BEGIN
      SELECT VERSAO
      INTO v_Versao
      FROM PCVERSAOBD
      WHERE ROTINA = PNOMEOBJETO;
    EXCEPTION 
      WHEN OTHERS THEN
        v_Versao := '0.0.00.000';  
    END;
      
    RETURN v_Versao;
  END GET_VERSAO_OBJETO;


