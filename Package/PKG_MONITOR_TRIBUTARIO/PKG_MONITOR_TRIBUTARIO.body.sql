  FUNCTION USA_CONTABIL RETURN BOOLEAN IS 
    V_USACONTABIL NUMBER(3);
  BEGIN
    SELECT COUNT(1)
      INTO V_USACONTABIL
      FROM PCPARAMFILIAL F
     WHERE F.NOME = 'FIL_USACONTABIL'
       AND F.VALOR = 'S';
       
    RETURN V_USACONTABIL > 0;
  END USA_CONTABIL;


  PROCEDURE CRIAR_MONITOR_TRIBUTARIO(PDATAINICIAL IN DATE := SYSDATE - 30, PDATAFINAL IN DATE := SYSDATE) IS
    v_telemetria T_TELEMETRIA;
    v_datainicial DATE;
    v_datafinal DATE;
  BEGIN
    CRIA_ESTRUTURAS_MONITOR;
    INICIAR_TELEMETRIA_CABECALHO(v_telemetria, 'CRIAR_MONITOR_TRIBUTARIO');

    v_datainicial := NVL(PDATAINICIAL, SYSDATE - 30);
    v_datafinal := NVL(PDATAFINAL, SYSDATE);
    
    IF USA_MONITOR_TRIBUTARIO THEN
      IF EXECUTA_CARGA_DO_MONITOR THEN
        CRIARTABELA_PCNFSAID(v_datainicial, v_datafinal);
        CRIARTABELA_PCMOV(v_datainicial, v_datafinal);
        CRIARTABELA_MOV_UNIFICADA(v_datainicial, v_datafinal);
        CRIARTABELA_PCNFBASESAID(v_datainicial, v_datafinal);
        CRIARTABELA_PCNFBASEENT(v_datainicial, v_datafinal);
        CRIARTABELA_PCNFENT(v_datainicial, v_datafinal);
        CRIARTABELA_PCESTCOM(v_datainicial, v_datafinal);
        CRIARTABELA_PCDEVFORNEC(v_datainicial, v_datafinal);
        CRIARTABELA_PCDEVAVULSO(v_datainicial, v_datafinal);

        IF USA_CONTABIL THEN
          CRIARTABELA_PCLANCINTER(v_datainicial, v_datafinal);
        END IF;
        
        ATUALIZAR_DATA_ULTIMA_EXECUCAO;
      END IF;
    END IF;
    
    FINALIZAR_TELEMETRIA_CABECALHO(v_telemetria);
  END CRIAR_MONITOR_TRIBUTARIO;

END PKG_MONITOR_TRIBUTARIO;