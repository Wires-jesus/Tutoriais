    v_equipamento := SUBSTR(SYS_CONTEXT('USERENV', 'TERMINAL'), 1, 64);
    v_usuario     := SUBSTR(SYS_CONTEXT('USERENV', 'OS_USER'), 1, 30);
    v_rotina    := SUBSTR(SYS_CONTEXT('USERENV', 'MODULE'), 1, 48);
    v_tempo_decorrido := (PTELEMETRIA.DataHoraTermino -  PTELEMETRIA.DataHoraInicio) * 24*60*60;
    
    v_dados := '{'
        || 'Identificador: @Identificador, '
        || 'TempoDecorridoEmSegundos: @TempoDecorridoEmSegundos, '
        || 'VersaoDaPacakage: @VersaoDaPacakage, '
        || 'Usuario: @Usuario, '
        || 'Terminal: @Terminal, '
        || 'RotinaDoWinthor: @RotinaDoWinthor, '
        || 'NomeDaTabelaDoWinthor: @NomeDaTabelaDoWinthor, '
        || 'QuantidadeDeRegistrosTabelaWinthor: @QuantidadeDeRegistrosTabelaWinthor, '
        || 'TamanhoTabelaEmMBWinthor: @TamanhoTabelaEmMBWinthor, '
        || 'NomeDaTabelaDoMonitor: @NomeDaTabelaDoMonitor, '
      || 'QuantidadeDeRegistrosTabelaMonitor: @QuantidadeDeRegistrosTabelaMonitor, '
      || 'TamanhoTabelaEmMBMonitor: @TamanhoTabelaEmMBMonitor, '
      || 'EspacoUtilizadoNaTableSpaceEmMBAntesDeExecutar: @EspacoUtilizadoNaTableSpaceEmMBAntesDeExecutar, '
      || 'EspacoLivreNaTableSpaceEmMBAntesExecutar: @EspacoLivreNaTableSpaceEmMBAntesExecutar, '
      || 'EspacoUtilizadoNaTableSpaceEmMBAposExecutar: @EspacoUtilizadoNaTableSpaceEmMBAposExecutar, '
      || 'EspacoLivreNaTableSpaceEmMBAposExecutar: @EspacoLivreNaTableSpaceEmMBAposExecutar' 
      || '}';


    v_dados := REPLACE(v_dados, '@Identificador', QUOTEDDUPLO(PTELEMETRIA.Objeto));
    v_dados := REPLACE(v_dados, '@TempoDecorridoEmSegundos', TO_CHAR(v_tempo_decorrido));
    v_dados := REPLACE(v_dados, '@VersaoDaPacakage', QUOTEDDUPLO(v_versao));
    v_dados := REPLACE(v_dados, '@Usuario', QUOTEDDUPLO(v_usuario));
    v_dados := REPLACE(v_dados, '@Terminal', QUOTEDDUPLO(v_equipamento));
    v_dados := REPLACE(v_dados, '@RotinaDoWinthor', QUOTEDDUPLO(v_rotina));
    v_dados := REPLACE(v_dados, '@NomeDaTabelaDoWinthor', QUOTEDDUPLO(PTELEMETRIA.DadosDaTabelaWinthor.NomeDaTabela));
    v_dados := REPLACE(v_dados, '@QuantidadeDeRegistrosTabelaWinthor', TO_CHAR(TRUNC(PTELEMETRIA.DadosDaTabelaWinthor.QuantidadeRegistrosTabela)));
    v_dados := REPLACE(v_dados, '@TamanhoTabelaEmMBWinthor', TO_CHAR(TRUNC(PTELEMETRIA.DadosDaTabelaWinthor.TamanhoTabelaEmMB)));
    v_dados := REPLACE(v_dados, '@NomeDaTabelaDoMonitor', QUOTEDDUPLO(PTELEMETRIA.DadosDaTabelaMonitor.NomeDaTabela));    
    v_dados := REPLACE(v_dados, '@QuantidadeDeRegistrosTabelaMonitor', TO_CHAR(TRUNC(PTELEMETRIA.DadosDaTabelaMonitor.QuantidadeRegistrosTabela)));
    v_dados := REPLACE(v_dados, '@TamanhoTabelaEmMBMonitor', TO_CHAR(TRUNC(PTELEMETRIA.DadosDaTabelaMonitor.TamanhoTabelaEmMB)));
    v_dados := REPLACE(v_dados, '@EspacoUtilizadoNaTableSpaceEmMBAntesDeExecutar', TO_CHAR(TRUNC(PTELEMETRIA.DadosTableSapceInicio.EspacoUsadoEmMB)));
    v_dados := REPLACE(v_dados, '@EspacoLivreNaTableSpaceEmMBAntesExecutar', TO_CHAR(TRUNC(PTELEMETRIA.DadosTableSapceInicio.EspacoLivreEmMB)));
    v_dados := REPLACE(v_dados, '@EspacoUtilizadoNaTableSpaceEmMBAposExecutar', TO_CHAR(TRUNC(PTELEMETRIA.DadosTableSapceFinal.EspacoUsadoEmMB)));
    v_dados := REPLACE(v_dados, '@EspacoLivreNaTableSpaceEmMBAposExecutar', TO_CHAR(TRUNC(PTELEMETRIA.DadosTableSapceFinal.EspacoLivreEmMB)));
    
    PTELEMETRIA.Dados := v_dados;
  END FINALIZAR_TELEMETRIA;
  
  PROCEDURE GRAVAR_TELEMETRIA(PTELEMETRIA IN T_TELEMETRIA) IS 
    v_sql VARCHAR2(4000);
  BEGIN
    v_sql := 'INSERT INTO PCTELEMETRIAMONITOR (
       DATAPROCESSAMENTO,
       OBJETO,
       DATAHORAINICIO,
       DATAHORATERMINO,
       DADOS    
     ) VALUES (
       TRUNC(SYSDATE),
       ''@param1'',
       TO_DATE(''@param2'', ''DD/MM/YYYY HH24:MI:SS''),
       TO_DATE(''@param3'', ''DD/MM/YYYY HH24:MI:SS''),
       ''@param4''
     )';
     
    v_sql := REPLACE(v_sql, '@param1', PTELEMETRIA.Objeto);
    v_sql := REPLACE(v_sql, '@param2', TO_CHAR(PTELEMETRIA.DataHoraInicio, 'DD/MM/YYYY HH24:MI:SS'));
    v_sql := REPLACE(v_sql, '@param3', TO_CHAR(PTELEMETRIA.DataHoraTermino, 'DD/MM/YYYY HH24:MI:SS'));
    v_sql := REPLACE(v_sql, '@param4', PTELEMETRIA.Dados);
  
    EXECUTE IMMEDIATE v_sql;
    COMMIT;
  END GRAVAR_TELEMETRIA;
  
  PROCEDURE FINALIZAR_E_GRAVAR_TELEMETRIA(PTELEMETRIA IN OUT T_TELEMETRIA) IS 
  BEGIN
    FINALIZAR_TELEMETRIA(PTELEMETRIA);
    GRAVAR_TELEMETRIA(PTELEMETRIA);
  END FINALIZAR_E_GRAVAR_TELEMETRIA;



