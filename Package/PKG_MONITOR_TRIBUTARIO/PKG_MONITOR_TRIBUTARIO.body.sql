CREATE OR REPLACE PACKAGE BODY PKG_MONITOR_TRIBUTARIO AS

  FUNCTION QUOTED(PTEXTO VARCHAR2) RETURN VARCHAR2 IS 
  BEGIN
    RETURN CHR(39) || PTEXTO || CHR(39);
  END QUOTED;
  
  
  FUNCTION QUOTEDDUPLO(PTEXTO VARCHAR2) RETURN VARCHAR2 IS 
  BEGIN
    RETURN CHR(39) || CHR(39) || PTEXTO || CHR(39) || CHR(39);
  END QUOTEDDUPLO;


  FUNCTION USA_MONITOR_TRIBUTARIO RETURN BOOLEAN IS 
  BEGIN
    RETURN TRUE;
  END USA_MONITOR_TRIBUTARIO;


  FUNCTION EXECUTA_CARGA_DO_MONITOR RETURN BOOLEAN IS 
    v_data_ultima_execucao DATE;
    v_ativo VARCHAR2(1);
    v_existe_tabela_monitor NUMBER;
  BEGIN
    BEGIN
      
      EXECUTE IMMEDIATE 'SELECT DTULTPROCESSAMENTO, ATIVO
                FROM PCCONTROLEPROCESSAMENTO
                WHERE OBJETO = ''PKG_MONITOR_TRIBUTARIO'''
      INTO v_data_ultima_execucao, v_ativo;
    EXCEPTION 
      WHEN OTHERS THEN
        v_data_ultima_execucao := SYSDATE - 1;
        v_ativo := 'N';
    END;
    
    SELECT COUNT(1)
    INTO v_existe_tabela_monitor
    FROM USER_OBJECTS
    WHERE OBJECT_TYPE = 'TABLE'
          AND OBJECT_NAME LIKE 'PCMONITOR%'
          AND CREATED >= SYSDATE;
    
    RETURN nvl(v_ativo, 'N') = 'S';
    RETURN TRUNC(SYSDATE) > TRUNC(v_data_ultima_execucao)
           and nvl(v_ativo, 'N') = 'S'
           and v_existe_tabela_monitor <= 0;
  END EXECUTA_CARGA_DO_MONITOR;
  

  PROCEDURE ATUALIZAR_DATA_ULTIMA_EXECUCAO IS
  BEGIN
    EXECUTE IMMEDIATE 'UPDATE PCCONTROLEPROCESSAMENTO SET 
                DTULTPROCESSAMENTO = SYSDATE
              WHERE OBJETO = ''PKG_MONITOR_TRIBUTARIO'''; 
  END ATUALIZAR_DATA_ULTIMA_EXECUCAO;


  FUNCTION GET_NOME_TABELA_FORMATADA(PPREFIXO IN VARCHAR2, PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
    v_tabela VARCHAR2(30);
  BEGIN
    v_tabela := SUBSTR(PTABELA, 3, LENGTH(PTABELA)); -- remove o prefixo PC
    v_tabela := PPREFIXO || v_tabela;
    v_tabela := 'PC' || v_tabela; -- restaura o prefixo PC

    RETURN  SUBSTR(v_tabela, 1, 30);
  END GET_NOME_TABELA_FORMATADA;


  FUNCTION GET_NOME_TABELA_TEMPORARIA(PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN GET_NOME_TABELA_FORMATADA('TMPMON', PTABELA);
  END GET_NOME_TABELA_TEMPORARIA;


  FUNCTION GET_NOME_TABELA_MONITOR(PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN GET_NOME_TABELA_FORMATADA('MONITOR', PTABELA);
  END GET_NOME_TABELA_MONITOR;


  FUNCTION VERIFICAR_EXISTE_TABELA_TMPMON(PTABELA IN VARCHAR2) RETURN BOOLEAN IS
    v_existe_tabela NUMBER;
  BEGIN
    SELECT COUNT(TABLE_NAME)
    INTO v_existe_tabela
    FROM USER_TABLES
    WHERE TABLE_NAME = PTABELA;

    RETURN v_existe_tabela > 0;
  END VERIFICAR_EXISTE_TABELA_TMPMON;
  

  FUNCTION GET_TAMANHO_TABELA_EM_MB(PTABELA IN VARCHAR2) RETURN NUMBER IS 
    v_tamanho NUMBER;
  BEGIN
    SELECT SUM(BYTES)/POWER(1024,2) 
    INTO v_tamanho
    FROM USER_SEGMENTS 
    WHERE SEGMENT_NAME = PTABELA;  
    
    RETURN v_tamanho;
  END GET_TAMANHO_TABELA_EM_MB; 

  
  FUNCTION GET_QUANTIDADE_REGISTROS_TAB(PTABELA IN VARCHAR2) RETURN NUMBER IS 
    C_COUNT_TABELA CONSTANT VARCHAR(100) := 'SELECT COUNT(1) FROM @TABELA';
    v_sql VARCHAR(100);
    v_quantidade_registros NUMBER;
  BEGIN
    v_sql := REPLACE(C_COUNT_TABELA, '@TABELA', PTABELA);
    EXECUTE IMMEDIATE v_sql INTO v_quantidade_registros;
    
    RETURN v_quantidade_registros;
  END GET_QUANTIDADE_REGISTROS_TAB;


  FUNCTION GET_ESPACO_LIVRE_TABLESPACE_MB(PTABLESPACE IN VARCHAR2 := 'TS_DADOS') RETURN NUMBER IS 
    v_tamanho NUMBER;
  BEGIN
    SELECT SUM(BYTES)/POWER(1024,2)
    INTO v_tamanho
    FROM USER_FREE_SPACE
    WHERE TABLESPACE_NAME = PTABLESPACE;
    
    RETURN v_tamanho;
  END GET_ESPACO_LIVRE_TABLESPACE_MB;


  FUNCTION GET_ESPACO_USADO_TABLESPACE(PTABLESPACE IN VARCHAR2 := 'TS_DADOS') RETURN NUMBER IS 
    v_tamanho NUMBER;
  BEGIN
    SELECT SUM(BYTES)/POWER(1024,2) 
    INTO v_tamanho
    FROM USER_SEGMENTS 
    WHERE TABLESPACE_NAME = PTABLESPACE;
    
    RETURN v_tamanho;
  END GET_ESPACO_USADO_TABLESPACE;


  FUNCTION GET_VERSAO_OBJETO(PNOMEOBJETO IN VARCHAR2) RETURN VARCHAR2 IS
    v_versao VARCHAR2(15);
  BEGIN
    BEGIN
      SELECT VERSAO
      INTO v_versao
      FROM PCVERSAOBD
      WHERE ROTINA = PNOMEOBJETO;
    EXCEPTION 
      WHEN OTHERS THEN
        v_versao := '0.0.01.000';  
    END;
      
    RETURN v_versao;
  END;

  PROCEDURE INICIAR_TELEMETRIA(PTELEMETRIA IN OUT T_TELEMETRIA, PNOME_OBJETO IN VARCHAR2) IS 
  BEGIN
    PTELEMETRIA.Objeto := PNOME_OBJETO;
    PTELEMETRIA.DataHoraInicio := SYSDATE;
    PTELEMETRIA.DadosTableSapceInicio.EspacoUsadoEmMB := GET_ESPACO_USADO_TABLESPACE();
    PTELEMETRIA.DadosTableSapceInicio.EspacoLivreEmMB := GET_ESPACO_LIVRE_TABLESPACE_MB();
  END INICIAR_TELEMETRIA;

  
  PROCEDURE CAPTURA_DADOS_TELEMETRIA_WT(PTELEMETRIA IN OUT T_TELEMETRIA, PTABELA IN VARCHAR2) IS
  BEGIN
    PTELEMETRIA.DadosDaTabelaWinthor.NomeDaTabela := PTABELA;
    PTELEMETRIA.DadosDaTabelaWinthor.QuantidadeRegistrosTabela := GET_QUANTIDADE_REGISTROS_TAB(PTABELA);
    PTELEMETRIA.DadosDaTabelaWinthor.TamanhoTabelaEmMB := GET_TAMANHO_TABELA_EM_MB(PTABELA);
  END CAPTURA_DADOS_TELEMETRIA_WT;
  
  PROCEDURE CAPTURA_DADOS_TELEMETRIA_MON(PTELEMETRIA IN OUT T_TELEMETRIA, PTABELA IN VARCHAR2) IS
  BEGIN
    PTELEMETRIA.DadosDaTabelaMonitor.NomeDaTabela := GET_NOME_TABELA_MONITOR(PTABELA);
    PTELEMETRIA.DadosDaTabelaMonitor.QuantidadeRegistrosTabela := GET_QUANTIDADE_REGISTROS_TAB(PTELEMETRIA.DadosDaTabelaMonitor.NomeDaTabela);
    PTELEMETRIA.DadosDaTabelaMonitor.TamanhoTabelaEmMB := GET_TAMANHO_TABELA_EM_MB(PTELEMETRIA.DadosDaTabelaMonitor.NomeDaTabela);
  END CAPTURA_DADOS_TELEMETRIA_MON;


  PROCEDURE FINALIZAR_TELEMETRIA(PTELEMETRIA IN OUT T_TELEMETRIA) IS 
