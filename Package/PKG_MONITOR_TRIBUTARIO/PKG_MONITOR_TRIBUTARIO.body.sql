  PROCEDURE GRAVAR_TELEMETRIA(PTELEMETRIA IN T_TELEMETRIA) IS 
    v_Query VARCHAR2(4000);
  BEGIN
    v_Query := 'INSERT INTO PCTELEMETRIAMONITOR (
                  DATAPROCESSAMENTO,
                  OBJETO,
                  DATAHORAINICIO,
                  DATAHORATERMINO,
                  DADOS    
                ) VALUES (
                  TRUNC(SYSDATE),
                  ''@param1'',
                  TO_DATE(''@param2'', ''DD/MM/YYYY HH24:MI:SS''),
                  TO_DATE(''@param3'', ''DD/MM/YYYY HH24:MI:SS''),
                  ''@param4''
                )';
              
    v_Query := REPLACE(v_Query, '@param1', PTELEMETRIA.Objeto);
    v_Query := REPLACE(v_Query, '@param2', TO_CHAR(PTELEMETRIA.DataHoraInicio, 'DD/MM/YYYY HH24:MI:SS'));
    v_Query := REPLACE(v_Query, '@param3', TO_CHAR(PTELEMETRIA.DataHoraTermino, 'DD/MM/YYYY HH24:MI:SS'));
    v_Query := REPLACE(v_Query, '@param4', PTELEMETRIA.Dados);
  
    EXECUTE IMMEDIATE v_Query;
    COMMIT;
  EXCEPTION 
    WHEN OTHERS THEN
      ROLLBACK;
  END GRAVAR_TELEMETRIA;
  
  
