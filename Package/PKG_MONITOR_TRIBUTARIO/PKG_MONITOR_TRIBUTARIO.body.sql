          RAISE;
        END IF;
    END;
  END APAGAR_TABELA;


  PROCEDURE CRIAR_ESTRUTURA_TELEMETRIA IS 
    v_tabela_telemetria_existe NUMBER;
  BEGIN
      
    SELECT COUNT(1)
    INTO v_tabela_telemetria_existe
    FROM USER_TABLES
    WHERE TABLE_NAME = 'PCTELEMETRIAMONITOR';
    
    IF v_tabela_telemetria_existe = 0 THEN
      EXECUTE IMMEDIATE PKG_MONITOR_TRIBUTARIO_CONSULT.GET_CREATE_TABELA_TELEMETRIA;
      EXECUTE IMMEDIATE 'CREATE INDEX PCTELEMETRIAMONITOR_IDX01 ON PCTELEMETRIAMONITOR(DATAPROCESSAMENTO) NOLOGGING TABLESPACE TS_INDICE';
    END IF;
  END CRIAR_ESTRUTURA_TELEMETRIA;


  PROCEDURE CRIAR_ESTRUTURA_CONTROLEPROC IS 
    v_tabela_telemetria_existe NUMBER;
  BEGIN
      
    SELECT COUNT(1)
    INTO v_tabela_telemetria_existe
    FROM USER_TABLES
    WHERE TABLE_NAME = 'PCCONTROLEPROCESSAMENTO';
    
    IF v_tabela_telemetria_existe = 0 THEN
      EXECUTE IMMEDIATE PKG_MONITOR_TRIBUTARIO_CONSULT.GET_CREATE_TABELA_CONTROLEPROC;
      EXECUTE IMMEDIATE 'CREATE INDEX PCCONTROLEPROCESSAMENTO_IDX01 ON PCCONTROLEPROCESSAMENTO(OBJETO) NOLOGGING TABLESPACE TS_INDICE';
      EXECUTE IMMEDIATE 'INSERT INTO PCCONTROLEPROCESSAMENTO(OBJETO, ATIVO) VALUES (''PCCONTROLEPROCESSAMENTO'', ''S'')';

      COMMIT;
    END IF;
  END CRIAR_ESTRUTURA_CONTROLEPROC;


