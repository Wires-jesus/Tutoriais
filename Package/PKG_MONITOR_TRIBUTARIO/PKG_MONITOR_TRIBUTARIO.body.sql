    v_Query := 'INSERT INTO PCMONITORTRIBUTARIOLISTATABELA (
                  TABELAORIGEM,
                  TABELATEMPORARIA,
                  TABELAMONITOR,
                  TABELADEPENDENCIA,
                  INDICE
                ) VALUES (
                  @TABELAORIGEM,
                  @TABELATEMPORARIA,
                  @TABELAMONITOR,
                  @TABELADEPENDENCIA,
                  @INDICE
                )';
                
    v_Query := REPLACE(v_Query, '@TABELAORIGEM', QUOTED(PTABELA));
    v_Query := REPLACE(v_Query, '@TABELATEMPORARIA', QUOTED(GET_NOME_TABELA_TEMPORARIA(PTABELA)));
