  FUNCTION TABELA_MONITOR_DESATUALIZADA(PTABELA VARCHAR2) RETURN BOOLEAN IS 
    v_DataUltimoProcessamento DATE;
    v_DataCriacaoTabela DATE;
  BEGIN
    BEGIN      
      EXECUTE IMMEDIATE 'SELECT DTULTPROCESSAMENTO
                         FROM PCCONTROLEPROCESSAMENTO
                         WHERE OBJETO = ''PKG_MONITOR_TRIBUTARIO'''
      INTO v_DataUltimoProcessamento;
    EXCEPTION 
      WHEN OTHERS THEN
        v_DataUltimoProcessamento := SYSDATE - 1;
    END;  
      
    BEGIN      
      EXECUTE IMMEDIATE 'SELECT CREATED
                         FROM USER_OBJECTS
                         WHERE OBJECT_TYPE = ''TABLE''
                           AND OBJECT_NAME = ' || QUOTED(PTABELA)
      INTO v_DataCriacaoTabela;
    EXCEPTION 
      WHEN OTHERS THEN
        v_DataCriacaoTabela := SYSDATE - 1;
    END;

    RETURN (TRUNC(v_DataCriacaoTabela) < TRUNC(v_DataUltimoProcessamento)) 
           OR (v_DataUltimoProcessamento IS NULL);
  END TABELA_MONITOR_DESATUALIZADA;


  FUNCTION DADOS_ESTAO_DESATUALIZADOS RETURN BOOLEAN IS 
    v_DataUltimoProcessamento DATE;
    v_DataPreparacaoAmbiente DATE;
    v_Ativo VARCHAR2(1);
  BEGIN
    BEGIN
      EXECUTE IMMEDIATE 'SELECT
                           NVL(DTULTPROCESSAMENTO, SYSDATE - 1),
                           NVL(DTPREPARACAOAMBIENTE, SYSDATE),
                           ATIVO
                         FROM PCCONTROLEPROCESSAMENTO
                         WHERE OBJETO = ''PKG_MONITOR_TRIBUTARIO'''
      INTO v_DataUltimoProcessamento,
           v_DataPreparacaoAmbiente,
           v_Ativo;
    EXCEPTION 
      WHEN OTHERS THEN
        v_DataUltimoProcessamento := SYSDATE - 1;
        v_Ativo := 'N';
    END;

    RETURN TRUNC(v_DataUltimoProcessamento) < TRUNC(v_DataPreparacaoAmbiente)
           AND NVL(v_Ativo, 'N') = 'S';
  END DADOS_ESTAO_DESATUALIZADOS;

