  PROCEDURE GERAR_DADOS_PCDEVAVULSO(P_QUANTIDADE_REGISTROS_A_GERAR NUMBER) IS
    V_QUANTIDADE_REGISTROS_A_GERAR NUMBER;

    C_LIMITE_REGISTROS_POR_PAGINA CONSTANT NUMBER := 10000;
    
    V_QUANTIDADE_REGISTROS_TABELA NUMBER;
    V_LIMITE_DA_PAGINACAO NUMBER;
    
    V_LIMITE_DA_PAGINA_EXCEDIDO BOOLEAN;
    V_EXISTE_REGISTRO_RESIDUAL BOOLEAN;
    V_REG_SOLICITADOS_INFERIOR_LIM BOOLEAN;
    
    V_NUMTRANSVENDA NUMBER;
    V_NUMTRANSENT NUMBER;
  BEGIN
    V_QUANTIDADE_REGISTROS_A_GERAR := NVL(P_QUANTIDADE_REGISTROS_A_GERAR, 1000);
    
    SELECT 
      COUNT(1)
    INTO 
      V_QUANTIDADE_REGISTROS_TABELA
    FROM PCDEVAVULSO;
    
    V_LIMITE_DA_PAGINACAO := V_QUANTIDADE_REGISTROS_TABELA;

    V_LIMITE_DA_PAGINA_EXCEDIDO := V_QUANTIDADE_REGISTROS_TABELA >
    C_LIMITE_REGISTROS_POR_PAGINA;
    
    IF V_LIMITE_DA_PAGINA_EXCEDIDO THEN
      V_LIMITE_DA_PAGINACAO := C_LIMITE_REGISTROS_POR_PAGINA;
     
      V_REG_SOLICITADOS_INFERIOR_LIM := C_LIMITE_REGISTROS_POR_PAGINA > V_QUANTIDADE_REGISTROS_A_GERAR ;
    
      IF NOT V_REG_SOLICITADOS_INFERIOR_LIM THEN
        V_LIMITE_DA_PAGINACAO := V_QUANTIDADE_REGISTROS_A_GERAR;
      END IF;
    END IF; 
    
    WHILE V_QUANTIDADE_REGISTROS_A_GERAR > 0 LOOP
      SELECT 
        MAX(NUMTRANSVENDA),
        MAX(NUMTRANSENT)
      INTO 
        V_NUMTRANSVENDA,
        V_NUMTRANSENT
      FROM PCDEVAVULSO;

      INSERT INTO PCDEVAVULSO (
        NUMTRANSVENDA,
        NUMTRANSENT,
        NUMNOTA,
        SERIE,
        DTEMISSAO,
        CHAVENFE,
        MODELO,
        CODPAIS,
        CNPJCPFFORNEC,
        INSCESTADUAL,
        CODMUN,
        INSCSUFRAMA,
        ENDERECOFORNEC,
        NUMIMOVELFORNEC,
        COMPLENDFORNEC,
        BAIRROFORNEC,
        NOMEFORNEC,
        CODFORNEC
      )
      SELECT 
        (V_NUMTRANSVENDA + ROWNUM) NUMTRANSVENDA,
        (V_NUMTRANSENT + ROWNUM) NUMTRANSENT,
        NUMNOTA,
        SERIE,
        DECODE(MOD(ROWNUM, 4), 0, TO_DATE(TO_CHAR(NVL(DTEMISSAO, SYSDATE), 'DD/MM') ||  '/' || TO_CHAR(SYSDATE, 'YYYY'), 'DD/MM/YYYY'), DTEMISSAO),
        CHAVENFE,
        MODELO,
        CODPAIS,
        CNPJCPFFORNEC,
        INSCESTADUAL,
        CODMUN,
        INSCSUFRAMA,
        ENDERECOFORNEC,
        NUMIMOVELFORNEC,
        COMPLENDFORNEC,
        BAIRROFORNEC,
        NOMEFORNEC,
        CODFORNEC
      FROM PCDEVAVULSO
      WHERE ROWNUM <= V_LIMITE_DA_PAGINACAO;
    
      COMMIT;   
      
      V_QUANTIDADE_REGISTROS_A_GERAR := V_QUANTIDADE_REGISTROS_A_GERAR - V_LIMITE_DA_PAGINACAO;
      
      V_EXISTE_REGISTRO_RESIDUAL := V_LIMITE_DA_PAGINACAO > V_QUANTIDADE_REGISTROS_A_GERAR;
    
      IF V_EXISTE_REGISTRO_RESIDUAL THEN
        V_LIMITE_DA_PAGINACAO := V_QUANTIDADE_REGISTROS_A_GERAR;
      END IF;
    END LOOP;
    
  EXCEPTION
    WHEN OTHERS THEN
      DBMS_OUTPUT.put_line('PCDEVAVULSO => ERRO: ' || SQLERRM);
      ROLLBACK;  
  END GERAR_DADOS_PCDEVAVULSO;


