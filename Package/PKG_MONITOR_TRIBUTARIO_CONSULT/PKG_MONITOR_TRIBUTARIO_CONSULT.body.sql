CREATE OR REPLACE PACKAGE BODY "PKG_MONITOR_TRIBUTARIO_CONSULT" AS

  FUNCTION GET_DATA_INICIAL(P_DATA IN DATE) RETURN DATE IS
  BEGIN
    RETURN NVL(P_DATA, SYSDATE - 90);
  END GET_DATA_INICIAL;


  FUNCTION GET_DATA_FINAL(PDATA IN DATE) RETURN DATE IS
  BEGIN
    RETURN NVL(PDATA, SYSDATE);
  END GET_DATA_FINAL;


  FUNCTION GET_FILTRO_DATA_FORMATADA(PCAMPO IN VARCHAR2, PDATA_INICIAL IN DATE, PDATA_FINAL IN DATE) RETURN VARCHAR2 IS
    v_filtro VARCHAR(200);
  BEGIN
    v_filtro := PCAMPO || ' BETWEEN TO_DATE(''@DTINICIAL'', ''DD/MM/YYYY HH24:MI:SS'')'
                       || ' AND TO_DATE(''@DTFINAL'', ''DD/MM/YYYY HH24:MI:SS'') ';
                       
    v_filtro := REPLACE(v_filtro, '@DTINICIAL', TO_CHAR(GET_DATA_INICIAL(PDATA_INICIAL), 'DD/MM/YYYY') || ' 00:00:00');
    v_filtro := REPLACE(v_filtro, '@DTFINAL', TO_CHAR(GET_DATA_FINAL(PDATA_FINAL), 'DD/MM/YYYY') || ' 23:59:59');
    
    RETURN v_filtro;
  END GET_FILTRO_DATA_FORMATADA;


  FUNCTION GET_VALORES_TABELA_MONITOR(PTABELA IN VARCHAR2) RETURN VARCHAR2 IS
  BEGIN
    RETURN 'SELECT T.* FROM ' || PTABELA || ' T';
  END GET_VALORES_TABELA_MONITOR;


