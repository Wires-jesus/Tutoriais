  PROCEDURE PROCESSAR_ET_VIRT(P_NUMTRANSENT         IN NUMBER,
                              P_NUMTRANSVENDA       IN NUMBER,
                              P_CODFILIAL           IN PCPEDC.CODFILIAL%TYPE,
                              P_CODFILIALNF         IN PCPEDC.CODFILIALNF%TYPE,
                              P_VBDESTACAIPIENTRADA IN BOOLEAN,
                              P_VBDESTACAIPISAIDA   IN BOOLEAN,
                              P_NUMTRANSITEM        IN PCMOVCOMPLE.NUMTRANSITEM%TYPE,
                              P_MSG                 OUT VARCHAR2) IS

    -- INÍCIO - Cursor para processar o registro de transferência "ET" ---------------
    CURSOR C_TRANSF_ET(P_NUMTRANSVENDA PCMOV.NUMTRANSVENDA%TYPE, P_NUMTRANSITEM NUMBER) IS
      SELECT TODOS_REG.*
        FROM (SELECT M.*,
                     NVL(P.TIPOCALCST, 'N') TIPOCALCST,
                     P.UNIDADETRIB,
                     CM.VERSAOREGISTRO,
                     CM.VLICMS VLICMS_MOV_COMPLE,
                     S.CONDVENDA,
                     S.TIPOVENDA
                FROM PCMOV M, PCPRODUT P, PCNFSAID S, PCMOVCOMPLE CM
               WHERE M.CODPROD = P.CODPROD
                 AND M.NUMTRANSVENDA = S.NUMTRANSVENDA
                 AND M.NUMTRANSITEM = CM.NUMTRANSITEM
                 AND M.NUMTRANSVENDA = P_NUMTRANSVENDA
                 AND M.CODOPER = 'ST'
                 AND NOT EXISTS (SELECT 1
                       FROM PCTRANSFVIRT
                      WHERE NUMCAR = M.NUMCAR
                        AND CODPROD = M.CODPROD
                        AND NUMTRANSENTTRANSF IS NOT NULL
                        AND DTCANCEL IS NULL
                        )) TODOS_REG
       WHERE 1 = 1
         AND TODOS_REG.NUMTRANSITEM =
             DECODE(P_NUMTRANSITEM,
                    0,
                    TODOS_REG.NUMTRANSITEM,
                    P_NUMTRANSITEM);

    MOV_ENT C_TRANSF_ET%ROWTYPE; -- Variável contendo tipo do cursor
    -- FIM - Cursor para processar o registro de transferência "ET" ---------------

    VNCODST                  NUMBER;
    VSCODFILIALNF            VARCHAR2(4);
    VSCODFILIAL              VARCHAR2(4);
    ROWPCMOV_ET              PCMOV%ROWTYPE;
    ROWPCMOVCOMPLE           PCMOVCOMPLE%ROWTYPE;
    VSGERAICMSLIVROFISCAL    VARCHAR2(1);
    VNUFORIGEM               PCFILIAL.UF%TYPE;
    VNCODFORNEC              PCFILIAL.CODFORNEC%TYPE;
    VNNUMREGIAO              PCNFSAID.NUMREGIAO%TYPE;
    VNCODCLI                 PCNFSAID.CODCLI%TYPE;
    VNUFDESTINO              PCFILIAL.UF%TYPE;
    VNCODFISCALITEM          NUMBER;
    VNCODFISCALENTRADAITEM   NUMBER;
    VNBASEICMS_ENTRADA       NUMBER;
    VNBASEICMS_PUNIT         NUMBER;
    VNSTENTRADA              NUMBER;
    VNBASESTENTRADA          NUMBER;
    VNPERCIPIVENDA           NUMBER;
    VLBASEIPIVENDA           NUMBER;
    VLIPIVENDA               NUMBER;
    VNCUSTOCONTABIL          NUMBER;
    VNCODENQIPI              NUMBER;
    VN_CST_ENTRADA           NUMBER;
    VN_CST_SAIDA             NUMBER;
    vlProdutoSemST_IPI       NUMBER;
    VN_GERABASEALIQZERO      VARCHAR2(1);
    VN_MSG                   VARCHAR2(100);
    VNNUMTRANSITEM           PCMOVCOMPLE.NUMTRANSITEM%TYPE;
    ROWPCNFBASE              PCNFBASE%ROWTYPE;
    vsCODENQIPIENT           varchar2(3);
    vsCODENQIPI              varchar2(3);
    vsDADOSTRIBUTACAOIPI     varchar(1);
    VBGEROUMOV               BOOLEAN := FALSE;
    vsTipoCustoTrVirtual     PCCLIENT.TIPOCUSTOTRANSF_FILIALVIRTUAL%TYPE;
    vnCodSitTribpiscofins    PCPRODUT.Codsittribpiscofins%TYPE;
  BEGIN

    VSCODFILIAL   := P_CODFILIAL;
    VSCODFILIALNF := P_CODFILIALNF;

    --- BUSCA O CODIGO DO FORNECEDOR DA FILIAL ORIGEM
    BEGIN
      SELECT CODFORNEC, UF
        INTO VNCODFORNEC, VNUFORIGEM
        FROM PCFILIAL
       WHERE CODIGO = VSCODFILIAL; --FILIAL
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        VNCODFORNEC := 0;
        VNUFORIGEM  := '';
    END;

    -- BUSCA O CODIGO DO CLIENTE DA FILIAL DESTINO
    BEGIN
      SELECT CODCLI, UF
        INTO VNCODCLI, VNUFDESTINO
        FROM PCFILIAL
       WHERE CODIGO = VSCODFILIALNF; --FILIAL NF
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        VNCODCLI    := 0;
        VNUFDESTINO := '';
    END;

    --  TRANSF.CLIENTE := BUSCAR_CLIENTE(10);
    BEGIN
      -- VERIFICAR DADOS DO CLIENTE
      SELECT /* PCTABPRCLI.CODFILIALNF,*/
       PCTABPRCLI.NUMREGIAO /*, PCREGIAO.PERFRETE*/
        INTO VNNUMREGIAO
        FROM PCTABPRCLI, PCREGIAO, PCFILIAL
       WHERE PCTABPRCLI.CODCLI = VNCODCLI
         AND PCTABPRCLI.NUMREGIAO = PCREGIAO.NUMREGIAO
         AND PCTABPRCLI.CODFILIALNF = PCFILIAL.CODIGO
         AND PCTABPRCLI.CODFILIALNF = VSCODFILIALNF;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        VNNUMREGIAO := 0;
    END;

    IF VNNUMREGIAO = 0 THEN
      BEGIN
        SELECT --PCUSUARI.CODUSUR,
        --   NVL(PCCLIENT.TIPOFJ, 'J') TIPOFJ,
        --   PCCLIENT.CODPRACA,
        --   PCCLIENT.CODCOB,
        --   PCUSUARI.CODSUPERVISOR,
         PCPRACA.NUMREGIAO
        --   PCCLIENT.ESTENT
          INTO VNNUMREGIAO
          FROM PCCLIENT, PCUSUARI, PCPRACA
         WHERE PCCLIENT.CODUSUR1 = PCUSUARI.CODUSUR
           AND PCCLIENT.CODPRACA = PCPRACA.CODPRACA
           AND PCCLIENT.CODCLI = VNCODCLI;

      EXCEPTION
        WHEN NO_DATA_FOUND THEN
          VNNUMREGIAO := 0;
      END;
    END IF;
    -- TIPO CUSTO
    BEGIN
     SELECT NVL(TIPOCUSTOTRANSF_FILIALVIRTUAL, 'P') TIPOCUSTOTRANSF --,
            --PERACRESTRANSF
            INTO vsTipoCustoTrVirtual
       FROM PCCLIENT
    WHERE CODCLI = VNCODCLI;
      EXCEPTION
        WHEN NO_DATA_FOUND THEN
          vsTipoCustoTrVirtual := 'P';
    END;


    -- INICIA O LOOP - PCMOV ---------------------------------
    VBGEROUMOV := FALSE;
    OPEN C_TRANSF_ET(NVL(P_NUMTRANSVENDA, 0), NVL(P_NUMTRANSITEM, 0));
    LOOP
      --\* BEGIN*\
      FETCH C_TRANSF_ET
        INTO MOV_ENT;
      EXIT WHEN C_TRANSF_ET%NOTFOUND;
      /*\*    EXCEPTION
        WHEN OTHERS THEN
          NULL;
      END;*\*/

      SELECT M.*
        INTO ROWPCMOV_ET
        FROM PCMOV M
       WHERE NUMTRANSITEM = MOV_ENT.NUMTRANSITEM
         AND ROWNUM = 1;

      SELECT DFSEQ_PCMOVCOMPLE.NEXTVAL INTO VNNUMTRANSITEM FROM DUAL;

      IF TRANSF.PARAMETRO.USATRIBUTACAOPORUF = 'S' THEN
        VNCODST := 0;
        BEGIN
          SELECT PCTABTRIB.CODST
            INTO VNCODST
            FROM PCTABTRIB, PCTRIBUT
           WHERE PCTABTRIB.CODST = PCTRIBUT.CODST
