  PROCEDURE CANCELAR_NOTA_TRANSFERENCIA(P_NUMTRANSVENDA  IN  NUMBER,
                                        P_NUMTRANSENT    IN  NUMBER,
                                        P_MSG            OUT VARCHAR2)
  IS
    MSG_PKG_ESTOQUE        VARCHAR2(100);
    vrow                   pcmov%ROWTYPE;
    vnretorno_pkg_estoque  NUMBER(10);
  BEGIN
    p_msg := 'OK';
    /** CANCELANDO NOTA DE SAIDA **/
    UPDATE PCNFSAID
       SET VLTOTAL          = 0
          ,COMISSAO         = 0
          ,VLTOTGER         = 0
          ,VLTABELA         = 0
          ,VLCUSTOREAL      = 0
          ,VLCUSTOFIN       = 0
          ,VLCUSTOREP       = 0
          ,VLOUTRASDESP     = 0
          ,VLFRETE          = 0
          ,VLDESCONTO       = 0
          ,BCST             = 0
          ,STRIBUT          = 0
          ,DTCANCEL         = TRUNC(SYSDATE)
          ,CODFUNCCANCEL    = 0
          ,TOTPESO          = 0
          ,TOTVOLUME        = 0
          ,NUMITENS         = 0
          ,ICMSRETIDO       = 0
          ,OBS              = 'CANCELAMENTO DE NOTA NÃO PROCESSADA PELO SERVIDOR.. ' || OBS
          ,VLBASEIPI        = 0
          ,VLIPI            = 0
          ,VLICMSANTECIPADO = 0
          ,VLRECEBIDOCARTAO = 0
          ,ESPECIE          = 'TP' --para não aparecer no sped.
          ,SITUACAONFE      = -1
          ,DTDENEGADA       = NULL
     WHERE NUMTRANSVENDA = P_NUMTRANSVENDA;
	 
     VSPROGRAMANOVO := SUBSTR(SYS_CONTEXT('USERENV','MODULE'),1,30); 

    /** CANCELANDO NOTA DE ENTRADA **/
    UPDATE PCNFENT
       SET DTCANCEL    = TRUNC(SYSDATE)
          ,VLTOTAL     = 0
          ,TOTPESO     = 0
          ,VLDESCFIN   = 0
          ,VLIPI       = 0
          ,VLDESCONTO  = 0
          ,VLBASEIPI   = 0
          ,BASEICST    = 0
          ,VLST        = 0
          ,ESPECIE     = 'TP' --para não aparecer no sped
          ,SITUACAONFE = -1
          ,OBS         = 'CANCELAMENTO DE NOTA NÃO PROCESSADA PELO SERVIDOR.. ' || OBS
     WHERE NUMTRANSENT = P_NUMTRANSENT;

    /** CANCELANDO ITENS DA ENTRADA E SAIDA **/
    FOR PCMOV_INSERT IN (SELECT ROWID ROWID_PCMOV, NUMTRANSITEM, CODPROD
                           FROM PCMOV
                          WHERE (NUMTRANSVENDA = P_NUMTRANSVENDA OR
                                 NUMTRANSENT = P_NUMTRANSENT)
                            AND DTCANCEL IS NULL
                           ORDER BY PCMOV.NUMTRANSITEM)
    LOOP

      VROW              := FERRAMENTAS.RETORNAR_ROWTIPEPCMOV(PCMOV_INSERT.ROWID_PCMOV);
      VROW.NUMTRANSITEM := NULL;
      VROW.QT           := VROW.QT * -1;
      VROW.QTCONT       := VROW.QTCONT * -1;
      VROW.NUMTRANSDEV  := NULL;
      VROW.DTCANCEL     := TRUNC(SYSDATE);
	  VROW.DATAESTOQUE  := NULL;
	  VROW.ROTINACAD    := VSPROGRAMANOVO;

      INSERT INTO PCMOV VALUES VROW;

      UPDATE PCMOV SET DTCANCEL = SYSDATE
       WHERE ROWID = PCMOV_INSERT.ROWID_PCMOV;
    END LOOP;

    INSERT INTO PCLANC
      (RECNUM
      ,DTLANC
      ,DTVENC
      ,CODCONTA
      ,TIPOPARCEIRO
      ,CODFORNEC
      ,HISTORICO
      ,NUMNOTA
      ,DUPLIC
      ,VALOR
      ,VPAGO
      ,CODFILIAL
      ,INDICE
      ,NOMEFUNC
      ,DTPAGTO
      ,NUMTRANSVENDA
      ,NUMTRANSENT)
      SELECT FERRAMENTAS.F_PROX_RECNUM
            ,TRUNC(SYSDATE)
            ,TRUNC(SYSDATE)
            ,CODCONTA
            ,TIPOPARCEIRO
            ,CODFORNEC
            ,'CANCELAMENTO DE NF DE TRANSFERENCIA - ' || HISTORICO
            ,NUMNOTA
            ,DUPLIC
            ,(VALOR * -1)
            ,(VPAGO * -1)
            ,CODFILIAL
            ,INDICE
            ,NOMEFUNC
            ,TRUNC(SYSDATE)
            ,NUMTRANSVENDA
            ,NUMTRANSENT
        FROM PCLANC
       WHERE NUMTRANSENT = P_NUMTRANSENT;

    vnretorno_pkg_estoque := PKG_ESTOQUE.VENDAS_MOVIMENTACAO(P_NUMTRANSENT,P_NUMTRANSVENDA, 'S', MSG_PKG_ESTOQUE);

    IF NVL(vnretorno_pkg_estoque, 0) <= 0 THEN
      P_MSG := 'NÃO FOI POSSÍVEL EFETUAR O CANCELAMENTO DA MOVIMENTAÇÃO DOS ITENS DE TRANSFERENCIA. ' || CHR(13) ||
               'MENSAGEM PKG: ' || MSG_PKG_ESTOQUE;
			   
			INSERT INTO PCLOGALTERACAODADOS
				(DATA,
				CODFUNC,
				CODROTINA,
				TABELA,
				COLUNA,
				TIPOVALOR,
				VALORNUM,
				VALORNUMANT,
				VALORALFA,
				VALORALFAANT,
				USACTRLACESSO,
				CODCTRLACESSO,
				OBSERVACOES,
				MAQUINA,
				PROGRAMA,
				TERMINAL,
				OSUSER)
			VALUES
				(SYSDATE,
				8888,
				1452,
				'PCLOGPKG_ESTOQUE',
				'IDENTIFICADOR',
				'N',
				P_NUMTRANSVENDA,
				P_NUMTRANSVENDA,
				P_NUMTRANSENT,
				P_NUMTRANSENT,
				'S',
				NULL,
				P_MSG,
				SYS_CONTEXT('USERENV', 'TERMINAL'),
				SYS_CONTEXT('USERENV', 'HOST'),
				SYS_CONTEXT('USERENV', 'MODULE'),
				SYS_CONTEXT('USERENV', 'OS_USER')
				);
				RAISE ExecacaoEstoque;
    END IF;
  END;

BEGIN
  --Inicializando as variáveis
  P_MSG := 'OK';
  vnRetornoPkgEstoque := 0;

  IF  P_EFATURAMENTO = 'S' AND P_ECANCELAMENTO = 'N' THEN
     vsACAO := 'Faturamento';
     vnCodigoAcao := 0;
  ELSIF P_EFATURAMENTO = 'S' AND P_ECANCELAMENTO = 'S'THEN
     vsACAO := 'Canc. fat.';
     vnCodigoAcao := 1;
  ELSIF P_EFATURAMENTO = 'N' AND P_ECANCELAMENTO = 'N' THEN
     vsACAO := 'Devolução';
     vnCodigoAcao := 2;
  ELSIF P_EFATURAMENTO = 'N' AND P_ECANCELAMENTO = 'S' THEN
     vsACAO := 'Canc. Devolução';
     vnCodigoAcao := 3;
  END IF;

  /**************************************************************************/

  /**************************************************************************/
  IF ((P_EFATURAMENTO = 'S') AND (P_ECANCELAMENTO = 'S')) THEN
   begin
      SELECT DADOS.ESP_VENDA
            ,DADOS.SITNFE_VENDA
            ,DADOS.TRANS_SAIDA_TRANSF
            ,DADOS.TRANS_ENT_TRANSF
            ,DADOS.ESP_TRANSF
        INTO VESPECIENFEVENDA
            ,VSITUACAONFEVENDA
            ,VNUMTRANSVENDATRANSF
            ,VNUMTRANSENTTRANSF
            ,vEspecieNfeTransf
         from (SELECT VENDA.ESPECIE                  ESP_VENDA,
               VENDA.SITUACAONFE              SITNFE_VENDA,
               SAIDA_TRANSF.NUMTRANSVENDA     TRANS_SAIDA_TRANSF,
               SAIDA_TRANSF.NUMTRANSENTORIGEM TRANS_ENT_TRANSF,
               SAIDA_TRANSF.ESPECIE           ESP_TRANSF
          FROM PCNFSAID VENDA, PCNFSAID SAIDA_TRANSF
         WHERE VENDA.NUMTRANSVENDA = P_NUMTRANSVENDA
           AND SAIDA_TRANSF.NUMTRANSVENDADEPENDENTE = VENDA.NUMTRANSVENDA
           and SAIDA_TRANSF.Dtsaida >=
               (select DTSAIDA
                  from pcnfsaid
                 where NUMTRANSVENDA = P_NUMTRANSVENDA
                   and rownum = 1)
        UNION
        SELECT VENDA.ESPECIE                  ESP_VENDA,
               VENDA.SITUACAONFE              SITNFE_VENDA,
               SAIDA_TRANSF.NUMTRANSVENDA     TRANS_SAIDA_TRANSF,
               SAIDA_TRANSF.NUMTRANSENTORIGEM TRANS_ENT_TRANSF,
               SAIDA_TRANSF.ESPECIE           ESP_TRANSF
          FROM PCNFSAIDPREFAT VENDA, PCNFSAID SAIDA_TRANSF
         WHERE VENDA.NUMTRANSVENDA = P_NUMTRANSVENDA
           AND SAIDA_TRANSF.NUMTRANSVENDADEPENDENTE = VENDA.NUMTRANSVENDA
           and SAIDA_TRANSF.Dtsaida >=
               (select DTSAIDA
                  from PCNFSAIDPREFAT
                 where NUMTRANSVENDA = P_NUMTRANSVENDA
                   and rownum = 1)) DADOS;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
           RETURN;
    end;

    IF  (VSITUACAONFEVENDA NOT IN (100, 101)) AND
        (VNUMTRANSVENDATRANSF > 0)
    THEN
      CANCELAR_NOTA_TRANSFERENCIA(VNUMTRANSVENDATRANSF, VNUMTRANSENTTRANSF, vsResultadoEstoque);
      IF vsResultadoEstoque <> 'OK' THEN
        RAISE ExecacaoEstoque;
      END IF;

      RETURN;
    END IF;
  END IF;
  /**************************************************************************/

  BEGIN --Listando as possíveis filiais a serem tratadas.
    FOR LISTAPOSSIVEISREGISTROS IN (SELECT DISTINCT CASE WHEN P_EFATURAMENTO = 'S' AND P_ECANCELAMENTO = 'N'
                                                THEN PCMOV.CODFILIALRETIRA
                                                WHEN P_EFATURAMENTO = 'S' AND P_ECANCELAMENTO = 'S'
                                                THEN PCMOV.CODFILIAL
                                                WHEN P_EFATURAMENTO = 'N' AND P_ECANCELAMENTO = 'N'
                                                THEN PCMOV.CODFILIAL
                                                WHEN P_EFATURAMENTO = 'N' AND P_ECANCELAMENTO = 'S'
                                                THEN PCMOV.CODFILIALRETIRA
                                                ELSE PCMOV.CODFILIAL
                                           END CODFILIALSAIDA
                                         , CASE WHEN P_EFATURAMENTO = 'S' AND P_ECANCELAMENTO = 'N'
                                                THEN PCMOV.CODFILIAL
                                                WHEN P_EFATURAMENTO = 'S' AND P_ECANCELAMENTO = 'S'
                                                THEN PCMOV.CODFILIALRETIRA
                                                WHEN P_EFATURAMENTO = 'N' AND P_ECANCELAMENTO = 'N'
                                                THEN PCMOV.CODFILIALRETIRA
                                                WHEN P_EFATURAMENTO = 'N' AND P_ECANCELAMENTO = 'S'
                                                THEN PCMOV.CODFILIAL
                                                ELSE PCMOV.CODFILIALRETIRA
                                           END CODFILIALENTRADA
                                         , NVL(PCCONSUM.USATRIBUTACAOPORUF,'N')  USATRIBUTACAOPORUF
                                         , FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('GERATRIBUTENTTRANSFDEP', '99', 'N') GERATRIBUTENTTRANSFDEP
                                         , PCMOV.CODFILIALRETIRA CODFILIALCDVIRTUAL --Filial que tem o estoque
                                         , CASE WHEN NVL(PCCLIENT.ORGAOPUB,'N') = 'S'
                                                  OR NVL(PCCLIENT.ORGAOPUBFEDERAL,'N') = 'S'
                                                  OR NVL(PCCLIENT.ORGAOPUBMUNICIPAL,'N') = 'S'
                                                THEN 'S'
                                                ELSE 'N'
                                           END ORGAOPUBLICO
                                         , DECODE(NVL(PCCLIENT.CALCULAST, 'N'),'N', 'S','N')  ISENTODEST
                                      FROM PCMOV
                                         , PCMOVCOMPLE
                                         , PCCONSUM
                                         , PCNFSAID
                                         , PCCLIENT
                                     WHERE PCMOV.NUMTRANSITEM    = PCMOVCOMPLE.NUMTRANSITEM
                                       AND PCMOVCOMPLE.ORIGMERCTRIB = '1'
                                       AND PCMOV.CODFILIALRETIRA IS NOT NULL
                                       AND PCMOV.QT > 0
                                       AND NVL(PARAMFILIAL.obtercomovarchar2('CDVIRTUALIMPORTACAO', PCMOV.CODFILIALRETIRA),'N') = 'S'
                                       AND PCMOV.NUMTRANSVENDA = P_NUMTRANSVENDA
                                       AND PCMOV.NUMTRANSVENDA = PCNFSAID.NUMTRANSVENDA
                                       AND PCNFSAID.CODCLI = PCCLIENT.CODCLI
                                       AND ((P_EFATURAMENTO    = 'S')
                                        OR  (P_EFATURAMENTO    = 'N'
                                       AND EXISTS (SELECT 1
                                                     FROM PCMOV A
                                                    WHERE A.CODPROD       = PCMOV.CODPROD
                                                      AND A.NUMTRANSENT   = P_NUMTRANSENT
                                                      AND A.QT            > 0/*
                                                      AND A.DTCANCEL      IS NULL*/)))
                                     GROUP
                                        BY PCMOV.CODFILIALRETIRA
                                         , PCMOV.CODFILIAL
                                         , PCCONSUM.USATRIBUTACAOPORUF
                                         , PCCLIENT.ORGAOPUB
                                         , PCCLIENT.ORGAOPUBFEDERAL
                                         , PCCLIENT.ORGAOPUBMUNICIPAL
                                         , PCCLIENT.CALCULAST
                                   )
    LOOP
      --Carregando parametro sobre o WMS
      BEGIN
        SELECT SYSDATE
          INTO vdDATAWMS
          FROM (SELECT (SELECT PCPARAMETROWMS.VALOR
                          FROM PCPARAMETROWMS
                         WHERE PCPARAMETROWMS.NOME = 'FILIALESTOQUE'
                           AND PCPARAMETROWMS.CODFILIAL = LISTAPOSSIVEISREGISTROS.CODFILIALSAIDA) VALORSAIDA
                     , (SELECT PCPARAMETROWMS.VALOR
                          FROM PCPARAMETROWMS
                         WHERE PCPARAMETROWMS.NOME = 'FILIALESTOQUE'
                           AND PCPARAMETROWMS.CODFILIAL = LISTAPOSSIVEISREGISTROS.CODFILIALENTRADA) VALORENTRADA
          FROM DUAL) A
         WHERE VALORSAIDA = VALORENTRADA;
      EXCEPTION
         WHEN NO_DATA_FOUND THEN
             vdDATAWMS := Null;
      END;



    --------------------------------------------------------------------
    -- GERANDO NOTA FISCAL DE SAIDA DE TRANSFERENCIA NA FILIAL RETIRA --
    --------------------------------------------------------------------
      --Clonar cabeçalho da venda, sempre da nota orginal da venda
      voCURSORT_TEMP := FERRAMENTAS.RETORNAR_ROWTYPE('PCNFSAID',
                                                     'NUMTRANSVENDA',
                                                     TO_CHAR(P_NUMTRANSVENDA));
      FETCH voCURSORT_TEMP
       INTO voPCNFSAID;

      CLOSE voCURSORT_TEMP;

      BEGIN--Dados da filial de origem da nota de saída
        SELECT PCFILIAL.UF
             , PCFILIAL.CGC
             , PCFILIAL.IE
             , PCFILIAL.CODIGO
             , PCFILIAL.CODIGO
          INTO voPCNFSAID.UFFILIAL
             , voPCNFSAID.CGCFILIAL
             , voPCNFSAID.IEFILIAL
             , voPCNFSAID.CODFILIAL
             , voPCNFSAID.CODFILIALNF
          FROM PCFILIAL
         WHERE PCFILIAL.CODIGO = LISTAPOSSIVEISREGISTROS.CODFILIALSAIDA;
      EXCEPTION
      WHEN NO_DATA_FOUND THEN
        P_MSG := 'Filial de origem da nota de saída de transferência não encontrada!';
      END;

      BEGIN--Dados do cliente para a nota
        SELECT PCCLIENT.CODCLI
             , PCCLIENT.CODCLI
             , PCCLIENT.CLIENTE
             , PCCLIENT.CGCENT
             , PCCLIENT.CODPRACA
             , PCCLIENT.CODATV1
             , PCCLIENT.CONSUMIDORFINAL
             , PCCLIENT.BAIRROENT
             , PCCLIENT.MUNICENT
             , PCCLIENT.ENDERENT
             , PCCLIENT.NUMEROENT
             , PCCLIENT.CODCIDADE
             , PCCLIENT.CEPENT
             , PCCLIENT.CODPAIS
             , PCCLIENT.ESTENT
             , PCCLIENT.IEENT
             , PCCLIENT.IEENT
             , NVL(PCPRACA.NUMREGIAO, PCFILIAL.NUMREGIAOPADRAO)
             , NVL(PCCLIENT.EMAILNFE, PCCLIENT.EMAIL)
             , NVL(PCCLIENT.TELENT, PCCLIENT.TELCOB)
             , PCCIDADE.CODIBGE
          INTO voPCNFSAID.CODCLI
             , voPCNFSAID.CODCLINF
             , voPCNFSAID.CLIENTE
             , voPCNFSAID.CGC
             , voPCNFSAID.CODPRACA
             , voPCNFSAID.CODATV1
             , voPCNFSAID.CONSUMIDORFINAL
             , voPCNFSAID.BAIRRO
             , voPCNFSAID.MUNICIPIO
             , voPCNFSAID.ENDERECO
             , voPCNFSAID.NUMENDERECO
             , voPCNFSAID.CODMUNICIPIO
             , voPCNFSAID.CEP
             , voPCNFSAID.CODPAIS
             , voPCNFSAID.UF
             , voPCNFSAID.IE
             , voPCNFSAID.IEENT
             , voPCNFSAID.NUMREGIAO
             , voPCNFSAID.EMAILDEST
             , voPCNFSAID.TELEFONE
             , voPCNFSAID.CODIBGE
          FROM PCCLIENT
             , PCFILIAL
             , PCPRACA
             , PCCIDADE
         WHERE PCFILIAL.CODCLI    = PCCLIENT.CODCLI
           AND PCCLIENT.CODPRACA  = PCPRACA.CODPRACA(+)
           AND PCCLIENT.CODCIDADE = PCCIDADE.CODCIDADE(+)
           AND PCFILIAL.CODIGO    = LISTAPOSSIVEISREGISTROS.CODFILIALENTRADA;
      EXCEPTION
      WHEN NO_DATA_FOUND THEN
        P_MSG := 'Cliente relacionado a filial ' || LISTAPOSSIVEISREGISTROS.CODFILIALENTRADA || ' não encontrado!';
      END;

      --Valores default
      voPCNFSAID.CHAVENFE                := NULL;
      voPCNFSAID.DTHORANFE               := NULL;
      voPCNFSAID.PROTOCOLONFE            := NULL;
      voPCNFSAID.SITUACAONFE             := 0;
      voPCNFSAID.CONSUMIUNUMNFE          := 'N';
      voPCNFSAID.DTCANCEL                := NULL;
      voPCNFSAID.FINALIDADENFE           := NULL;
      voPCNFSAID.DTWMS                   := vdDATAWMS;
      voPCNFSAID.OBS                     := 'SAIDA DE TRANSF. CD VIRTUAL - Origem: ' || vsACAO;
      voPCNFSAID.ENVIADA                 := 'N';
      voPCNFSAID.ENVIADANFE              := 'N';
      voPCNFSAID.AGREGASTVLMERC          := 'N';
      voPCNFSAID.GERACP                  := 'S';
      voPCNFSAID.TIPOFJ                  := 'J';
      voPCNFSAID.VLDESCONTO              := 0;
      voPCNFSAID.VLFRETE                 := 0;
      voPCNFSAID.COMISSAO                := 0;
      voPCNFSAID.CONDVENDA               := 10;
      voPCNFSAID.TIPOVENDA               := 'TR';
      voPCNFSAID.HORALANC                := TO_CHAR(SYSDATE,'HH24');
      voPCNFSAID.MINUTOLANC              := TO_CHAR(SYSDATE,'MI');
      voPCNFSAID.DTSAIDA                 := TRUNC(SYSDATE);
      voPCNFSAID.DTENTREGA               := voPCNFSAID.DTSAIDA;
      voPCNFSAID.ESPECIE                 := 'NE';
      voPCNFSAID.SITUACAONFE             := 0;
      voPCNFSAID.NUMVIAS                 := 0;
	  voPCNFSAID.TIPOEMISSAO             := '1';

      --Guardar o número de transação de origem deste lançamento
      voPCNFSAID.NUMTRANSVENDADEPENDENTE := voPCNFSAID.NUMTRANSVENDA;

      IF ((P_EFATURAMENTO = 'S') AND (P_ECANCELAMENTO = 'S')) THEN
        SELECT N.NUMTRANSVENDA
          INTO voPCNFSAID.NUMTRANSVENDADEPENDENTE
          FROM PCNFSAID N
         WHERE N.NUMTRANSVENDADEPENDENTE = voPCNFSAID.NUMTRANSVENDA
           AND N.DTCANCEL IS NULL;
      END IF;

      --Geradores de valores

      voPCNFSAID.NUMTRANSVENDA           := FERRAMENTAS.F_PROX_NUMTRANSVENDA();
      iTentativa := 0;
      while iTentativa < 50 or voPCNFSAID.NUMTRANSVENDA = 0
      loop
        voPCNFSAID.NUMTRANSVENDA           := FERRAMENTAS.F_PROX_NUMTRANSVENDA();
         if voPCNFSAID.NUMTRANSVENDA > 0 then
           exit;
         end if;
        iTentativa := iTentativa+1;
      end loop;

      voPCNFSAID.NUMTRANSENTORIGEM       := FERRAMENTAS.F_PROX_NUMTRANSENT;
      iTentativa := 0;
      while iTentativa < 50 or voPCNFSAID.NUMTRANSVENDA = 0
      loop
        voPCNFSAID.NUMTRANSENTORIGEM       := FERRAMENTAS.F_PROX_NUMTRANSENT;
         if  voPCNFSAID.NUMTRANSENTORIGEM > 0 then
           exit;
         end if;
        iTentativa := iTentativa+1;
      end loop;

      voPCNFSAID.NUMNOTA                 := voPCNFSAID.NUMTRANSVENDA ;

      --Tratamento para os itens
      FOR LISTAITENSSAIDA IN (SELECT PCMOV.ROWID IDPCMOV
                                   , PCMOVCOMPLE.ROWID IDPCMOVCOMPLE
                                   , NVL(PCMOVCOMPLE.VLPAUTABCR,0) VLPAUTABCR
                                   , NVL(PCMOVCOMPLE.VLICMS,0) VLICMS
                                   , NVL(PCMOVCOMPLE.VLADICIONALBCSTBCR,0) VLADICIONALBCSTBCR
                                   , NVL(PCMOVCOMPLE.REDBASEIVABCR,0) REDBASEIVABCR
                                   , NVL(PCMOVCOMPLE.REDBASEALIQEXTBCR,0) REDBASEALIQEXTBCR
                                   , NVL(PCMOVCOMPLE.PERCIVABCR,0) PERCIVABCR
                                   , NVL(PCMOVCOMPLE.PERCICMSFRETEFOBSTBCR,0) PERCICMSFRETEFOBSTBCR
                                   , NVL(PCMOVCOMPLE.PERCALIQINTBCR,0) PERCALIQINTBCR
                                   , NVL(PCMOVCOMPLE.PERCALIQEXTBCR,0) PERCALIQEXTBCR
                                   , NVL(PCMOVCOMPLE.PERICMBCR,0) PERICMBCR
                                   , NVL(PCEST.QTESTGER, 0) QTESTGER
                                   , NVL(PCEST.QTEST, 0) QTEST
                                   --Campos substitutos do BCR da PCMOV
                                   , NVL(PCMOVCOMPLE.BASESTTRANSFCD, 0) BASESTTRANSFCD
                                   , NVL(PCMOVCOMPLE.VLSTTRANSFCD, 0) VLSTTRANSFCD
                                   , NVL(PCMOVCOMPLE.BASEICMSTRANSFCD, 0) BASEICMSTRANSFCD
                                   , NVL(PCMOVCOMPLE.VLICMSTRANSFCD, 0) VLICMSTRANSFCD
                                   , NVL(PCMOVCOMPLE.VLFECPTRANSFCD, 0) VLFECPTRANSFCD
                                   , NVL(PCMOVCOMPLE.PERCFECPTRANSFCD, 0) PERCFECPTRANSFCD
                                   , NVL(PCMOVCOMPLE.BASEFECPTRANSFCD, 0) BASEFECPTRANSFCD
                                   , NVL(PCMOVCOMPLE.VLFUNCEPTRANSFCD, 0) VLFUNCEPTRANSFCD
                                   , NVL(PCMOVCOMPLE.PERCFUNCEPTRANSFCD, 0) PERCFUNCEPTRANSFCD
                                   , NVL(PCMOVCOMPLE.BASEFUNCEPTRANSFCD, 0) BASEFUNCEPTRANSFCD
                                FROM PCMOV
                                   , PCMOVCOMPLE
                                   , PCEST
                               WHERE PCMOV.NUMTRANSITEM       = PCMOVCOMPLE.NUMTRANSITEM
                                 AND PCMOV.CODPROD            = PCEST.CODPROD
                                 AND PCEST.CODFILIAL          = NVL(PCMOV.CODFILIALRETIRA, PCMOV.CODFILIAL) --Estoque da filial de saída
                                 AND PCMOVCOMPLE.ORIGMERCTRIB = '1'
                                 AND NVL(PCMOV.QT,0) > 0
                                 AND ((P_EFATURAMENTO      = 'S' AND
                                       PCMOV.NUMTRANSVENDA = P_NUMTRANSVENDA AND
                                       NVL(PARAMFILIAL.obtercomovarchar2('CDVIRTUALIMPORTACAO', PCMOV.CODFILIALRETIRA),'N') = 'S')
                                  OR  (P_EFATURAMENTO      = 'N' AND
                                       PCMOV.NUMTRANSENT   = P_NUMTRANSENT AND
                                       EXISTS (SELECT 1
                                                 FROM PCMOV A
                                                WHERE A.CODPROD       = PCMOV.CODPROD
                                                  AND A.NUMTRANSVENDA = P_NUMTRANSVENDA
                                                  AND NVL(PARAMFILIAL.obtercomovarchar2('CDVIRTUALIMPORTACAO', A.CODFILIALRETIRA),'N') = 'S'
                                                  AND NVL(A.QT,0)     > 0)))
                              )
      LOOP
        vnRetornoPkgEstoque := vnRetornoPkgEstoque + 1;

        --Clonando item do PCMOV
        voPCMOV := ferramentas.RETORNAR_ROWTIPEPCMOV(LISTAITENSSAIDA.IDPCMOV);

        --Obtendo valor de sequence
        SELECT dfseq_pcmovcomple.NEXTVAL
          INTO voPCMOV.NUMTRANSITEM
          FROM DUAL;

        --Ajustando campos
        voPCMOV.NUMTRANSENT     := NULL;
        voPCMOV.NUMTRANSVENDA   := voPCNFSAID.NUMTRANSVENDA;
        voPCMOV.NUMNOTA         := voPCNFSAID.NUMNOTA;
        voPCMOV.CODFILIALRETIRA := voPCNFSAID.CODFILIAL;
        voPCMOV.CODFILIAL       := voPCNFSAID.CODFILIAL;
        voPCMOV.CODFILIALNF     := voPCNFSAID.CODFILIAL;
        voPCMOV.CODCLI          := voPCNFSAID.CODCLI;
        voPCMOV.DTMOV           := voPCNFSAID.DTSAIDA;
        voPCMOV.NUMREGIAO       := voPCNFSAID.NUMREGIAO;
        voPCMOV.CODOPER         := 'ST';
        voPCMOV.DTMOVLOG        := SYSDATE;
        voPCMOV.DTCANCEL        := NULL;
		voPCMOV.DATAESTOQUE     := NULL;
        voPCMOV.QTESTGERANT     := LISTAITENSSAIDA.QTESTGER;
        voPCMOV.QTESTANT        := LISTAITENSSAIDA.QTEST;      
        voPCMOV.PUNIT           := GREATEST(COALESCE(voPCMOV.PUNIT, 0),
                                            COALESCE(voPCMOV.PUNITCONT, 0));
        voPCMOV.PUNITCONT       := voPCMOV.PUNIT;            
        --ST / STBCR
        voPCMOV.ST              := LISTAITENSSAIDA.VLSTTRANSFCD; -- NVL(voPCMOV.STBCR,0);
        voPCMOV.BASEICST        := LISTAITENSSAIDA.BASESTTRANSFCD; --NVL(voPCMOV.BASEBCR,0);
        voPCMOV.BASEICMS        := GREATEST(LISTAITENSSAIDA.BASEICMSTRANSFCD,NVL(voPCMOV.BASEICMS,0));   --GREATEST(NVL(voPCMOV.BASEICMSBCR,0), NVL(voPCMOV.BASEICMS,0));
        vnVALORICMS             := GREATEST(LISTAITENSSAIDA.VLICMSTRANSFCD, NVL(LISTAITENSSAIDA.VLICMS,0)); --GREATEST(NVL(voPCMOV.VLICMSBCR,0), NVL(LISTAITENSSAIDA.VLICMS,0));		
--        voPCMOV.STBCR           := 0;
        voPCMOV.PERCICM         := GREATEST(LISTAITENSSAIDA.PERICMBCR, voPCMOV.PERCICM);
        voPCMOV.PERCIVA         := LISTAITENSSAIDA.PERCIVABCR;
        voPCMOV.PAUTA           := LISTAITENSSAIDA.VLPAUTABCR;
        voPCMOV.ALIQICMS1       := LISTAITENSSAIDA.PERCALIQINTBCR;
        voPCMOV.ALIQICMS2       := LISTAITENSSAIDA.PERCALIQEXTBCR;
        voPCMOV.REDBASEIVA      := LISTAITENSSAIDA.REDBASEIVABCR;
        voPCMOV.VLADICIONALBCST := LISTAITENSSAIDA.VLADICIONALBCSTBCR;
        --PIS/COFINS
        voPCMOV.VLCREDPIS       := 0;
        voPCMOV.VLCREDCOFINS    := 0;
        voPCMOV.VLPIS           := 0;
        voPCMOV.VLCOFINS        := 0;
        voPCMOV.VLBASEPISCOFINS := 0;
        voPCMOV.PERPIS          := 0;
        voPCMOV.PERCOFINS       := 0;
		voPCMOV.ROTINACAD       := VSPROGRAMANOVO;

        --Obter tributação
        TRANSF.TRIBUTACAO := BUSCAR_TRIBUTACAO_PORID( voPCMOV.CODST
                                                    , voPCMOV.CODPROD      
                                                    , LISTAPOSSIVEISREGISTROS.GERATRIBUTENTTRANSFDEP
                                                    , LISTAPOSSIVEISREGISTROS.ISENTODEST
                                                    , LISTAPOSSIVEISREGISTROS.ORGAOPUBLICO
                                                  );
        /*IF LISTAPOSSIVEISREGISTROS.USATRIBUTACAOPORUF = 'S' THEN
          TRANSF.TRIBUTACAO := BUSCAR_TRIBUTACAO_PORUF( voPCMOV.CODST
                                                      , LISTAPOSSIVEISREGISTROS.GERATRIBUTENTTRANSFDEP
                                                      , LISTAPOSSIVEISREGISTROS.ISENTODEST
                                                      , LISTAPOSSIVEISREGISTROS.ORGAOPUBLICO
                                                      );
        ELSE
          TRANSF.TRIBUTACAO := BUSCAR_TRIBUTACAO( voPCNFSAID.NUMREGIAO
                                                , voPCMOV.CODPROD
                                                , LISTAPOSSIVEISREGISTROS.GERATRIBUTENTTRANSFDEP
                                                , LISTAPOSSIVEISREGISTROS.ISENTODEST
                                                , LISTAPOSSIVEISREGISTROS.ORGAOPUBLICO
                                                );
        END IF;*/

        --Obtendo os códigos fiscais
        IF UPPER(TRIM(voPCNFSAID.UF)) = UPPER(TRIM(voPCNFSAID.UFFILIAL)) THEN
          voPCMOV.CODFISCAL := TRANSF.TRIBUTACAO.COL.CODFISCALTRANSF;
        ELSE
          voPCMOV.CODFISCAL := TRANSF.TRIBUTACAO.COL.CODFISCALTRANSFINTER;
        END IF;

        voPCMOV.SITTRIBUT :=  TRANSF.TRIBUTACAO.COL.SITTRIBUTTRANSF;

        --Inserindo PCMOV
        INSERT INTO PCMOV
             VALUES voPCMOV;

        --Gerando o PCMOVCOMPLE
        GERARPCMOVCOMPLE( voPCMOV
		                , LISTAITENSSAIDA.IDPCMOVCOMPLE
						, vnVALORICMS
						, LISTAITENSSAIDA.VLFECPTRANSFCD
						, LISTAITENSSAIDA.PERCFECPTRANSFCD
						, LISTAITENSSAIDA.BASEFECPTRANSFCD
						, LISTAITENSSAIDA.VLFUNCEPTRANSFCD
						, LISTAITENSSAIDA.PERCFUNCEPTRANSFCD
						, LISTAITENSSAIDA.BASEFUNCEPTRANSFCD
						);


      END LOOP; --FOR ITENS

      IF vnRetornoPkgEstoque <= 0 THEN
         P_MSG := 'Nenhum produto encontrado. '         || chr(13) ||
                  'Número de transação de venda: '      || to_char(P_NUMTRANSVENDA) || chr(13) ||
                  'Número de transação de entrada: '    || to_char(P_NUMTRANSENT) || chr(13) ||
                  'É Faturamento: '  || P_EFATURAMENTO  || chr(13) ||
                  'É Cancelamento: ' || P_ECANCELAMENTO || chr(13) ||
                  'Processo abortado!';
         RAISE ExecacaoEstoque;
      END IF;
      --Totalizando o cabeçalho
      FOR TOTALIZADORES IN (SELECT SUM(ROUND((NVL(PUNITCONT, 0) - NVL(PCMOV.VLIPI, 0) -
                                             NVL(PCMOV.ST, 0) -
                                             decode(NVL(PCMOVCOMPLE.Utilizoumotorcalculo, 'N'),
                                                     'S',
                                                     NVL(PCMOVCOMPLE.VLFECP, 0),
                                                     0)) * QTCONT,
                                             2)) +
                                    SUM(ROUND(NVL(PCMOV.VLIPI, 0) * QTCONT, 2)) +
                                    SUM(ROUND(NVL(PCMOV.ST, 0) * QTCONT, 2)) +
                                    SUM(ROUND(decode(NVL(PCMOVCOMPLE.Utilizoumotorcalculo, 'N'),
                                                     'S',
                                                     NVL(PCMOVCOMPLE.VLFECP, 0),
                                                     0) * QTCONT, 2))  +
                                   SUM(ROUND((NVL(PCMOV.VLOUTROS, 0) * PCMOV.QT), 2))+
								   SUM(ROUND((NVL(PCMOV.VLFRETE, 0) * PCMOV.QT), 2)) VLTOTAL
                                 , SUM(ROUND((NVL(PCMOV.PTABELA,0)   * PCMOV.QT), 2)) VLTABELA
                                 , SUM(ROUND((NVL(PCMOV.VLFRETE,0)   * PCMOV.QT), 2)) VLFRETE
                                 , SUM(ROUND((NVL(PCMOV.PESOBRUTO,0) * PCMOV.QT), 2)) TOTPESO
                                 , SUM(ROUND((NVL(PCMOV.VLIPI,0)     * PCMOV.QT), 2)) TOTVLIPI
                                 , SUM(ROUND((NVL(PCMOV.ST,0)        * PCMOV.QT), 2)) TOTVLST
                                 , SUM(ROUND((NVL(PCMOV.VLBASEIPI,0) * PCMOV.QT), 2)) TOTVLBASEIPI
                                 , SUM(ROUND((NVL(PCMOV.VLOUTROS,0)  * PCMOV.QT), 2)) TOTOUTRAS
                                 , SUM(ROUND((NVL(PCMOV.CUSTOREAL,0) * PCMOV.QT), 2)) TOTCUSTOREAL
                                 , SUM(ROUND((NVL(PCMOV.CUSTOFIN,0)  * PCMOV.QT), 2)) TOTCUSTOFIN
                                 , SUM(ROUND((NVL(PCMOV.CUSTOREP,0)  * PCMOV.QT), 2)) TOTCUSTOREP
                                 , SUM(ROUND((NVL(PCMOV.CUSTOCONT,0) * PCMOV.QT), 2)) TOTCUSTOCONT
                                 , SUM(ROUND((NVL(PCMOV.VLPIS,0)     * PCMOV.QT), 2)) TOTPIS
                                 , SUM(ROUND((NVL(PCMOV.VLCOFINS,0)  * PCMOV.QT), 2)) TOTCOFINS
                                 , SUM(ROUND((NVL(PCMOV.BASEICST,0)  * PCMOV.QT), 2)) TOTBASEICST
                                 , COUNT(1) QTITENS
                              FROM PCMOV, PCMOVCOMPLE
                             WHERE PCMOV.NUMTRANSVENDA = voPCNFSAID.NUMTRANSVENDA
                               AND PCMOV.NUMTRANSITEM = PCMOVCOMPLE.NUMTRANSITEM
                               AND PCMOV.QT > 0
                            )
      LOOP
        voPCNFSAID.VLTOTAL      := TOTALIZADORES.VLTOTAL;
        voPCNFSAID.VLTOTALNF    := TOTALIZADORES.VLTOTAL;
        voPCNFSAID.VLTOTGER     := TOTALIZADORES.VLTOTAL;
        voPCNFSAID.VLTABELA     := TOTALIZADORES.VLTABELA;
        voPCNFSAID.NUMITENS     := TOTALIZADORES.QTITENS;
        voPCNFSAID.TOTPESO      := TOTALIZADORES.TOTPESO;
        voPCNFSAID.VLFRETE      := TOTALIZADORES.VLFRETE;
        voPCNFSAID.VLBASEIPI    := TOTALIZADORES.TOTVLBASEIPI;
        voPCNFSAID.VLCUSTOREAL  := TOTALIZADORES.TOTCUSTOREAL;
        voPCNFSAID.VLCUSTOFIN   := TOTALIZADORES.TOTCUSTOFIN;
        voPCNFSAID.VLCUSTOREP   := TOTALIZADORES.TOTCUSTOREP;
        voPCNFSAID.VLCUSTOCONT  := TOTALIZADORES.TOTCUSTOCONT;
        VOPCNFSAID.VLPIS        := TOTALIZADORES.TOTPIS;
        VOPCNFSAID.VLCOFINS     := TOTALIZADORES.TOTCOFINS;
        VOPCNFSAID.VLOUTRASDESP := TOTALIZADORES.TOTOUTRAS;
        VOPCNFSAID.ICMSRETIDO  := TOTALIZADORES.TOTVLST;
        VOPCNFSAID.BCST        := TOTALIZADORES.TOTBASEICST;
      END LOOP;

      --Inserindo o novo cabeçalho
      INSERT INTO PCNFSAID
           VALUES voPCNFSAID;

      --Transferir estoque
      BEGIN
        vnRetornoPkgEstoque := PKG_ESTOQUE.VENDAS_SAIDA_PROD(voPCNFSAID.NUMTRANSVENDA, 0, 'N', vsResultadoEstoque);
        IF NVL(vnRetornoPkgEstoque, 0) <= 0 THEN
           P_MSG := 'Não foi possível efetuar a baixa de estoque dos produtos na transferência' || chr(13) ||
                    'Msg. PKG_ESTOQUE: ' || vsResultadoEstoque;
			
			INSERT INTO PCLOGALTERACAODADOS
				(DATA,
				CODFUNC,
				CODROTINA,
				TABELA,
				COLUNA,
				TIPOVALOR,
				VALORNUM,
				VALORNUMANT,
				VALORALFA,
				VALORALFAANT,
				USACTRLACESSO,
				CODCTRLACESSO,
				OBSERVACOES,
				MAQUINA,
				PROGRAMA,
				TERMINAL,
				OSUSER)
			VALUES
				(SYSDATE,
				8888,
				1452,
				'PCLOGPKG_ESTOQUE',
				'IDENTIFICADOR',
				'N',
				voPCNFSAID.NUMTRANSVENDA,
				voPCNFSAID.NUMTRANSVENDA,
				NULL,
				NULL,
				'S',
				NULL,
				P_MSG,
				SYS_CONTEXT('USERENV', 'TERMINAL'),
				SYS_CONTEXT('USERENV', 'HOST'),
				SYS_CONTEXT('USERENV', 'MODULE'),
				SYS_CONTEXT('USERENV', 'OS_USER')
				);
			RAISE ExecacaoEstoque; 
        END IF;
      END;

      --Zerar reservar na filial retira durante o processamento da nota de venda
/*
      IF (P_EFATURAMENTO = 'S') AND (P_ECANCELAMENTO = 'N') THEN
        --Verificar com o Fernandes o processo na PKG_ESTOQUE para efetuar a baixa na filial retira.
        FOR LISTAPRODUTO IN (SELECT PCMOV.CODFILIALRETIRA
                                  , PCMOV.CODPROD
                                  , SUM(PCMOV.QT) QT
                               FROM PCMOV
                              WHERE PCMOV.NUMTRANSVENDA = voPCNFSAID.NUMTRANSVENDA
                              GROUP
                                 BY PCMOV.CODFILIALRETIRA
                                  , PCMOV.CODPROD
                            )
        LOOP
          UPDATE PCEST
             SET PCEST.QTRESERV  = GREATEST(NVL(PCEST.QTRESERV,0) - LISTAPRODUTO.QT,0)
           WHERE PCEST.CODPROD   = LISTAPRODUTO.CODPROD
             AND PCEST.CODFILIAL = LISTAPRODUTO.CODFILIALRETIRA;
        END LOOP;
      END IF;
*/

      ------------------------------------------------------------------------
      -- GERANDO NOTA FISCAL DE ENTRADA DE TRANSFERENCIA NA FILIAL DE VENDA --
      ------------------------------------------------------------------------
      GERARENTRADATRANSFERENCIA( voPCNFSAID
                               , LISTAPOSSIVEISREGISTROS.CODFILIALENTRADA
                               , LISTAPOSSIVEISREGISTROS.USATRIBUTACAOPORUF
                               , LISTAPOSSIVEISREGISTROS.GERATRIBUTENTTRANSFDEP
                               , LISTAPOSSIVEISREGISTROS.ISENTODEST
                               , LISTAPOSSIVEISREGISTROS.ORGAOPUBLICO
                               );
    END LOOP; --FOR LISTAPOSSIVEISREGISTROS

  EXCEPTION
  WHEN ExecacaoEstoque THEN
     raise_application_error (-20001, P_MSG);
  WHEN OTHERS THEN
    P_MSG := P_MSG ||dbms_utility.format_error_stack|| ' ERRO. Linha: ' ||
                     dbms_utility.format_error_backtrace;
  END;
END;

