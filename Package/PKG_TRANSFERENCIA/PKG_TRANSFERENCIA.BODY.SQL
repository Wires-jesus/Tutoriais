    FUNCTION NOTA_EXISTE(P_NUMTRANSVENDA IN NUMBER, TIPO IN VARCHAR2)
      RETURN BOOLEAN IS
      VNNFENT  NUMBER;
      VNENTMOV NUMBER;
    BEGIN
      IF TIPO = 'ET' THEN
        SELECT COUNT(1)
          INTO VNNFENT
          FROM PCNFENT E
         WHERE NUMTRANSENT IN
               (SELECT DISTINCT NUMTRANSENTORIGEM
                  FROM PCNFSAID
                 WHERE NUMTRANSVENDA = P_NUMTRANSVENDA);

        SELECT COUNT(1)
          INTO VNENTMOV
          FROM PCMOV E
         WHERE NUMTRANSENT IN
               (SELECT DISTINCT NUMTRANSENTORIGEM
                  FROM PCNFSAID
                 WHERE NUMTRANSVENDA = P_NUMTRANSVENDA);

        RETURN (VNENTMOV > 0) AND(VNNFENT > 0);
      else
        RETURN FALSE;
      END IF;
    END;


