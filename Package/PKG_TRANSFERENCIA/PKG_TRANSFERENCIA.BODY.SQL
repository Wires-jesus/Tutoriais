  FUNCTION BUSCAR_PCLOGTRANSFVIRTRET(P_NUMTRANSVENDA IN NUMBER,
                                     P_CODPROD       IN NUMBER,
                                     P_NUMSEQ        IN NUMBER)
    RETURN T_PCLOGTRANSFVIRTRET IS
    R_PCLOGTRANSFVIRTRET T_PCLOGTRANSFVIRTRET;
  BEGIN

    BEGIN
      SELECT PRECOORIGINAL, PSEMIMPOSTO, PERCIPI, BASEIPI, VLIPI
        INTO R_PCLOGTRANSFVIRTRET.CAMPO.PRECOORIGINAL,
             R_PCLOGTRANSFVIRTRET.CAMPO.PSEMIMPOSTO,
             R_PCLOGTRANSFVIRTRET.CAMPO.PERCIPI,
             R_PCLOGTRANSFVIRTRET.CAMPO.BASEIPI,
             R_PCLOGTRANSFVIRTRET.CAMPO.VLIPI
        FROM (SELECT PRECOORIGINAL, PSEMIMPOSTO, PERCIPI, BASEIPI, VLIPI
                FROM PCLOGTRANSFVIRTRET L
               WHERE NUMTRANSVENDA = P_NUMTRANSVENDA
                 AND CODPROD = P_CODPROD
                 AND NUMSEQ = P_NUMSEQ
               ORDER BY L.DTTRANSF DESC) R
       WHERE ROWNUM = 1;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        R_PCLOGTRANSFVIRTRET.CAMPO.PERCIPI       := 0;
        R_PCLOGTRANSFVIRTRET.CAMPO.BASEIPI       := 0;
        R_PCLOGTRANSFVIRTRET.CAMPO.VLIPI         := 0;
        R_PCLOGTRANSFVIRTRET.CAMPO.PRECOORIGINAL := 0;
        R_PCLOGTRANSFVIRTRET.CAMPO.PSEMIMPOSTO   := 0;
    END;

    RETURN R_PCLOGTRANSFVIRTRET;
  end;

