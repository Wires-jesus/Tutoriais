   PROCEDURE ATUALIZAR_STBCR(pBASEBCR     IN NUMBER,
                             pSTBCR       IN NUMBER,
                             pBASEICMSBCR IN NUMBER,
                             pPERCICMSBCR IN NUMBER,
                             pCODPROD     IN NUMBER,
                             pCODFILIAL   IN VARCHAR2,
                             pNumTransEnt IN NUMBER) IS
     VSQL  VARCHAR2(2000);
   BEGIN
     IF ferramentas.F_BUSCARPARAMETRO_ALFA('FIL_TRANSFEREVLSTBASESTNATRANSF',
                                           pCODFILIAL,
                                           'N') = 'N'
     THEN
       RETURN;
     END IF;	 

     VSQL := 'UPDATE PCEST SET NUMTRANSULTENTST = ' || pNumTransEnt || CHR(13);

     IF NVL(pSTBCR, 0) > 0 and NVL(pBASEBCR, 0) > 0 THEN
       VSQL :=  VSQL || ', BASEBCR = ' || NVL(pBASEBCR, 0) ||
                        ', STBCR = '   || NVL(pSTBCR, 0) || CHR(13);
     END IF;

     IF NVL(pPERCICMSBCR, 0) > 0 AND nvl(pBASEICMSBCR, 0) > 0 THEN
       VSQL :=  VSQL || ', BASEICMSBCR = ' || NVL(pBASEICMSBCR, 0) ||
                        ', VLICMSBCR = '   || ROUND(NVL(pBASEICMSBCR, 0)*nvl(pPERCICMSBCR, 0)/100, 6) || CHR(13);
     END IF;

     VSQL :=  VSQL || ' WHERE CODPROD = :CODPROD ' ||
                      '   AND CODFILIAL = :CODFILIAL ';

     EXECUTE IMMEDIATE vsql
	   USING pCODPROD, pCODFILIAL;
   EXCEPTION
     WHEN OTHERS THEN
      raise_application_error (-20001, 'ATUALIZAR_STBCR' || VSQL || dbms_utility.format_error_stack|| ' ERRO. Linha: ' ||
                               dbms_utility.format_error_backtrace);
   END;

