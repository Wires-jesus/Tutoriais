    BEGIN
      BEGIN
        EXECUTE IMMEDIATE
          'UPDATE PCPEDI SET ' ||
          'CODIGOTRIBUTACAOCBSIBS  = :1, ' ||
          'CSTIBSCBS               = :2, ' ||
          'CCLASSTRIBIBSCBS        = :3, ' ||
          'VLBASEIBSCBS            = :4, ' ||
          'IBSUF                   = :5, ' ||
