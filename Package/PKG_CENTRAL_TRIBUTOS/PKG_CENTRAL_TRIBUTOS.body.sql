    V_DADOS_TRIBUTACAO.VALOR_FRETE   := P_LISTA_DADOS_NOTAS.VLOUTROS;
    V_DADOS_TRIBUTACAO.VALOR_OUTROS  := P_LISTA_DADOS_NOTAS.VLFRETE;

    RETURN (V_DADOS_TRIBUTACAO);
  END TRANSPORTAR_LISTA_PARA_TRIB;

