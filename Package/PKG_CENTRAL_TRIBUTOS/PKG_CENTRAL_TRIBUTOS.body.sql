            IF P_PARAMETROS_CENTRAL_TRIBUTOS.ESPECIE IN ('CT', 'CO', 'CE') THEN
              PKG_DEBUGGING_FWPC.ATIVARDEBUG('CALCULARPISCOFINS_TRANSPORTE','1.0', P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO);
            ELSE
              PKG_DEBUGGING_FWPC.ATIVARDEBUG('CALCULARPISCOFINS_VENDA','1.0', P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO);
            END IF;
            PKG_DEBUGGING_FWPC.LOG(V_ERROS, 'S'); 

            IF (RETORNA_GRAVARLOG <> 'S') THEN  
              PKG_DEBUGGING_FWPC.DESATIVARDEBUG;
            END IF; 
          END IF;
        END IF;
      EXCEPTION 
        WHEN OTHERS THEN 
          IF P_PARAMETROS_CENTRAL_TRIBUTOS.ESPECIE IN ('CT', 'CO', 'CE') THEN
            PKG_DEBUGGING_FWPC.ATIVARDEBUG('CALCULARPISCOFINS_TRANSPORTE','1.0', P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO);
            PKG_DEBUGGING_FWPC.LOG('CALCULARPISCOFINS_TRANSPORTE - '|| SQLERRM, 'S');
          ELSE
            PKG_DEBUGGING_FWPC.ATIVARDEBUG('CALCULARPISCOFINS_VENDA','1.0', P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO);
            PKG_DEBUGGING_FWPC.LOG('CALCULARPISCOFINS_VENDA - '|| SQLERRM, 'S');
          END IF;
          IF (RETORNA_GRAVARLOG <> 'S') THEN  
              PKG_DEBUGGING_FWPC.DESATIVARDEBUG;
          END IF; 
      END;
   ELSE 
     IF P_PARAMETROS_CENTRAL_TRIBUTOS.DEVOLUCAO = 'S' THEN 
       BEGIN 
         V_RESULT := FISCAL.CALCULARPISCOFINS_DEVOLUCAO(P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO,V_MSG);
         IF LENGTH(V_MSG) > 1 THEN
           IF SUBSTR(V_MSG, 1, 1) = 'N' THEN
             V_ERROS := SUBSTR(TRIM(V_ERROS) || 'Devolução: Núm.Transação: ' || P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO
                                             || ' Núm.Nota: ' || P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_NOTA
                                             || ' Mensagem: ' || CHR(13) 
                                             || TRIM(SUBSTR(V_MSG,3,4000)) || CHR(13) || CHR(13),1,4000);
                                            
             PKG_DEBUGGING_FWPC.ATIVARDEBUG('CALCULARPISCOFINS_DEVOLUCAO','1.0', P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO);
             PKG_DEBUGGING_FWPC.LOG(V_ERROS, 'S'); 
             PKG_DEBUGGING_FWPC.LOG(V_RESULT, 'S');             

             IF (RETORNA_GRAVARLOG <> 'S') THEN  
               PKG_DEBUGGING_FWPC.DESATIVARDEBUG;
             END IF; 
           END IF;
         END IF;
       EXCEPTION 
         WHEN OTHERS THEN 
           PKG_DEBUGGING_FWPC.ATIVARDEBUG('CALCULARPISCOFINS_VENDA','1.0', P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO);
