    V_DADOS_TRIBUTACAO.CODPROD       := P_LISTA_DADOS_NOTAS.CODPROD;
    V_DADOS_TRIBUTACAO.NCM           := P_LISTA_DADOS_NOTAS.NCM;
    V_DADOS_TRIBUTACAO.VALOR_PRODUTO := P_LISTA_DADOS_NOTAS.PUNITCONT;
    V_DADOS_TRIBUTACAO.VALOR_ICMS_ST := P_LISTA_DADOS_NOTAS.VLST;
    V_DADOS_TRIBUTACAO.VALOR_FCP_ST  := P_LISTA_DADOS_NOTAS.VLFECP;
