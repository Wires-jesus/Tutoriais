      IF (RETORNA_GRAVARLOG <> 'S') THEN  
         PKG_DEBUGGING_FWPC.DESATIVARDEBUG;
      END IF; 

      V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem1;
      V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO        := 'Tributos não calculados. Motivo: Filial optante pelo Simples Nacional.';
      RETURN P_PARAMETROS_CENTRAL_TRIBUTOS;
    END IF;   
  EXCEPTION
    WHEN OTHERS THEN
      PKG_DEBUGGING_FWPC.LOG('Erro no CALCULAR_E_GRAVAR_IS: ' || SQLERRM, 'S');
      V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem4;
      V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO := cMensagem4||SQLERRM;
      RETURN P_PARAMETROS_CENTRAL_TRIBUTOS;
  END CALCULAR_E_GRAVAR_IS;

  --Função para calcular e gravar todos os tributos
  FUNCTION CALCULAR_E_GRAVAR_TODOS_TRIB(P_PARAMETROS_CENTRAL_TRIBUTOS IN T_PARAMETROS_CENTRAL_TRIBUTOS)
    RETURN T_PARAMETROS_CENTRAL_TRIBUTOS IS
    V_DADOS_TRIBUTACAO FISCAL.TIPO_TRIBUT_REFORMA;

    V_DADOS_BENEFICIOFISCAL FISCAL.TIPO_CODIGO_BENEFICIO_FISCAL;
    V_RESULT_CODBENEF       FISCAL.TIPO_CODIGO_BENEFICIO_FISCAL;

    V_PARAMETROS_CENTRAL_TRIBUTOS T_PARAMETROS_CENTRAL_TRIBUTOS;
    V_LISTA_DADOS_NOTA T_LISTA_DADOS_NOTAS;
    V_MSG VARCHAR2(4000);    
  BEGIN
    IF NOT FISCAL.FIL_OPTANTESIMPLESNACIONAL(P_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_FILIAL) THEN

      IF (RETORNA_GRAVARLOG = 'S') THEN
        PKG_DEBUGGING_FWPC.ATIVARDEBUG('CALCULAR_E_GRAVAR_TODOS_TRIB', '1.0', P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO);
      END IF;

      PKG_DEBUGGING_FWPC.LOG('Inicio do cálculo e gravação de todos os tributos', 'S');

      V_PARAMETROS_CENTRAL_TRIBUTOS := P_PARAMETROS_CENTRAL_TRIBUTOS;

      IF P_PARAMETROS_CENTRAL_TRIBUTOS.CALCULAR_PISCOFINS = 'S' THEN 
        CALCULAR_E_GRAVAR_PISCOFINS(V_PARAMETROS_CENTRAL_TRIBUTOS);
      END IF;

      V_LISTA_DADOS_NOTA := CONSULTAR_DADOS_MOVIMENTACAO(P_PARAMETROS_CENTRAL_TRIBUTOS);

      FOR POSICAO_ITEM IN 1 .. V_LISTA_DADOS_NOTA.COUNT LOOP

        TRANSPORTAR_LISTA_PARA_TRIB(V_DADOS_TRIBUTACAO,
                                    V_DADOS_BENEFICIOFISCAL,
                                    P_PARAMETROS_CENTRAL_TRIBUTOS,
                                    V_LISTA_DADOS_NOTA(POSICAO_ITEM));

        V_DADOS_TRIBUTACAO := LIMPAR_VALOR_TRIBUTO(V_DADOS_TRIBUTACAO);
        V_DADOS_TRIBUTACAO := FISCAL.CALCULAR_TODOS_TRIBUTOS(V_DADOS_TRIBUTACAO, V_MSG);
        V_RESULT_CODBENEF := FISCAL.CODIGO_BENEFICIO_FISCAL(V_DADOS_BENEFICIOFISCAL);

        GERAR_MEMORIA_CALCULO(V_DADOS_TRIBUTACAO,
                              V_DADOS_BENEFICIOFISCAL,
                              P_PARAMETROS_CENTRAL_TRIBUTOS,
                              V_LISTA_DADOS_NOTA(POSICAO_ITEM));
        
        IF V_RESULT_CODBENEF.CODBENEFICIOFISCAL IS NOT NULL THEN 
          GRAVAR_PCMOVCOMPLE(V_RESULT_CODBENEF);   
        END IF; 
        
        IF (V_MSG LIKE 'ERRO%') then        
          V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem6;       
          V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO := cMensagem6||' '||V_MSG;

          PKG_DEBUGGING_FWPC.LOG(V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO, 'S');        
          vProblemaPKGFiscal := True;
        END IF;      

        --Gravar itens de movimentação PCMOV, PCMOVPREFAT, PCMOVCIAP
        IF (P_PARAMETROS_CENTRAL_TRIBUTOS.ESPECIE = 'NF') AND
           (P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO > 0) AND
           (P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_PEDIDO = 0) THEN
          GRAVAR_PCMOV_TRIBUTO(V_DADOS_TRIBUTACAO,
                               V_LISTA_DADOS_NOTA(POSICAO_ITEM),
                               P_PARAMETROS_CENTRAL_TRIBUTOS,
                               'TODOS');
        END IF;

        --Gravar dados de cte, PCNFBASE
        IF (P_PARAMETROS_CENTRAL_TRIBUTOS.ESPECIE IN ('CT', 'CO', 'CE')) AND
           (P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO > 0) AND
           (P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_PEDIDO = 0) THEN
          GRAVAR_CTE_TRIBUTO(V_DADOS_TRIBUTACAO,
                             V_LISTA_DADOS_NOTA(POSICAO_ITEM));
        END IF;

        --Gravar dados de pedido, PCPEDI
        IF (P_PARAMETROS_CENTRAL_TRIBUTOS.ESPECIE = 'NF') AND
           (P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_PEDIDO > 0) AND
           (P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO = 0) THEN
          GRAVAR_PCPEDI_TRIBUTO(V_DADOS_TRIBUTACAO,
                                V_LISTA_DADOS_NOTA(POSICAO_ITEM),
                                'TODOS');
        END IF;

        vAchouRegistro := True;
      END LOOP;

      -- Gravar dados cabeçalhoNF "PCNFSAID / PCNFSAIDPREFAT / PCNFENT"
      IF (P_PARAMETROS_CENTRAL_TRIBUTOS.ESPECIE = 'NF') AND
         (P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO > 0) THEN
        GRAVAR_CABECALHO_NF(V_DADOS_TRIBUTACAO,
                            P_PARAMETROS_CENTRAL_TRIBUTOS);
      END IF;
      
      IF vProblemaPKGFiscal THEN
         V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem6;
         V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO        := cMensagem6;

      ELSIF vAchouRegistro THEN
         V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem1;
         V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO        := cMensagem1;

      ELSE
         V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem5;
         V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO        := cMensagem5;
      END IF;

      RETURN V_PARAMETROS_CENTRAL_TRIBUTOS;
    ELSE  
      PKG_DEBUGGING_FWPC.ATIVARDEBUG('Processando Filial: '|| 
                                      P_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_FILIAL,
                                      '',
                                      P_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_FILIAL);
      
      PKG_DEBUGGING_FWPC.LOG(
        'Tributos não calculados.' || CHR(10) ||
        'Motivo: Filial optante pelo Simples Nacional.','S');     
        
      IF (RETORNA_GRAVARLOG <> 'S') THEN  
         PKG_DEBUGGING_FWPC.DESATIVARDEBUG;
      END IF; 
      
      V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem1;
      V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO        := 'Tributos não calculados. Motivo: Filial optante pelo Simples Nacional.';
      RETURN V_PARAMETROS_CENTRAL_TRIBUTOS;
    END IF;         
  EXCEPTION
     WHEN OTHERS THEN
     BEGIN
       IF (V_PARAMETROS_CENTRAL_TRIBUTOS.DEVOLUCAO = 'S' AND 
           V_PARAMETROS_CENTRAL_TRIBUTOS.TIPOMOVIMENTO = 'E') THEN 
          PKG_DEBUGGING_FWPC.LOG('Devolução não deve ser calculada, somente replicada da saída', 'S');
          V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem7;
          V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO := cMensagem7;
       ELSE    
         PKG_DEBUGGING_FWPC.LOG('Erro no CALCULAR_E_GRAVAR_TODOS_TRIB: ' || SQLERRM, 'S');
         V_PARAMETROS_CENTRAL_TRIBUTOS.CODIGO_MENSAGEM_RETORNO := cCodMensagem2;
         V_PARAMETROS_CENTRAL_TRIBUTOS.MENSAGEM_RETORNO := cMensagem2||SQLERRM;
       END IF; 
       
       RETURN V_PARAMETROS_CENTRAL_TRIBUTOS;
     END;
  END CALCULAR_E_GRAVAR_TODOS_TRIB;


