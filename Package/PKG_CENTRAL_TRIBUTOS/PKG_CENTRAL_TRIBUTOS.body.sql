  V_ERROS VARCHAR2(4000);    
  V_RESULT VARCHAR2(400);
  V_MSG VARCHAR2(4000);
 BEGIN
   IF P_PARAMETROS_CENTRAL_TRIBUTOS.TIPOMOVIMENTO = 'S' THEN 
     BEGIN 
      IF P_PARAMETROS_CENTRAL_TRIBUTOS.ESPECIE IN ('CT', 'CO', 'CE') THEN
        V_RESULT := FISCAL.CALCULARPISCOFINS_TRANSPORTE(P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO, V_MSG);
      ELSE
        V_RESULT := FISCAL.CALCULARPISCOFINS_VENDA(P_PARAMETROS_CENTRAL_TRIBUTOS.NUMERO_TRANSACAO, V_MSG);
      END IF;
        IF LENGTH(V_MSG) > 1 THEN
