CREATE OR REPLACE PACKAGE BODY CONTROLANUMEROSERIE IS

  /*  PROCEDURES PRIVADAS */

  /*  DELETA O NUMERO DE SERIE DA PCNUMEROSERIE   */
  PROCEDURE PRC_DELETE_NUMSERIE(P_CODFILIAL PCNUMEROSERIECONFERENCIA.CODFILIAL%TYPE,
                                P_CODPROD   PCNUMEROSERIECONFERENCIA.CODPROD%TYPE,
                                P_NUMSERIE  PCNUMEROSERIECONFERENCIA.NUMSERIE%TYPE) IS
  BEGIN
    DELETE FROM PCNUMEROSERIE
     WHERE 1 = 1
       AND PCNUMEROSERIE.CODFILIAL = P_CODFILIAL
       AND PCNUMEROSERIE.CODPROD = P_CODPROD
       AND PCNUMEROSERIE.NUMSERIE = P_NUMSERIE;
  END PRC_DELETE_NUMSERIE;

  /*  DELETA O NUMERO DE SERIE DA PCNUMEROSERIE   */
  PROCEDURE PRC_INSERIR_NUMSERIE(P_CODFILIAL PCNUMEROSERIE.CODFILIAL%TYPE,
                                 P_CODPROD   PCNUMEROSERIE.CODPROD%TYPE,
                                 P_NUMSERIE  PCNUMEROSERIE.NUMSERIE%TYPE,
                                 P_NUMLOTE   PCNUMEROSERIE.NUMLOTE%TYPE,
                                 P_BLOQUEIO  PCNUMEROSERIE.BLOQUEIO%TYPE,
                                 P_AVARIA    PCNUMEROSERIE.AVARIA%TYPE,
                                 P_RESERVADA PCNUMEROSERIE.RESERVADA%TYPE) IS
  BEGIN

    INSERT INTO PCNUMEROSERIE
      (PCNUMEROSERIE.CODFILIAL,
       PCNUMEROSERIE.CODPROD,
       PCNUMEROSERIE.NUMLOTE,
       PCNUMEROSERIE.NUMSERIE,
       PCNUMEROSERIE.BLOQUEIO,
       PCNUMEROSERIE.AVARIA,
       PCNUMEROSERIE.RESERVADA)
    VALUES
      (P_CODFILIAL,
       P_CODPROD,
       P_NUMLOTE,
       P_NUMSERIE,
       P_BLOQUEIO,
       P_AVARIA,
       P_RESERVADA);

  END PRC_INSERIR_NUMSERIE;

  /*  DELETA A CONFERENCIA PARA O NUMERO DE SERIE   */
  PROCEDURE PRC_DELETE_CONFERENCIA(P_NUMPED    PCNUMEROSERIECONFERENCIA.NUMPED%TYPE,
                                   P_CODFILIAL PCNUMEROSERIECONFERENCIA.CODFILIAL%TYPE,
                                   P_CODPROD   PCNUMEROSERIECONFERENCIA.CODPROD%TYPE,
                                   P_NUMSEQ    PCNUMEROSERIECONFERENCIA.NUMSEQ%TYPE,
                                   P_NUMSERIE  PCNUMEROSERIECONFERENCIA.NUMSERIE%TYPE) IS
  BEGIN
    DELETE FROM PCNUMEROSERIECONFERENCIA
     WHERE 1 = 1
       AND PCNUMEROSERIECONFERENCIA.CODFILIAL = P_CODFILIAL
       AND PCNUMEROSERIECONFERENCIA.CODPROD = P_CODPROD
       AND PCNUMEROSERIECONFERENCIA.NUMPED = P_NUMPED
       AND PCNUMEROSERIECONFERENCIA.NUMSERIE = P_NUMSERIE
       AND PCNUMEROSERIECONFERENCIA.NUMSEQ = P_NUMSEQ;
  END PRC_DELETE_CONFERENCIA;

  /*  INSERIR A CONFERENCIA PARA O NUMERO DE SERIE   */
