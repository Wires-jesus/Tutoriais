  /*  ATUALIZA NA PCMOVCOMPLE O VINCULO DA MOVIMENTACAO DE NUMERO DE SERIE   */

  PROCEDURE PRC_ATU_PCMOVCOMPLE_CNTRSERIE(P_NUMTRANSITEM        PCMOV.NUMTRANSITEM%TYPE,
                                          P_NUMTRANSNUMEROSERIE PCMOVCOMPLE.NUMTRANSNUMEROSERIE%TYPE) IS
  BEGIN
    UPDATE PCMOVCOMPLE
       SET PCMOVCOMPLE.NUMTRANSNUMEROSERIE = P_NUMTRANSNUMEROSERIE
     WHERE 1 = 1
       AND PCMOVCOMPLE.NUMTRANSITEM = P_NUMTRANSITEM;

  END PRC_ATU_PCMOVCOMPLE_CNTRSERIE;

  /*  ATUALIZAR O VINCULO DA PCMOV COM A PCMOVCOMPLE E PCMOVNUMEROSERIE  */

  PROCEDURE PRC_ATU_PCMOV_CNTRSERIE(P_ROWID               IN VARCHAR2,
                                    P_NUMTRANSNUMEROSERIE PCMOVCOMPLE.NUMTRANSNUMEROSERIE%TYPE) IS
    V_NUMTRANSITEM PCMOV.NUMTRANSITEM%TYPE;
  BEGIN
    SELECT PCMOV.NUMTRANSITEM
      INTO V_NUMTRANSITEM
      FROM PCMOV
     WHERE 1 = 1
       AND PCMOV.ROWID = P_ROWID;

    IF (V_NUMTRANSITEM IS NULL) OR (V_NUMTRANSITEM = 0) THEN
      SELECT DFSEQ_PCMOVCOMPLE.NEXTVAL INTO V_NUMTRANSITEM FROM DUAL;

      UPDATE PCMOV
         SET PCMOV.NUMTRANSITEM = V_NUMTRANSITEM
       WHERE 1 = 1
         AND PCMOV.ROWID = P_ROWID;

      INSERT INTO PCMOVCOMPLE
        (PCMOVCOMPLE.NUMTRANSITEM, PCMOVCOMPLE.NUMTRANSNUMEROSERIE)
      VALUES
        (V_NUMTRANSITEM, P_NUMTRANSNUMEROSERIE);
    END IF;

    PRC_ATU_PCMOVCOMPLE_CNTRSERIE(V_NUMTRANSITEM, P_NUMTRANSNUMEROSERIE);

  END PRC_ATU_PCMOV_CNTRSERIE;

