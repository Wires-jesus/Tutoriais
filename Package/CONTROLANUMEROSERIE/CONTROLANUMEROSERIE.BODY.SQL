  /*  VERIFICA SE EXISTE CONFERENCIA PARA O NUMERO DE SERIE   */
  FUNCTION FUNC_EXISTE_CONFERENCIA(P_NUMPED    PCMOV.NUMPED%TYPE,
                                   P_CODFILIAL PCMOV.CODFILIAL%TYPE,
                                   P_CODPROD   PCMOV.CODPROD%TYPE,
                                   P_NUMSEQ    PCMOV.NUMSEQ%TYPE)
    RETURN VARCHAR2 IS
    VCONTADOR NUMBER;
  BEGIN
    SELECT COUNT(1)
      INTO VCONTADOR
      FROM PCNUMEROSERIECONFERENCIA
     WHERE 1 = 1
       AND PCNUMEROSERIECONFERENCIA.CODFILIAL = P_CODFILIAL
       AND PCNUMEROSERIECONFERENCIA.CODPROD = P_CODPROD
       AND PCNUMEROSERIECONFERENCIA.NUMPED = P_NUMPED
       AND PCNUMEROSERIECONFERENCIA.NUMSEQ = P_NUMSEQ;

    IF (VCONTADOR <= 0) THEN
      SELECT COUNT(1)
        INTO VCONTADOR
        FROM PCCONTROLENUMSERIE
       WHERE 1 = 1
         AND PCCONTROLENUMSERIE.CODPROD = P_CODPROD
         AND PCCONTROLENUMSERIE.NUMPED = P_NUMPED
         AND PCCONTROLENUMSERIE.NUMSEQ = P_NUMSEQ;
    END IF;

    IF (VCONTADOR <= 0) THEN
      RETURN 'N';
    ELSE
      RETURN 'S';
    END IF;
  END FUNC_EXISTE_CONFERENCIA;

  PROCEDURE PRC_NUMERO_SERIE_FATURAMENTO(P_ROWID    IN VARCHAR2,
                                         P_MENSAGEM OUT VARCHAR2) IS

    /*  EXECOES  */
    NAO_EXISTE_CONFERENCIA      EXCEPTION;
    NUMERO_SEQUENCIA_NAO_EXISTE EXCEPTION;

    /*  VARIAVEIS PROCEDURE  */
    VNNUMPED              PCMOV.NUMPED%TYPE;
    VNCODFILIAL           PCMOV.CODFILIAL%TYPE;
    VNFILIALRETIRA        PCMOV.CODFILIALRETIRA%TYPE;
    VNCODPROD             PCMOV.CODPROD%TYPE;
    VNNUMLOTE             PCMOV.NUMLOTE%TYPE;
    VNNUMTRANSITEM        PCMOV.NUMTRANSITEM%TYPE;
    VNNUMSEQ              PCMOV.NUMSEQ%TYPE;
    VNTEMPCMOV            NUMBER;
    VSCONDVENDA           PCNFSAID.CONDVENDA%TYPE;
    VNNUMTRANSNUMEROSERIE PCMOVCOMPLE.NUMTRANSNUMEROSERIE%TYPE;
  BEGIN
    IF (P_ROWID IS NULL) THEN
      RAISE PARAMETRO_ENTRADA_NULO;
    END IF;

    BEGIN

      VNTEMPCMOV := 0;
      SELECT COUNT(*) INTO VNTEMPCMOV FROM PCMOV WHERE ROWID = P_ROWID;

      IF (VNTEMPCMOV > 0) THEN
        SELECT PCMOV.NUMPED,
               PCMOV.CODFILIAL,
               PCMOV.CODPROD,
               PCMOV.NUMLOTE,
               PCMOV.NUMTRANSITEM,
               PCMOV.NUMSEQ,
               NVL(PCMOV.CODFILIALRETIRA, PCMOV.CODFILIAL)
          INTO VNNUMPED,
               VNCODFILIAL,
               VNCODPROD,
               VNNUMLOTE,
               VNNUMTRANSITEM,
               VNNUMSEQ,
               VNFILIALRETIRA
          FROM PCMOV
         WHERE 1 = 1
           AND PCMOV.ROWID = P_ROWID;
      ELSE
        SELECT PCMOVPREFAT.NUMPED,
               PCMOVPREFAT.CODFILIAL,
               PCMOVPREFAT.CODPROD,
               PCMOVPREFAT.NUMLOTE,
               PCMOVPREFAT.NUMTRANSITEM,
               PCMOVPREFAT.NUMSEQ,
               NVL(PCMOVPREFAT.CODFILIALRETIRA, PCMOVPREFAT.CODFILIAL)
          INTO VNNUMPED,
               VNCODFILIAL,
               VNCODPROD,
               VNNUMLOTE,
               VNNUMTRANSITEM,
               VNNUMSEQ,
               VNFILIALRETIRA
          FROM PCMOVPREFAT
         WHERE 1 = 1
           AND PCMOVPREFAT.ROWID = P_ROWID;

      END IF;

    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RAISE DADOS_ROWID_NAO_ENCONTRADO;
      WHEN OTHERS THEN
        RAISE;

    END;

    IF (VNTEMPCMOV > 0) THEN
      SELECT CONDVENDA
        INTO VSCONDVENDA
        FROM PCPEDC
       WHERE NUMPED = VNNUMPED
         AND CODFILIAL = VNCODFILIAL;
    END IF;

    IF VSCONDVENDA NOT IN (7, 14) THEN
      IF (FUNC_EXISTE_CONFERENCIA(VNNUMPED,
                                  VNFILIALRETIRA,
                                  VNCODPROD,
                                  VNNUMSEQ) = 'N') THEN
        RAISE NAO_EXISTE_CONFERENCIA;
      END IF;
    END IF;

    IF (VSCONDVENDA = 7) AND
       (NVL(PARAMFILIAL.OBTERCOMOVARCHAR2('GERAICMSLIVROFISCALTV7'), 'N') = 'S') THEN
      IF (FUNC_EXISTE_CONFERENCIA(VNNUMPED,
                                  VNFILIALRETIRA,
                                  VNCODPROD,
                                  VNNUMSEQ) = 'N') THEN
        RAISE NAO_EXISTE_CONFERENCIA;
      END IF;
    END IF;
    
    BEGIN
      SELECT DFSEQ_PCMOVNUMEROSERIE.NEXTVAL
        INTO VNNUMTRANSNUMEROSERIE
        FROM DUAL;

    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RAISE NUMERO_SEQUENCIA_NAO_EXISTE;
      WHEN OTHERS THEN
        RAISE;

    END;

    PRC_ATU_PCMOV_CNTRSERIE(P_ROWID, VNNUMTRANSNUMEROSERIE);

    IF VSCONDVENDA <> 14 THEN
  
      FOR R_NUMEROSERIECONFERENCIA IN (SELECT PCNUMEROSERIECONFERENCIA.CODFILIAL,
                                              PCNUMEROSERIECONFERENCIA.CODPROD,
                                              PCNUMEROSERIECONFERENCIA.NUMSERIE,
                                              PCNUMEROSERIECONFERENCIA.NUMPED,
                                              PCNUMEROSERIECONFERENCIA.NUMSEQ,
                                              PCNUMEROSERIECONFERENCIA.DATASEPARACAO,
                                              PCNUMEROSERIE.BLOQUEIO,
                                              PCNUMEROSERIE.AVARIA
                                         FROM PCNUMEROSERIECONFERENCIA,
                                              PCNUMEROSERIE
                                        WHERE 1 = 1
                                          AND PCNUMEROSERIECONFERENCIA.NUMSERIE = PCNUMEROSERIE.NUMSERIE
                                          AND PCNUMEROSERIECONFERENCIA.CODPROD = PCNUMEROSERIE.CODPROD
                                          AND PCNUMEROSERIECONFERENCIA.CODFILIAL = PCNUMEROSERIE.CODFILIAL
                                          AND PCNUMEROSERIECONFERENCIA.NUMPED = VNNUMPED
                                          AND PCNUMEROSERIECONFERENCIA.CODPROD = VNCODPROD
                                          AND PCNUMEROSERIECONFERENCIA.CODFILIAL = VNFILIALRETIRA
                                          AND PCNUMEROSERIECONFERENCIA.NUMSEQ = VNNUMSEQ)

      LOOP
        PRC_INSERIR_MOVIMENTACAO(R_NUMEROSERIECONFERENCIA.CODFILIAL,
                                 R_NUMEROSERIECONFERENCIA.CODPROD,
                                 R_NUMEROSERIECONFERENCIA.NUMSERIE,
                                 VNNUMTRANSNUMEROSERIE,
                                 R_NUMEROSERIECONFERENCIA.BLOQUEIO,
                                 R_NUMEROSERIECONFERENCIA.AVARIA);

        PRC_DELETE_CONFERENCIA(R_NUMEROSERIECONFERENCIA.NUMPED,
                               R_NUMEROSERIECONFERENCIA.CODFILIAL,
                               R_NUMEROSERIECONFERENCIA.CODPROD,
                               R_NUMEROSERIECONFERENCIA.NUMSEQ,
                               R_NUMEROSERIECONFERENCIA.NUMSERIE);

        PRC_DELETE_NUMSERIE(R_NUMEROSERIECONFERENCIA.CODFILIAL,
                            R_NUMEROSERIECONFERENCIA.CODPROD,
                            R_NUMEROSERIECONFERENCIA.NUMSERIE);

      END LOOP;

    ELSE
      
      FOR R_CONTROLENUMSERIE IN (SELECT PCCONTROLENUMSERIE.CODPROD,
                                        PCCONTROLENUMSERIE.NUMSERIE,
                                        PCCONTROLENUMSERIE.NUMPED,
                                        PCCONTROLENUMSERIE.NUMSEQ,
                                        PCNUMEROSERIE.BLOQUEIO,
                                        PCNUMEROSERIE.AVARIA                                             
                                   FROM PCCONTROLENUMSERIE,
                                        PCNUMEROSERIE
                                  WHERE 1 = 1
                                    AND PCCONTROLENUMSERIE.NUMSERIE = PCNUMEROSERIE.NUMSERIE
                                    AND PCCONTROLENUMSERIE.CODPROD = PCNUMEROSERIE.CODPROD
                                    AND PCNUMEROSERIE.CODFILIAL = VNFILIALRETIRA
                                    AND PCCONTROLENUMSERIE.NUMPED = VNNUMPED
                                    AND PCCONTROLENUMSERIE.CODPROD = VNCODPROD
                                    AND PCCONTROLENUMSERIE.NUMSEQ = VNNUMSEQ)

        LOOP
          PRC_INSERIR_MOVIMENTACAO(VNFILIALRETIRA,
                                   R_CONTROLENUMSERIE.CODPROD,
                                   R_CONTROLENUMSERIE.NUMSERIE,
                                   VNNUMTRANSNUMEROSERIE,
                                   R_CONTROLENUMSERIE.BLOQUEIO,
                                   R_CONTROLENUMSERIE.AVARIA);          

          PRC_DELETE_NUMSERIE(VNFILIALRETIRA,
                              R_CONTROLENUMSERIE.CODPROD,
                              R_CONTROLENUMSERIE.NUMSERIE);

        END LOOP;      
    
    END IF;

    P_MENSAGEM := 'OK';
  EXCEPTION
    WHEN PARAMETRO_ENTRADA_NULO THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' - Paramatro de entrada "rowid" esta nulo.';

    WHEN DADOS_ROWID_NAO_ENCONTRADO THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' - Erro ao buscar dados da tabela de movimentacao. ' ||
                    'Nao foi encontrado nenhum registro para este numero de ROWID.';

    WHEN NAO_EXISTE_CONFERENCIA THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' - Erro nao existe conferencia registrada para esta movimentacao. ';

    WHEN NUMERO_SEQUENCIA_NAO_EXISTE THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' - Erro ao buscar dados do sequencial de numero de serie. ';

    WHEN OTHERS THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' -2.1-Erro ao realizar faturamento do numero de serie.';

  END;

  /*
    DESCRICAO: ESTE PROCEDIMENTO CANCELA UM NUMERO DE SERIE.

    CAMPOS:
            P_ROWID: ROWID DA PCMOV;
            P_MANTERRESERVADO: INFORMAR 'S' SE DESEJA MANTER RESERVADO, CASO N?O DESEJE INFORME 'N'
            P_MENSAGEM: CASO N?O OCORRA ERRO RETORNA 'OK', CASO OCORRA RETORNA A MENSAGEM DE ERRO.

  */

  PROCEDURE PRC_NUMERO_SERIE_CANCELAMENTO(P_ROWID           IN VARCHAR2,
                                          P_MANTERRESERVADO IN VARCHAR2,
                                          P_MENSAGEM        OUT VARCHAR2) IS
    /*  VARIAVEIS PROCEDURE  */
    VNNUMPED              PCMOV.NUMPED%TYPE;
    VNCODFILIAL           PCMOV.CODFILIAL%TYPE;
    VNFILIALRETIRA        PCMOV.CODFILIALRETIRA%TYPE;
    VNCODPROD             PCMOV.CODPROD%TYPE;
    VNNUMLOTE             PCMOV.NUMLOTE%TYPE;
    VNNUMTRANSITEM        PCMOV.NUMTRANSITEM%TYPE;
    VNNUMSEQ              PCMOV.NUMSEQ%TYPE;
    VNNUMTRANSNUMEROSERIE PCMOVCOMPLE.NUMTRANSNUMEROSERIE%TYPE;
    VNTEMPCMOV            NUMBER;

  BEGIN
    IF (P_ROWID IS NULL) THEN
      RAISE PARAMETRO_ENTRADA_NULO;
    END IF;

    BEGIN

      VNTEMPCMOV := 0;
      SELECT COUNT(*) INTO VNTEMPCMOV FROM PCMOV WHERE ROWID = P_ROWID;

      IF (VNTEMPCMOV > 0) THEN
        SELECT PCMOV.NUMPED,
               PCMOV.CODFILIAL,
               PCMOV.CODPROD,
               PCMOV.NUMLOTE,
               PCMOV.NUMTRANSITEM,
               PCMOV.NUMSEQ,
               PCMOVCOMPLE.NUMTRANSNUMEROSERIE,
               NVL(PCMOV.CODFILIALRETIRA, PCMOV.CODFILIAL)
          INTO VNNUMPED,
               VNCODFILIAL,
               VNCODPROD,
               VNNUMLOTE,
               VNNUMTRANSITEM,
               VNNUMSEQ,
               VNNUMTRANSNUMEROSERIE,
               VNFILIALRETIRA
          FROM PCMOV, PCMOVCOMPLE
         WHERE 1 = 1
           AND PCMOV.ROWID = P_ROWID
           AND PCMOV.NUMTRANSITEM = PCMOVCOMPLE.NUMTRANSITEM;
      ELSE
        SELECT PCMOVPREFAT.NUMPED,
               PCMOVPREFAT.CODFILIAL,
               PCMOVPREFAT.CODPROD,
               PCMOVPREFAT.NUMLOTE,
               PCMOVPREFAT.NUMTRANSITEM,
               PCMOVPREFAT.NUMSEQ,
               PCMOVCOMPLEPREFAT.NUMTRANSNUMEROSERIE,
               NVL(PCMOVPREFAT.CODFILIALRETIRA, PCMOVPREFAT.CODFILIAL)
          INTO VNNUMPED,
               VNCODFILIAL,
               VNCODPROD,
               VNNUMLOTE,
               VNNUMTRANSITEM,
               VNNUMSEQ,
               VNNUMTRANSNUMEROSERIE,
               VNFILIALRETIRA
          FROM PCMOVPREFAT, PCMOVCOMPLEPREFAT
         WHERE 1 = 1
           AND PCMOVPREFAT.ROWID = P_ROWID
           AND PCMOVPREFAT.NUMTRANSITEM = PCMOVCOMPLEPREFAT.NUMTRANSITEM;
      END IF;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RAISE DADOS_ROWID_NAO_ENCONTRADO;

      WHEN OTHERS THEN
        RAISE;
    END;

    FOR R_MOVIMENTACAONUMEROSERIE IN (SELECT PCMOVNUMEROSERIE.CODFILIAL,
                                             PCMOVNUMEROSERIE.CODPROD,
                                             PCMOVNUMEROSERIE.NUMSERIE,
                                             PCMOVNUMEROSERIE.NUMTRANSNUMEROSERIE,
                                             PCMOVNUMEROSERIE.DATAMOVNUMEROSERIE,
                                             PCMOVNUMEROSERIE.BLOQUEIO,
                                             PCMOVNUMEROSERIE.AVARIA
                                        FROM PCMOVNUMEROSERIE
                                       WHERE 1 = 1
                                         AND PCMOVNUMEROSERIE.NUMTRANSNUMEROSERIE =
                                             VNNUMTRANSNUMEROSERIE) LOOP

      PRC_INSERIR_NUMSERIE(R_MOVIMENTACAONUMEROSERIE.CODFILIAL,
                           R_MOVIMENTACAONUMEROSERIE.CODPROD,
                           R_MOVIMENTACAONUMEROSERIE.NUMSERIE,
                           VNNUMLOTE,
                           R_MOVIMENTACAONUMEROSERIE.BLOQUEIO,
                           R_MOVIMENTACAONUMEROSERIE.AVARIA,
                           P_MANTERRESERVADO);

      IF (P_MANTERRESERVADO = 'S') THEN
        PRC_INSERIR_CONFERENCIA(VNNUMPED,
                                R_MOVIMENTACAONUMEROSERIE.CODFILIAL,
                                R_MOVIMENTACAONUMEROSERIE.CODPROD,
                                VNNUMSEQ,
                                R_MOVIMENTACAONUMEROSERIE.NUMSERIE);
      END IF;

    END LOOP;

    P_MENSAGEM := 'OK';
  EXCEPTION
    WHEN PARAMETRO_ENTRADA_NULO THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' - Paramatro de entrada "rowid" esta nulo.';

    WHEN DADOS_ROWID_NAO_ENCONTRADO THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' - Erro ao buscar dados da tabela de movimentacao. ' ||
                    'Nao foi encontrado nenhum registro para este numero de ROWID.';

    WHEN OTHERS THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    '-Erro ao realizar cancelamento do numero de serie.';
  END;

  /*
    DESCRICAO: ESTE PROCEDIMENTO CONSULTA SE O PRODUTO CONTROLA NÃ¿MERO DE SERIE.

    CAMPOS:
            P_CODFILIAL: CÃ¿DIGO DA FILIAL;
            P_CODPRODUTO: CÃ¿DIGO DO PRODUTO;
            RETORNO: CASO CONTROLE NÃ¿MERO DE SERIE RETORNA 'S', CASO CONTRARIO 'N'

  */

  FUNCTION FUNC_CONTROLA_NUMERO_SERIE(P_CODFILIAL  IN VARCHAR2,
                                      P_CODPRODUTO IN PCPRODUT.CODPROD%TYPE)
    RETURN VARCHAR2 IS
    VCONTROLANUMSERIE        PCPRODFILIAL.CONTROLANUMSERIE%TYPE;
    VNUMEROSSERIECONTROLADOS PCPRODFILIAL.NUMEROSSERIECONTROLADOS%TYPE;
  BEGIN
    IF (P_CODFILIAL IS NULL) OR (P_CODPRODUTO IS NULL) THEN
      RAISE PARAMETRO_ENTRADA_NULO;
    END IF;

    BEGIN
      SELECT NVL(PCPRODFILIAL.CONTROLANUMSERIE, 'N') AS CONTROLANUMSERIE,
             NVL(PCPRODFILIAL.NUMEROSSERIECONTROLADOS, 0) AS NUMEROSSERIECONTROLADOS
        INTO VCONTROLANUMSERIE, VNUMEROSSERIECONTROLADOS
        FROM PCPRODFILIAL
       WHERE 1 = 1
         AND PCPRODFILIAL.CODPROD = P_CODPRODUTO
         AND PCPRODFILIAL.CODFILIAL = P_CODFILIAL;

      RETURN VCONTROLANUMSERIE;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RETURN 'N';
      WHEN OTHERS THEN
        RAISE;
    END;

  END FUNC_CONTROLA_NUMERO_SERIE;

  PROCEDURE TRANSFERIR_NUMERO_SERIE(P_NUMPEDDESTINO PCCONTROLENUMSERIE.NUMPED%TYPE,
                                    P_NUMSEQDESTINO PCCONTROLENUMSERIE.NUMSEQ%TYPE,
                                    P_NUMPEDORIGEM  PCCONTROLENUMSERIE.NUMPED%TYPE,
                                    P_NUMSEQORIGEM  PCCONTROLENUMSERIE.NUMSEQ%TYPE,
                                    P_CODPROD       PCCONTROLENUMSERIE.CODPROD%TYPE,
                                    P_NUMSERIE      PCCONTROLENUMSERIE.NUMSERIE%TYPE,
                                    P_MENSAGEM      OUT VARCHAR2) IS
    VNEXISTE NUMBER;
  BEGIN
    --TODOS OS PARAMETROS SAO OBRIGATORIOS
    IF (P_NUMPEDDESTINO IS NULL) OR (P_NUMSEQDESTINO IS NULL) OR
       (P_NUMPEDORIGEM IS NULL) OR (P_NUMSEQORIGEM IS NULL) OR
       (P_CODPROD IS NULL) OR (P_NUMSERIE IS NULL) THEN
      RAISE PARAMETRO_ENTRADA_NULO;
    END IF;

    BEGIN
      --VERIFICA SE EXISTE A MOVIMENTACAO A SER TRANSFERIDA
      SELECT 1
        INTO VNEXISTE
        FROM PCCONTROLENUMSERIE
       WHERE NUMPED = P_NUMPEDORIGEM
         AND NUMSEQ   = P_NUMSEQORIGEM
         AND CODPROD = P_CODPROD
         AND NUMSERIE = P_NUMSERIE;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        RAISE DADOS_NAO_ENCONTRADO;
      WHEN OTHERS THEN
        RAISE;
    END;

    --EFETUANDO A TROCA
    UPDATE PCCONTROLENUMSERIE
       SET NUMPED = P_NUMPEDDESTINO
         , NUMSEQ   = P_NUMSEQDESTINO
     WHERE NUMPED = P_NUMPEDORIGEM
       AND NUMSEQ   = P_NUMSEQORIGEM
       AND CODPROD = P_CODPROD
       AND NUMSERIE = P_NUMSERIE;

    P_MENSAGEM := 'OK';
  EXCEPTION
    WHEN PARAMETRO_ENTRADA_NULO THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' - todos os parametros de entrada sÃ£o obrigatÃ³rios.';
    WHEN DADOS_NAO_ENCONTRADO THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    ' - Erro ao buscar dados da tabela de movimentacao. ' ||
                    'Nao foi encontrado nenhum registro.';
    WHEN OTHERS THEN
      P_MENSAGEM := SQLCODE || ' - ' || SQLERRM ||
                    '-Erro ao realizar cancelamento do numero de serie.';

  END TRANSFERIR_NUMERO_SERIE;

END;
