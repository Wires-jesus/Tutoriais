                                    P_NUMSEQ    PCNUMEROSERIECONFERENCIA.NUMSEQ%TYPE,
                                    P_NUMSERIE  PCNUMEROSERIECONFERENCIA.NUMSERIE%TYPE) IS
  BEGIN
    INSERT INTO PCNUMEROSERIECONFERENCIA
      (PCNUMEROSERIECONFERENCIA.CODFILIAL,
