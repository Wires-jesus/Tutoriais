CREATE OR REPLACE PROCEDURE PRC_EXPLODE_BOICASADO(PREG_PCMOV  PCMOV%ROWTYPE)
IS REG_PCMOV                PCMOV%ROWTYPE;
   REG_PCEST                PCEST%ROWTYPE;
   V_NUMTRANSENT            PCCONSUM.PROXNUMTRANSENT%TYPE;
   REG_PCFILIAL             PCFILIAL%ROWTYPE;
   V_PERCVALORPRODACABADO   PCFORMPROD.PERCVALORPRODACABADO%TYPE;
   V_CUSTOULTIMAENTRADA     PCEST.CUSTOULTENT%TYPE;

   V_CUSTOREAL_ACAB         PCEST.CUSTOREAL%TYPE;
   V_CUSTOFINANCEIRO_ACAB   PCEST.CUSTOFIN%TYPE;
   V_CUSTOREPOSICAO_ACAB    PCEST.CUSTOREP%TYPE;
   V_CUSTOCONTABIL_ACAB     PCEST.CUSTOCONT%TYPE;

   V_CUSTOREAL_BOI          PCEST.CUSTOREAL%TYPE;
   V_CUSTOFIN_BOI           PCEST.CUSTOFIN%TYPE;
   V_CUSTOREP_BOI           PCEST.CUSTOREP%TYPE;
   V_CUSTOCONT_BOI          PCEST.CUSTOCONT%TYPE;
   V_QTEST_BOI              PCEST.QTEST%TYPE;
   V_QTESTGER_BOI           PCEST.QTESTGER%TYPE;


BEGIN
  /*
  Autor: Israel.Junior
  Data: 17/09/2014
  Objetivo: Procedure tem o Objetivo de realizar desmembramento de um produto TIPOMERC = 'BC'
            em varios outros de acordo com sua formulação informada na PCFORMPROD.
  Passos:
    1 Realizar movimentação PCMOV tipo 'SP' do produto 'BC'
    2 Realizar atualização da PCEST retirando do estoque o produto 'BC'
    3 Realizar movimentação PCMOV tipo 'EP' dos produtos da PCFORMPROD
    4 Realizar atualização da PCEST colocando no estoque os produtos PCFORMPROD
    5 Registrar na tabela PCPRODAUTOBOICASADO o processo de produdução automatica.
  */

  /*
  Autor: Lucas.Cruz
  Data: 20/11/2018
  Objetivo: realizar ajustes dos cálculos dos produtos acabados utilizando média ponderada
  */

  /*obtendo dados da filial*/
  SELECT PCFILIAL.*
    INTO REG_PCFILIAL
    FROM PCFILIAL
    WHERE PCFILIAL.CODIGO = PREG_PCMOV.CODFILIAL;

  /*Carrega registro da PCEST*/
  SELECT PCEST.*
    INTO REG_PCEST
    FROM PCEST
   WHERE CODPROD = PREG_PCMOV.CODPROD
     AND CODFILIAL = PREG_PCMOV.CODFILIAL;

  /*Obtendo o Proximo NumTranEnt*/
  SELECT PCCONSUM.PROXNUMTRANSENT
    INTO V_NUMTRANSENT
    FROM PCCONSUM;

  /*Atualizando o próximo NumTranEnt*/
  UPDATE PCCONSUM
     SET PROXNUMTRANSENT = PROXNUMTRANSENT + 1;

  /* guardando os custos e estoque do boi */
  V_CUSTOREAL_BOI := REG_PCEST.CUSTOREAL;
  V_CUSTOFIN_BOI  := REG_PCEST.CUSTOFIN;
  V_CUSTOREP_BOI  := REG_PCEST.CUSTOREP;
  V_CUSTOCONT_BOI := REG_PCEST.CUSTOCONT;
  V_QTEST_BOI     := REG_PCEST.QTEST;
  V_QTESTGER_BOI  := REG_PCEST.QTESTGER;

  /*Alimentando dados para geração da PCMOV*/
  REG_PCMOV                := NULL;
  REG_PCMOV.DTMOV          := TRUNC (SYSDATE);
  REG_PCMOV.CODOPER        := 'SP';
  REG_PCMOV.STATUS         := 'AB';
  REG_PCMOV.NUMTRANSVENDA  := NULL;
  REG_PCMOV.CODPROD        := PREG_PCMOV.CODPROD;
  REG_PCMOV.QT             := PREG_PCMOV.QT;
  REG_PCMOV.PUNIT          := PREG_PCMOV.PUNIT;
  REG_PCMOV.CODFILIAL      := PREG_PCMOV.CODFILIAL;
  REG_PCMOV.CODCLI         := PREG_PCMOV.CODFILIAL;
  REG_PCMOV.CUSTOFINANT    := REG_PCEST.CUSTOFIN;
  REG_PCMOV.CUSTOCONTANT   := REG_PCEST.CUSTOCONT;
  REG_PCMOV.CUSTOREALANT   := REG_PCEST.CUSTOREAL;
  REG_PCMOV.CUSTOFIN       := REG_PCEST.CUSTOFIN;
  REG_PCMOV.CUSTOCONT      := REG_PCEST.CUSTOCONT;
  REG_PCMOV.CUSTOREAL      := REG_PCEST.CUSTOREAL;
  REG_PCMOV.QTCONT         := PREG_PCMOV.QT;
  REG_PCMOV.PUNITCONT      := PREG_PCMOV.PUNIT;
  REG_PCMOV.NUMTRANSENT    := V_NUMTRANSENT;
  REG_PCMOV.CODFORNEC      := REG_PCFILIAL.CODFORNEC;
  REG_PCMOV.QTESTGERANT    := REG_PCEST.QTESTGER;
  REG_PCMOV.QTESTANT       := REG_PCEST.QTEST;
  REG_PCMOV.CUSTOULTENTANT := REG_PCEST.CUSTOULTENT;
  REG_PCMOV.CUSTOULTENT    := REG_PCEST.CUSTOULTENT;
  REG_PCMOV.CODFUNCLANC    := PREG_PCMOV.CODFUNCLANC;
  REG_PCMOV.NUMNOTA        := V_NUMTRANSENT;
  REG_PCMOV.CODUSUR        := PREG_PCMOV.CODUSUR;

  /*Incluindo registro na PCMOV*/
  INSERT INTO PCMOV
       VALUES REG_PCMOV;

  /*Calculando o estoque */
  REG_PCEST.QTEST    := NVL(REG_PCEST.QTEST, 0) - PREG_PCMOV.QT;
  REG_PCEST.QTESTGER := NVL(REG_PCEST.QTESTGER, 0) - PREG_PCMOV.QT;
  
  /*Data da ultima entrada */
  REG_PCEST.DTULTENT := TRUNC (SYSDATE);
  
  /*Atualizando PCEST*/
  UPDATE PCEST
     SET ROW = REG_PCEST
   WHERE CODPROD   = PREG_PCMOV.CODPROD
     AND CODFILIAL = PREG_PCMOV.CODFILIAL;


  /*Realizar processo de movimentação dos produtos acabados*/
  FOR R_ITEM IN (SELECT *
                   FROM PCFORMPROD
                  WHERE CODPRODACAB = PREG_PCMOV.CODPROD)
  LOOP
    /*Obtendo registro PCEST do produto*/
    SELECT PCEST.*
      INTO REG_PCEST
      FROM PCEST
     WHERE CODPROD = R_ITEM.CODPRODMP
       AND CODFILIAL = PREG_PCMOV.CODFILIAL;


	/* obtendo dados do produto na PCFORMPROD */
    SELECT PCFORMPROD.PERCVALORPRODACABADO
      INTO V_PERCVALORPRODACABADO
      FROM PCFORMPROD
     WHERE PCFORMPROD.CODPRODACAB = PREG_PCMOV.CODPROD
	   AND PCFORMPROD.CODPRODMP = R_ITEM.CODPRODMP;


    /*Alimentando dados para geração da PCMOV*/
    REG_PCMOV                := NULL;
    REG_PCMOV.DTMOV          := TRUNC(SYSDATE);
    REG_PCMOV.NUMLOTE        := 'CB1';
    REG_PCMOV.CODOPER        := 'EP';
    REG_PCMOV.STATUS         := 'AB';
    REG_PCMOV.NUMTRANSVENDA  := NULL;
    REG_PCMOV.CODPROD        := R_ITEM.CODPRODMP;
    REG_PCMOV.PUNIT          := PREG_PCMOV.PUNIT;
    REG_PCMOV.CODFILIAL      := PREG_PCMOV.CODFILIAL;
    REG_PCMOV.CODCLI         := REG_PCFILIAL.CODCLI;
    REG_PCMOV.CUSTOFINANT    := REG_PCEST.CUSTOFIN;
    REG_PCMOV.CUSTOCONTANT   := REG_PCEST.CUSTOCONT;
    REG_PCMOV.CUSTOREALANT   := REG_PCEST.CUSTOREAL;
    REG_PCMOV.PUNITCONT      := PREG_PCMOV.PUNITCONT;
    REG_PCMOV.NUMTRANSENT    := V_NUMTRANSENT;
    REG_PCMOV.CODFORNEC      := REG_PCFILIAL.CODFORNEC;
    REG_PCMOV.QTESTGERANT    := REG_PCEST.QTESTGER;
    REG_PCMOV.QTESTANT       := REG_PCEST.QTEST;
    REG_PCMOV.CUSTOULTENTANT := REG_PCEST.CUSTOULTENT;
    REG_PCMOV.CUSTOULTENT    := PREG_PCMOV.CUSTOULTENT;
    REG_PCMOV.CODFUNCLANC    := PREG_PCMOV.CODFUNCLANC;
    REG_PCMOV.NUMNOTA        := V_NUMTRANSENT;
    REG_PCMOV.CODUSUR        := PREG_PCMOV.CODUSUR;

    IF R_ITEM.QTPRODMP > 0 THEN
      REG_PCMOV.QT := PREG_PCMOV.QT * R_ITEM.QTPRODMP / NVL (R_ITEM.QTPRODACAB, 1);
      REG_PCMOV.QTCONT := PREG_PCMOV.QT * R_ITEM.QTPRODMP / NVL (R_ITEM.QTPRODACAB, 1);
    ELSE
      REG_PCMOV.QT := PREG_PCMOV.QT * (R_ITEM.PERCPRODACABADO / 100);
      REG_PCMOV.QTCONT := PREG_PCMOV.QT * (R_ITEM.PERCPRODACABADO / 100);
    END IF;

    /* calculando custo da última entrada e os custos real, financeiro, reposição e contábil */
    V_CUSTOULTIMAENTRADA   := ((V_CUSTOREAL_BOI     * PREG_PCMOV.QT) * (V_PERCVALORPRODACABADO / 100)) / REG_PCMOV.QT;
    V_CUSTOREAL_ACAB       := ((REG_PCEST.CUSTOREAL * GREATEST(REG_PCEST.QTESTGER,0)) + (V_CUSTOULTIMAENTRADA * REG_PCMOV.QT)) / (GREATEST(REG_PCEST.QTESTGER,0) + REG_PCMOV.QT);

    V_CUSTOULTIMAENTRADA   := ((V_CUSTOFIN_BOI      * PREG_PCMOV.QT) * (V_PERCVALORPRODACABADO / 100)) / REG_PCMOV.QT;
    V_CUSTOFINANCEIRO_ACAB := ((REG_PCEST.CUSTOFIN  * GREATEST(REG_PCEST.QTESTGER,0)) + (V_CUSTOULTIMAENTRADA * REG_PCMOV.QT)) / (GREATEST(REG_PCEST.QTESTGER,0) + REG_PCMOV.QT);

    V_CUSTOULTIMAENTRADA   := ((V_CUSTOREP_BOI      * PREG_PCMOV.QT) * (V_PERCVALORPRODACABADO / 100)) / REG_PCMOV.QT;
    V_CUSTOREPOSICAO_ACAB  := ((REG_PCEST.CUSTOREP  * GREATEST(REG_PCEST.QTESTGER,0)) + (V_CUSTOULTIMAENTRADA * REG_PCMOV.QT)) / (GREATEST(REG_PCEST.QTESTGER,0) + REG_PCMOV.QT);

    V_CUSTOULTIMAENTRADA   := ((V_CUSTOCONT_BOI     * PREG_PCMOV.QT) * (V_PERCVALORPRODACABADO / 100)) / REG_PCMOV.QT;
    V_CUSTOCONTABIL_ACAB   := ((REG_PCEST.CUSTOCONT * GREATEST(REG_PCEST.QTEST,0)) + (V_CUSTOULTIMAENTRADA * REG_PCMOV.QT)) / (GREATEST(REG_PCEST.QTEST,0) + REG_PCMOV.QT);
    /* fim cálculo custo ultima entrada e os custos real, financeiro, reposição e contábil */

    REG_PCMOV.CUSTOREAL    := V_CUSTOREAL_ACAB;
    REG_PCMOV.CUSTOFIN     := V_CUSTOFINANCEIRO_ACAB;
    REG_PCMOV.CUSTOREP     := V_CUSTOREPOSICAO_ACAB;
  	REG_PCMOV.CUSTOCONT    := V_CUSTOCONTABIL_ACAB;

    /*Incluindo registro de movimentação*/
    INSERT INTO PCMOV
         VALUES REG_PCMOV;

    /*ATUALIZA OS CUSTOS*/
    REG_PCEST.CUSTOULTENTANT := V_CUSTOULTIMAENTRADA;
    REG_PCEST.CUSTOULTENT    := V_CUSTOULTIMAENTRADA;

    REG_PCEST.CUSTOREAL      := V_CUSTOREAL_ACAB;
    REG_PCEST.CUSTOFIN       := V_CUSTOFINANCEIRO_ACAB;
    REG_PCEST.CUSTOREP       := V_CUSTOREPOSICAO_ACAB;
    REG_PCEST.CUSTOCONT      := V_CUSTOCONTABIL_ACAB;

    IF R_ITEM.QTPRODMP > 0 THEN
      REG_PCEST.QTEST     := NVL (REG_PCEST.QTEST, 0) + (PREG_PCMOV.QT * R_ITEM.QTPRODMP / NVL (R_ITEM.QTPRODACAB, 1));
      REG_PCEST.QTESTGER  := NVL (REG_PCEST.QTESTGER, 0) + (PREG_PCMOV.QT * R_ITEM.QTPRODMP / NVL (R_ITEM.QTPRODACAB, 1));
    ELSE
      REG_PCEST.QTEST     := NVL (REG_PCEST.QTEST, 0) + (PREG_PCMOV.QT * (R_ITEM.PERCPRODACABADO / 100));
      REG_PCEST.QTESTGER  := NVL (REG_PCEST.QTESTGER, 0) + (PREG_PCMOV.QT * (R_ITEM.PERCPRODACABADO / 100));
    END IF;
    
    /*Data da ultima entrada */
    REG_PCEST.DTULTENT := TRUNC (SYSDATE);

    /*Atualizando o regitro na PCEST*/
    UPDATE PCEST
       SET ROW = REG_PCEST
     WHERE CODPROD = R_ITEM.CODPRODMP
       AND CODFILIAL = PREG_PCMOV.CODFILIAL;

  END LOOP;

  /*Gravar informando que já foi processado*/
  INSERT INTO PCPRODAUTOBOICASADO
              (NUMTRANSENTORIG
             , CODCONTORIG
             , NUMTRANSENTDEST
             , CODCONTDEST
             , DATA
              )
       VALUES (PREG_PCMOV.NUMTRANSENT
             , NULL
             , V_NUMTRANSENT
             , NULL
             , SYSDATE
              );

END PRC_EXPLODE_BOICASADO;
