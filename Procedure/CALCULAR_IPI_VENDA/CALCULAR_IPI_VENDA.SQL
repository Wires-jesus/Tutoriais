CREATE OR REPLACE PROCEDURE CALCULAR_IPI_VENDA(
                                         pCODFILIAL IN VARCHAR2,
                                         pCODPROD IN NUMBER,
                                         pPRECOVENDA IN NUMBER,
                                         pVLIPI OUT NUMBER,
                                         pPERCIPI OUT NUMBER,
                                         pPRECOFINAL OUT NUMBER)
IS
  vCALCULASTCOMIPI  VARCHAR2(1);
  vCALCULARIPIVENDA VARCHAR2(1);
  vTESTE            VARCHAR2(1);

  vVLPAUTAIPIVENDA PCPRODUT.VLPAUTAIPIVENDA%TYPE;
  vVLIPIPORKGVENDA PCPRODUT.VLIPIPORKGVENDA%TYPE;
  vPESOBRUTO       PCPRODUT.PESOBRUTO%TYPE;
  vPERCIPIVENDA    PCPRODUT.PERCIPIVENDA%TYPE;

  vPRECOSEMIPI PCPEDI.PVENDA%TYPE;
  vPRECOVENDA PCPEDI.PVENDA%TYPE;

  vVLIPI   PCPEDI.VLIPI%TYPE;
  vPERCIPI PCPEDI.PERCIPI%TYPE;

  vPRECOFINAL PCPEDI.PVENDA%TYPE;


BEGIN
  SELECT PCCONSUM.CALCULARSTCOMIPI
    INTO vCALCULASTCOMIPI
    FROM PCCONSUM;

  SELECT PCFILIAL.CALCULARIPIVENDA
    INTO vCALCULARIPIVENDA
    FROM PCFILIAL
   WHERE PCFILIAL.CODIGO = pCODFILIAL;

  SELECT NVL(PCPRODUT.VLPAUTAIPIVENDA, 0)
        ,NVL(PCPRODUT.VLIPIPORKGVENDA, 0)
        ,NVL(PCPRODUT.PESOBRUTO, 0)
        ,NVL(PCPRODUT.PERCIPIVENDA, 0)
    INTO vVLPAUTAIPIVENDA, vVLIPIPORKGVENDA, vPESOBRUTO, vPERCIPIVENDA
    FROM PCPRODUT
   WHERE PCPRODUT.CODPROD = pCODPROD;

  vVLIPI      := 0;
  vPERCIPI    := 0;
  vPRECOFINAL := pPRECOVENDA;

  vPRECOVENDA  := pPRECOVENDA;
  vPRECOSEMIPI := (vPRECOVENDA / (1 + (vPERCIPIVENDA / 100)));


  IF vPRECOSEMIPI <> 0 THEN

    IF (vCALCULASTCOMIPI = 'N') AND (vCALCULARIPIVENDA = 'S') THEN

      IF vVLPAUTAIPIVENDA > 0 THEN
        vVLIPI   := vVLPAUTAIPIVENDA;
        vPERCIPI := 100 * (vVLIPI / vPRECOSEMIPI);
      ELSE
        IF vVLIPIPORKGVENDA > 0 THEN
          vVLIPI   := vPESOBRUTO * vVLIPIPORKGVENDA;
          vPERCIPI := 100 * (vVLIPI / vPRECOSEMIPI);
        ELSE
          IF vPERCIPIVENDA > 0 THEN
            vVLIPI   := vPRECOVENDA * (vPERCIPIVENDA / 100);
            vPERCIPI := 100 * (vVLIPI / vPRECOSEMIPI);
          END IF;
        END IF;
      END IF;
      vPRECOFINAL := vPRECOVENDA + vVLIPI;
    ELSE
      IF (vCalculaStComIpi = 'S') AND (vCalcularIpiVenda = 'S') THEN
        IF vVLPAUTAIPIVENDA > 0 THEN
          vVLIPI   := vVLPAUTAIPIVENDA;
          vPERCIPI := 100 * (vVLIPI / vPRECOSEMIPI);
        ELSE
          IF vVLIPIPORKGVENDA > 0 THEN
            vVLIPI   := vPESOBRUTO * vVLIPIPORKGVENDA;
            vPERCIPI := 100 * (vVLIPI / vPRECOSEMIPI);
          ELSE
            IF vPERCIPIVENDA > 0 THEN
              vPRECOSEMIPI := (vPRECOVENDA / (1 + (vPERCIPIVENDA / 100)));
              vVLIPI   := vPRECOVENDA - vPRECOSEMIPI;
              vPERCIPI := vPERCIPIVENDA;
            END IF;
          END IF;
        END IF;
      END IF;
    END IF;
  END IF;
  pVLIPI   := vVLIPI;
  pPERCIPI := vPERCIPI;
  pPRECOFINAL := vPRECOFINAL;

END;
