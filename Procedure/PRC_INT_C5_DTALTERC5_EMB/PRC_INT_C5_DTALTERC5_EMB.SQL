CREATE OR REPLACE PROCEDURE PRC_INT_C5_DTALTERC5_EMB IS
BEGIN
   EXECUTE IMMEDIATE ('ALTER TRIGGER trg_c5_dt_pcembalagem DISABLE');

  MERGE INTO PCEMBALAGEM E
      USING (SELECT CODPROD, MAX(DTALTERC5) DTALTERC5 FROM Vw_Int_C5_Embprod GROUP BY CODPROD) B
    ON (E.CODPROD = B.CODPROD)
    WHEN MATCHED THEN
    UPDATE SET
       E.DTALTERC5 = B.DTALTERC5
     WHERE E.CODFILIAL IN (SELECT CODFILIAL FROM VW_INT_C5_OBTER_FILIAIS_C5)
     AND E.DTINATIVO IS NULL
     AND NVL(ENVIAFRENTECAIXA, 'S') = 'S' ;

  COMMIT;

  EXECUTE IMMEDIATE ('ALTER TRIGGER trg_c5_dt_pcembalagem ENABLE');

  EXECUTE IMMEDIATE ('ALTER TRIGGER trg_c5_dt_pcembalagem COMPILE');
END PRC_INT_C5_DTALTERC5_EMB;