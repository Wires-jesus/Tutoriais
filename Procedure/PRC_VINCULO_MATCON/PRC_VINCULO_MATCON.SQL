CREATE OR REPLACE PROCEDURE PRC_VINCULO_MATCON(PNUMPED IN NUMBER, PROTINA IN VARCHAR2 := 'SF', PIDVINCULO IN VARCHAR2, POUT OUT VARCHAR2) IS

  /**
     PROTINA = 'SF' = CHAMADA VIA SERVIDOR DE FATURAMENTO VAREJO
     PROTINA = 'AV' = CHAMADA VIA  API DE VENDAS
  **/
  ERRO_LOCKPCVINCULOPEDIDOMATCON EXCEPTION;
  ROWPCVINCULOPEDIDOMATCON PCVINCULOPEDIDOMATCON%ROWTYPE;
  ROWPCPEDC                PCPEDC%ROWTYPE;
  VVALORNUMPEDHUBE         VARCHAR(50);

  PROCEDURE INSERIR_PCVINCULOPEDIDOMATCON(TIPOVENDA VARCHAR2) IS
  BEGIN
    ROWPCVINCULOPEDIDOMATCON.IDVINCULO := PIDVINCULO;
    IF (TIPOVENDA = 'TV7') THEN
      ROWPCVINCULOPEDIDOMATCON.NUMPEDTV7   := ROWPCPEDC.NUMPED;
      ROWPCVINCULOPEDIDOMATCON.DTPEDIDOTV7 := ROWPCPEDC.DATA;
    ELSE
      ROWPCVINCULOPEDIDOMATCON.NUMPEDTV8   := ROWPCPEDC.NUMPED;
      ROWPCVINCULOPEDIDOMATCON.DTPEDIDOTV8 := ROWPCPEDC.DATA;
    END IF;
    ROWPCVINCULOPEDIDOMATCON.DATA      := SYSDATE;
    ROWPCVINCULOPEDIDOMATCON.VINCULADO := 'N'; 
    INSERT INTO PCVINCULOPEDIDOMATCON VALUES ROWPCVINCULOPEDIDOMATCON;
  END;

  FUNCTION EXISTE_ALGUM_REGISTRO(PPCVINCULOPEDIDOMATCON PCVINCULOPEDIDOMATCON%ROWTYPE) RETURN BOOLEAN IS
  BEGIN
    RETURN PPCVINCULOPEDIDOMATCON.IDVINCULO IS NOT NULL;
  END;

  FUNCTION EXISTE_APENAS_REG_COM_TV7(PPCVINCULOPEDIDOMATCON PCVINCULOPEDIDOMATCON%ROWTYPE) RETURN BOOLEAN IS
  BEGIN
    RETURN(PPCVINCULOPEDIDOMATCON.IDVINCULO IS NOT NULL AND PPCVINCULOPEDIDOMATCON.NUMPEDTV7 IS NOT NULL AND
           PPCVINCULOPEDIDOMATCON.NUMPEDTV8 IS NULL AND NVL(PPCVINCULOPEDIDOMATCON.VINCULADO, 'N') = 'N');
  END;

  FUNCTION EXISTE_APENAS_REG_COM_TV8(PPCVINCULOPEDIDOMATCON PCVINCULOPEDIDOMATCON%ROWTYPE) RETURN BOOLEAN IS
  BEGIN
    RETURN(ROWPCVINCULOPEDIDOMATCON.IDVINCULO IS NOT NULL AND ROWPCVINCULOPEDIDOMATCON.NUMPEDTV8 IS NOT NULL AND
           ROWPCVINCULOPEDIDOMATCON.NUMPEDTV7 IS NULL AND NVL(PPCVINCULOPEDIDOMATCON.VINCULADO, 'N') = 'N');
  END;
BEGIN
  BEGIN
    -- INÍCIO
    POUT := 'OK';
  
    BEGIN
      SELECT S.*
        INTO ROWPCVINCULOPEDIDOMATCON
        FROM PCVINCULOPEDIDOMATCON S
       WHERE IDVINCULO = PIDVINCULO
         AND ROWNUM = 1
         FOR UPDATE NOWAIT;
    EXCEPTION
      WHEN NO_DATA_FOUND THEN
        ROWPCVINCULOPEDIDOMATCON := NULL;
      WHEN OTHERS THEN
        RAISE ERRO_LOCKPCVINCULOPEDIDOMATCON;
    END;
  
    BEGIN
      SELECT S.* INTO ROWPCPEDC FROM PCPEDC S WHERE NUMPED = PNUMPED;
    EXCEPTION
      WHEN OTHERS THEN
        ROWPCPEDC := NULL;
    END;
  
    VVALORNUMPEDHUBE := ROWPCVINCULOPEDIDOMATCON.IDVINCULO || ' - TV8 - RI';
  
    IF NVL(PROTINA, 'SF') = 'SF' AND ROWPCPEDC.NUMPED IS NOT NULL THEN
      -- CHAMADO PELO SERVIDOR DE FATURAMENTO
      IF EXISTE_APENAS_REG_COM_TV7(ROWPCVINCULOPEDIDOMATCON) THEN
      
        -- ATUALIZAR O TV8
        UPDATE PCPEDC S
           SET NUMPEDHUBE = VVALORNUMPEDHUBE, NUMPEDENTFUT = ROWPCVINCULOPEDIDOMATCON.NUMPEDTV7, CONDVENDA = 8
         WHERE NUMPED = ROWPCPEDC.NUMPED;
      
        UPDATE PCVINCULOPEDIDOMATCON S
           SET VINCULADO = 'S', NUMPEDTV8 = ROWPCPEDC.NUMPED, DTPEDIDOTV8 = ROWPCPEDC.DATA
         WHERE IDVINCULO = ROWPCVINCULOPEDIDOMATCON.IDVINCULO;
      
      ELSE
        IF NOT EXISTE_ALGUM_REGISTRO(ROWPCVINCULOPEDIDOMATCON) THEN
          INSERIR_PCVINCULOPEDIDOMATCON('TV8');
        ELSE
          POUT := 'REGISTRO NÃO INSERIDO NEM ALTERADO. VERIFIQUE SE JÁ EXISTE NA TABELA ''PCVINCULOPEDIDOMATCON''. VERIFIQUE SE O CAMPO ''VINCULO'' ESTA MARCADO COMO ''S''';
        END IF;
      END IF;
    ELSIF (NVL(PROTINA, 'SF') = 'AV') AND ROWPCPEDC.NUMPED IS NOT NULL THEN
    
      -- CHAMADO PELA API DE VENDAS
      IF EXISTE_APENAS_REG_COM_TV8(ROWPCVINCULOPEDIDOMATCON) THEN
      
        -- ATUALIZAR O TV7
        UPDATE PCPEDC S
           SET NUMPEDENTFUT = ROWPCPEDC.NUMPED, 
               NUMPEDHUBE = VVALORNUMPEDHUBE, 
               CONDVENDA = 8, 
               VENDAASSISTIDA = 'S', 
               MATERIALDECONSTRUCAO = 'S'
         WHERE NUMPED = ROWPCVINCULOPEDIDOMATCON.NUMPEDTV8;
      
        UPDATE PCVINCULOPEDIDOMATCON S
           SET S.VINCULADO = 'S', S.NUMPEDTV7 = ROWPCPEDC.NUMPED, S.DTPEDIDOTV7 = ROWPCPEDC.DATA
         WHERE IDVINCULO = ROWPCVINCULOPEDIDOMATCON.IDVINCULO;
      
      ELSE
        --IF (ROWPCPEDC.CONDVENDA = 7 AND ROWPCVINCULOPEDIDOMATCON.NUMPEDTV7 IS NULL AND (NOT EXISTE_ALGUM_REGISTRO(ROWPCVINCULOPEDIDOMATCON))) THEN
        IF (NOT EXISTE_ALGUM_REGISTRO(ROWPCVINCULOPEDIDOMATCON)) THEN
          INSERIR_PCVINCULOPEDIDOMATCON('TV7');
        ELSE
          POUT := 'REGISTRO NÃO INSERIDO NEM ALTERADO. VERIFIQUE SE JÁ EXISTE NA TABELA ''PCVINCULOPEDIDOMATCON''. VERIFIQUE SE O CAMPO ''VINCULO'' ESTA MARCADO COMO ''S''';
        END IF;
      END IF;
    ELSE
      POUT := 'DADOS NÃO ENCONTRADOS. PROTINA: ' || PROTINA || ' PNUMPED: ' || PNUMPED || ' PIDVINCULO: ' || PIDVINCULO;
    END IF;
  
  EXCEPTION
    WHEN ERRO_LOCKPCVINCULOPEDIDOMATCON THEN
      POUT := 'ERRO AO TENTAR REALIZAR O LOCK DA TABELA PCVINCULOPEDIDOMATCON. ERRO: ' || DBMS_UTILITY.format_error_stack;
      ROLLBACK;
    WHEN OTHERS THEN
      POUT := 'ERRO AO TENTAR REALIZAR VÍNCULO DO PEDIDO: ' || PNUMPED;
      ROLLBACK;
  END;

END PRC_VINCULO_MATCON;