CREATE OR REPLACE PROCEDURE PRC_LOG_INTEGRADORA(PPARAMS IN OUT PCLOG_INTEGRADORA%ROWTYPE
                                               ,PLINHA  IN VARCHAR DEFAULT '') IS
  PRAGMA AUTONOMOUS_TRANSACTION;
  VSLINHAERRO VARCHAR2(2000);
BEGIN
  SELECT DFSEQ_LOG_INTEGRADORA.NEXTVAL
    INTO PPARAMS.SEQ
    FROM DUAL;

  PPARAMS.DATA := SYSDATE;

  VSLINHAERRO := '';
  IF PPARAMS.MSG LIKE '%ORA-%'
  THEN
    VSLINHAERRO := PLINHA;
  END IF;

  PPARAMS.MSG := SUBSTR( PPARAMS.MSG || CHR(13) || CASE
                   WHEN VSLINHAERRO <> '' THEN
                    ' ERRO: ' || VSLINHAERRO
                   WHEN PLINHA <> '' THEN
                    ' LINHA MSG: ' || PLINHA
                   ELSE
                    ' LINHA: ' || PLINHA
                 END,1 , 2000);


  INSERT INTO PCLOG_INTEGRADORA
  VALUES PPARAMS;

  COMMIT;
END;
