--
-- PROCEDURE DE EXCLUSAO DOS REGISTROS
CREATE OR REPLACE PROCEDURE PRC_REMOVER_TABELAS_ECFS IS
   DATA_LIMITE DATE;

   CURSOR CVENDAS IS
      SELECT PCPEDCECF.NUMPEDECF,
             PCPEDCECF.NUMCAIXA,
             PCPEDCECF.CODFUNCCX,
             PCPEDCECF.NUMSERIEEQUIP
        FROM PCPEDCECF
       WHERE DATA < (TRUNC(SYSDATE) - 90);

   CVENDASREG CVENDAS%ROWTYPE;
BEGIN
   IF CVENDAS%ISOPEN THEN
      CLOSE CVENDAS;
   END IF;

   OPEN CVENDAS;

   LOOP
      FETCH CVENDAS
         INTO CVENDASREG;
   
      EXIT WHEN CVENDAS%NOTFOUND;
   
      DELETE FROM PCPEDCECF
       WHERE NUMPEDECF = CVENDASREG.NUMPEDECF
         AND CODFUNCCX = CVENDASREG.CODFUNCCX
         AND NUMSERIEEQUIP = CVENDASREG.NUMSERIEEQUIP
         AND NUMCAIXA = CVENDASREG.NUMCAIXA;
   
      DELETE FROM PCPEDIECF
       WHERE NUMPEDECF = CVENDASREG.NUMPEDECF
         AND CODFUNCCX = CVENDASREG.CODFUNCCX
         AND NUMSERIEEQUIP = CVENDASREG.NUMSERIEEQUIP
         AND NUMCAIXA = CVENDASREG.NUMCAIXA;
   
      DELETE FROM PCPEDIECFCESTA
       WHERE NUMPEDECF = CVENDASREG.NUMPEDECF
         AND CODFUNCCX = CVENDASREG.CODFUNCCX
         AND NUMSERIEEQUIP = CVENDASREG.NUMSERIEEQUIP
         AND NUMCAIXA = CVENDASREG.NUMCAIXA;
   
      DELETE FROM PCPRESTECF
       WHERE NUMPEDECF = CVENDASREG.NUMPEDECF
         AND CODFUNCCHECKOUT = CVENDASREG.CODFUNCCX
         AND NUMSERIEEQUIP = CVENDASREG.NUMSERIEEQUIP
         AND NUMCHECKOUT = CVENDASREG.NUMCAIXA;
   
      DELETE FROM PCAUTORIECF
       WHERE NUMPEDECF = CVENDASREG.NUMPEDECF
         AND CODFUNCCX = CVENDASREG.CODFUNCCX
         AND SERIEEQUIP = CVENDASREG.NUMSERIEEQUIP
         AND NUMCAIXA = CVENDASREG.NUMCAIXA;
   
      DELETE FROM PCVENDACONSUMECF
       WHERE NUMPEDECF = CVENDASREG.NUMPEDECF
         AND CODFUNCCX = CVENDASREG.CODFUNCCX
         AND NUMSERIEEQUIP = CVENDASREG.NUMSERIEEQUIP
         AND NUMCAIXA = CVENDASREG.NUMCAIXA;
   
      DELETE FROM PCORIGEMPRECOECF
       WHERE NUMPEDECF = CVENDASREG.NUMPEDECF
         AND CODFUNCCX = CVENDASREG.CODFUNCCX
         AND NUMSERIEEQUIP = CVENDASREG.NUMSERIEEQUIP
         AND NUMCAIXA = CVENDASREG.NUMCAIXA;
   
      COMMIT;
   END LOOP;
EXCEPTION
   WHEN OTHERS THEN
      NULL;
END PRC_REMOVER_TABELAS_ECFS;
