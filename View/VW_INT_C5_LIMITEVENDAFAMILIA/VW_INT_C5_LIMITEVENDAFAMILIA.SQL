CREATE OR REPLACE VIEW VW_INT_C5_LIMITEVENDAFAMILIA AS
 SELECT DISTINCT PROD.SEQFAMILIA,
                NVL(I.QTMAXVENDA, NVL(EMB.QTMAXVENDA, 0)) QTMAXVENDA,
                NVL(EMB.QTUNIT, 1) QTUNIT,
                '2011' ORIGEM,
                DECODE(C.HORAINICIAL, NULL, 1, 0) PRIORIDADE,
                1 SEQLIMITEVENDA
  FROM VW_INT_C5_EMBPROD           EMB,
       PCOFERTAPROGRAMADAC         C,
       PCOFERTAPROGRAMADAI         I,
       PCDEPARAPRODC5              DEPARA,
       MONITORPDVMIDDLE.TB_PRODUTO PROD
 WHERE C.CODOFERTA = I.CODOFERTA
   AND I.CODPROD = EMB.CODPROD
   AND I.CODAUXILIAR = EMB.CODAUXILIAR
   AND TRUNC(SYSDATE) BETWEEN C.DTINICIAL AND C.DTFINAL
   AND PROD.SEQPRODUTO = DEPARA.SEQPRODUTO
   AND DEPARA.CODPROD = EMB.CODPROD
   AND DEPARA.ATIVO = 'S'
   AND (DEPARA.CODAUXILIAR = 0 OR DEPARA.CODAUXILIAR = EMB.CODAUXILIAR)
   AND C.PRIORIDADEOFERTA =
       (SELECT MIN(C1.PRIORIDADEOFERTA)
          FROM VW_INT_C5_EMBPROD           EMB1,
               PCOFERTAPROGRAMADAC         C1,
               PCOFERTAPROGRAMADAI         I1,
               PCDEPARAPRODC5              DEPARA1,
               MONITORPDVMIDDLE.TB_PRODUTO PROD1
         WHERE C1.CODOFERTA = I1.CODOFERTA
           AND I1.CODPROD = EMB1.CODPROD
           AND I1.CODAUXILIAR = EMB1.CODAUXILIAR
           AND TRUNC(SYSDATE) BETWEEN TRUNC(C1.DTINICIAL) AND
               TRUNC(C1.DTFINAL)
           AND PROD1.SEQPRODUTO = DEPARA1.SEQPRODUTO
           AND DEPARA1.CODPROD = EMB1.CODPROD
           AND DEPARA1.ATIVO = 'S'
           AND (DEPARA1.CODAUXILIAR = 0 OR
               DEPARA1.CODAUXILIAR = EMB1.CODAUXILIAR))
 UNION ALL
 SELECT DISTINCT PROD.SEQFAMILIA,
                NVL(EMB.QTMAXVENDA, 0) QTMAXVENDA,
                EMB.QTUNIT QTUNIT,
                '2017' ORIGEM,
                2 PRIORIDADE,
                1 SEQLIMITEVENDA
  FROM VW_INT_C5_EMBPROD           EMB,
       PCDEPARAPRODC5              DEPARA,
       MONITORPDVMIDDLE.TB_PRODUTO PROD
 WHERE PROD.SEQPRODUTO = DEPARA.SEQPRODUTO
   AND DEPARA.CODPROD = EMB.CODPROD
   AND DEPARA.ATIVO = 'S'
   AND (DEPARA.CODAUXILIAR = 0 OR DEPARA.CODAUXILIAR = EMB.CODAUXILIAR)
