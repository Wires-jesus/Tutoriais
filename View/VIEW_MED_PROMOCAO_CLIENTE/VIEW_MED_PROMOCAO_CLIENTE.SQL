CREATE OR REPLACE VIEW VIEW_MED_PROMOCAO_CLIENTE AS
SELECT VIEW_MED_PROMOCAO.CODPROMOCAOMED,
       PCCLIENT.CODCLI,
       PCCLIENT.ESTENT,
       regexp_replace(PCCLIENT.CGCENT, '[^0-9]', '') CGCENT,
       NVL((SELECT MIN(COMBO.QTMAXCOMBOMED) AS QTMAXCOMBOMED
              FROM (SELECT PCCLIENT.CODCLI,
                           PCPROMOCAOGRUPOCOMERCMED.CODPROMOCAOMED,
                           NVL(PCPROMOCAOGRUPOCOMERCMED.QTMAXCOMBOMED, 0) AS QTMAXCOMBOMED
                      FROM PCCLIENT,
                           PCPROMOCAOGRUPOCOMERCMED
                     WHERE PCCLIENT.CODGRUPOCOMERCIALMED = PCPROMOCAOGRUPOCOMERCMED.CODGRUPOCOMERCIALMED                   
                     UNION ALL                   
                    SELECT PCCLIENT.CODCLI,
                           PCPROMOCAOCLASSEVENDAMED.CODPROMOCAOMED,
                           NVL(PCPROMOCAOCLASSEVENDAMED.QTMAXCOMBOMED, 0) AS QTMAXCOMBOMED
                      FROM PCCLIENT,
                           PCPROMOCAOCLASSEVENDAMED
                     WHERE PCCLIENT.CLASSEVENDA = PCPROMOCAOCLASSEVENDAMED.CLASSEVENDA                   
                     UNION ALL                   
                    SELECT PCCLIENT.CODCLI,
                           PCPROMOCAOPRACAMED.CODPROMOCAOMED,
                           NVL(PCPROMOCAOPRACAMED.QTMAXCOMBOMED, 0) AS QTMAXCOMBOMED
                      FROM PCCLIENT,
                           PCPROMOCAOPRACAMED
                     WHERE PCCLIENT.CODPRACA = PCPROMOCAOPRACAMED.CODPRACA                   
                     UNION ALL                   
                    SELECT PCCLIENT.CODCLI,
                           PCPROMOCAORAMOATIVMED.CODPROMOCAOMED,
                           NVL(PCPROMOCAORAMOATIVMED.QTMAXCOMBOMED, 0) AS QTMAXCOMBOMED
                      FROM PCCLIENT,
                           PCPROMOCAORAMOATIVMED
                     WHERE PCCLIENT.CODATV1 = PCPROMOCAORAMOATIVMED.CODATIV                   
                     UNION ALL                   
                    SELECT PCCLIENT.CODCLI,
                           PCPROMOCAOREDEMED.CODPROMOCAOMED,
                           NVL(PCPROMOCAOREDEMED.QTMAXCOMBOMED, 0) AS QTMAXCOMBOMED
                      FROM PCCLIENT,
                           PCPROMOCAOREDEMED
                     WHERE PCCLIENT.CODREDE = PCPROMOCAOREDEMED.CODREDE) COMBO
             WHERE COMBO.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
               AND COMBO.CODCLI = PCCLIENT.CODCLI),
           0) AS QTMAXCOMBOMED
  FROM PCCLIENT,
       VIEW_MED_PROMOCAO
 WHERE (PCCLIENT.DTEXCLUSAO IS NULL)
   AND ((NOT EXISTS (SELECT 'S'
                       FROM PCPROMOCAOGRUPOCOMERCMED
                      WHERE PCPROMOCAOGRUPOCOMERCMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) OR
        ((EXISTS (SELECT 'S'
                    FROM PCPROMOCAOGRUPOCOMERCMED
                   WHERE PCPROMOCAOGRUPOCOMERCMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) AND
         (EXISTS (SELECT 'S'
                    FROM PCPROMOCAOGRUPOCOMERCMED
                   WHERE PCPROMOCAOGRUPOCOMERCMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                     AND PCPROMOCAOGRUPOCOMERCMED.CODGRUPOCOMERCIALMED = PCCLIENT.CODGRUPOCOMERCIALMED))))
   AND ((NOT EXISTS (SELECT 'S'
                       FROM PCPROMOCAOCLASSEVENDAMED
                      WHERE PCPROMOCAOCLASSEVENDAMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) OR
        ((EXISTS (SELECT 'S'
                    FROM PCPROMOCAOCLASSEVENDAMED
                   WHERE PCPROMOCAOCLASSEVENDAMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) AND
         (EXISTS (SELECT 'S'
                    FROM PCPROMOCAOCLASSEVENDAMED
                   WHERE PCPROMOCAOCLASSEVENDAMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                     AND PCPROMOCAOCLASSEVENDAMED.CLASSEVENDA = PCCLIENT.CLASSEVENDA))))
   AND ((NOT EXISTS (SELECT 'S'
                      FROM PCPROMOCAOPRACAMED
                     WHERE PCPROMOCAOPRACAMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) OR
        ((EXISTS (SELECT 'S'
                    FROM PCPROMOCAOPRACAMED
                   WHERE PCPROMOCAOPRACAMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) AND
         (EXISTS (SELECT 'S'
                    FROM PCPROMOCAOPRACAMED
                   WHERE PCPROMOCAOPRACAMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                     AND PCPROMOCAOPRACAMED.CODPRACA = PCCLIENT.CODPRACA))))
   AND ((NOT EXISTS (SELECT 'S'
                       FROM PCPROMOCAORAMOATIVMED
                      WHERE PCPROMOCAORAMOATIVMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) OR
        ((EXISTS (SELECT 'S'
                    FROM PCPROMOCAORAMOATIVMED
                   WHERE PCPROMOCAORAMOATIVMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) AND
         (EXISTS (SELECT 'S'
                    FROM PCPROMOCAORAMOATIVMED
                   WHERE PCPROMOCAORAMOATIVMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                     AND PCPROMOCAORAMOATIVMED.CODATIV = PCCLIENT.CODATV1))))
   AND ((NOT EXISTS (SELECT 'S'
                       FROM PCPROMOCAOREDEMED
                      WHERE PCPROMOCAOREDEMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                      UNION
                     SELECT 'S'
                       FROM PCPROMOCAOCLIMED PCLI
                      WHERE PCLI.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) OR
        ((EXISTS (SELECT 'S'
                    FROM PCPROMOCAOREDEMED
                   WHERE PCPROMOCAOREDEMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                   UNION
                  SELECT 'S'
                    FROM PCPROMOCAOCLIMED PCLI
                   WHERE PCLI.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) AND
         (EXISTS (SELECT 'S'
                    FROM PCPROMOCAOREDEMED
                   WHERE PCPROMOCAOREDEMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                     AND PCPROMOCAOREDEMED.CODREDE = PCCLIENT.CODREDE
                   UNION
                  SELECT 'S'
                    FROM PCPROMOCAOCLIMED PCLI
                   WHERE PCLI.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                     AND PCLI.CODCLI = PCCLIENT.CODCLI))))