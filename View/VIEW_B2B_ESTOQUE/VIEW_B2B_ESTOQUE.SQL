CREATE OR REPLACE VIEW VIEW_B2B_ESTOQUE(SKUID, STOCKTYPE, STOCKTYPESOURCENAME, QUANTITY, FILAID) 
AS
SELECT DECODE(NVL(OBSERVACAO, ''), '', CODAUXILIAR, OBSERVACAO) SKUID,
       DECODE(NVL(OBSERVACAO, ''), '',
              CASE WHEN ESTOQUE <= 0 THEN
                 0
              ELSE
                 1
              END, 0) STOCKTYPE,
       'controlUnitQuantity' STOCKTYPESOURCENAME,
       DECODE(NVL(OBSERVACAO,''), '', ESTOQUE_ECOMMERCE,0) QUANTITY, 
       FILAID
FROM (SELECT 
	CASE 
	  WHEN (FNC_B2B_CONVERSAO(P.CODPROD, F.CODFILIAL, C.TIPOCONVERSAO, C.FATORCONVERSAO) > 1 AND C.TIPOCONVERSAO = 'AL') THEN  
	    TO_CHAR(C.CODAUXILIAR) || 'C' || FNC_B2B_CONVERSAO(P.CODPROD, F.CODFILIAL, C.TIPOCONVERSAO, C.FATORCONVERSAO) 
	  WHEN (FNC_B2B_CONVERSAO(P.CODPROD, F.CODFILIAL, C.TIPOCONVERSAO, C.FATORCONVERSAO) > 1 AND C.TIPOCONVERSAO <> 'AL')  THEN  
	    TO_CHAR(P.CODAUXILIAR) || 'C' || FNC_B2B_CONVERSAO(P.CODPROD, F.CODFILIAL, C.TIPOCONVERSAO, C.FATORCONVERSAO)
	  WHEN (C.TIPOCONVERSAO = 'AL')  THEN  
	    TO_CHAR(C.CODAUXILIAR)
          WHEN (C.TIPOCONVERSAO <> 'AL')  THEN  
	    TO_CHAR(P.CODAUXILIAR)                           
        END AS CODAUXILIAR,  			 
			 
        PKG_ESTOQUE.ESTOQUE_DISPONIVEL(P.CODPROD, F.CODFILIAL, '') - (E.QTGIRODIA * NVL(B.NUMDIASESTOQUE,0)) ESTOQUE,
        TRUNC((PKG_ESTOQUE.ESTOQUE_DISPONIVEL(P.CODPROD, F.CODFILIAL, '') * P.QTUNIT) / FNC_B2B_CONVERSAO(P.CODPROD, F.CODFILIAL, C.TIPOCONVERSAO, C.FATORCONVERSAO) * (NVL(B.PRCESTOQUE,0) / 100)) ESTOQUE_ECOMMERCE,
        C.TIPOINTEGRACAO,
        F.OBSERVACAO,
        F.ROWID FILAID
      FROM PCEST E,
        PCPRODUT P,
        PCECOMMERCEB2BFILA F,
        PCECOMMERCEB2BPRODUTO C,
        PCECOMMERCEB2B B
      WHERE E.CODPROD = P.CODPROD
        AND C.CODPROD = P.CODPROD
        AND C.CODFILIAL = F.CODFILIAL
        AND P.rowid = F.ID
        AND E.CODFILIAL = F.CODFILIAL
        AND B.CODFILIAL = F.CODFILIAL
        AND F.TIPOINTEGRACAO = B.TIPOINTEGRACAO
        AND F.TABELA = 'ESTOQUE'
        AND ROWNUM < 151 -- Deverá buscar apenas os 150 primeiros registros em cada consulta
      ORDER BY F.DTINCLUSAO) TAB
