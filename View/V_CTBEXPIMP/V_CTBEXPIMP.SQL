CREATE OR REPLACE VIEW V_CTBEXPIMP AS
SELECT DTMOVIMENTO,
       VALORLANCTO,
       HISTORICO_COMP,
       DOCUMENTO,
       CODCONTACRED,
       CODCONTADEB,
       RECNUM,
       RECNUMPRINC
FROM   (SELECT TO_DATE('01-JAN-1980') DTMOVIMENTO,
               0 VALORLANCTO,
               NULL HISTORICO_COMP,
               0 DOCUMENTO,
               0 CODCONTACRED,
               0 CODCONTADEB,
               NULL RECNUM,
               NULL RECNUMPRINC
        FROM PCFILIAL WHERE ROWNUM = 1)
