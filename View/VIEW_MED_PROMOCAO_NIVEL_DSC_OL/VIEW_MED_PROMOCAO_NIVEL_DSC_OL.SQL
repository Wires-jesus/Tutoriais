CREATE OR REPLACE VIEW VIEW_MED_PROMOCAO_NIVEL_DSC_OL AS
SELECT PCDESCONTO.CODPROMOCAOMED,
       VIEW_MED_PROMOCAO_NIVEL.CODPROD,
       NVL(PCDESCONTO.INICIOINTERVALOPROMOCAOMED, 0) AS INICIOINTERVALOPROMOCAOMED,
       NVL(PCDESCONTO.FIMINTERVALOPROMOCAOMED, 0) AS FIMINTERVALOPROMOCAOMED,
       VIEW_MED_PROMOCAO_NIVEL.NIVEL
  FROM PCDESCONTO,
       VIEW_MED_PROMOCAO,
       VIEW_MED_PROMOCAO_NIVEL
 WHERE (PCDESCONTO.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)
   AND (PCDESCONTO.CODPROMOCAOMED = VIEW_MED_PROMOCAO_NIVEL.CODPROMOCAOMED)
   AND (PCDESCONTO.CODDESCONTO = VIEW_MED_PROMOCAO_NIVEL.CODDESCONTO)
   AND (VIEW_MED_PROMOCAO_NIVEL.NIVEL = (SELECT MIN(MED_PROMOCAO_DESC_NIVEL.NIVEL)
                                           FROM VIEW_MED_PROMOCAO_NIVEL MED_PROMOCAO_DESC_NIVEL
                                          WHERE MED_PROMOCAO_DESC_NIVEL.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                                            AND MED_PROMOCAO_DESC_NIVEL.CODPROD = VIEW_MED_PROMOCAO_NIVEL.CODPROD))