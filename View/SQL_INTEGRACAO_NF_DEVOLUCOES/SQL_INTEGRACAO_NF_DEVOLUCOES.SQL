--NF DEVOLUÇÕES
CREATE OR REPLACE VIEW SQL_INTEGRACAO_NF_DEVOLUCOES AS
SELECT PCPRODUT.CODPROD
      ,PCPRODUT.DESCRICAO
      ,PCPRODUT.CODFAB
      ,PCPRODUT.QTUNIT
      ,PCPRODUT.QTUNITCX
      ,PCPRODUT.CODAUXILIAR
      ,PCPRODUT.CODAUXILIAR2
      ,PCPRODUT.CODFORNEC
      ,NVL(PCPRODUT.PESOBRUTO,0) AS PESOBRUTO
      ,NVL(PCPRODUT.PESOLIQ,0) AS PESOLIQ
      ,NVL(PCPRODUT.VOLUME,0) AS VOLUME
      ,PCMOV.NUMLOTE
      ,PCMOV.CODAUXILIAR AS CODAUXILIARMOV
      ,NVL(PCMOV.QT,0) AS QT
      ,NVL(PCMOV.QTCONT,0) AS QTCONT
      ,NVL(PCMOV.PUNIT,0) AS PUNIT
      ,NVL(PCMOV.PUNITCONT,0) AS PUNITCONT
      ,NVL(PCMOV.PTABELA,0) AS PTABELA
      ,NVL(PCMOV.PBONIFIC,0) AS PBONIFIC
      ,PCMOV.STATUS
      ,PCMOV.CODOPER
      ,(CASE WHEN NVL(PCMOV.QT,0) = 0 THEN NVL(PCMOV.QTCONT,0) ELSE NVL(PCMOV.QT,0) END) AS QTDEVOLUCAO
      ,PCNFENT.NUMTRANSENT
      ,PCNFENT.NUMNOTA
      ,PCNFENT.CODFILIAL
      ,PCNFENT.CODFORNEC AS CODCLI
      ,PCNFENT.CODUSURDEVOL AS CODUSUR
      ,NVL(PCESTCOM.NUMTRANSVENDA,0) AS NUMTRANSVENDA
      ,PCNFENT.DTENT
      ,NVL(PCNFCAN.DATACANC, PCMOV.DTCANCEL) AS DTCANCEL
      ,PCNFENT.TIPODESCARGA
      ,PCNFSAID.CONDVENDA
      ,(DECODE(PCESTCOM.VLDEVOLUCAO, 0, 0, DECODE(NVL(PCMOV.QT,0),0,PCMOV.QTCONT,PCMOV.QT)) * NVL(DECODE(PCNFSAID.CONDVENDA, 7, PCMOV.PUNITCONT, PCMOV.PUNIT), 0)) AS VLDEVOLUCAO
      ,PCNFENT.CODFISCAL
      ,PCNFENT.OBS
      ,PCNFSAID.DTSAIDA
  FROM PCNFENT
      ,PCMOV
      ,PCPRODUT
      ,PCESTCOM
      ,PCNFCAN
      ,PCNFSAID
 WHERE PCNFENT.NUMTRANSENT  = PCMOV.NUMTRANSENT
   AND PCNFENT.CODCONT IN(SELECT PCCONSUM.CODCONTCLI FROM PCCONSUM UNION SELECT PCCONSUM.CODCONTDEVCLI FROM PCCONSUM)
   AND PCPRODUT.CODPROD     = PCMOV.CODPROD
   AND PCESTCOM.NUMTRANSENT = PCNFENT.NUMTRANSENT
   AND PCESTCOM.NUMTRANSVENDA = PCNFSAID.NUMTRANSVENDA(+)
   AND PCNFENT.NUMTRANSENT  = PCNFCAN.NUMTRANSVENDA(+)
   AND PCNFENT.CODFILIAL    = PCNFCAN.CODFILIAL(+)
   AND (PCPRODUT.TIPOMERC NOT IN('CB') OR PCPRODUT.TIPOMERC IS NULL)
   AND (PCMOV.QT > 0 OR PCMOV.QTCONT > 0)
   AND PCNFENT.TIPODESCARGA IN('6','7')
   AND PCMOV.CODOPER = 'ED'
   AND (PCNFENT.DTENT <> TRUNC(NVL(PCNFCAN.DATACANC,PCMOV.DTCANCEL)) OR NVL(PCNFENT.OBS,'XX') <> 'NF CANCELADA')
