CREATE OR REPLACE VIEW VW_INT_C5_PARCELDEPTO AS
(
SELECT
     D.CODPARCEL SEQPARCELA,
     'PARCELA CLASSIFICACAO MERCADOLOGICA' DESCRICAO,
     D.CODFILIAL NROEMPRESA,
     'C' TIPO,
     TRUNC(SYSDATE) DTAHORINICIAL,
     TRUNC(SYSDATE) + 2000 DTAHORFINAL,
     DF.CODFINALIZADORA NROFORMAPAGTO,

     (SELECT R.NRODIVISAO
      FROM   PCDEPARAREGIAOC5 R
      WHERE  R.NUMREGIAO = ferramentas.f_buscarparametro_num('NUMREGIAOPADRAOVAREJO',
                                                             D.CODFILIAL,
                                                             '1')
     )NRODIVISAO,

     ferramentas.f_buscarparametro_num('NUMREGIAOPADRAOVAREJO', D.CODFILIAL, '1') NUMREGIAO,
     PROD.CODPROD SEQFAMILIA,
     D.CODDEPTO,
     D.CODSEC,
     D.CODCATEGORIA,
     D.CODSUBCATEGORIA,
     NVL(F.QTMAXPARCELAS, D.QTMAXPARCELA) NROMAXIMOPARCELA,
     (CASE
        WHEN D.STATUS = 'A' THEN
             'S'
        ELSE 'N'
     END) ATIVO

  FROM PCPARCELDEPTO D,
       PCFINALIZADORA F,
       MONITORPDVMIDDLE.TB_FAMILIA P,
       PCPRODUT PROD,
       PCPARCELDEPTOFINALIZADORA DF,
       VW_INT_C5_OBTER_FILIAIS_C5 C5
  WHERE DF.CODPARCEL = D.CODPARCEL
  AND D.CODFILIAL = C5.CODFILIAL
  AND P.SEQFAMILIA = PROD.CODEPTO
  AND ((D.CODDEPTO IS NOT NULL AND D.CODSEC IS NULL AND D.CODDEPTO = PROD.CODEPTO) OR
       (D.CODSEC IS NOT NULL AND D.CODCATEGORIA IS NULL AND D.CODSEC = PROD.CODSEC) OR
       (D.CODCATEGORIA IS NOT NULL AND D.CODSUBCATEGORIA IS NULL AND D.CODCATEGORIA = PROD.CODCATEGORIA) OR
       (D.CODSUBCATEGORIA IS NOT NULL AND D.CODSUBCATEGORIA = PROD.CODSUBCATEGORIA) OR
       (D.CODPROD IS NOT NULL AND D.CODPROD = PROD.CODPROD)
      )   
  AND DF.CODFINALIZADORA = F.CODFINALIZADORA
)

