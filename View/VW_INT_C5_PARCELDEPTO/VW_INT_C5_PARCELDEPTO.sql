CREATE OR REPLACE VIEW VW_INT_C5_PARCELDEPTO AS
(
SELECT  
  PARCELDEPTO.*,
  C.SEQCATEGORIAC5 SECATEGORIA
FROM
   (SELECT 
     D.CODPARCEL SEQPARCELA,
     'PARCELA CLASSIFICACAO MERCADOLOGICA' DESCRICAO, 
     D.CODFILIAL NROEMPRESA,
     'C' TIPO,
     TRUNC(SYSDATE) DTAHORINICIAL,
     TO_DATE('31/DEC/2099') DTAHORFINAL, 
     DF.CODFINALIZADORA NROFORMAPAGTO, 
     
     (SELECT R.NRODIVISAO
      FROM   PCDEPARAREGIAOC5 R
      WHERE  R.NUMREGIAO = ferramentas.f_buscarparametro_num('NUMREGIAOPADRAOVAREJO', 
                                                             D.CODFILIAL, 
                                                             '1') 
     )NRODIVISAO,
     
     (CASE
            WHEN D.CODSUBCATEGORIA IS NOT NULL THEN
                 D.CODSEC||D.CODCATEGORIA||D.CODSUBCATEGORIA||4
            WHEN CODCATEGORIA IS NOT NULL THEN
                 D.CODCATEGORIA||D.CODCATEGORIA||3
            WHEN CODSEC IS NOT NULL THEN
                 D.CODSEC||2
            ELSE D.CODDEPTO||1
     END) SEQCATEGORIAWINTHOR,
     
     NVL(F.QTMAXPARCELAS, D.QTMAXPARCELA) NROMAXIMOPARCELA,
     
     (CASE
        WHEN STATUS = 'A' THEN
             'S'
        ELSE 'N'
     END) ATIVO 
     
  FROM PCPARCELDEPTO D,
       PCFINALIZADORA F,
       PCPARCELDEPTOFINALIZADORA DF
  WHERE DF.CODPARCEL = D.CODPARCEL
  AND   DF.CODFINALIZADORA = F.CODFINALIZADORA) PARCELDEPTO,

  PCDEPARACATEGORIAC5 C
WHERE PARCELDEPTO.SEQCATEGORIAWINTHOR = C.SEQCATEGORIAWINTHOR(+)      
)
