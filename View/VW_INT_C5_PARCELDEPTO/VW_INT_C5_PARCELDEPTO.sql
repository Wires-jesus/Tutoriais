CREATE OR REPLACE VIEW VW_INT_C5_PARCELDEPTO AS
(
SELECT
  PARCELDEPTO."SEQPARCELA",PARCELDEPTO."DESCRICAO",PARCELDEPTO."NROEMPRESA",PARCELDEPTO."TIPO",PARCELDEPTO."DTAHORINICIAL",PARCELDEPTO."DTAHORFINAL",PARCELDEPTO."NROFORMAPAGTO",PARCELDEPTO."NRODIVISAO",PARCELDEPTO."SEQCATEGORIAWINTHOR",PARCELDEPTO."NROMAXIMOPARCELA",PARCELDEPTO."ATIVO",
  C.SEQCATEGORIAC5 SEQCATEGORIA
FROM
   (SELECT
     D.CODPARCEL SEQPARCELA,
     'PARCELA CLASSIFICACAO MERCADOLOGICA' DESCRICAO,
     D.CODFILIAL NROEMPRESA,
     'C' TIPO,
     TRUNC(SYSDATE) DTAHORINICIAL,
     TRUNC(SYSDATE) + 2000 DTAHORFINAL,
     DF.CODFINALIZADORA NROFORMAPAGTO,

     (SELECT R.NRODIVISAO
      FROM   PCDEPARAREGIAOC5 R
      WHERE  R.NUMREGIAO = ferramentas.f_buscarparametro_num('NUMREGIAOPADRAOVAREJO',
                                                             D.CODFILIAL,
                                                             '1')
     )NRODIVISAO,

     ferramentas.f_buscarparametro_num('NUMREGIAOPADRAOVAREJO', D.CODFILIAL, '1') NUMREGIAO,

     (CASE
            WHEN D.CODSUBCATEGORIA IS NOT NULL THEN
                 D.CODSEC||'0'||D.CODCATEGORIA||'0'||D.CODSUBCATEGORIA||'0'||4
            WHEN CODCATEGORIA IS NOT NULL THEN
                 D.CODCATEGORIA||'0'||D.CODCATEGORIA||'0'||3
            WHEN CODSEC IS NOT NULL THEN
                 D.CODSEC||'0'||2
            ELSE D.CODDEPTO||1
     END) SEQCATEGORIAWINTHOR,

     NVL(F.QTMAXPARCELAS, D.QTMAXPARCELA) NROMAXIMOPARCELA,

     (CASE
        WHEN STATUS = 'A' THEN
             'S'
        ELSE 'N'
     END) ATIVO

  FROM PCPARCELDEPTO D,
       PCFINALIZADORA F,
       PCPARCELDEPTOFINALIZADORA DF,
       VW_INT_C5_OBTER_FILIAIS_C5 C5
  WHERE DF.CODPARCEL = D.CODPARCEL
    AND D.CODFILIAL = C5.CODFILIAL
  AND   DF.CODFINALIZADORA = F.CODFINALIZADORA) PARCELDEPTO,

  PCDEPARACATEGORIAC5 C
WHERE TO_NUMBER(PARCELDEPTO.NUMREGIAO)||TO_NUMBER(PARCELDEPTO.SEQCATEGORIAWINTHOR) = C.SEQCATEGORIAWINTHOR(+)
)

