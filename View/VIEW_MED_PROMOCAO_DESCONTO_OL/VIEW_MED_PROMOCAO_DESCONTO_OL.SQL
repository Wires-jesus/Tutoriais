CREATE OR REPLACE VIEW VIEW_MED_PROMOCAO_DESCONTO_OL AS
SELECT DISTINCT PCDESCONTO.CODPROMOCAOMED,
                PCPRODUT.CODPROD,
                PCPRODUT.CODAUXILIAR,
                PCPRODUT.TIPOMERC,
                PCPRODUT.UTILIZAPRECOFABRICA,
                PCPRODUT.CODMARCA,
                VIEW_MED_PROMOCAO.TIPOPROMOCAO,
                VIEW_MED_PROMOCAO.TIPOPOLITICA,
                PCDESCONTO.PERCDESC,
                PCDESCONTO.PERCDESCFIN,
                PCDESCONTO.PRECOFIXOPROMOCAOMED,
                PCDESCONTO.PERCMARKUPMED,
                PCDESCONTO.INICIOINTERVALOPROMOCAOMED,
                PCDESCONTO.FIMINTERVALOPROMOCAOMED,
                PCDESCONTO.PERMITIRAUMENTARQTDEKITMED,
                PCDESCONTO.PROMOCAOMEDOBRIGATORIO,
                CASE
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 1 THEN
                    MIN(PCDESCONTO.INICIOINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODPROD)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 2 THEN
                    MIN(PCDESCONTO.INICIOINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODGRUPOREST)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 3 THEN
                    MIN(PCDESCONTO.INICIOINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODFORNEC)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 4 THEN
                    MIN(PCDESCONTO.INICIOINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODMARCA)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 5 THEN
                    MIN(PCDESCONTO.INICIOINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODCATEGORIA)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 6 THEN
                    MIN(PCDESCONTO.INICIOINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODSEC)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 7 THEN
                    MIN(PCDESCONTO.INICIOINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODLINHAPROD)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 8 THEN
                    MIN(PCDESCONTO.INICIOINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODEPTO)
                  ELSE
                    0.00
                END MENORINTERVALODEFAIXA,
                CASE
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 1 THEN
                    MAX(PCDESCONTO.FIMINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODPROD)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 2 THEN
                    MAX(PCDESCONTO.FIMINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODGRUPOREST)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 3 THEN
                    MAX(PCDESCONTO.FIMINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODFORNEC)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 4 THEN
                    MAX(PCDESCONTO.FIMINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODMARCA)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 5 THEN
                    MAX(PCDESCONTO.FIMINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODCATEGORIA)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 6 THEN
                    MAX(PCDESCONTO.FIMINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODSEC)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 7 THEN
                    MAX(PCDESCONTO.FIMINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODLINHAPROD)
                  WHEN VIEW_MED_PROMOCAO_NIVEL.NIVEL = 8 THEN
                    MAX(PCDESCONTO.FIMINTERVALOPROMOCAOMED) OVER(PARTITION BY PCDESCONTO.CODPROMOCAOMED, PCDESCONTO.CODEPTO)
                  ELSE
                    9999.99
                END MAIORINTERVALODEFAIXA,
                NVL(PCDESCONTO.QTCOMBOMED, 0) QTCOMBOMED,
                NVL(PCDESCONTO.QTMINIMAMED, 0) QTMINIMAMED
  FROM PCPRODUT,
       PCDESCONTO,
       VIEW_MED_PROMOCAO,
       VIEW_MED_PROMOCAO_NIVEL
 WHERE (PCDESCONTO.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)
   AND (PCDESCONTO.CODDESCONTO = VIEW_MED_PROMOCAO_NIVEL.CODDESCONTO)
   AND (PCDESCONTO.CODPROMOCAOMED = VIEW_MED_PROMOCAO_NIVEL.CODPROMOCAOMED)
   AND (PCPRODUT.CODPROD = VIEW_MED_PROMOCAO_NIVEL.CODPROD)
   AND (EXISTS(SELECT 'S'
                 FROM VIEW_MED_PROMOCAO_NIVEL_DSC_OL
                WHERE VIEW_MED_PROMOCAO_NIVEL_DSC_OL.CODPROMOCAOMED = PCDESCONTO.CODPROMOCAOMED
                  AND VIEW_MED_PROMOCAO_NIVEL_DSC_OL.CODPROD = PCPRODUT.CODPROD
                  AND VIEW_MED_PROMOCAO_NIVEL_DSC_OL.INICIOINTERVALOPROMOCAOMED = NVL(PCDESCONTO.INICIOINTERVALOPROMOCAOMED, 0)
                  AND VIEW_MED_PROMOCAO_NIVEL_DSC_OL.FIMINTERVALOPROMOCAOMED = NVL(PCDESCONTO.FIMINTERVALOPROMOCAOMED, 0)
                  AND VIEW_MED_PROMOCAO_NIVEL_DSC_OL.NIVEL = VIEW_MED_PROMOCAO_NIVEL.NIVEL))