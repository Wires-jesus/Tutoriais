CREATE OR REPLACE VIEW VW_EVENTOS_PEDIDOS AS

SELECT 'Iniciou Digitação pedido de venda' AS ID,
       1 AS CONTADOR,
       NVL(DTINICIODIGITACAOPEDIDO,DTABERTURAPEDPALM) AS DTINICIO,
       CODEMITENTE,
       DATA,
       CODCLI,
       POSICAO,
       NUMPED,
       NUMCAR,
       NUMNOTA,
       NULL CODPROD,
       NULL QTCORTADA,
       NULL MOTIVO,
       NULL NUMCARANTERIOR,
       NULL NUMCARATUAL
  FROM PCPEDC
 WHERE 1=1
 AND (DTINICIODIGITACAOPEDIDO IS NOT NULL) OR (DTABERTURAPEDPALM IS NOT NULL)
 AND PCPEDC.NUMCAR <> 0

