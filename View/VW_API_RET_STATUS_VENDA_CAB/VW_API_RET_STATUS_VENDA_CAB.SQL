CREATE OR REPLACE VIEW VW_API_RET_STATUS_VENDA_CAB AS
SELECT DATA_PEDIDO,
       ID_MOVIMENTACAO,
       CODIGO_CLIENTE,
       CODIGO_FILIAL,
       CODIGO_FILIAL_NF,
       IMPORTADO,
       OBSERVACAO_PC,
       NUMERO_PEDIDO_RCA,
       ATENDIDO,
       CODIGO_RCA,
       DATA_ABERTURA_PEDIDO_RCA,
       DATA_FECHAMENTO_PEDIDO_RCA,
       NUMERO_PEDIDO,
       CODIGO_COBRANCA,
       CODIGO_PLANO_PAGAMENTO,
       VALOR_ATENDIDO,
       DATA_LIBERACAO_PEDIDO,
       POSICAO_PEDIDO,
       NUMERO_CARREGAMENTO,
       DATA_CANCELAMENTO,
       DATA_FATURAMENTO,
       DATA_ENTREGA,
       NUMERO_NOTA,
       NUMERO_TRANSACAO,
       CHAVE_NFE,
       SERIE_NFE,
       DATA_SAIDA_NFE,
	   IDFV FROM(
/* **** COM EVENTOS DE PEDIDOS **** */
-- Pedidos Não-Processados
SELECT TRUNC(CF.DTINCLUSAO) AS DATA_PEDIDO,
       NULL AS ID_MOVIMENTACAO,
       CF.CODCLI AS CODIGO_CLIENTE,
       CF.CODFILIAL AS CODIGO_FILIAL,
       CF.CODFILIALNF AS CODIGO_FILIAL_NF,
       CF.IMPORTADO,
       CF.OBSERVACAO_PC,
       CF.NUMPEDRCA AS NUMERO_PEDIDO_RCA,
       CASE
         WHEN (SELECT COUNT(*)
                 FROM PCPEDIFV V
                WHERE V.NUMPEDRCA = CF.NUMPEDRCA
                  AND V.DTABERTURAPEDPALM = CF.DTABERTURAPEDPALM
                  AND V.QT <> V.QT_FATURADA) > 0 THEN
          'PARCIAL'
         ELSE
          'TOTAL'
       END AS ATENDIDO,
       CF.CODUSUR AS CODIGO_RCA,
       CF.DTABERTURAPEDPALM AS DATA_ABERTURA_PEDIDO_RCA,
       CF.DTFECHAMENTOPEDPALM AS DATA_FECHAMENTO_PEDIDO_RCA,
       CF.NUMPED AS NUMERO_PEDIDO,
       CF.CODCOB AS CODIGO_COBRANCA,
       CF.CODPLPAG AS CODIGO_PLANO_PAGAMENTO,
       NULL AS VALOR_ATENDIDO,
       NULL DATA_LIBERACAO_PEDIDO,
       NULL AS POSICAO_PEDIDO,
       NULL AS NUMERO_CARREGAMENTO,
       NULL AS DATA_CANCELAMENTO,
       NULL AS DATA_FATURAMENTO,
       NULL AS DATA_ENTREGA,
       NULL AS NUMERO_NOTA,
       NULL AS NUMERO_TRANSACAO,
       NULL AS CHAVE_NFE,
       NULL AS SERIE_NFE,
       NULL AS DATA_SAIDA_NFE,
	   CF.IDFV
  FROM PCPEDCFV CF
  WHERE IMPORTADO IN(1,3,4)
UNION
SELECT C.DATA AS DATA_PEDIDO,
       (SELECT CONCAT_COLUNA(VW.ID || ' | Data: ' || VW.DTINICIO || ' ' || CHR(13) )
       FROM VW_EVENTOS_PEDIDOS VW
       WHERE VW.NUMPED = C.NUMPED) AS ID_MOVIMENTACAO,
       C.CODCLI AS CODIGO_CLIENTE,
       C.CODFILIAL AS CODIGO_FILIAL,
       C.CODFILIALNF AS CODIGO_FILIAL_NF,
       CF.IMPORTADO,
       CF.OBSERVACAO_PC,
       CF.NUMPEDRCA AS NUMERO_PEDIDO_RCA,
       CASE
         WHEN (SELECT COUNT(*)
                 FROM PCPEDIFV V
                WHERE V.NUMPEDRCA = CF.NUMPEDRCA
                  AND V.DTABERTURAPEDPALM = CF.DTABERTURAPEDPALM
                  AND V.QT <> V.QT_FATURADA) > 0 THEN
          'PARCIAL'
         ELSE
          'TOTAL'
       END AS ATENDIDO,
       CF.CODUSUR AS CODIGO_RCA,
       CF.DTABERTURAPEDPALM AS DATA_ABERTURA_PEDIDO_RCA,
       CF.DTFECHAMENTOPEDPALM AS DATA_FECHAMENTO_PEDIDO_RCA,
       C.NUMPED AS NUMERO_PEDIDO,
       C.CODCOB AS CODIGO_COBRANCA,
       C.CODPLPAG AS CODIGO_PLANO_PAGAMENTO,
       C.VLATEND AS VALOR_ATENDIDO,
       C.DTLIBERA AS DATA_LIBERACAO_PEDIDO,
       C.POSICAO AS POSICAO_PEDIDO,
       C.NUMCAR AS NUMERO_CARREGAMENTO,
       NULL AS DATA_CANCELAMENTO,
       C.DTFAT AS DATA_FATURAMENTO,
       C.DTENTREGA AS DATA_ENTREGA,
       C.NUMNOTA AS NUMERO_NOTA,
       C.NUMTRANSVENDA AS NUMERO_TRANSACAO,
       (SELECT S.CHAVENFE
          FROM PCNFSAID S
         WHERE S.NUMTRANSVENDA = C.NUMTRANSVENDA) AS CHAVE_NFE,
       (SELECT S.SERIE
          FROM PCNFSAID S
         WHERE S.NUMTRANSVENDA = C.NUMTRANSVENDA) AS SERIE_NFE,
       (SELECT NVL(S.DTSAIDANF, S.DTSAIDA)
          FROM PCNFSAID S
         WHERE S.NUMTRANSVENDA = C.NUMTRANSVENDA) AS DATA_SAIDA_NFE,
		 CF.IDFV
  FROM PCPEDC C, PCPEDCFV CF
 WHERE C.NUMPEDRCA = CF.NUMPEDRCA
   AND C.CODUSUR = CF.CODUSUR
   AND C.DTABERTURAPEDPALM = CF.DTABERTURAPEDPALM
UNION
-- Pedidos Cancelados e não-faturados
SELECT N.DATAEMISSAO AS DATA_PEDIDO,
      (SELECT CONCAT_COLUNA(VW.ID || ' | Data: ' || VW.DTINICIO || ' ' || CHR(13) )
       FROM VW_EVENTOS_PEDIDOS VW
       WHERE VW.NUMPED = N.NUMPED) AS ID_MOVIMENTACAO,
      N.CODCLI AS CODIGO_CLIENTE,
      N.CODFILIAL AS CODIGO_FILIAL,
      CF.CODFILIALNF AS CODIGO_FILIAL_NF,
      CF.IMPORTADO,
      CF.OBSERVACAO_PC,
      CF.NUMPEDRCA AS NUMERO_PEDIDO_RCA,
      CASE
        WHEN (SELECT COUNT(*)
                FROM PCPEDIFV V
               WHERE V.NUMPEDRCA = CF.NUMPEDRCA
                 AND V.DTABERTURAPEDPALM = CF.DTABERTURAPEDPALM
                 AND V.QT <> V.QT_FATURADA) > 0 THEN
         'PARCIAL'
        ELSE
         'TOTAL'
      END AS ATENDIDO,
      CF.CODUSUR AS CODIGO_RCA,
      CF.DTABERTURAPEDPALM AS DATA_ABERTURA_PEDIDO_RCA,
      CF.DTFECHAMENTOPEDPALM AS DATA_FECHAMENTO_PEDIDO_RCA,
      N.NUMPED AS NUMERO_PEDIDO,
      N.CODCOB AS CODGO_COBRANCA,
      N.CODPLPAG AS CODIGO_PLANO_PAGAMENTO,
      N.VLTOTAL AS VALOR_ATENDIDO,
      NULL AS DATA_LIBERACAO_PEDIDO,
      CF.POSICAO_ATUAL AS POSICAO_PEDIDO,
      N.NUMCAR AS NUMERO_CARREGAMENTO,
      N.DATACANC AS DATA_CANCELAMENTO,
      NULL AS DATA_FATURAMENTO,
      NULL AS DATA_ENTREGA,
      NULL AS NUMERO_NOTA,
      N.NUMTRANSVENDA AS NUMERO_TRANSACAO,
      NULL AS CHAVE_NFE,
      NULL AS SERIE_NFE,
      NULL AS DATA_SAIDA_NFE,
	  CF.IDFV
 FROM PCPEDCFV CF, PCNFCAN N
WHERE CF.NUMPEDRCA = N.NUMPEDRCA
  AND CF.CODUSUR = N.CODUSUR
  AND CF.DTABERTURAPEDPALM = N.DTABERTURAPEDPALM
UNION
-- Pedidos Cancelados e faturados
SELECT C.DATA AS DATA_PEDIDO,
      (SELECT CONCAT_COLUNA(VW.ID || ' | Data: ' || VW.DTINICIO || ' ' || CHR(13) )
       FROM VW_EVENTOS_PEDIDOS VW
       WHERE VW.NUMPED = C.NUMPED) AS ID_MOVIMENTACAO,
      C.CODCLI AS CODIGO_CLIENTE,
      C.CODFILIAL AS CODIGO_FILIAL,
      C.CODFILIALNF AS CODIGO_FILIAL_NF,
      CF.IMPORTADO,
      CF.OBSERVACAO_PC,
      CF.NUMPEDRCA AS NUMERO_PEDIDO_RCA,
      CASE
        WHEN (SELECT COUNT(*)
                FROM PCPEDIFV V
               WHERE V.NUMPEDRCA = CF.NUMPEDRCA
                 AND V.DTABERTURAPEDPALM = CF.DTABERTURAPEDPALM
                 AND V.QT <> V.QT_FATURADA) > 0 THEN
         'PARCIAL'
        ELSE
         'TOTAL'
      END AS ATENDIDO,
      CF.CODUSUR AS CODIGO_RCA,
      CF.DTABERTURAPEDPALM AS DATA_ABERTURA_PEDIDO_RCA,
      CF.DTFECHAMENTOPEDPALM AS DATA_FECHAMENTO_PEDIDO_RCA,
      C.NUMPED AS NUMERO_PEDIDO,
      C.CODCOB AS CODIGO_COBRANCA,
      C.CODPLPAG AS CODIGO_PLANO_PAGAMENTO,
      C.VLATEND AS VALOR_ATENDIDO,
      C.DTLIBERA AS DATA_LIBERACAO_PEDIDO,
      C.POSICAO AS POSICAO_PEDIDO,
      C.NUMCAR AS NUMERO_CARREGAMENTO,
      S.DTCANCEL AS DATA_CANCELAMENTO,
      C.DTFAT AS DATA_FATURAMENTO,
      C.DTENTREGA AS DATA_ENTREGA,
      S.NUMNOTA AS NUMERO_NOTA,
      S.NUMTRANSVENDA AS NUMERO_TRANSACAO,
      S.CHAVENFE AS CHAVE_NFE,
      S.SERIE AS SERIE_NFE,
      NVL(S.DTSAIDANF, S.DTSAIDA) AS DATA_SAIDA_NFE,
	  CF.IDFV
 FROM PCPEDC C, PCPEDCFV CF, PCNFSAID S
WHERE C.NUMTRANSVENDA = S.NUMTRANSVENDA
  AND CF.NUMPEDRCA = C.NUMPEDRCA
  AND CF.CODUSUR = C.CODUSUR
  AND CF.DTABERTURAPEDPALM = C.DTABERTURAPEDPALM
  AND S.DTCANCEL IS NOT NULL)