CREATE OR REPLACE VIEW VW_VENDASPEDIDO AS
SELECT PCPEDC.CODFILIAL
      ,PCPEDC.CODUSUR
      ,PCPEDC.DATA
      ,PCSUPERV.CODSUPERVISOR
      ,PCSUPERV.NOME
      ,PCCLIENT.TIPOFJ
      ,PCCLIENT.CODCLI
      ,PCCLIENT.CLIENTE
      ,PCCLIENT.FANTASIA
      ,PCCLIENT.CODATV1
      ,PCATIVI.CODATIV
      ,PCATIVI.RAMO
      ,PCPRODUT.CODPROD
      ,PCPRODUT.DESCRICAO
      ,PCPRODUT.UNIDADE
      ,PCPRODUT.CODFAB
      ,PCPRODUT.CODMARCA
      ,PCPRODUT.EMBALAGEM
       ,ROUND(NVL(PCPEDI.QT,
                 0) * (NVL(PCPEDI.PVENDA, 0) + NVL(PCPEDI.VLOUTRASDESP, 0) + NVL(PCPEDI.VLFRETE, 0)),
             2) AS PVENDA
       ,ROUND(NVL(PCPEDI.QT,
                 0) * NVL(PCPEDI.PTABELA,
                          0),
             2) AS PTABELA
      ,ROUND(NVL(PCPRODUT.PESOBRUTO,
                 0) * NVL(PCPEDI.QT,
                          0),
             2) AS TOTPESOBRUTO
      ,ROUND(NVL(PCPRODUT.PESOLIQ,
                 0) * NVL(PCPEDI.QT,
                          0),
             2) AS TOTPESOLIQUIDO
      ,ROUND(NVL(PCPRODUT.VOLUME,
                 0) * NVL(PCPEDI.QT,
                          0),
             2) LITRAGEM
      ,ROUND(DECODE(NVL(PCPRODUT.QTUNIT,0),
                    0,
                    1,
                    PCPRODUT.QTUNIT) * NVL(PCPEDI.QT,
                             1),
             2) AS TOTQTUNIT
      ,NVL(PCPEDI.QT,
           0) / DECODE(NVL(PCPRODUT.QTUNITCX,0),
                       0,
                       1,
                       PCPRODUT.QTUNITCX) AS TOTQTUNITCX
      ,NVL(PCPEDI.QT,
           0) * NVL(PCPEDI.VLCUSTOFIN,
                    0) AS VLCUSTOFIN
      ,PCPEDI.QT AS QT
      ,PCPRODUT.QTUNIT
      ,PCPRODUT.CODCATEGORIA
      ,PCCLIENT.CODCIDADE
      ,PCCLIENT.CODCLIPRINC
      ,PCDEPTO.CODEPTO
      ,PCFORNEC.CODFORNEC
      ,PCPRACA.CODPRACA
      ,PCPRODUT.CODPRODPRINC
      ,PCREGIAO.NUMREGIAO
      ,PCCLIENT.CODREDE
      ,PCPRODUT.CODSEC
      ,PCPRODUT.CODSUBCATEGORIA
      ,PCPEDC.POSICAO
      ,PCPRODUT.OBS2
      ,PCCLIENT.DTEXCLUSAO
      ,PCPRODUT.PESOBRUTO
      ,PCPEDC.NUMPED
      ,PCPEDC.VLATEND
      ,PCPEDC.ORIGEMPED
      ,PCPEDC.CONDVENDA
      ,PCPRODUT.CODDISTRIB
      ,PCSUPERV.CODGERENTE
      ,PCPLPAG.CODPLPAG
      ,COMPRADOR.MATRICULA MATRICULA_COMPRADOR
      ,EMITENTE.MATRICULA MATRICULA_EMITENTE
      ,(SELECT COUNT(P.CODPROD)
        FROM   PCPRODUT P
              ,PCDEPTO  D
        WHERE  P.CODEPTO = D.CODEPTO
        AND    P.CODFORNEC = PCFORNEC.CODFORNEC
        AND    NVL(P.OBS2,
                   ' ') <> ('FL')) AS QTMIXCAD
      ,DECODE(PCPEDI.CONDVENDA,
              5,
              NVL(PCPEDI.VLBONIFIC,
                  PCPEDI.PTABELA),
              6,
              NVL(PCPEDI.VLBONIFIC,
                  PCPEDI.PTABELA),
              11,
              NVL(PCPEDI.VLBONIFIC,
                  PCPEDI.PTABELA),
              12,
              NVL(PCPEDI.VLBONIFIC,
                  PCPEDI.PTABELA),
              0) AS VLBONIFIC
    , PCCLIENT.CODBAIRROCOB
    , PCCLIENT.CODBAIRROCOM      
    , PCCLIENT.CODBAIRROENT   
    , PCFORNEC.CODCOMPRADOR
    , PCPEDI.VLREPASSE
FROM   PCPEDI
      ,PCPEDC
      ,PCPRODUT
      ,PCFORNEC
      ,PCDEPTO
      ,PCCLIENT
      ,PCUSUARI
      ,PCATIVI
      ,PCPRACA
      ,PCCIDADE
      ,PCSUPERV
      ,PCDISTRIB
      ,PCREGIAO
      ,PCGERENTE
      ,PCEMPR COMPRADOR
      ,PCEMPR EMITENTE
      ,PCPLPAG
WHERE  PCPEDI.NUMPED = PCPEDC.NUMPED
AND    PCUSUARI.CODSUPERVISOR NOT IN ('9999')
AND    PCCLIENT.CODCIDADE = PCCIDADE.CODCIDADE(+)
AND    PCPEDC.CODCLI = PCCLIENT.CODCLI
AND    PCSUPERV.CODSUPERVISOR = PCUSUARI.CODSUPERVISOR
AND    PCCLIENT.CODATV1 = PCATIVI.CODATIV(+)
AND    PCPEDC.DTCANCEL IS NULL
AND    PCPEDI.CODPROD = PCPRODUT.CODPROD
AND    PCPRODUT.CODEPTO = PCDEPTO.CODEPTO
AND    PCPRODUT.CODFORNEC = PCFORNEC.CODFORNEC
AND    PCPEDC.CODUSUR = PCUSUARI.CODUSUR
AND    PCPEDC.CODPRACA = PCPRACA.CODPRACA
AND    PCPEDC.CODPLPAG = PCPLPAG.CODPLPAG(+)
AND    PCPEDC.NUMREGIAO = PCREGIAO.NUMREGIAO(+)
AND    PCPEDC.CODEMITENTE = EMITENTE.MATRICULA(+)
AND    PCFORNEC.CODCOMPRADOR = COMPRADOR.MATRICULA(+)
AND    PCPRODUT.CODDISTRIB = PCDISTRIB.CODDISTRIB(+)
AND    PCSUPERV.CODGERENTE = PCGERENTE.CODGERENTE(+)
AND    PCPEDC.CONDVENDA IN (1, 2, 3, 7, 9, 14, 15, 17, 18, 19, 98)
