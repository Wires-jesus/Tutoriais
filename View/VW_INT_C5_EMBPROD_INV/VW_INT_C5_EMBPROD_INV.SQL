CREATE OR REPLACE VIEW VW_INT_C5_EMBPROD_INV
AS 
WITH CTE_EMBPROD AS (
  SELECT e.codprod, e.codfilial, e.codauxiliar, e.qtunit, e.tipoembalagem
    FROM pcembalagem e,
         pcprodut p,
         pcprodfilial f,
         VW_INT_C5_OBTER_FILIAIS_C5 c5
   WHERE p.codprod = e.codprod
     AND e.codprod = f.codprod
     AND p.codprod = f.codprod
     AND e.codfilial = f.codfilial
     and e.codfilial = c5.codfilial
     AND P.DTEXCLUSAO IS NULL
     AND (LENGTH(p.nbm) >= 2 OR p.TIPOMERC in ('KT', 'CB'))
     AND e.codprod >= 0
     AND f.codprod >= 0
     AND LENGTH(e.codauxiliar) <= 14
     AND PRECOPOREMBALAGEM.CODFILIAL = c5.codfilial
     AND PRECOPOREMBALAGEM.VALOR = 'S'
  UNION ALL
  SELECT e.codprod, e.codfilial, e.codauxiliar, e.qtunit , e.tipoembalagem
    FROM PCTABPR TPR,
         PCEMBALAGEM E,
         PCPRODUT P,
         PCPRODFILIAL PF,
         VW_INT_C5_OBTER_FILIAIS_C5 FC5,
         (SELECT VALOR TIPOPRECIFICACAO, CODFILIAL
            FROM PCPARAMFILIAL
           WHERE NOME = 'FIL_TIPOPRECIFICACAO') TIPOPRECIFICACAOFILIAL,
         (SELECT p1.VALOR, p1.CODFILIAL
            FROM PCPARAMFILIAL p1
           WHERE p1.NOME = 'FIL_PRECOPOREMBALAGEM') PRECOPOREMBALAGEM,
         (SELECT p2.VALOR, p2.CODFILIAL
            FROM PCPARAMFILIAL p2
           WHERE p2.NOME = 'NUMREGIAOPADRAOVAREJO') NUMREGIAOPADRAOVAREJO
   WHERE P.DTEXCLUSAO IS NULL
     AND E.CODPROD = TPR.CODPROD
     AND P.CODPROD = TPR.CODPROD
     AND P.CODPROD = E.CODPROD
     AND PF.CODPROD = TPR.CODPROD
     AND PF.CODPROD = P.CODPROD
     AND PF.CODPROD = E.CODPROD
     AND FC5.CODFILIAL = PF.CODFILIAL
     AND FC5.CODFILIAL = E.CODFILIAL
     AND E.CODFILIAL = PF.CODFILIAL
     AND TIPOPRECIFICACAOFILIAL.CODFILIAL = E.CODFILIAL
     AND (LENGTH(P.NBM) >= 2 OR P.TIPOMERC IN ('KT', 'CB'))
     AND E.CODPROD >= 0
     AND PF.CODPROD >= 0
     AND LENGTH(E.CODAUXILIAR) <= 14
     AND NUMREGIAOPADRAOVAREJO.CODFILIAL = FC5.CODFILIAL
     AND TPR.NUMREGIAO = NUMREGIAOPADRAOVAREJO.VALOR
     AND PRECOPOREMBALAGEM.VALOR = 'N'
     AND PRECOPOREMBALAGEM.CODFILIAL = FC5.CODFILIAL
SELECT CODPROD, CODFILIAL, CODAUXILIAR, 'PRODUTOS COM QTUNIT ZERADO' MOTIVO
  FROM CTE_EMBPROD
 WHERE QTUNIT = 0
UNION ALL
SELECT DISTINCT T.CODPROD,
                T.CODFILIAL,
                0 CODAUXILIAR,
                'QTMINIMAATACADO DIFERENTE ENTRE EMBALAGENS' MOTIVO
  FROM PCEMBALAGEM T,
       VW_INT_C5_OBTER_FILIAIS_C5 CF,
       CTE_EMBPROD VW,
       (SELECT CODPROD, AVG(NVL(QTMINIMAATACADO, 1)) MEDIA, CODFILIAL
          FROM PCEMBALAGEM
         WHERE DTINATIVO IS NULL
         GROUP BY CODPROD, CODFILIAL) AUX,
       (SELECT COUNT(*), E2.SEQPRODUTO, E2.QTDEMBALAGEM, E2.NROEMPRESA
          FROM VW_INT_C5_PRODPRECO E2
         GROUP BY E2.SEQPRODUTO, E2.QTDEMBALAGEM, E2.NROEMPRESA
        HAVING COUNT(*) > 1) E_PRECO,
       PCDEPARAPRODC5 C5
 WHERE T.CODPROD = AUX.CODPROD
   AND T.CODFILIAL = AUX.CODFILIAL
   AND T.QTMINIMAATACADO > 0
   AND E_PRECO.SEQPRODUTO = C5.SEQPRODUTO
   AND E_PRECO.NROEMPRESA = CF.CODFILIALINTEGRACAO
   AND T.CODPROD = VW.CODPROD
   AND T.CODFILIAL = VW.CODFILIAL
   AND AUX.MEDIA <> T.QTMINIMAATACADO
   AND CF.CODFILIAL = T.CODFILIAL
   AND CF.CODFILIAL = VW.CODFILIAL
   AND C5.CODPROD = AUX.CODPROD
   AND C5.CODPROD = T.CODPROD
   AND C5.CODPROD = VW.CODPROD
   AND C5.ATIVO = 'S'
   AND C5.CODAUXILIAR = 0
   AND T.DTINATIVO IS NULL
 GROUP BY T.CODPROD, T.CODFILIAL
UNION ALL
SELECT P.CODPROD,
       P.CODFILIAL,
       P.CODAUXILIAR,
       'EMBALAGENS COM TIPOEMBALAGEM DIFERENTES ENTRE FILIAIS' MOTIVO
  FROM PCEMBALAGEM P, CTE_EMBPROD V
 WHERE P.CODPROD = V.CODPROD
   AND P.CODAUXILIAR = V.CODAUXILIAR
   AND P.TIPOEMBALAGEM IS NOT NULL
   AND P.CODFILIAL IN (SELECT CODFILIAL FROM VW_INT_C5_OBTER_FILIAIS_C5)
   AND (P.CODFILIAL <> V.CODFILIAL AND P.TIPOEMBALAGEM <> V.TIPOEMBALAGEM)
UNION ALL
SELECT P.CODPROD,
       P.CODFILIAL,
       P.CODAUXILIAR,
       'PRODUTO COM EMBALAGENS COM CODIGO DE BARRAS ZERADO' MOTIVO
  FROM CTE_EMBPROD P, VW_INT_C5_OBTER_FILIAIS_C5 C5
 WHERE P.CODFILIAL = C5.CODFILIAL
   AND NVL(P.CODAUXILIAR, 0) = 0
UNION ALL
SELECT E.CODPROD,
       E.CODFILIAL,
       E.CODAUXILIAR,
       'PRODUTOS DIFERENTES EM DIFERENTES FILIAIS COM CODIGO DE BARRAS IGUAIS' MOTIVO
  FROM PCEMBALAGEM E
 WHERE EXISTS (SELECT 1
                 FROM PCEMBALAGEM PCEMB
                WHERE PCEMB.CODAUXILIAR = E.CODAUXILIAR
                  AND PCEMB.CODFILIAL IN (select CODFILIAL from vw_int_c5_obter_filiais_c5)
                  AND E.CODPROD <> PCEMB.CODPROD)
   AND E.CODFILIAL IN (select CODFILIAL from vw_int_c5_obter_filiais_c5)
