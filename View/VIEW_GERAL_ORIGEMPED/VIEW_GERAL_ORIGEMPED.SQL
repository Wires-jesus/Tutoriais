CREATE OR REPLACE VIEW VIEW_GERAL_ORIGEMPED AS
SELECT 'T' ORIGEMPED
     , 'Televendas' DESCRICAO
  FROM DUAL
UNION
SELECT 'C' ORIGEMPED
     , 'Call-Center' DESCRICAO
  FROM DUAL
UNION
SELECT 'L' ORIGEMPED
     , 'Operador Logístico' DESCRICAO
  FROM DUAL
UNION
SELECT 'E' ORIGEMPED
     , 'Pedido Eletrônico' DESCRICAO
  FROM DUAL
UNION
SELECT 'F' ORIGEMPED
     , 'Força de Vendas' DESCRICAO
  FROM DUAL
UNION
SELECT 'W' ORIGEMPED
     , 'E-Commerce' DESCRICAO
  FROM DUAL;
