CREATE OR REPLACE VIEW VIEW_MED_ALVARA_SUBSTANCIA AS
SELECT DISTINCT
        PCPRODCONTROLEVENDA.CODTIPOCONTROLEVENDA
      , PCPRINCIPATIVO.SUBSTANCIA
   FROM PCPRODCONTROLEVENDA
      , PCTIPOCONTROLEVENDA
      , PCPRODUT
      , PCPRINCIPATIVO
  WHERE (PCTIPOCONTROLEVENDA.CODTIPOCONTROLEVENDA = PCPRODCONTROLEVENDA.CODTIPOCONTROLEVENDA)
    AND (PCPRODUT.CODPROD                         = PCPRODCONTROLEVENDA.CODPROD)
    AND (NVL((SELECT VALOR FROM PCPARAMFILIAL WHERE NOME = 'VALIDALVARAPORTIPOCTLVEN' AND CODFILIAL = '99'),'N') = 'S')
    AND (PCTIPOCONTROLEVENDA.TIPOVALIDACAO       IN ('1','2','3'))
    AND (PCPRODUT.CODPRINCIPATIVO                 = PCPRINCIPATIVO.CODPRINCIPATIVO(+))
  UNION
 SELECT DISTINCT
        1 CODTIPOCONTROLEVENDA
      , NVL(PCPRINCIPATIVO.SUBSTANCIA,PCPRODUT.SUBSTANCIA)
   FROM PCPRODUT
      , PCPRINCIPATIVO
  WHERE (PCPRODUT.TIPOMERC IN ('M','MA'))
    AND (NVL((SELECT VALOR FROM PCPARAMFILIAL WHERE NOME = 'VALIDALVARAPORTIPOCTLVEN' AND CODFILIAL = '99'),'N') = 'N')
    AND (PCPRODUT.CODPRINCIPATIVO = PCPRINCIPATIVO.CODPRINCIPATIVO(+))
  UNION
 SELECT DISTINCT
        4 CODTIPOCONTROLEVENDA
      , NVL(PCPRINCIPATIVO.SUBSTANCIA,PCPRODUT.SUBSTANCIA)
   FROM PCPRODUT
      , PCPRINCIPATIVO
  WHERE (PCPRODUT.PSICOTROPICO = 'S')
    AND (NVL((SELECT VALOR FROM PCPARAMFILIAL WHERE NOME = 'VALIDALVARAPORTIPOCTLVEN' AND CODFILIAL = '99'),'N') = 'N')
    AND (PCPRODUT.CODPRINCIPATIVO = PCPRINCIPATIVO.CODPRINCIPATIVO(+))
  UNION
 SELECT DISTINCT
        5 CODTIPOCONTROLEVENDA
      , NVL(PCPRINCIPATIVO.SUBSTANCIA,PCPRODUT.SUBSTANCIA)
   FROM PCPRODUT
      , PCPRINCIPATIVO
  WHERE (PCPRODUT.RETINOICO = 'S')
    AND (NVL((SELECT VALOR FROM PCPARAMFILIAL WHERE NOME = 'VALIDALVARAPORTIPOCTLVEN' AND CODFILIAL = '99'),'N') = 'N')
    AND (PCPRODUT.CODPRINCIPATIVO = PCPRINCIPATIVO.CODPRINCIPATIVO(+))
