--ESTOQUE ATUAL
CREATE OR REPLACE VIEW SQL_INTEGRACAO_EST_ATUAL AS
SELECT CODPROD
      ,DESCRICAO
      ,CODFAB
      ,QTUNIT
      ,QTUNITCX
      ,CODAUXILIAR
      ,CODAUXILIAR2
      ,CODFORNEC
      ,PESOBRUTO
      ,PESOLIQ
      ,VOLUME
      ,CODFILIAL
      ,SUM(QTESTGER) AS QTESTGER
      ,SUM(QTRESERV) AS QTRESERV
      ,SUM(QTBLOQUEADA) AS QTBLOQUEADA
      ,SUM((QTESTGER-QTESTGER-QTBLOQUEADA)) AS QTDISPONIVEL
      ,SUM(QTEST) AS QTEST
      ,DATA AS DATA
  FROM (
SELECT PCPRODUT.CODPROD
      ,PCPRODUT.DESCRICAO
      ,PCPRODUT.CODFAB
      ,PCPRODUT.QTUNIT
      ,PCPRODUT.QTUNITCX
      ,PCPRODUT.CODAUXILIAR
      ,PCPRODUT.CODAUXILIAR2
      ,PCPRODUT.CODFORNEC
      ,NVL(PCPRODUT.PESOBRUTO,0) AS PESOBRUTO
      ,NVL(PCPRODUT.PESOLIQ,0) AS PESOLIQ
      ,NVL(PCPRODUT.VOLUME,0) AS VOLUME
      ,ESTOQUE.CODFILIAL
      ,ESTOQUE.QTESTGER
      ,ESTOQUE.QTRESERV
      ,ESTOQUE.QTBLOQUEADA
      ,(ESTOQUE.QTESTGER-ESTOQUE.QTESTGER-ESTOQUE.QTBLOQUEADA) AS QTDISPONIVEL
      ,ESTOQUE.QTEST
      ,TRUNC(SYSDATE) AS DATA
      ,'N' AS ITEMDACESTA
  FROM PCEST ESTOQUE
      ,PCPRODUT
 WHERE (PCPRODUT.TIPOMERC NOT IN('CB') OR PCPRODUT.TIPOMERC IS NULL)
   AND PCPRODUT.CODPROD = ESTOQUE.CODPROD
UNION
SELECT PCPRODUT.CODPROD
      ,PCPRODUT.DESCRICAO
      ,PCPRODUT.CODFAB
      ,PCPRODUT.QTUNIT
      ,PCPRODUT.QTUNITCX
      ,PCPRODUT.CODAUXILIAR
      ,PCPRODUT.CODAUXILIAR2
      ,PCPRODUT.CODFORNEC
      ,NVL(PCPRODUT.PESOBRUTO,0) AS PESOBRUTO
      ,NVL(PCPRODUT.PESOLIQ,0) AS PESOLIQ
      ,NVL(PCPRODUT.VOLUME,0) AS VOLUME
      ,ESTOQUE.CODFILIAL
      ,(ESTOQUE.QTESTGER * PCFORMPROD.QTPRODMP) AS QTESTGER
      ,(ESTOQUE.QTRESERV * PCFORMPROD.QTPRODMP) AS QTRESERV
      ,(ESTOQUE.QTBLOQUEADA * PCFORMPROD.QTPRODMP) AS QTBLOQUEADA
      ,((ESTOQUE.QTESTGER-ESTOQUE.QTESTGER-ESTOQUE.QTBLOQUEADA) * PCFORMPROD.QTPRODMP) AS QTDISPONIVEL
      ,(ESTOQUE.QTEST * PCFORMPROD.QTPRODMP) AS QTEST
      ,TRUNC(SYSDATE) AS DATA
      ,'S' AS ITEMDACESTA
  FROM PCEST ESTOQUE
      ,PCPRODUT CESTA
      ,PCFORMPROD
      ,PCPRODUT
 WHERE ESTOQUE.CODPROD        = CESTA.CODPROD
   AND PCFORMPROD.CODPRODACAB = CESTA.CODPROD
   AND PCPRODUT.CODPROD      = PCFORMPROD.CODPRODMP
   AND CESTA.TIPOMERC      = 'CB'
)
GROUP BY CODPROD
      ,DESCRICAO
      ,CODFAB
      ,QTUNIT
      ,QTUNITCX
      ,CODAUXILIAR
      ,CODAUXILIAR2
      ,CODFORNEC
      ,PESOBRUTO
      ,PESOLIQ
      ,VOLUME
      ,CODFILIAL
      ,DATA
