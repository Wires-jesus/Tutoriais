CREATE OR REPLACE VIEW VW_FILTROAVANCADO_DADOS AS
SELECT CODIGO,
       CODCONFIG,
       NULL AS NUMREGRA,
       OPERADOR,
       VALOR_VARCHAR,
       INICIAL_NUMBER,
       FINAL_NUMBER,
       INICIAL_DATE,
       FINAL_DATE,
       DATA_CORRENTE,
       NUMCRITERIO,
       'C' AS TIPO_DADO
  FROM PCFILTROAVANCADOCONDICAO
UNION ALL
SELECT CODIGO,
       CODCONFIG,
       NUMREGRA,
       OPERADOR,
       VALOR_VARCHAR,
       INICIAL_NUMBER,
       FINAL_NUMBER,
       INICIAL_DATE,
       FINAL_DATE,
       DATA_CORRENTE,
       NUMCRITERIO,
       'R' AS TIPO_DADO
  FROM PCFILTROAVANCADOREGRA
UNION ALL
SELECT CODIGO,
       CODCONFIG,
       NUMREGRA,
       OPERADOR,
       VALOR_VARCHAR,
       INICIAL_NUMBER,
       FINAL_NUMBER,
       INICIAL_DATE,
       FINAL_DATE,
       DATA_CORRENTE,
       NUMCRITERIO,
       'E' AS TIPO_DADO
  FROM PCFILTROAVANCADOEXCECAO