CREATE OR REPLACE VIEW VIEW_MED_PROMOCAO_REGIAO AS
SELECT VIEW_MED_PROMOCAO.CODPROMOCAOMED,
       PCREGIAO.NUMREGIAO,
       PCREGIAO.UF,
       PCREGIAO.REGIAOZFM,
       NVL((SELECT PCPROMOCAOREGIAOMED.QTMAXCOMBOMED
              FROM PCPROMOCAOREGIAOMED
             WHERE PCPROMOCAOREGIAOMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
               AND PCPROMOCAOREGIAOMED.NUMREGIAO = PCREGIAO.NUMREGIAO),
           0) AS QTMAXCOMBOMED
  FROM PCREGIAO,
       VIEW_MED_PROMOCAO
 WHERE (NVL(PCREGIAO.STATUS, 'A') = 'A')
   AND ((NOT EXISTS (SELECT 'S'
                       FROM PCPROMOCAOREGIAOMED
                      WHERE PCPROMOCAOREGIAOMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) OR
        ((EXISTS (SELECT 'S'
                    FROM PCPROMOCAOREGIAOMED
                   WHERE PCPROMOCAOREGIAOMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED)) AND
         (EXISTS (SELECT 'S'
                    FROM PCPROMOCAOREGIAOMED
                   WHERE PCPROMOCAOREGIAOMED.CODPROMOCAOMED = VIEW_MED_PROMOCAO.CODPROMOCAOMED
                     AND PCPROMOCAOREGIAOMED.NUMREGIAO = PCREGIAO.NUMREGIAO))))