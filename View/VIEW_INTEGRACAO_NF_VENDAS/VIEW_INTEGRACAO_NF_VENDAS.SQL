--NF VENDAS
CREATE OR REPLACE VIEW VIEW_INTEGRACAO_NF_VENDAS AS
SELECT /*+ INDEX(PCMOV PCMOV_IDX21)*/ PCNFSAID.NUMTRANSVENDA 
      ,PCPRODUT.CODPROD
      ,PCPRODUT.DESCRICAO
      ,PCPRODUT.CODFAB
      ,PCPRODUT.QTUNIT
      ,PCPRODUT.QTUNITCX
      ,PCPRODUT.CODAUXILIAR
      ,PCPRODUT.CODAUXILIAR2
      ,PCPRODUT.CODFORNEC
      ,NVL(PCPRODUT.PESOBRUTO,0) AS PESOBRUTO
      ,NVL(PCPRODUT.PESOLIQ,0) AS PESOLIQ
      ,NVL(PCPRODUT.VOLUME,0) AS VOLUME
      ,PCMOV.NUMLOTE
      ,PCMOV.CODAUXILIAR AS CODAUXILIARMOV
      ,NVL(PCMOV.QT,0) AS QT
      ,NVL(PCMOV.QTCONT,0) AS QTCONT
      ,NVL(PCMOV.PUNIT,0) AS PUNIT
      ,NVL(PCMOV.PUNITCONT,0) AS PUNITCONT
      ,NVL(PCMOV.PTABELA,0) AS PTABELA
      ,NVL(PCMOV.PBONIFIC,0) AS PBONIFIC
      ,PCMOV.STATUS
      ,PCMOV.CODOPER
      ,(CASE WHEN NVL(PCMOV.QT,0) = 0 THEN NVL(PCMOV.QTCONT,0) ELSE NVL(PCMOV.QT,0) END) AS QTMOV
      ,PCNFSAID.NUMNOTA
      ,PCNFSAID.NUMPED
      ,PCNFSAID.CODFILIAL
      ,PCNFSAID.CODCLI
      ,PCNFSAID.CODUSUR
      ,PCNFSAID.CODSUPERVISOR
      ,PCNFSAID.DTSAIDA
      ,PCNFSAID.CODPLPAG
      ,PCNFSAID.CODCOB
      ,PCNFSAID.CONDVENDA
      ,TRUNC(NVL(PCNFCAN.DATACANC,PCNFSAID.DTCANCEL)) AS DTCANCEL
      ,(CASE WHEN NVL(PCMOV.QT,0) = 0 THEN NVL(PCMOV.QTCONT,0) ELSE NVL(PCMOV.QT,0) END) AS QTVENDA
      ,(CASE WHEN PCNFSAID.CONDVENDA IN (5,6,11,12) OR PCMOV.CODOPER = 'SB' THEN 0 ELSE 1 END * 
        CASE WHEN NVL(PCMOV.QT,0) = 0 THEN NVL(PCMOV.QTCONT,0) ELSE NVL(PCMOV.QT,0) END) AS QTVENDIDA
      ,(CASE WHEN PCNFSAID.CONDVENDA IN (5,6,11,12) OR PCMOV.CODOPER = 'SB' THEN 0 ELSE 1 END * 
        CASE WHEN NVL(PCMOV.QT,0) = 0 THEN NVL(PCMOV.QTCONT,0) ELSE NVL(PCMOV.QT,0) END * 
        CASE WHEN NVL(PCMOV.PUNIT,0) = 0 THEN NVL(PCMOV.PUNITCONT,0) ELSE NVL(PCMOV.PUNIT,0) END) AS VLVENDA
      ,(CASE WHEN PCNFSAID.CONDVENDA IN (5,6,11,12) OR PCMOV.CODOPER = 'SB' THEN 1 ELSE 0 END * 
        CASE WHEN NVL(PCMOV.QT,0) = 0 THEN NVL(PCMOV.QTCONT,0) ELSE NVL(PCMOV.QT,0) END * 
        CASE WHEN NVL(PCMOV.PBONIFIC,0) = 0 THEN NVL(PCMOV.PTABELA,0) ELSE NVL(PCMOV.PBONIFIC,0) END) AS VLBONIFIC
      ,PCNFSAID.CODFISCAL
  FROM PCNFSAID
      ,PCMOV
      ,PCPRODUT
      ,PCNFCAN
 WHERE PCNFSAID.NUMTRANSVENDA = PCMOV.NUMTRANSVENDA
   AND PCPRODUT.CODPROD       = PCMOV.CODPROD
   AND PCNFSAID.NUMTRANSVENDA = PCNFCAN.NUMTRANSVENDA(+)
   AND PCNFSAID.CODFILIAL     = PCNFCAN.CODFILIAL(+)
   AND (PCPRODUT.TIPOMERC NOT IN('CB') OR PCPRODUT.TIPOMERC IS NULL)
   AND (PCMOV.QT > 0 OR PCMOV.QTCONT > 0)
   AND PCNFSAID.CONDVENDA NOT IN(4,8,13,98,99)
   AND (PCNFSAID.DTSAIDA <> TRUNC(NVL(PCNFCAN.DATACANC,PCNFSAID.DTCANCEL)) OR PCNFSAID.DTCANCEL IS NULL)
