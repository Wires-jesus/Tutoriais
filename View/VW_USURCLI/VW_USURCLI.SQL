CREATE OR REPLACE VIEW VW_USURCLI AS
SELECT PCCLIENT.CODCLI, PCCLIENT.CODUSUR1 CODUSUR, PCCLIENT.DTEXCLUSAO, 'EE' TIPOOPER, (SELECT MAX(DATA)
          FROM PCPEDC
         WHERE CODCLI = PCCLIENT.CODCLI
           AND CODUSUR = PCCLIENT.CODUSUR1
           AND DTCANCEL IS NULL
           AND NVL(TIPOOPER,'T') <> 'A') DTULTTELEPPEDIDO
  FROM PCCLIENT
 WHERE PCCLIENT.CODUSUR1 IS NOT NULL
UNION
SELECT PCCLIENT.CODCLI, PCCLIENT.CODUSUR2 CODUSUR, PCCLIENT.DTEXCLUSAO, 'OO' TIPOOPER, NULL DTULTTELEPPEDIDO
  FROM PCCLIENT
 WHERE PCCLIENT.CODUSUR2 <> NVL(PCCLIENT.CODUSUR1, 0)
   AND PCCLIENT.CODUSUR2 IS NOT NULL
UNION
SELECT PCUSURCLI.CODCLI, PCUSURCLI.CODUSUR, PCCLIENT.DTEXCLUSAO, PCUSURCLI.TIPOOPER, DECODE(PCUSURCLI.TIPOOPER, 'E',
(SELECT MAX(DATA)
          FROM PCPEDC
         WHERE CODCLI = PCCLIENT.CODCLI
           AND CODUSUR = PCUSURCLI.CODUSUR
           AND DTCANCEL IS NULL
           AND NVL(TIPOOPER,'T') <> 'A'), NULL) DTULTTELEPPEDIDO
  FROM PCUSURCLI, PCCLIENT
 WHERE PCUSURCLI.CODCLI = PCCLIENT.CODCLI
   AND PCUSURCLI.CODUSUR <> NVL(PCCLIENT.CODUSUR2, 0)
   AND PCUSURCLI.CODUSUR <> NVL(PCCLIENT.CODUSUR1, 0);

