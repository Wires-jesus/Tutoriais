CREATE OR REPLACE TRIGGER TRG_pclogrca_log
  BEFORE DELETE ON PCLOGRCA
  REFERENCING OLD AS OLD NEW AS NEW
  FOR EACH ROW
DECLARE
  VMACHINE     VARCHAR2(64);
  VPROGRAM     VARCHAR2(48);
  VTERMINAL    VARCHAR2(30);
  VOSUSER      VARCHAR2(30);
  VICONTADOR  INTEGER;
BEGIN

        SELECT SUBSTR(TERMINAL, 1, 64),
               SUBSTR(OSUSER, 1, 30),
               SUBSTR(PROGRAM, 1, 48),
               SUBSTR(MACHINE, 1, 64)
          INTO VTERMINAL, VOSUSER, VPROGRAM, VMACHINE
          FROM USUARIOS_LOGADOS
         WHERE AUDSID = USERENV('SESSIONID')
           AND ROWNUM = 1;

  INSERT INTO pclogrca_LOG
    (TERMINAL,
     USUARIO,
     PROGRAMA,
     MAQUINA,
     DATA,
     DTBLOQUEIO,
     DTBLOQCOMIS,
     CODFUNC,
     CODUSUR,
     ROTINA,
     VLCORRENTE,
     VLLIMCRED,
     VLCORRENTEANT,
     VLLIMCREDANT,
     HISTORICO,
     NUMPED,
     POSICAO,
     NUMTRANSVENDA,
     CODPROD,
     NUMSEQ,
     HISTORICO2,
     VLDIFERENCA,
     VLRESERVA,
     CODFILIAL)
  VALUES
    (VTERMINAL,
     VOSUSER,
     VPROGRAM,
     VMACHINE,
     :OLD.DATA,
     :OLD.DTBLOQUEIO,
     :OLD.DTBLOQCOMIS,
     :OLD.CODFUNC,
     :OLD.CODUSUR,
     :OLD.ROTINA,
     :OLD.VLCORRENTE,
     :OLD.VLLIMCRED,
     :OLD.VLCORRENTEANT,
     :OLD.VLLIMCREDANT,
     :OLD.HISTORICO,
     :OLD.NUMPED,
     :OLD.POSICAO,
     :OLD.NUMTRANSVENDA,
     :OLD.CODPROD,
     :OLD.NUMSEQ,
     :OLD.HISTORICO2,
     :OLD.VLDIFERENCA,
     :OLD.VLRESERVA,
     :OLD.CODFILIAL);

    EXCEPTION
WHEN OTHERS THEN
  RAISE_APPLICATION_ERROR(-20000, 'ERRO ORIGINAL' || SQLERRM)  ;
END;

