CREATE OR REPLACE TRIGGER TRG_ECOMMERCE_PCTABPR
   BEFORE INSERT OR UPDATE OF pVenda1, pVenda2, pVenda3, pVenda4, pVenda5, pVenda6, pVenda7 ON PCTABPR
   REFERENCING NEW AS NEW OLD AS OLD FOR EACH ROW
DECLARE 
  vnUSAALIENACAOPRODUTO NUMBER;
  vnCONTADOR NUMBER;
BEGIN
  vnCONTADOR := 0;
  BEGIN
    SELECT COUNT(PCPRODUT.CODPROD)
      INTO vnCONTADOR
      FROM PCPRODUT
     WHERE NVL(PCPRODUT.USAECOMMERCEUNILEVER,'N') = 'S'
       AND PCPRODUT.CODPROD = :NEW.CODPROD;
  EXCEPTION WHEN OTHERS THEN
      vnCONTADOR := 0; 
  END; 
  
  IF vnCONTADOR > 0 THEN
    BEGIN 
     SELECT COUNT(1)
       INTO vnUSAALIENACAOPRODUTO
       FROM PCECOMMERCEUNILEVER
       WHERE PRODUTO_DISTRIB='S';
    EXCEPTION WHEN NO_DATA_FOUND THEN
     vnUSAALIENACAOPRODUTO := 0;
    END;  

    BEGIN --Inserindo registros a ser enviado para todas as configurações validas
    --Enviar preço
      IF (NVL(:NEW.pVenda1,0) > 0) AND (NVL(:NEW.pVenda2,0) > 0) AND (NVL(:NEW.pVenda3,0) > 0) AND 
         (NVL(:NEW.pVenda4,0) > 0) AND (NVL(:NEW.pVenda5,0) > 0) AND (NVL(:NEW.pVenda6,0) > 0) AND (NVL(:NEW.pVenda7,0) > 0) THEN 
         INSERT INTO PCECOMMERCEUNILEVERFILA (CODFILIAL, TABELA, ID, DTINCLUSAO)
            SELECT PCECOMMERCEUNILEVER.CODFILIAL
                 , 'SELECTPRECO'
                 , PCPRODUT.ROWID
                 , SYSDATE
            FROM PCECOMMERCEUNILEVER
               , PCPRODUT
               , PCFILIAL
               , PCCONSUM
            WHERE PCECOMMERCEUNILEVER.CODFILIAL = PCFILIAL.CODIGO
              AND PCPRODUT.CODPROD = :NEW.CODPROD
              AND PCPRODUT.USAECOMMERCEUNILEVER = 'S'
              AND NVL(PCECOMMERCEUNILEVER.NUMREGIAO, NVL(PCFILIAL.NUMREGIAOPADRAO, PCCONSUM.NUMREGIAOPADRAO)) = :NEW.NUMREGIAO 
              AND ((vnUSAALIENACAOPRODUTO = 0) 
               OR  ((vnUSAALIENACAOPRODUTO > 0) AND EXISTS (SELECT 1
                                                            FROM PCECOMMERCEUNILEVERPRODUTO
                                                            WHERE PCECOMMERCEUNILEVERPRODUTO.CODPROD = :NEW.CODPROD
                                                              AND PCECOMMERCEUNILEVERPRODUTO.CODFILIAL = PCECOMMERCEUNILEVER.CODFILIAL)));
      END IF;
    EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
      NULL;
    END;  
  END IF;  
END;
