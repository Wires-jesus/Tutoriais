CREATE OR REPLACE TRIGGER trg_gravar_pccontato_cgccpfaux
 BEFORE
  INSERT OR UPDATE
 ON pccontato
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
DECLARE
  -- local variables here
BEGIN
  :NEW.CGCCPFAUX := LPAD(REPLACE(REPLACE(REPLACE(:NEW.CGCCPF,'/'),'.'),'-'), 14, '0');
END TRG_GRAVAR_PCCONTATO_CGCCPFAUX;
/
