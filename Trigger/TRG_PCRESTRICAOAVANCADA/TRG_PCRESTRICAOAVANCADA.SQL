CREATE OR REPLACE TRIGGER TRG_PCRESTRICAOAVANCADA
AFTER INSERT OR UPDATE ON PCRESTRICAOAVANCADA
FOR EACH ROW
BEGIN
  BEGIN
    SELECT CASE WHEN COUNT(DISTINCT CONFIG.TIPOVALIDACAO) > 1
             THEN 'A'
             ELSE MAX(CONFIG.TIPOVALIDACAO)
           END
      INTO :NEW.TIPOVALIDACAO

      FROM PCRESTRICAOAVANCADACONFIG CONFIG
     WHERE EXISTS(
      SELECT 1
        FROM PCRESTRICAOAVANCADACONDICAO
       WHERE CODCONFIG = CONFIG.CODCONFIG
         AND CODIGO = :NEW.CODIGO
      UNION
      SELECT 1
        FROM PCRESTRICAOAVANCADAREGRA
       WHERE CODCONFIG = CONFIG.CODCONFIG
         AND CODIGO = :NEW.CODIGO
      UNION
      SELECT 1
        FROM PCRESTRICAOAVANCADAEXCECAO
       WHERE CODCONFIG = CONFIG.CODCONFIG
         AND CODIGO = :NEW.CODIGO);
  EXCEPTION
    WHEN NO_DATA_FOUND  THEN
      :NEW.TIPOVALIDACAO := 'A';
  END;
END;