CREATE OR REPLACE TRIGGER TR_WMS_PCMOVENDPENDDATAGER
BEFORE INSERT
ON PCMOVENDPEND
REFERENCING NEW AS NEW OLD AS OLD 
FOR EACH ROW
DECLARE
vHORA PCMOVENDPEND.HORA%TYPE;
vMINUTO PCMOVENDPEND.MINUTO%TYPE;
vSEGUNDO PCMOVENDPEND.MINUTO%TYPE;
vDATA DATE;
BEGIN
  /*27/09/2012 - Marcio Morais*/
  vData := nvl(:new.datageracao,Sysdate);
  
  SELECT TO_NUMBER(TO_CHAR(vDATA,'HH24'))
         ,TO_NUMBER(TO_CHAR(vDATA,'MI'))
         ,TO_NUMBER(TO_CHAR(vDATA,'SS')) 
  INTO
         vHORA,vMINUTO,vSEGUNDO
  FROM DUAL;   
  :NEW.DATA := TRUNC(vDATA);
  :NEW.HORA := vHORA;
  :NEW.MINUTO := vMINUTO;
  :NEW.DATAGERACAO := vDATA;
END;
