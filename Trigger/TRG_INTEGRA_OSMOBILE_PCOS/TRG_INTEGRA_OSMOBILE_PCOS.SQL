CREATE OR REPLACE TRIGGER TRG_INTEGRA_OSMOBILE_PCOS
  AFTER INSERT ON PCORDEMSERVICO
  REFERENCING NEW AS NEW OLD AS OLD
  FOR EACH ROW
DECLARE
  STATUS_OPERACAO varchar2(50);
  CHAVETRANSACAO raw(16);
BEGIN
  SELECT SYS_GUID INTO CHAVETRANSACAO FROM DUAL;

  IF (INSERTING) THEN
     SELECT 'INCLUIR' INTO STATUS_OPERACAO FROM DUAL;
  END IF;

  IF (STATUS_OPERACAO IN ('INCLUIR')) THEN
      INSERT INTO PCOSMOBILE_ORDEMSERVICO
        (ID, CODIGO, STATUSOPERACAO, DATA)
      VALUES
        (CHAVETRANSACAO, :NEW.NUMOS , STATUS_OPERACAO, TRUNC(SYSDATE));
  END IF;
END;
