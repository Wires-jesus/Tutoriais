CREATE OR REPLACE TRIGGER TR_WMS_PCMOVENDPENDCONF
BEFORE INSERT OR UPDATE
ON PCMOVENDPEND
REFERENCING NEW AS NEW OLD AS OLD 
FOR EACH ROW
DECLARE
BEGIN
  /*27/10/2011 Daniel Lobo */
    IF nvl(:OLD.CODFUNCCONF,-1) <> nvl(:NEW.CODFUNCCONF,-1) THEN
       :NEW.CODFUNCCOFERENTE := :NEW.CODFUNCCONF;
    ELSIF nvl(:OLD.CODFUNCCOFERENTE, -1) <> nvl(:NEW.CODFUNCCOFERENTE,-1) THEN
       :NEW.CODFUNCCONF := :NEW.CODFUNCCOFERENTE;
    END IF;
END;
