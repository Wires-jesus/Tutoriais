CREATE OR REPLACE TRIGGER 
TR_WMS_DELETACONFLOTE 
BEFORE                   
  UPDATE OR INSERT       
OF DTESTORNO             
ON PCMOVENDPEND          
REFERENCING NEW AS NEW OLD 
AS OLD 
FOR EACH ROW             
BEGIN                    
/*TEM A FUNÇÃO DE DELETAR AS 
MOVIMENTAÇÕES DE 
CONFERÊNCIA POR LOTE*/ 
IF :NEW.DTESTORNO IS NOT NULL 
THEN 
  DELETE FROM 
PCMOVENDPENDCONFLOTE WHERE 
NUMOS = :NEW.NUMOS; 
END IF; 
END;


