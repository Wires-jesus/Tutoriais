CREATE OR REPLACE TRIGGER GRAVAR_PCPEDI_QTUNITCX
 BEFORE INSERT OR UPDATE
 ON PCPEDI
 REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW 
DECLARE 
   vQT pcpedi.QTUNITCX%TYPE;
BEGIN
  BEGIN
    IF NVL( :NEW.QTUNITCX, 0) = 0 THEN
      SELECT NVL(QTUNITCX, 0)
        INTO vQT
        FROM PCPRODUT
       WHERE CODPROD = :NEW.CODPROD;

      :NEW.QTUNITCX := vQT;
    END IF;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      NULL;
  END;  
END;
