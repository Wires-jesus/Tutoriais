CREATE OR REPLACE TRIGGER TRG_MD5PAF_PCTABPR
  BEFORE UPDATE OR INSERT OR DELETE ON PCTABPR
  REFERENCING OLD AS OLD NEW AS NEW
  FOR EACH ROW
BEGIN
  if (F_LIMPA_MD5PAF = 'S') then
    if ( UPDATING) then
      UPDATE PCEMBALAGEM
         SET MD5PAF = NULL
       WHERE PCEMBALAGEM.CODPROD = :NEW.CODPROD;
	  end if;

    if ( INSERTING or DELETING ) then
	    UPDATE PCTABELASMD5PAF set MD5PAF = NULL WHERE TABELA = 'PCEMBALAGEM';
	  end if;
  end if;

END TRG_MD5PAF_PCTABPR;