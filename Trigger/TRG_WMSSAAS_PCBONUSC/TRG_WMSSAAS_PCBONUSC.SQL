CREATE OR REPLACE TRIGGER TRG_WMSSAAS_PCBONUSC
BEFORE UPDATE OF DTCANCEL ON PCBONUSC
FOR EACH ROW
DECLARE
    V_USAINTEGRACAOWMSSAAS VARCHAR2(1);
    V_WMS_GERADO VARCHAR2(1);
    V_COUNT NUMBER;
BEGIN
    -- Obtendo o valor do parâmetro
    BEGIN
        SELECT NVL(VALOR, 'N')
        INTO V_USAINTEGRACAOWMSSAAS
        FROM PCPARAMINTEGRACAOWMSSAAS
        WHERE PARAMETRO = 'USAINTEGRACAOWMSSAAS' AND CODFILIAL = :NEW.CODFILIAL;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            V_USAINTEGRACAOWMSSAAS := 'N';
        WHEN OTHERS THEN
            V_USAINTEGRACAOWMSSAAS := 'N';
    END;
    
    BEGIN
        SELECT SUM(NVL(QTENTRADA,0)) + SUM(NVL(QTAVARIA, 0)) AS TOTAL_CONFERIDO_BONUS
        INTO V_COUNT
          FROM PCBONUSI 
           WHERE NUMBONUS = :NEW.NUMBONUS;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            V_USAINTEGRACAOWMSSAAS := 'N';
        WHEN OTHERS THEN
            V_USAINTEGRACAOWMSSAAS := 'N';
    END;
    
    SELECT DECODE((SELECT COUNT(1) FROM PCNFENT WHERE NUMBONUS = :NEW.NUMBONUS AND DTWMS IS NOT NULL AND CODFILIAL =  :NEW.CODFILIAL),0,'N','S') AS WMW_GERADO
    INTO V_WMS_GERADO
    FROM DUAL;


    -- Verificando as condições para lançar exceção
    IF :OLD.DTCANCEL IS NULL AND :NEW.DTCANCEL IS NOT NULL AND V_WMS_GERADO = 'S' AND V_USAINTEGRACAOWMSSAAS = 'S' AND V_COUNT = 0 THEN
        RAISE_APPLICATION_ERROR(-20001, 'Processo em andamento no WMS Saas');
    END IF;
END;
