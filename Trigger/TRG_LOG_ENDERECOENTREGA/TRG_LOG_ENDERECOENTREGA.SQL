CREATE OR REPLACE TRIGGER TRG_LOG_ENDERECOENTREGA
  AFTER INSERT OR DELETE OR UPDATE OF CODENDENTCLI ON PCCLIENTENDENT
  REFERENCING NEW AS NOVO OLD AS ANTIGO
  FOR EACH ROW
  
DECLARE

  V_PROGRAMA             VARCHAR2(80);
  V_MAQUINA              VARCHAR2(80);
  V_USUARIO              VARCHAR2(80);
  V_DATA_CRIACAO         LOGENDERECOENTREGA.DATA_CRIACAO%TYPE;
  V_DATA_ATUALIZACAO     LOGENDERECOENTREGA.DATA_ATUALIZACAO%TYPE;
  V_LOG_ENDERECO_ENTREGA LOGENDERECOENTREGA%ROWTYPE;
  
BEGIN

  V_PROGRAMA := F_OBTERNOMEAPLICACAO;
  V_MAQUINA := SYS_CONTEXT('USERENV', 'TERMINAL');
  V_USUARIO := SYS_CONTEXT('USERENV', 'OS_USER');

  IF INSERTING OR UPDATING THEN
    
    IF INSERTING THEN
      
      V_DATA_CRIACAO := SYSDATE;
      V_DATA_ATUALIZACAO := NULL;
      
    ELSE
      
      V_DATA_CRIACAO := NULL;
      V_DATA_ATUALIZACAO := SYSDATE;
      
    END IF;
    
    V_LOG_ENDERECO_ENTREGA.CODCLI := :NOVO.CODCLI;
    V_LOG_ENDERECO_ENTREGA.CODENDENTCLI := :NOVO.CODENDENTCLI;
    V_LOG_ENDERECO_ENTREGA.CODBAIRROENT := :NOVO.CODBAIRROENT;
    V_LOG_ENDERECO_ENTREGA.BAIRROENT := :NOVO.BAIRROENT;
    V_LOG_ENDERECO_ENTREGA.MUNICENT := :NOVO.MUNICENT;
    V_LOG_ENDERECO_ENTREGA.ESTENT := :NOVO.ESTENT;
    V_LOG_ENDERECO_ENTREGA.CEPENT := :NOVO.CEPENT;
    V_LOG_ENDERECO_ENTREGA.ENDERENT := :NOVO.ENDERENT;
    V_LOG_ENDERECO_ENTREGA.COMPLEMENTOENT := :NOVO.COMPLEMENTOENT;
    V_LOG_ENDERECO_ENTREGA.CODPRACAENT := :NOVO.CODPRACAENT;
    V_LOG_ENDERECO_ENTREGA.NUMEROENT := :NOVO.NUMEROENT;
    V_LOG_ENDERECO_ENTREGA.CODMUNICIPIO := :NOVO.CODMUNICIPIO;
    V_LOG_ENDERECO_ENTREGA.CODCIDADE := :NOVO.CODCIDADE;
    V_LOG_ENDERECO_ENTREGA.PONTOREFER := :NOVO.PONTOREFER;
    V_LOG_ENDERECO_ENTREGA.LONGITUDE := :NOVO.LONGITUDE;
    V_LOG_ENDERECO_ENTREGA.LATITUDE := :NOVO.LATITUDE;
    V_LOG_ENDERECO_ENTREGA.DTEXCLUSAO := :NOVO.DTEXCLUSAO;
    V_LOG_ENDERECO_ENTREGA.CODFUNCCAD := :NOVO.CODFUNCCAD;
    V_LOG_ENDERECO_ENTREGA.CODFUNCULTALTER := :NOVO.CODFUNCULTALTER;
    V_LOG_ENDERECO_ENTREGA.DTCAD := :NOVO.DTCAD;
    V_LOG_ENDERECO_ENTREGA.DTULTALTER := :NOVO.DTULTALTER;
    V_LOG_ENDERECO_ENTREGA.OBSERVACAO := :NOVO.OBSERVACAO;
    V_LOG_ENDERECO_ENTREGA.DATA_CRIACAO := V_DATA_CRIACAO;
    V_LOG_ENDERECO_ENTREGA.DATA_ATUALIZACAO := V_DATA_ATUALIZACAO;    
    V_LOG_ENDERECO_ENTREGA.DATA_EXCLUSAO := NULL;
    
  ELSE
    
    V_LOG_ENDERECO_ENTREGA.CODCLI := :ANTIGO.CODCLI;
    V_LOG_ENDERECO_ENTREGA.CODENDENTCLI := :ANTIGO.CODENDENTCLI;
    V_LOG_ENDERECO_ENTREGA.CODBAIRROENT := :ANTIGO.CODBAIRROENT;
    V_LOG_ENDERECO_ENTREGA.BAIRROENT := :ANTIGO.BAIRROENT;
    V_LOG_ENDERECO_ENTREGA.MUNICENT := :ANTIGO.MUNICENT;
    V_LOG_ENDERECO_ENTREGA.ESTENT := :ANTIGO.ESTENT;
    V_LOG_ENDERECO_ENTREGA.CEPENT := :ANTIGO.CEPENT;
    V_LOG_ENDERECO_ENTREGA.ENDERENT := :ANTIGO.ENDERENT;
    V_LOG_ENDERECO_ENTREGA.COMPLEMENTOENT := :ANTIGO.COMPLEMENTOENT;
    V_LOG_ENDERECO_ENTREGA.CODPRACAENT := :ANTIGO.CODPRACAENT;
    V_LOG_ENDERECO_ENTREGA.NUMEROENT := :ANTIGO.NUMEROENT;
    V_LOG_ENDERECO_ENTREGA.CODMUNICIPIO := :ANTIGO.CODMUNICIPIO;
    V_LOG_ENDERECO_ENTREGA.CODCIDADE := :ANTIGO.CODCIDADE;
    V_LOG_ENDERECO_ENTREGA.PONTOREFER := :ANTIGO.PONTOREFER;
    V_LOG_ENDERECO_ENTREGA.LONGITUDE := :ANTIGO.LONGITUDE;
    V_LOG_ENDERECO_ENTREGA.LATITUDE := :ANTIGO.LATITUDE;
    V_LOG_ENDERECO_ENTREGA.DTEXCLUSAO := :ANTIGO.DTEXCLUSAO;
    V_LOG_ENDERECO_ENTREGA.CODFUNCCAD := :ANTIGO.CODFUNCCAD;
    V_LOG_ENDERECO_ENTREGA.CODFUNCULTALTER := :ANTIGO.CODFUNCULTALTER;
    V_LOG_ENDERECO_ENTREGA.DTCAD := :ANTIGO.DTCAD;
    V_LOG_ENDERECO_ENTREGA.DTULTALTER := :ANTIGO.DTULTALTER;
    V_LOG_ENDERECO_ENTREGA.OBSERVACAO := :ANTIGO.OBSERVACAO;    
    V_LOG_ENDERECO_ENTREGA.DATA_CRIACAO := NULL;
    V_LOG_ENDERECO_ENTREGA.DATA_ATUALIZACAO := NULL;
    V_LOG_ENDERECO_ENTREGA.DATA_EXCLUSAO := SYSDATE;
    
  END IF;
  
  V_LOG_ENDERECO_ENTREGA.PROGRAMA := V_PROGRAMA;
  V_LOG_ENDERECO_ENTREGA.MAQUINA := V_MAQUINA;
  V_LOG_ENDERECO_ENTREGA.USUARIO := V_USUARIO;

  INSERT INTO LOGENDERECOENTREGA VALUES V_LOG_ENDERECO_ENTREGA;

END TRG_LOG_ENDERECOENTREGA;