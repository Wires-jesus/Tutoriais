CREATE OR REPLACE TRIGGER TRG_PCTABPR_PRECOANTERIOR
 BEFORE
  INSERT OR UPDATE
 ON PCTABPR
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN
  IF (:NEW.PVENDA <> :OLD.PVENDA) THEN
    :NEW.PANTERIOR      := :OLD.PVENDA;
  END IF;
  IF (:NEW.PVENDA1 <> :OLD.PVENDA1) THEN
    :NEW.PANTERIOR1     := :OLD.PVENDA1;
  END IF;
  IF (:NEW.PVENDA2 <> :OLD.PVENDA2) THEN
    :NEW.PANTERIOR2     := :OLD.PVENDA2;
  END IF;
  IF (:NEW.PVENDA3 <> :OLD.PVENDA3) THEN
    :NEW.PANTERIOR3     := :OLD.PVENDA3;
  END IF;
  IF (:NEW.PVENDA4 <> :OLD.PVENDA4) THEN
    :NEW.PANTERIOR4     := :OLD.PVENDA4;
  END IF;
  IF (:NEW.PVENDA5 <> :OLD.PVENDA5) THEN
    :NEW.PANTERIOR5     := :OLD.PVENDA5;
  END IF;
  IF (:NEW.PVENDA6 <> :OLD.PVENDA6) THEN
    :NEW.PANTERIOR6     := :OLD.PVENDA6;
  END IF;
  IF (:NEW.PVENDA7 <> :OLD.PVENDA7) THEN
    :NEW.PANTERIOR7     := :OLD.PVENDA7;
  END IF;
  IF (:NEW.PVENDAATAC <> :OLD.PVENDAATAC) THEN
    :NEW.PANTERIORATAC  := :OLD.PVENDAATAC;
  END IF;
  IF (:NEW.PVENDAATAC1 <> :OLD.PVENDAATAC1) THEN
    :NEW.PANTERIORATAC1 := :OLD.PVENDAATAC1;
  END IF;
  IF (:NEW.PVENDAATAC2 <> :OLD.PVENDAATAC2) THEN
    :NEW.PANTERIORATAC2 := :OLD.PVENDAATAC2;
  END IF;
  IF (:NEW.PVENDAATAC3 <> :OLD.PVENDAATAC3) THEN
    :NEW.PANTERIORATAC3 := :OLD.PVENDAATAC3;
  END IF;
  IF (:NEW.PVENDAATAC4 <> :OLD.PVENDAATAC4) THEN
    :NEW.PANTERIORATAC4 := :OLD.PVENDAATAC4;
  END IF;
  IF (:NEW.PVENDAATAC5 <> :OLD.PVENDAATAC5) THEN
    :NEW.PANTERIORATAC5 := :OLD.PVENDAATAC5;
  END IF;
  IF (:NEW.PVENDAATAC6 <> :OLD.PVENDAATAC6) THEN
    :NEW.PANTERIORATAC6 := :OLD.PVENDAATAC6;
  END IF;
  IF (:NEW.PVENDAATAC7 <> :OLD.PVENDAATAC7) THEN
   :NEW.PANTERIORATAC7 := :OLD.PVENDAATAC7;
  END IF;

END;
