CREATE OR REPLACE TRIGGER TRG_PCPRODUT_PROG_ALTERAR
BEFORE UPDATE ON PCPRODUT_PROGRAMADA
FOR EACH ROW
BEGIN
  IF :OLD.DATAPROGRAMADA <> :NEW.DATAPROGRAMADA THEN
    UPDATE PCTABPR_PROGRAMADA
       SET DATAPROGRAMADA = :NEW.DATAPROGRAMADA
     WHERE ID_PCPRODUT_PROGRAMADA = :OLD.ID;
  END IF;
END;