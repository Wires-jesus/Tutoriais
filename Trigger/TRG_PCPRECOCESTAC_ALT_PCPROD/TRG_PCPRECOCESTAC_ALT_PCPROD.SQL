CREATE OR REPLACE TRIGGER TRG_PCPRECOCESTAC_ALT_PCPROD
    AFTER INSERT OR UPDATE OR DELETE ON PCPRECOCESTAC
    FOR EACH ROW
DECLARE
    v_codprod PCPRODUT.CODPROD%TYPE;
BEGIN
    IF INSERTING OR UPDATING THEN
        v_codprod := :NEW.CODPRODACAB;
    ELSIF DELETING THEN
        v_codprod := :OLD.CODPRODACAB;
    END IF;

    UPDATE PCPRODUT
    SET DTULTALTER = SYSDATE
    WHERE CODPROD = v_codprod;
END;
