CREATE OR REPLACE TRIGGER TRG_MD5PAF_PCVENDACONSUM
  BEFORE UPDATE OR INSERT OR DELETE ON PCVENDACONSUM
  REFERENCING OLD AS OLD NEW AS NEW
  FOR EACH ROW
BEGIN
  if F_LIMPA_MD5PAF = 'S' then
    if ( INSERTING or DELETING ) then
	    UPDATE PCTABELASMD5PAF set MD5PAF = NULL WHERE TABELA = 'PCVENDACONSUM' AND DATA = TRUNC(SYSDATE);
	  end if;
  end if;

END TRG_MD5PAF_PCVENDACONSUM;