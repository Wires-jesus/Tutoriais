CREATE OR REPLACE TRIGGER TRG_INTEGRA_PRECEDENCIA
  BEFORE INSERT OR UPDATE ON PCINTEGRACAOCORE
  REFERENCING OLD AS OLD NEW AS NEW
  FOR EACH ROW

DECLARE
  ROWPCINTEGRAPRECEDENTEENVIO PCINTEGRAPRECEDENTEENVIO%ROWTYPE;
BEGIN
  IF :NEW.IDINTERNO IS NOT NULL THEN
    BEGIN
      SELECT S.*
        INTO ROWPCINTEGRAPRECEDENTEENVIO
        FROM PCINTEGRAPRECEDENTEENVIO S
       WHERE S.IDINTERNO = :NEW.IDINTERNO
         AND S.IDROTASERVICO = :NEW.IDROTASERVICO;
    EXCEPTION
      WHEN OTHERS THEN
        ROWPCINTEGRAPRECEDENTEENVIO := NULL;
    END;
  
    IF ROWPCINTEGRAPRECEDENTEENVIO.IDINTERNO IS NOT NULL THEN
      ROWPCINTEGRAPRECEDENTEENVIO. DTULTALTER := SYSDATE;
      -- ATUALIZAR
      UPDATE PCINTEGRAPRECEDENTEENVIO
         SET ROW = ROWPCINTEGRAPRECEDENTEENVIO
       WHERE IDINTERNO = :NEW.IDINTERNO
         AND IDROTASERVICO = :NEW.IDROTASERVICO;
      --- 
    ELSE
      ROWPCINTEGRAPRECEDENTEENVIO.IDINTERNO     := :NEW.IDINTERNO;
      ROWPCINTEGRAPRECEDENTEENVIO.IDROTASERVICO := :NEW.IDROTASERVICO;
      ROWPCINTEGRAPRECEDENTEENVIO.DTULTALTER    := SYSDATE;
      --- INSERIR      
      INSERT INTO PCINTEGRAPRECEDENTEENVIO VALUES ROWPCINTEGRAPRECEDENTEENVIO;
      --- 
    END IF;
  END IF;
EXCEPTION
  WHEN OTHERS THEN
    NULL;
END;