CREATE OR REPLACE TRIGGER trg_srvprc_pcprecoprom
 BEFORE
  INSERT OR DELETE OR UPDATE
 ON pcprecoprom
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN

    UPDATE PCCAIXA SET  DTPROXCARGAPARCIAL = SYSDATE
    WHERE NUMREGIAO = :NEW.NUMREGIAO AND ATUALIZAPRECOFIMVENDA='S';
    
    UPDATE PCCAIXA SET  DTPROXCARGAPARCIAL = SYSDATE
    WHERE NUMREGIAO = :OLD.NUMREGIAO AND ATUALIZAPRECOFIMVENDA='S';
END;
