CREATE OR REPLACE TRIGGER TRG_PCTRIBUTACAO
BEFORE UPDATE ON PCTRIBUTACAO
FOR EACH ROW
BEGIN
    :NEW.DTULTALTER := SYSDATE;

    IF :NEW.DTEXCLUSAO IS NOT NULL AND :OLD.DTEXCLUSAO IS NULL THEN
        :NEW.DTINATIVACAO := SYSDATE;

        UPDATE PCTRIBUTACAO_EXCECAO
        SET DTINATIVACAO = SYSDATE
        WHERE CODIGO_TRIBUTACAO = :OLD.CODIGO_TRIBUTACAO;

        UPDATE PCTRIBUTACAO_FILTRO_NCM
        SET DTINATIVACAO = SYSDATE
        WHERE CODIGO_TRIBUTACAO = :OLD.CODIGO_TRIBUTACAO;

        UPDATE PCTRIBUTACAO_FILTRO_PRODUTO
        SET DTINATIVACAO = SYSDATE
        WHERE CODIGO_TRIBUTACAO = :OLD.CODIGO_TRIBUTACAO;
		
        UPDATE PCTRIBUTACAO_FILTRO_CFOP
        SET DTINATIVACAO = SYSDATE
        WHERE CODIGO_TRIBUTACAO = :OLD.CODIGO_TRIBUTACAO;		

    ELSIF :NEW.DTEXCLUSAO IS NULL AND :OLD.DTEXCLUSAO IS NOT NULL THEN
        :NEW.DTINATIVACAO := NULL;

        UPDATE PCTRIBUTACAO_EXCECAO
        SET DTINATIVACAO = NULL
        WHERE CODIGO_TRIBUTACAO = :OLD.CODIGO_TRIBUTACAO;

        UPDATE PCTRIBUTACAO_FILTRO_NCM
        SET DTINATIVACAO = NULL
        WHERE CODIGO_TRIBUTACAO = :OLD.CODIGO_TRIBUTACAO;

        UPDATE PCTRIBUTACAO_FILTRO_PRODUTO
        SET DTINATIVACAO = NULL
        WHERE CODIGO_TRIBUTACAO = :OLD.CODIGO_TRIBUTACAO;
		
        UPDATE PCTRIBUTACAO_FILTRO_CFOP
        SET DTINATIVACAO = NULL
        WHERE CODIGO_TRIBUTACAO = :OLD.CODIGO_TRIBUTACAO;

    END IF;
END;