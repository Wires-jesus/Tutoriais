CREATE OR REPLACE TRIGGER TRG_ECOMMERCEB2B_PRODUTO
   BEFORE INSERT OR DELETE OR UPDATE ON PCECOMMERCEB2BPRODUTO
   REFERENCING NEW AS NEW OLD AS OLD FOR EACH ROW
DECLARE
  vsENVIARREGISTRO VARCHAR2(1);
  vsPOSSUIPRECOMAIORZERO INTEGER;
BEGIN
  vsENVIARREGISTRO := 'S';

  BEGIN
    SELECT DECODE(PCPRODUT.DTEXCLUSAO, NULL, 'S', 'N')
    INTO vsENVIARREGISTRO
    FROM PCPRODUT
    WHERE PCPRODUT.CODPROD = NVL(:OLD.CODPROD, :NEW.CODPROD);
  EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
     NULL;
  END;

  IF vsENVIARREGISTRO = 'S' THEN
    IF DELETING THEN
        INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO, OBSERVACAO)
           SELECT NVL(:NEW.CODFILIAL, :OLD.CODFILIAL)
                , NVL(:NEW.TIPOINTEGRACAO, :OLD.TIPOINTEGRACAO)
                , 'ESTOQUE'
                , PCPRODUT.ROWID
                , SYSDATE
                , CASE WHEN :OLD.TIPOCONVERSAO = 'AL' THEN
                                CASE WHEN :OLD.FATORCONVERSAO > 1 THEN :OLD.CODAUXILIAR||'C'||:OLD.FATORCONVERSAO
                                   ELSE TO_CHAR(:OLD.CODAUXILIAR) END
                       WHEN :OLD.TIPOCONVERSAO = 'UN' THEN
                                CASE WHEN PCPRODUT.QTUNIT > 1 THEN PCPRODUT.CODAUXILIAR||'C'||PCPRODUT.QTUNIT
                                    ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
                       WHEN :OLD.TIPOCONVERSAO = 'MU' THEN
                                CASE WHEN PCPRODUT.MULTIPLO > 1 THEN PCPRODUT.CODAUXILIAR||'C'||PCPRODUT.MULTIPLO
                                    ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
                       WHEN :OLD.TIPOCONVERSAO = 'MT' THEN
                                CASE WHEN ( NVL(PCPRODUT.QTUNIT, 0) * NVL(PCPRODUT.QTUNITCX, 0) ) > 1 THEN
                                       PCPRODUT.CODAUXILIAR||'C'||( NVL(PCPRODUT.QTUNIT, 0) * NVL(PCPRODUT.QTUNITCX, 0) )
                                            ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
                       WHEN :OLD.TIPOCONVERSAO = 'EM' THEN
                                CASE WHEN :OLD.FATORCONVERSAO > 1 THEN PCPRODUT.CODAUXILIAR||'C'||:OLD.FATORCONVERSAO
                                    ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
                 END
                 END
           FROM PCPRODUT
           WHERE PCPRODUT.CODPROD = :OLD.CODPROD;

        INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO, OBSERVACAO)
           SELECT NVL(:NEW.CODFILIAL, :OLD.CODFILIAL)
                , NVL(:NEW.TIPOINTEGRACAO, :OLD.TIPOINTEGRACAO)
                , 'PRECO'
                , PCPRODUT.ROWID
                , SYSDATE
                , CASE WHEN :OLD.TIPOCONVERSAO = 'AL' THEN
                                CASE WHEN :OLD.FATORCONVERSAO > 1 THEN :OLD.CODAUXILIAR||'C'||:OLD.FATORCONVERSAO
                                   ELSE TO_CHAR(:OLD.CODAUXILIAR) END
                       WHEN :OLD.TIPOCONVERSAO = 'UN' THEN
                                CASE WHEN PCPRODUT.QTUNIT > 1 THEN PCPRODUT.CODAUXILIAR||'C'||PCPRODUT.QTUNIT
                                    ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
                       WHEN :OLD.TIPOCONVERSAO = 'MU' THEN
                                CASE WHEN PCPRODUT.MULTIPLO > 1 THEN PCPRODUT.CODAUXILIAR||'C'||PCPRODUT.MULTIPLO
                                    ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
                       WHEN :OLD.TIPOCONVERSAO = 'MT' THEN
                                CASE WHEN ( NVL(PCPRODUT.QTUNIT, 0) * NVL(PCPRODUT.QTUNITCX, 0) ) > 1 THEN
                                       PCPRODUT.CODAUXILIAR||'C'||( NVL(PCPRODUT.QTUNIT, 0) * NVL(PCPRODUT.QTUNITCX, 0) )
                                            ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
                       WHEN :OLD.TIPOCONVERSAO = 'EM' THEN
                                CASE WHEN :OLD.FATORCONVERSAO > 1 THEN PCPRODUT.CODAUXILIAR||'C'||:OLD.FATORCONVERSAO
                                    ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
                 END
                 END
           FROM PCPRODUT
           WHERE PCPRODUT.CODPROD = :OLD.CODPROD;
    ELSE
    BEGIN --Inserindo registros a ser enviado para todas as configurações validas
      --Enviar estoque
      INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO)
         SELECT NVL(:NEW.CODFILIAL, :OLD.CODFILIAL)
          , NVL(:NEW.TIPOINTEGRACAO, :OLD.TIPOINTEGRACAO)
          , 'ESTOQUE'
          , PCPRODUT.ROWID
          , SYSDATE
        FROM PCPRODUT
        WHERE PCPRODUT.CODPROD = NVL(:NEW.CODPROD, :OLD.CODPROD);
    EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
         NULL;
    END;

      BEGIN
      SELECT DISTINCT
            COUNT(1) POSSUIPRECO
      INTO vsPOSSUIPRECOMAIORZERO
      FROM PCPRODUT,
             PCTABPR,
             PCECOMMERCEB2B C,
             PCECOMMERCEB2BPRODUTO
      WHERE PCPRODUT.CODPROD = PCECOMMERCEB2BPRODUTO.CODPROD
          AND PCTABPR.CODPROD = PCECOMMERCEB2BPRODUTO.CODPROD
          AND C.TIPOINTEGRACAO = PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO
          AND C.NUMREGIAO = PCTABPR.NUMREGIAO
          AND C.CODFILIAL = PCECOMMERCEB2BPRODUTO.CODFILIAL
          AND C.CODFILIAL = NVL(:NEW.CODFILIAL,:OLD.CODFILIAL)
        AND PCPRODUT.CODPROD = :NEW.CODPROD
        AND NVL(PCTABPR.PVENDA1, 0) > 0
        AND NVL(PCTABPR.PVENDA2, 0) > 0
        AND NVL(PCTABPR.PVENDA3, 0) > 0
        AND NVL(PCTABPR.PVENDA4, 0) > 0
        AND NVL(PCTABPR.PVENDA5, 0) > 0
        AND NVL(PCTABPR.PVENDA6, 0) > 0
        AND NVL(PCTABPR.PVENDA7, 0) > 0
        AND ROWNUM = 1;
      EXCEPTION WHEN OTHERS THEN
       vsPOSSUIPRECOMAIORZERO := 0;
      END;

      IF (vsPOSSUIPRECOMAIORZERO > 0) THEN
         BEGIN --Inserindo registros a ser enviado para todas as configurações validas
         --Enviar preço
            INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO)
               SELECT NVL(:NEW.CODFILIAL, :OLD.CODFILIAL)
                    , NVL(:NEW.TIPOINTEGRACAO, :OLD.TIPOINTEGRACAO)
                    , 'PRECO'
                    , PCPRODUT.ROWID
                    , SYSDATE
               FROM PCPRODUT
               WHERE PCPRODUT.CODPROD = NVL(:NEW.CODPROD,:OLD.CODPROD);
         EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
            NULL;
         END;
      END IF;
    END IF;
  END IF;
END;
