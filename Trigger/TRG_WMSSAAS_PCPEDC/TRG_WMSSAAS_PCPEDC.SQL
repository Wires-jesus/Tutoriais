CREATE OR REPLACE TRIGGER TRG_WMSSAAS_PCPEDC
  BEFORE UPDATE OF POSICAO ON PCPEDC
  FOR EACH ROW
DECLARE
  V_USA_INTEGRACAO_WMSSAAS VARCHAR2(2);
  V_PERMITE_FATURAR_COMWMSPEND VARCHAR2(2);
BEGIN

  V_PERMITE_FATURAR_COMWMSPEND := 'N';

  BEGIN
    SELECT NVL(VALOR, 'N')
      INTO V_USA_INTEGRACAO_WMSSAAS
      FROM PCPARAMINTEGRACAOWMSSAAS
     WHERE PARAMETRO = 'USAINTEGRACAOWMSSAAS'
       AND CODFILIAL = :NEW.CODFILIAL;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      V_USA_INTEGRACAO_WMSSAAS := 'N';
    WHEN OTHERS THEN
      V_USA_INTEGRACAO_WMSSAAS := 'N';
  END;

  /*** VERIFICANDO SE O PEDIDO ESTÁ CONDICIONADO A TER FATURAMENTO FEITO ANTES DA CONLUSÃO DO WMS ***/
  BEGIN
    SELECT NVL(PERMITEFATURARCOMWMSPEND,'N')
      INTO V_PERMITE_FATURAR_COMWMSPEND
      FROM PCPEDC
     WHERE NUMPED = :NEW.NUMPED
       AND CODFILIAL = :NEW.CODFILIAL;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      V_PERMITE_FATURAR_COMWMSPEND := 'N';
    WHEN OTHERS THEN
      V_PERMITE_FATURAR_COMWMSPEND := 'N';
  END;


  IF V_USA_INTEGRACAO_WMSSAAS = 'S' THEN

    IF (:OLD.POSICAO <> :NEW.POSICAO) AND (:NEW.POSICAO = 'F') AND (V_PERMITE_FATURAR_COMWMSPEND = 'N') THEN
      IF :NEW.DTWMS IS NULL THEN
        RAISE_APPLICATION_ERROR(-20001,
                                'WMS NÃO INICIADO, FATURAMENTO NÃO PERMITIDO');
      END IF;

      IF (:NEW.DTFINALSEP IS NULL) AND (V_PERMITE_FATURAR_COMWMSPEND = 'N') THEN
        RAISE_APPLICATION_ERROR(-20001,
                                'WMS NÃO SEPARADO, FATURAMENTO NÃO PERMITIDO');
      END IF;

    END IF;

  END IF;

END;
