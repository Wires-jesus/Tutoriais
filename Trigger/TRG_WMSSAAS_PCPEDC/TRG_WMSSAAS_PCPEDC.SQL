CREATE OR REPLACE TRIGGER TRG_WMSSAAS_PCPEDC
BEFORE INSERT OR UPDATE OF DTCANCEL, POSICAO OR DELETE ON PCPEDC
FOR EACH ROW
DECLARE
  V_USA_INTEGRACAO_WMSSAAS  VARCHAR2(1);
  V_INTEGRACAO_EM_ANDAMENTO VARCHAR2(1);
  V_TIPO_ALTERACAO          VARCHAR2(50);
  V_WMS_GERADO              VARCHAR2(1);
  V_PEDIDO                  NUMBER(10);
  V_CODFILIAL               VARCHAR2(2);
  V_SEPARACAO_FINALIZADA    VARCHAR2(1);
BEGIN
  V_TIPO_ALTERACAO          := 'N';
  V_WMS_GERADO              := 'N';
  V_INTEGRACAO_EM_ANDAMENTO := 'N';
  V_PEDIDO := 0;
  V_CODFILIAL := '';
  V_SEPARACAO_FINALIZADA := 'N';


  IF UPDATING THEN
    V_PEDIDO := :OLD.NUMPED;
    V_CODFILIAL := :OLD.CODFILIAL;
  END IF;

  /*** VERIFICANDO SE O WMS DO PEDIDO JÁ FOI GERADO NO CASO DO UPDATE E DO DELETE***/
  IF (UPDATING OR DELETING) AND (:OLD.DTWMS IS NOT NULL) THEN
    V_WMS_GERADO := 'S';

    IF :OLD.DTFINALSEP IS NOT NULL THEN
     V_SEPARACAO_FINALIZADA := 'S';
    END IF;

  END IF;

  /*** VERIFICANDO SE O WMS DO PEDIDO JÁ FOI GERADO NO CASO DO INSERT***/
  IF INSERTING AND :NEW.DTWMS IS NOT NULL THEN
    V_WMS_GERADO := 'S';

    IF :NEW.DTFINALSEP IS NOT NULL THEN
     V_SEPARACAO_FINALIZADA := 'S';
    END IF;

  END IF;

  /*** VERIFICANDO SE ESTÁ SENDO FEITA UM EXCLUSÃO DO PEDIDO NA PCPEDC**/
  IF DELETING THEN
    V_TIPO_ALTERACAO := 'EXCLUSÃO_PEDIDO';
    V_PEDIDO := :OLD.NUMPED;
    V_CODFILIAL := :OLD.CODFILIAL;
  END IF;

  /*** VERIFICANDO SE ESTÁ SENDO FEITA UM INSERÇÃO DO PEDIDO NA PCPEDC - ESSE CENARIO OCORRE NO CANCELAMENTO NA 329 ONDE A PCPEDC E EXCLUIDA E RECRIADA **/
  IF INSERTING THEN
    V_TIPO_ALTERACAO := 'INSERÇÃO_PEDIDO';
    V_PEDIDO := :NEW.NUMPED;
    V_CODFILIAL := :NEW.CODFILIAL;
  END IF;

  /*** VERIFICANDO SE O PROCESSO E DE CANCELAMENTO DO PEDIDO**/
  IF UPDATING AND :OLD.DTCANCEL IS NULL AND :NEW.DTCANCEL IS NOT NULL THEN
    V_TIPO_ALTERACAO := 'CANCELAMENTO_PEDIDO';
  END IF;

  /*** VERIFICANDO SE O PROCESSO E DE TROCA DO PEDIDO**/
  IF UPDATING AND :OLD.POSICAO <> :NEW.POSICAO AND :NEW.POSICAO <> 'M' THEN
    V_TIPO_ALTERACAO := 'ALTERACAO_POSICAO_PEDIDO';
  END IF;


  /*** VERIFICANDO SE A FILIAL USA INTEGRAÇÃO COM WMS SAAS**/
  BEGIN
    SELECT NVL(VALOR, 'N')
      INTO V_USA_INTEGRACAO_WMSSAAS
      FROM PCPARAMINTEGRACAOWMSSAAS
     WHERE PARAMETRO = 'USAINTEGRACAOWMSSAAS'
       AND CODFILIAL = V_CODFILIAL;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      V_USA_INTEGRACAO_WMSSAAS := 'N';
    WHEN OTHERS THEN
      V_USA_INTEGRACAO_WMSSAAS := 'N';
  END;

  /*** VERIFICANDO SE O PEDIDO EM QUESTÃO JÁ TEVE INTEGRAÇÃO EM ANDAMENTO COM WMS SAAS**/
  BEGIN
    SELECT DECODE(COUNT(1), 0, 'N', 'S') INTEGRACAO_EM_ANDAMENTO
      INTO V_INTEGRACAO_EM_ANDAMENTO
      FROM PCINTEGRACAOWMSSAAS
     WHERE NUMDOCWINTHOR = V_PEDIDO
       AND DTCANCELAMENTO IS NULL
       AND DTFILAINTEGRACAO IS NOT NULL
       AND DTINTEGRACAO IS NULL
       AND DTFIMSEPARACAO IS NULL
       AND CODFILIAL = V_CODFILIAL;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      V_INTEGRACAO_EM_ANDAMENTO := 'N';
    WHEN OTHERS THEN
      V_INTEGRACAO_EM_ANDAMENTO := 'N';
  END;

  /*** CASO O PEDIDO JÁ TENHA WMS GERADO OU O PROCESSO DE INTEGRAÇÃO ESTEJA EM ANDAMENTO ENTÃO NÃO SE DEVE PERMITIR:***/
  /*** CANCELAR OU TROCAR A POSICAO DO PEDIDO***/
  /*** EXCLUIR OU INSERIR A PCPEDC COM PEDIDO COM INTEGRAÇÃO COM WMS SAAS ***/
  IF (V_WMS_GERADO = 'S' OR V_INTEGRACAO_EM_ANDAMENTO = 'S')
  AND V_USA_INTEGRACAO_WMSSAAS = 'S'
  AND V_SEPARACAO_FINALIZADA = 'N' THEN

    IF V_TIPO_ALTERACAO = 'EXCLUSÃO_PEDIDO' THEN
      RAISE_APPLICATION_ERROR(-20001,
                              'PROCESSO EM ANDAMENTO NO WMS SAAS EXCLUSÃO DO PEDIDO NÃO PERMITIDO');
    END IF;

    IF V_TIPO_ALTERACAO = 'CANCELAMENTO' THEN
      RAISE_APPLICATION_ERROR(-20001,
                              'PROCESSO EM ANDAMENTO NO WMS SAAS CANCELAMENTO NÃO PERMITIDO');
    END IF;

    IF V_TIPO_ALTERACAO = 'ALTERACAO_POSICAO_PEDIDO' THEN
      RAISE_APPLICATION_ERROR(-20001,
                              'INTEGRAÇÃO DO PEDIDO COM WMS SAAS JÁ INICIADO, TROCA DE POSIÇÃO NÃO PERMITIDA');
    END IF;

    IF V_TIPO_ALTERACAO = 'INSERÇÃO_PEDIDO' THEN
      RAISE_APPLICATION_ERROR(-20001,
                              'PROCESSO EM ANDAMENTO NO WMS SAAS, ESSE PEDIDO JÁ EXISTE NA INTERGRAÇÃO');
    END IF;

  END IF;
END;
