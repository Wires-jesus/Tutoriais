CREATE OR REPLACE TRIGGER TRG_WMSSAAS_PCPEDC
  BEFORE UPDATE OF POSICAO ON PCPEDC
  FOR EACH ROW
DECLARE
  V_USA_INTEGRACAO_WMSSAAS VARCHAR2(2);
BEGIN

  BEGIN
    SELECT NVL(VALOR, 'N')
      INTO V_USA_INTEGRACAO_WMSSAAS
      FROM PCPARAMINTEGRACAOWMSSAAS
     WHERE PARAMETRO = 'USAINTEGRACAOWMSSAAS'
       AND CODFILIAL = :NEW.CODFILIAL;
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      V_USA_INTEGRACAO_WMSSAAS := 'N';
    WHEN OTHERS THEN
      V_USA_INTEGRACAO_WMSSAAS := 'N';
  END;

  IF V_USA_INTEGRACAO_WMSSAAS = 'S' THEN
    /*** NÃO SERÁ PERMITIDO FATURAMENTO DE PEDIDO QUE JÁ ESTEJA NO WMS, MAS QUE AINDA NÃO TENHA SIDO SEPARADO  ***/
    /*** SE O PEDIDO NÃO TIVER WMS GERADO, ESSA VALIDAÇÃO NÃO SERA FEITA, DESSA FORMA PEDIDOS QUE NÃO PASSAM PELO WMS NÃO SÃO IMPACTADOS COM ESSA TRAVA ***/
    IF (:OLD.POSICAO <> :NEW.POSICAO) AND (:NEW.POSICAO = 'F') AND (NVL(:NEW.PERMITEFATURARCOMWMSPEND, 'N') = 'N') THEN

      IF (:NEW.DTFINALSEP IS NULL) AND (:NEW.DTWMS IS NOT NULL) THEN
        RAISE_APPLICATION_ERROR(-20001,
                                'PEDIDO DE VENDA COM WMS GERADO MAS COM SEPARAÇÃO PENDENTE!, FATURAMENTO NÃO PERMITIDO!');
      END IF;

    END IF;

  END IF;

END;