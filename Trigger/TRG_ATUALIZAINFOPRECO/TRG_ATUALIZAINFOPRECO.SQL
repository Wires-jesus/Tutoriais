create or replace trigger trg_atualizainfopreco
  before update on pcembalagem 
  referencing new as new old as old
  for each row
DECLARE
   -- local variables here
BEGIN
   IF UPDATING THEN
   
      IF (NVL(:OLD.PVENDA, 0) <> NVL(:NEW.PVENDA, 0)) THEN
         :NEW.DTEMISSAOETIQ  := NULL;
         :NEW.DTULTALTPVENDA := TRUNC(SYSDATE);
      END IF;
   
      IF (NVL(:OLD.PVENDAATAC, 0) <> NVL(:NEW.PVENDAATAC, 0)) THEN
         :NEW.DTEMISSAOETIQ      := NULL;
         :NEW.DTULTALTPVENDAATAC := TRUNC(SYSDATE);
      END IF;
   
      IF NVL(:OLD.PTABELA, 0) <> NVL(:NEW.PTABELA, 0) THEN
         :NEW.DTULTALTPTABELA := TRUNC(SYSDATE);
      END IF;
   
      IF NVL(:OLD.PTABELAATAC, 0) <> NVL(:NEW.PTABELAATAC, 0) THEN
         :NEW.DTULTALTPTABELAATAC := TRUNC(SYSDATE);
      END IF;
   
      IF ((NVL(:OLD.POFERTA, 0) <> NVL(:NEW.POFERTA, 0)) AND
         (TRUNC(SYSDATE) BETWEEN :NEW.DTOFERTAINI AND :NEW.DTOFERTAFIM)) OR
        
         (TRUNC(NVL(:OLD.DTOFERTAFIM, SYSDATE - 90)) <>
         TRUNC(NVL(:NEW.DTOFERTAFIM, SYSDATE - 90)) AND
         TRUNC(NVL(:NEW.DTOFERTAFIM, SYSDATE - 90)) >= TRUNC(SYSDATE)) THEN
         :NEW.DTEMISSAOETIQPOFERTAS := NULL;
      END IF;
      /*   
      IF ((nvl(:OLD.POFERTAATAC,0) <> nvl(:NEW.POFERTAATAC,0)) AND
         (TRUNC(SYSDATE) BETWEEN :NEW.DTOFERTAATACINI AND
         :NEW.DTOFERTAATACFIM)) OR
         ((TRUNC(:OLD.DTOFERTAATACFIM) <> TRUNC(:NEW.DTOFERTAATACFIM)) AND
         (TRUNC(:NEW.DTOFERTAATACFIM) >= TRUNC(SYSDATE))) THEN
         :NEW.DTEMISSAOETIQPOFERTAATAC := NULL;
      END IF; */
   
   END IF;
END TRG_ATUALIZAINFOPRECO;
