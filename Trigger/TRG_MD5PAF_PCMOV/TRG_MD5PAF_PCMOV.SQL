CREATE OR REPLACE TRIGGER TRG_MD5PAF_PCMOV
  BEFORE UPDATE OR INSERT OR DELETE ON PCMOV
  REFERENCING OLD AS OLD NEW AS NEW
  FOR EACH ROW
DECLARE
  VSPROGRAMA VARCHAR2(48);
BEGIN
  VSPROGRAMA    := F_OBTERNOMEAPLICACAO;
  if (SUBSTR(UPPER(VSPROGRAMA), 1, 2) = 'AU') OR (SUBSTR(UPPER(VSPROGRAMA), 6, 4) = '1452') then
    if ( UPDATING) then
      UPDATE PCMOVCOMPLE SET MD5PAF = NULL WHERE NUMTRANSITEM = :NEW.NUMTRANSITEM;
    end if;

    if ( INSERTING or DELETING ) then
      UPDATE PCTABELASMD5PAF set MD5PAF = NULL WHERE TABELA = 'PCMOV' AND DATA = TRUNC(:NEW.DTMOV);
    end if;
  end if;

END TRG_MD5PAF_PCMOV;

