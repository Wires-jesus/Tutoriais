CREATE OR REPLACE TRIGGER TRG_ECOMMERCE_PCCARREG
 BEFORE
   INSERT OR UPDATE OF DTFECHA
 ON PCCARREG
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN
  IF :NEW.DTFECHA IS NOT NULL THEN
    BEGIN
      INSERT INTO PCECOMMERCEUNILEVERFILA (CODFILIAL, TABELA, ID, DTINCLUSAO)
           SELECT NVL(PCPEDCTEMP.SERIEMANIF, PCPEDCTEMP.CODFILIAL),'SELECTPEDIDO', PCPEDCTEMP.ROWID, SYSDATE
             FROM PCPEDCTEMP
            WHERE PCPEDCTEMP.INTEGRADORA = 29
              AND PCPEDCTEMP.NUMPED > 0
              AND PCPEDCTEMP.NUMPED  IN (SELECT PCPEDC.NUMPED
                                           FROM PCPEDC, PCNFSAID, PCESTCOM
                                          WHERE PCPEDC.NUMPED = PCNFSAID.NUMPED
											AND NOT EXISTS (SELECT NUMTRANSVENDA FROM PCESTCOM WHERE PCESTCOM.NUMTRANSVENDA = PCNFSAID.NUMTRANSVENDA)
											AND PCNFSAID.DTCANCEL IS NULL
											AND PCPEDC.DTCANCEL IS NULL
                                            AND PCPEDC.POSICAO = 'F'
                                            AND PCPEDC.INTEGRADORA = 29
                                            AND PCPEDC.NUMCAR = :NEW.NUMCAR) ;
    EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
      NULL;
    END;                                            
  END IF;
END;
