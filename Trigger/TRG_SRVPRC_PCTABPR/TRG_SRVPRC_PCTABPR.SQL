CREATE OR REPLACE TRIGGER TRG_SRVPRC_PCTABPR
 BEFORE 
 INSERT OR UPDATE
 ON PCTABPR
 REFERENCING OLD AS OLD NEW AS NEW
 FOR EACH ROW 
BEGIN
   :NEW.dtultaltersrvprc := SYSDATE;

  IF ( NVL( :NEW.PVENDA,0)     <> NVL( :OLD.PVENDA,0) ) OR
     ( NVL( :NEW.PVENDAATAC,0) <> NVL( :OLD.PVENDAATAC,0) ) THEN
    UPDATE PCCAIXA SET  DTPROXCARGAPARCIAL = SYSDATE
    WHERE NUMREGIAO = :NEW.NUMREGIAO AND ATUALIZAPRECOFIMVENDA='S';
  END IF;

END;
