CREATE OR REPLACE TRIGGER trg_gravar_pcclient_cgeentaux
 BEFORE
  INSERT OR UPDATE
 ON pcclient
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
DECLARE
  -- local variables here
BEGIN
  :NEW.CGEENTAUX := LPAD(REPLACE(REPLACE(REPLACE(:NEW.CGCENT,'/'),'.'),'-'), 14, '0');
END TRG_GRAVAR_PCCLIENT_CGEENTAUX;