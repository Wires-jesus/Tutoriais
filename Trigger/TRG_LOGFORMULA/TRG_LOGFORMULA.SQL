CREATE OR REPLACE TRIGGER TRG_LOGFORMULA
 BEFORE
  DELETE OR UPDATE
 ON PCFORMULA
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
DECLARE voREGISTRO pclogformula%ROWTYPE;
BEGIN

  voREGISTRO.codformula := :OLD.CODFORMULA;
  voREGISTRO.DESCRICAO  := :OLD.DESCRICAO;
  voREGISTRO.formula := :OLD.FORMULA;
  voREGISTRO.CODTIPOformula := :OLD.CODTIPOFORMULA;  
  voREGISTRO.DTCADASTRO := :OLD.DTCADASTRO;  
  voREGISTRO.CODIGO := :OLD.CODIGO;
  voREGISTRO.CODUSUARIOINC := :OLD.CODUSUARIOINC;
  voREGISTRO.DTALTERACAO := :OLD.DTALTERACAO;
  voREGISTRO.CODUSUARIOALT := :OLD.CODUSUARIOALT;
  
  voREGISTRO.DTLOG := SYSDATE;
  voREGISTRO.CODUSUARIOLOG := :OLD.CODUSUARIOALT;

END;
