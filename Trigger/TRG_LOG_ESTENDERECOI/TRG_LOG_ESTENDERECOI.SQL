CREATE OR REPLACE TRIGGER TRG_LOG_ESTENDERECOI
  BEFORE UPDATE OR INSERT OR DELETE ON PCESTENDERECOI
  FOR EACH ROW
DECLARE
  VSMAQUINA      VARCHAR2(100);
  VSUSUARIO      VARCHAR2(100);
  VSPROGRAMA     VARCHAR2(100);
  VSVERSAO       VARCHAR2(100);
  VSACAO         VARCHAR2(100);
  VVALOR_BLOQ    VARCHAR2(100);
  VVALOR_DESBLOQ VARCHAR2(100);

BEGIN
  VSMAQUINA  := SYS_CONTEXT('USERENV', 'TERMINAL');
  VSUSUARIO  := SYS_CONTEXT('USERENV', 'OS_USER');
  VSPROGRAMA := SYS_CONTEXT('USERENV', 'MODULE');
  VSVERSAO   := SYS_CONTEXT('USERENV', 'MODULE');

  BEGIN
    SELECT VALOR
      INTO VVALOR_BLOQ
      FROM PCPARAMETROWMS
     WHERE NOME = 'BLOQUEIAPRODUTONAARMAZENAGEM'
       AND CODFILIAL IN
           (SELECT CODFILIAL
              FROM PCENDERECO
             WHERE CODENDERECO = NVL(:OLD.CODENDERECO, :NEW.CODENDERECO));
  EXCEPTION
    WHEN OTHERS THEN
      VVALOR_BLOQ := NULL;
  END;

  BEGIN
    SELECT VALOR
      INTO VVALOR_DESBLOQ
      FROM PCPARAMETROWMS
     WHERE NOME = 'DESBLOQUEIAPRODUTONAARMAZENAGEM'
       AND CODFILIAL IN
           (SELECT CODFILIAL
              FROM PCENDERECO
             WHERE CODENDERECO = NVL(:OLD.CODENDERECO, :NEW.CODENDERECO));
  EXCEPTION
    WHEN OTHERS THEN
      VVALOR_DESBLOQ := NULL;
  END;

  IF VSPROGRAMA IS NULL THEN
    VSPROGRAMA := 'Client do oracle desatualizado';
  END IF;

  IF INSERTING THEN
    VSACAO := 'INSERT';
  END IF;
  IF UPDATING THEN
    VSACAO := 'UPDATE';
  END IF;
  IF DELETING THEN
    VSACAO := 'DELETE';
  END IF;

  INSERT INTO PCESTENDERECOILOG
    (DATA,
     CODROTINA,
     CODUMA,
     DTENTRADA,
     CODPROD,
     CODENDERECO,
     NUMLOTE_ANT,
     NUMLOTE,
     DTVAL_ANT,
     DTVAL,
     QT_ANT,
     QT,
     CODROTINAALT,
     DTALT,
     CODFUNCALT,
     MAQUINA,
     USUARIO,
     PROGRAMA,
     VERSAO,
     ACAO)
  VALUES
    (SYSDATE,
     :NEW.CODROTINA,
     :NEW.CODUMA,
     :NEW.DTENTRADA,
     :NEW.CODPROD,
     :NEW.CODENDERECO,
     :OLD.NUMLOTE,
     :NEW.NUMLOTE,
     :OLD.DTVAL,
     :NEW.DTVAL,
     :OLD.QT,
     :NEW.QT,
     :NEW.CODROTINAALT,
     :NEW.DTALT,
     :NEW.CODFUNCALT,
     VSMAQUINA,
     VSUSUARIO,
     VSPROGRAMA,
     VSVERSAO,
     VSACAO);

END TRG_LOG_ESTENDERECOI;
