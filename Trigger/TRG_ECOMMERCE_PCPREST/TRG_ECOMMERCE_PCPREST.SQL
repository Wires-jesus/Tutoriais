CREATE OR REPLACE TRIGGER TRG_ECOMMERCE_PCPREST
 BEFORE
   INSERT OR UPDATE OF DTPAG, DTBAIXA, DTCANCEL
 ON PCPREST
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN
  BEGIN
    INSERT INTO PCECOMMERCEUNILEVERFILA (CODFILIAL, TABELA, ID, DTINCLUSAO)
         SELECT PCECOMMERCEUNILEVERCLIENTE.CODFILIAL
              , 'SELECTLIMITE'
              , PCCLIENT.ROWID
              , SYSDATE
           FROM PCECOMMERCEUNILEVERCLIENTE
              , PCCLIENT 
          WHERE NVL(PCCLIENT.SITUACAOECOMMERCEUNILEVER,'NAO') <> 'NAO'
            AND PCECOMMERCEUNILEVERCLIENTE.CODCLI = :NEW.CODCLI 
            AND PCECOMMERCEUNILEVERCLIENTE.CODFILIAL = :NEW.CODFILIAL
            AND PCCLIENT.CODCLI = :NEW.CODCLI ; 
  EXCEPTION
  WHEN DUP_VAL_ON_INDEX THEN
     NULL;
  END;
END;
