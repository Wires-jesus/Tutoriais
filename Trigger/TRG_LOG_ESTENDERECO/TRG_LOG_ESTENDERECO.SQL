CREATE OR REPLACE TRIGGER TRG_LOG_ESTENDERECO
  BEFORE UPDATE OR INSERT OR DELETE ON PCESTENDERECO
  FOR EACH ROW
DECLARE
  VSMAQUINA      VARCHAR2(100);
  VSUSUARIO      VARCHAR2(100);
  VSPROGRAMA     VARCHAR2(100);
  VSVERSAO       VARCHAR2(100);
  VSACAO         VARCHAR2(100);
  VVALOR_BLOQ    VARCHAR2(100);
  VVALOR_DESBLOQ VARCHAR2(100);

BEGIN
  VSMAQUINA  := SYS_CONTEXT('USERENV', 'TERMINAL');
  VSUSUARIO  := SYS_CONTEXT('USERENV', 'OS_USER');
  VSPROGRAMA := SYS_CONTEXT('USERENV', 'MODULE');
  VSVERSAO   := SYS_CONTEXT('USERENV', 'MODULE');

  BEGIN
    SELECT VALOR
      INTO VVALOR_BLOQ
      FROM PCPARAMETROWMS
     WHERE NOME = 'BLOQUEIAPRODUTONAARMAZENAGEM'
       AND CODFILIAL IN
           (SELECT CODFILIAL
              FROM PCENDERECO
             WHERE CODENDERECO = NVL(:OLD.CODENDERECO, :NEW.CODENDERECO));
  EXCEPTION
    WHEN OTHERS THEN
      VVALOR_BLOQ := NULL;
  END;

  BEGIN
    SELECT VALOR
      INTO VVALOR_DESBLOQ
      FROM PCPARAMETROWMS
     WHERE NOME = 'DESBLOQUEIAPRODUTONAARMAZENAGEM'
       AND CODFILIAL IN
           (SELECT CODFILIAL
              FROM PCENDERECO
             WHERE CODENDERECO = NVL(:OLD.CODENDERECO, :NEW.CODENDERECO));
  EXCEPTION
    WHEN OTHERS THEN
      VVALOR_DESBLOQ := NULL;
  END;

  IF VSPROGRAMA IS NULL THEN
    VSPROGRAMA := 'Client do oracle desatualizado';
  END IF;

  IF INSERTING THEN
    VSACAO := 'INSERT';
  END IF;
  IF UPDATING THEN
    VSACAO := 'UPDATE';
  END IF;
  IF DELETING THEN
    VSACAO := 'DELETE';
  END IF;

  INSERT INTO PCESTENDERECOLOG
    (DATA,
     CODPROD,
     QT_ANT,
     QT,
     QTBLOQUEADA_ANT,
     QTBLOQUEADA,
     QTPENDSAIDA_ANT,
     QTPENDSAIDA,
     QTPENDENTRADA_ANT,
     QTPENDENTRADA,
     DTVAL_ANT,
     DTVAL,
     NUMLOTE,
     CODENDERECO,
     CODIGOUMA,
     QTULTINVENT,
     DTULTINVENT,
     DTFABRICACAO,
     DTENTRADA,
     MAQUINA,
     USUARIO,
     PROGRAMA,
     VERSAO,
     ACAO,
     BLOQUEIAPRODARMAZENAGEM,
     DESBLOQUEIAPRODARMAZENAGEM)
  VALUES
    (SYSDATE,
     NVL(:OLD.CODPROD,:NEW.CODPROD),
     :OLD.QT,
     :NEW.QT,
     :OLD.QTBLOQUEADA,
     :NEW.QTBLOQUEADA,
     :OLD.QTPENDSAIDA,
     :NEW.QTPENDSAIDA,
     :OLD.QTPENDENTRADA,
     :NEW.QTPENDENTRADA,
     :OLD.DTVAL,
     :NEW.DTVAL,
     :OLD.NUMLOTE,
     NVL(:OLD.CODENDERECO, :NEW.CODENDERECO),
     :OLD.CODIGOUMA,
     :OLD.QTULTINVENT,
     :OLD.DTULTINVENT,
     :OLD.DTFABRICACAO,
     :OLD.DTENTRADA,
     VSMAQUINA,
     VSUSUARIO,
     VSPROGRAMA,
     VSVERSAO,
     VSACAO,
     VVALOR_BLOQ,
     VVALOR_DESBLOQ);

END TRG_LOG_ESTENDERECO;
