CREATE OR REPLACE TRIGGER TRG_PCPEDC_TXENTREGA
BEFORE INSERT OR UPDATE ON PCPEDC
REFERENCING NEW AS NEW OLD AS OLD
FOR EACH ROW
DECLARE
  VSCALCTAXAENTINCNF  VARCHAR2(1);
  VROW_REGIAO         PCREGIAO%ROWTYPE;
  VSOWNER             VARCHAR2(100);
  VSNAME              VARCHAR2(100);
  VNLINE              NUMBER;
  VSTYPE              VARCHAR2(100);
  VNFRETE             NUMBER;
  VSCLIENTISENTOTXENT PCCLIENT.ISENTOTXENTREGA%TYPE;
  VSPROGRAMA          VARCHAR2(60);
BEGIN
  OWA_UTIL.WHO_CALLED_ME(VSOWNER, VSNAME, VNLINE, VSTYPE);
  VSPROGRAMA := F_OBTERNOMEAPLICACAO;

  IF NOT (NVL(VSNAME, 'X') LIKE '%FATURAMENTO%')
  THEN

    BEGIN
      SELECT NVL(VALOR, 'N') into VSCALCTAXAENTINCNF
        FROM PCPARAMFILIAL
     WHERE NOME = 'CALCTAXAENTINCNF'
       AND CODFILIAL = '99';
    EXCEPTION
      WHEN OTHERS THEN
      VSCALCTAXAENTINCNF := 'N';
    END;
  
    BEGIN
      SELECT PCREGIAO.*
        INTO VROW_REGIAO
        FROM PCREGIAO
       WHERE PCREGIAO.NUMREGIAO = :NEW.NUMREGIAO;
    EXCEPTION
      WHEN OTHERS THEN
        NULL;
    END;
  
    BEGIN
      SELECT NVL(PCCLIENT.ISENTOTXENTREGA, 'N')
        INTO VSCLIENTISENTOTXENT
        FROM PCCLIENT
       WHERE PCCLIENT.CODCLI = NVL(:OLD.CODCLI, :NEW.CODCLI);
    EXCEPTION
      WHEN OTHERS THEN
        VSCLIENTISENTOTXENT := 'N';
    END;
  
    IF (VSCALCTAXAENTINCNF = 'S') AND
       (:NEW.VLATEND < VROW_REGIAO.VLMINVENDA) AND
       (VSCLIENTISENTOTXENT = 'N')
    THEN
      IF (INSERTING) AND (NOT (VSPROGRAMA LIKE '%316%'))
      THEN
        :NEW.VLFRETE  := NVL(:NEW.VLFRETE, 0) + NVL(VROW_REGIAO.VLTXENT, 0);
        :NEW.VLTXENT  := NVL(VROW_REGIAO.VLTXENT, 0);
        :NEW.VLATEND  := :NEW.VLATEND + NVL(VROW_REGIAO.VLTXENT, 0);
        :NEW.VLTABELA := :NEW.VLTABELA + NVL(VROW_REGIAO.VLTXENT, 0);
        :NEW.VLTOTAL  := :NEW.VLTOTAL + NVL(VROW_REGIAO.VLTXENT, 0);
      ELSIF UPDATING
      THEN
        VNFRETE := ((CASE
                     WHEN (NVL(:NEW.VLFRETE, 0) <= 0) THEN
                      NVL(:OLD.VLFRETE, 0)
                     ELSE
                      NVL(:NEW.VLFRETE, 0)
                   END) - NVL(:OLD.VLTXENT, 0)) +
                   NVL(VROW_REGIAO.VLTXENT, 0);
      
        :NEW.VLFRETE  := VNFRETE;
        :NEW.VLTXENT  := NVL(VROW_REGIAO.VLTXENT, 0);
        :NEW.VLATEND  := (:NEW.VLATEND - (NVL(:OLD.VLTXENT, 0))) +
                         VROW_REGIAO.VLTXENT;
        :NEW.VLTABELA := (:NEW.VLTABELA - (NVL(:OLD.VLTXENT, 0))) +
                         VROW_REGIAO.VLTXENT;
      
        IF (((:NEW.VLTOTAL - NVL(:OLD.VLTXENT, 0)) + VROW_REGIAO.VLTXENT) >
           :OLD.VLTOTAL) AND (:NEW.VLTOTAL < VROW_REGIAO.VLMINVENDA)
        THEN
          :NEW.VLTOTAL := ((NVL(:OLD.VLTOTAL, 0) - NVL(:OLD.VLTXENT, 0)) +
                          NVL(VROW_REGIAO.VLTXENT, 0));
        END IF;
      
      END IF;
    ELSIF (:NEW.VLATEND >= VROW_REGIAO.VLMINVENDA) OR
          (VSCLIENTISENTOTXENT = 'S')
    THEN
      IF UPDATING
      THEN
        :NEW.VLATEND  := :NEW.VLATEND - NVL(:OLD.VLTXENT, 0);
        :NEW.VLTABELA := :NEW.VLTABELA - NVL(:OLD.VLTXENT, 0);
        :NEW.VLTOTAL  := :NEW.VLTOTAL - NVL(:OLD.VLTXENT, 0);
        :NEW.VLFRETE  := NVL(:NEW.VLFRETE, NVL(:OLD.VLFRETE, 0)) -
                         NVL(:OLD.VLTXENT, 0);
        :NEW.VLTXENT  := 0;
      END IF;
    END IF;
  END IF;
END;
