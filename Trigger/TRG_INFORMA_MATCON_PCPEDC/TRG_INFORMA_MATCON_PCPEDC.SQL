CREATE OR REPLACE TRIGGER TRG_INFORMA_MATCON_PCPEDC
 BEFORE
  INSERT
 ON PCPEDC
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
DECLARE
 vPROGRAM VARCHAR2(48);
BEGIN
  IF INSERTING THEN
    IF (:NEW.MATERIALDECONSTRUCAO IS NULL) THEN
      vprogram := F_OBTERNOMEAPLICACAO;
      IF (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4116%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4136%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCINF2030MG%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCINF2030MG%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4182%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4159%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4139%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4109%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4153%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4192%') OR
		 (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS4158%')
      THEN
         :NEW.MATERIALDECONSTRUCAO := 'S';
      ELSE
         IF (UPPER(TRIM(vPROGRAM)) LIKE '%PCSIS%') THEN
            :NEW.MATERIALDECONSTRUCAO := 'N';
         END IF;
      END IF;
    END IF; 
  END IF;
END;