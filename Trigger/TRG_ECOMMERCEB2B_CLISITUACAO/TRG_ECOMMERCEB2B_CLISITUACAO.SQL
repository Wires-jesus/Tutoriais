CREATE OR REPLACE TRIGGER TRG_ECOMMERCEB2B_CLISITUACAO
   AFTER INSERT OR DELETE OR UPDATE OF SITUACAOECOMMERCEB2B ON PCECOMMERCEB2BCLIENTE
   REFERENCING NEW AS NEW OLD AS OLD FOR EACH ROW
DECLARE 
   vsENVIARREGISTRO VARCHAR2(1);
   VROWIDCLI VARCHAR2(50);
BEGIN
   vsENVIARREGISTRO := 'N';
   --Se estiver usando os status: PEN, APV, RPV
   IF (NVL(:NEW.SITUACAOECOMMERCEB2B,'PEN') <> 'PEN') THEN
      vsENVIARREGISTRO := 'S';
   END IF;
   
   IF vsENVIARREGISTRO = 'S' THEN
      IF :NEW.ROWIDCLI IS NULL THEN
         SELECT PCCLIENT.ROWID
         INTO VROWIDCLI
         FROM PCCLIENT
         WHERE PCCLIENT.CODCLI = :NEW.CODCLI;
      ELSE
         VROWIDCLI := :NEW.ROWIDCLI;
      END IF;  
      
      BEGIN --Inserindo registros a ser enviado para todas as configurações validas
         INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO)
         VALUES (:NEW.CODFILIAL
               , :NEW.TIPOINTEGRACAO
               , 'CLIENTE'
               , VROWIDCLI
               , SYSDATE );
      EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
         NULL;
      END;
   END IF;
END;
