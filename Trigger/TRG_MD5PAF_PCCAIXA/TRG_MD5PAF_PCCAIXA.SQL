CREATE OR REPLACE TRIGGER TRG_MD5PAF_PCCAIXA
  BEFORE UPDATE OR INSERT OR DELETE ON PCCAIXA
  REFERENCING OLD AS OLD NEW AS NEW
  FOR EACH ROW
BEGIN
  if (F_LIMPA_MD5PAF = 'S') then
    if ( UPDATING) then
      :NEW.MD5PAF := NULL;
	  end if;

    if ( INSERTING or DELETING ) then
	    UPDATE PCTABELASMD5PAF set MD5PAF = NULL WHERE TABELA = 'PCCAIXA';
	  end if;
  end if;

END TRG_MD5PAF_PCCAIXA;
