CREATE OR REPLACE TRIGGER TRG_PCTRIBUTACAO_UPD
BEFORE UPDATE ON PCTRIBUTACAO
FOR EACH ROW
BEGIN
  :NEW.DTULTALTER := SYSDATE;

  IF :NEW.STATUS = 'I' AND :OLD.STATUS != 'I' THEN
    :NEW.DTINATIVACAO := SYSDATE;
    
    UPDATE PCTRIBUTACAO_EXCECAO
    SET DTINATIVACAO = SYSDATE
    WHERE ID = :OLD.ID;
    
    UPDATE PCTRIBUTACAO_FILTRO_NCM
    SET DTINATIVACAO = SYSDATE
    WHERE ID = :OLD.ID;
    
    UPDATE PCTRIBUTACAO_FILTRO_PRODUTO
    SET DTINATIVACAO = SYSDATE
    WHERE ID = :OLD.ID;

  ELSIF :NEW.STATUS = 'A' AND :OLD.STATUS != 'A' THEN
    :NEW.DTINATIVACAO := NULL;
    
    UPDATE PCTRIBUTACAO_EXCECAO
    SET DTINATIVACAO = NULL
    WHERE ID = :OLD.ID;
    
    UPDATE PCTRIBUTACAO_FILTRO_NCM
    SET DTINATIVACAO = NULL
    WHERE ID = :OLD.ID;
    
    UPDATE PCTRIBUTACAO_FILTRO_PRODUTO
    SET DTINATIVACAO = NULL
    WHERE ID = :OLD.ID;

  END IF;
END;