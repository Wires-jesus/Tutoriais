CREATE OR REPLACE TRIGGER TRG_WMSSAAS_PCNFSAID
BEFORE UPDATE OF DTCANCEL ON PCNFSAID
FOR EACH ROW
DECLARE
    V_USAINTEGRACAOWMSSAAS VARCHAR2(1);
    V_EXISTEPEDIDOCOMWMSGERADO VARCHAR2(1);
BEGIN

    BEGIN
        SELECT NVL(VALOR, 'N')
        INTO V_USAINTEGRACAOWMSSAAS
        FROM PCPARAMINTEGRACAOWMSSAAS
        WHERE PARAMETRO = 'USAINTEGRACAOWMSSAAS' AND CODFILIAL = :NEW.CODFILIAL;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            V_USAINTEGRACAOWMSSAAS := 'N';
        WHEN OTHERS THEN
            V_USAINTEGRACAOWMSSAAS := 'N';
    END;

    /***VERIFICA SE A NOTA ESTÁ VINCULADA A UM PEDIDO COM WMS GERADO***/
    BEGIN
        SELECT DECODE(COUNT(1),0, 'N', 'S')
        INTO V_EXISTEPEDIDOCOMWMSGERADO
        FROM PCPEDC
        WHERE NUMNOTA = :NEW.NUMNOTA
        AND DTWMS IS NOT NULL
        AND DTCANCEL IS NULL
        AND CODFILIAL = :NEW.CODFILIAL;
    EXCEPTION
        WHEN NO_DATA_FOUND THEN
            V_EXISTEPEDIDOCOMWMSGERADO := 'N';
        WHEN OTHERS THEN
            V_EXISTEPEDIDOCOMWMSGERADO := 'N';
    END;

    IF V_USAINTEGRACAOWMSSAAS = 'S' AND V_EXISTEPEDIDOCOMWMSGERADO = 'S' THEN
      IF :OLD.DTCANCEL IS NULL AND :NEW.DTCANCEL IS NOT NULL AND :OLD.DTWMS IS NOT NULL THEN
        RAISE_APPLICATION_ERROR(-20001, 'PEDIDO VINCULADO A ESSA NOTA ESTÁ COM WMS GERADO');
      END IF;
    END IF;

END;
