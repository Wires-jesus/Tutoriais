CREATE OR REPLACE TRIGGER TRG_ECOMMERCEB2B_PCEMBALAGEM
   AFTER INSERT OR DELETE OR UPDATE OF QTUNIT, EXCLUIDO, CODAUXILIAR ON PCEMBALAGEM
   REFERENCING NEW AS NEW OLD AS OLD FOR EACH ROW
DECLARE
   vPOSSUIPRECOMAIORZERO INTEGER;
   vPOSSUITIPOCONVERSAO INTEGER;
BEGIN

   BEGIN
      SELECT COUNT(1)
      INTO vPOSSUITIPOCONVERSAO
      FROM PCECOMMERCEB2BPRODUTO
      WHERE CODPROD = :OLD.CODPROD
      AND CODFILIAL = :OLD.CODFILIAL
      AND CODAUXILIAR = :OLD.CODAUXILIAR
      AND TIPOCONVERSAO = 'EM'
      AND ROWNUM = 1;
    EXCEPTION WHEN OTHERS THEN
      vPOSSUITIPOCONVERSAO := 0;
   END;

   IF vPOSSUITIPOCONVERSAO > 0 THEN
       IF UPDATING THEN
           IF :OLD.CODAUXILIAR <> :NEW.CODAUXILIAR OR :OLD.QTUNIT <> :NEW.QTUNIT THEN
              BEGIN
                 UPDATE PCECOMMERCEB2BPRODUTO SET CODAUXILIAR = :NEW.CODAUXILIAR,
                          FATORCONVERSAO = :NEW.QTUNIT
              WHERE PCECOMMERCEB2BPRODUTO.CODPROD = :OLD.CODPROD
                   AND PCECOMMERCEB2BPRODUTO.CODFILIAL = :OLD.CODFILIAL
                   AND PCECOMMERCEB2BPRODUTO.CODAUXILIAR = :OLD.CODAUXILIAR;
              EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
                 NULL;
              END;
           END IF;
       END IF;

       IF DELETING THEN
          BEGIN

      DELETE FROM PCECOMMERCEB2BPRODUTO
             WHERE PCECOMMERCEB2BPRODUTO.CODPROD = :OLD.CODPROD
               AND PCECOMMERCEB2BPRODUTO.CODFILIAL = :OLD.CODFILIAL
               AND PCECOMMERCEB2BPRODUTO.CODAUXILIAR = :OLD.CODAUXILIAR;
          EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
             NULL;
       NULL;
          END;
       END IF;
   END IF;

   BEGIN
      SELECT DISTINCT COUNT(1)
      INTO vPOSSUIPRECOMAIORZERO
      FROM PCECOMMERCEB2BPRODUTO,
        PCPRODUT,
        PCTABPR,
        PCECOMMERCEB2B C
      WHERE PCPRODUT.CODPROD = PCECOMMERCEB2BPRODUTO.CODPROD
        AND PCTABPR.CODPROD = PCECOMMERCEB2BPRODUTO.CODPROD
        AND C.TIPOINTEGRACAO = PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO
        AND C.NUMREGIAO = PCTABPR.NUMREGIAO
        AND C.CODFILIAL = PCECOMMERCEB2BPRODUTO.CODFILIAL
        AND C.CODFILIAL = NVL(:NEW.CODFILIAL,:OLD.CODFILIAL)
        AND PCECOMMERCEB2BPRODUTO.CODPROD = :NEW.CODPROD
        AND NVL(PCTABPR.PVENDA1, 0) > 0
        AND NVL(PCTABPR.PVENDA2, 0) > 0
        AND NVL(PCTABPR.PVENDA3, 0) > 0
        AND NVL(PCTABPR.PVENDA4, 0) > 0
        AND NVL(PCTABPR.PVENDA5, 0) > 0
        AND NVL(PCTABPR.PVENDA6, 0) > 0
        AND NVL(PCTABPR.PVENDA7, 0) > 0
        AND ROWNUM = 1;
   EXCEPTION WHEN OTHERS THEN
      vPOSSUIPRECOMAIORZERO := 0;
   END;

   --Enviar estoque
   IF (UPDATING AND (NVL(:NEW.QTUNIT,0) <> NVL(:OLD.QTUNIT,0))) THEN
      BEGIN
         INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO, OBSERVACAO)
            SELECT PCECOMMERCEB2BPRODUTO.CODFILIAL
                 , PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO
                 , 'ESTOQUE'
                 , PCPRODUT.ROWID
                 , SYSDATE
                 , CASE WHEN NVL(:OLD.QTUNIT,0) > 1 THEN PCPRODUT.CODAUXILIAR||'C'||NVL(:OLD.QTUNIT,0)
                      ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
            FROM PCECOMMERCEB2BPRODUTO
               , PCPRODUT
            WHERE PCPRODUT.CODPROD = :NEW.CODPROD
              AND PCECOMMERCEB2BPRODUTO.CODFILIAL = :NEW.CODFILIAL
              AND PCECOMMERCEB2BPRODUTO.CODPROD = PCPRODUT.CODPROD
              AND PCECOMMERCEB2BPRODUTO.TIPOCONVERSAO = 'EM'
              AND PCECOMMERCEB2BPRODUTO.CODAUXILIAR = :NEW.CODAUXILIAR
              AND PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO = PCPRODUT.TIPOINTEGRACAOB2B;

         IF vPOSSUIPRECOMAIORZERO > 0 THEN
            INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO, OBSERVACAO)
               SELECT PCECOMMERCEB2BPRODUTO.CODFILIAL
                    , PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO
                    , 'PRECO'
                    , PCPRODUT.ROWID
                    , SYSDATE
                    , CASE WHEN NVL(:OLD.QTUNIT,0) > 1 THEN PCPRODUT.CODAUXILIAR||'C'||NVL(:OLD.QTUNIT,0)
                         ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
               FROM PCECOMMERCEB2BPRODUTO
                  , PCPRODUT
               WHERE PCPRODUT.CODPROD = :NEW.CODPROD
                 AND PCECOMMERCEB2BPRODUTO.CODFILIAL = :NEW.CODFILIAL
                 AND PCECOMMERCEB2BPRODUTO.CODPROD = PCPRODUT.CODPROD
                 AND PCECOMMERCEB2BPRODUTO.TIPOCONVERSAO = 'EM'
                 AND PCECOMMERCEB2BPRODUTO.CODAUXILIAR = :NEW.CODAUXILIAR
                 AND PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO = PCPRODUT.TIPOINTEGRACAOB2B;
         END IF;
      EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
         NULL;
      END;
   END IF;

   BEGIN
      INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO, OBSERVACAO)
       SELECT PCECOMMERCEB2BPRODUTO.CODFILIAL
          , PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO
          , 'ESTOQUE'
          , PCPRODUT.ROWID
          , SYSDATE
          , NULL
       FROM PCECOMMERCEB2BPRODUTO
        , PCPRODUT
       WHERE PCPRODUT.CODPROD = :NEW.CODPROD
         AND PCECOMMERCEB2BPRODUTO.CODFILIAL = :NEW.CODFILIAL
         AND PCECOMMERCEB2BPRODUTO.CODPROD = PCPRODUT.CODPROD
         AND PCECOMMERCEB2BPRODUTO.TIPOCONVERSAO = 'EM'
         AND PCECOMMERCEB2BPRODUTO.CODAUXILIAR = :NEW.CODAUXILIAR
         AND PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO = PCPRODUT.TIPOINTEGRACAOB2B;

      IF vPOSSUIPRECOMAIORZERO > 0 THEN
       INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO, OBSERVACAO)
        SELECT PCECOMMERCEB2BPRODUTO.CODFILIAL
           , PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO
           , 'PRECO'
           , PCPRODUT.ROWID
           , SYSDATE
           , NULL
        FROM PCECOMMERCEB2BPRODUTO
           , PCPRODUT
        WHERE PCPRODUT.CODPROD = :NEW.CODPROD
          AND PCECOMMERCEB2BPRODUTO.CODFILIAL = :NEW.CODFILIAL
          AND PCECOMMERCEB2BPRODUTO.CODPROD = PCPRODUT.CODPROD
          AND PCECOMMERCEB2BPRODUTO.TIPOCONVERSAO = 'EM'
          AND PCECOMMERCEB2BPRODUTO.CODAUXILIAR = :NEW.CODAUXILIAR
          AND PCECOMMERCEB2BPRODUTO.TIPOINTEGRACAO = PCPRODUT.TIPOINTEGRACAOB2B;
      END IF;
     EXCEPTION WHEN DUP_VAL_ON_INDEX THEN
      NULL;
   END;
END;
