CREATE OR REPLACE TRIGGER TRG_PCTRIBUTACAO_F_NCM_INS
BEFORE INSERT ON PCTRIBUTACAO_FILTRO_NCM
FOR EACH ROW
DECLARE
  v_Status VARCHAR2(1);
BEGIN
  BEGIN
    SELECT STATUS
      INTO v_Status
      FROM PCTRIBUTACAO
     WHERE CODIGO_TRIBUTACAO = :NEW.CODIGO_TRIBUTACAO;
  EXCEPTION
    WHEN OTHERS THEN
      v_Status := 'I';
  END;

  IF v_Status = 'I' THEN
    :NEW.DTINATIVACAO := SYSDATE;
  END IF;
END;