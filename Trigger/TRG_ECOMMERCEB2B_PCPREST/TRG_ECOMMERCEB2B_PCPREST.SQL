CREATE OR REPLACE TRIGGER TRG_ECOMMERCEB2B_PCPREST
 BEFORE
   INSERT OR UPDATE OF DTPAG, DTBAIXA, DTCANCEL
 ON PCPREST
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
BEGIN
  BEGIN
    INSERT INTO PCECOMMERCEB2BFILA (CODFILIAL, TIPOINTEGRACAO, TABELA, ID, DTINCLUSAO)
         SELECT PCECOMMERCEB2BCLIENTE.CODFILIAL
              , PCECOMMERCEB2BCLIENTE.TIPOINTEGRACAO
              , 'LIMITE'
              , PCCLIENT.ROWID
              , SYSDATE
           FROM PCECOMMERCEB2BCLIENTE
              , PCCLIENT 
          WHERE NVL(PCECOMMERCEB2BCLIENTE.SITUACAOECOMMERCEB2B,'NAO') <> 'NAO'
            AND PCECOMMERCEB2BCLIENTE.CODCLI = PCCLIENT.CODCLI
            AND PCECOMMERCEB2BCLIENTE.CODCLI = :NEW.CODCLI 
            AND PCECOMMERCEB2BCLIENTE.CODFILIAL = :NEW.CODFILIAL; 
  EXCEPTION
  WHEN DUP_VAL_ON_INDEX THEN
     NULL;
  END;
END;
