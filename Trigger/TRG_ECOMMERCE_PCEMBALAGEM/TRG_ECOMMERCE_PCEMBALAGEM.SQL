CREATE OR REPLACE TRIGGER trg_ecommerce_pcembalagem
 AFTER
   INSERT OR DELETE OR UPDATE OF usaecommerceunilever, excluido, qtunit
 ON pcembalagem
REFERENCING NEW AS NEW OLD AS OLD
 FOR EACH ROW
DECLARE vsENVIARREGISTRO VARCHAR2(1);
  vsOBSERVACAO PCECOMMERCEUNILEVERFILA.OBSERVACAO%TYPE;
  vsCODAUXILIAR VARCHAR2(30);
  vbOBSERVACAO VARCHAR2(1);
BEGIN
  vbOBSERVACAO := 'N';
  vsENVIARREGISTRO := 'N';

  IF (:NEW.QTUNIT <> :OLD.QTUNIT) AND (NVL(:NEW.USAECOMMERCEUNILEVER,'N') = 'S') THEN
      vsENVIARREGISTRO := 'S';
      vbOBSERVACAO := 'S';
  ELSIF (NVL(:NEW.EXCLUIDO, 'S') = 'S') AND (NVL(:NEW.USAECOMMERCEUNILEVER,'N') = 'S') THEN
      vsENVIARREGISTRO := 'S';
      vbOBSERVACAO := 'S';
  END IF;
  
  IF vbOBSERVACAO = 'N' THEN

      IF (NVL(:OLD.USAECOMMERCEUNILEVER,'N')='S') AND (NVL(:NEW.USAECOMMERCEUNILEVER,'N')='N') THEN
         vsENVIARREGISTRO := 'S';
         vbOBSERVACAO := 'S';
      ELSIF (NVL(:OLD.USAECOMMERCEUNILEVER,'N')='N') AND (NVL(:NEW.USAECOMMERCEUNILEVER,'N')='S') THEN
         vsENVIARREGISTRO := 'S';
         vbOBSERVACAO := 'N';
      END IF;


  END IF;


  
  vsOBSERVACAO := null;
  IF vbOBSERVACAO = 'S' THEN
    BEGIN
      SELECT CASE WHEN :OLD.QTUNIT > 1 THEN TO_CHAR(PCPRODUT.CODAUXILIAR) || 'C' || TO_CHAR(:OLD.QTUNIT) ELSE TO_CHAR(PCPRODUT.CODAUXILIAR) END
             AS CODAUXILIAR
        INTO vsCODAUXILIAR
        FROM PCPRODFILIAL, PCECOMMERCEUNILEVER, PCPRODUT
       WHERE 1=1
         AND PCPRODFILIAL.CODPROD          = :OLD.CODPROD
         AND PCPRODFILIAL.CODFILIAL        = :OLD.CODFILIAL
         AND PCECOMMERCEUNILEVER.CODFILIAL = PCPRODFILIAL.CODFILIAL
         AND PCPRODUT.CODPROD = :OLD.CODPROD
         AND ROWNUM = 1;
    EXCEPTION
      WHEN OTHERS THEN
        vsCODAUXILIAR := '';
    END;
    vsOBSERVACAO := vsCODAUXILIAR;
    IF NVL(:OLD.USAECOMMERCEUNILEVER,'N') = 'S' AND NVL(:NEW.USAECOMMERCEUNILEVER,'N') = 'N' THEN
       vsOBSERVACAO := vsOBSERVACAO || ';N';
    END IF;
  END IF;
  
  IF vsENVIARREGISTRO = 'S' THEN
    BEGIN --Inserindo registros a ser enviado para todas as configurações validas
      --Enviar estoque
      INSERT INTO PCECOMMERCEUNILEVERFILA (CODFILIAL, TABELA, ID, DTINCLUSAO, OBSERVACAO)
           SELECT PCECOMMERCEUNILEVER.CODFILIAL
                , 'SELECTESTOQUE'
                , PCPRODUT.ROWID
                , SYSDATE
                ,vsOBSERVACAO
             FROM PCECOMMERCEUNILEVER
                , PCPRODUT
            WHERE PCPRODUT.CODPROD = :NEW.CODPROD
              AND PCPRODUT.USAECOMMERCEUNILEVER = 'S'
              AND PCECOMMERCEUNILEVER.CODFILIAL = :NEW.CODFILIAL;
    EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
      NULL;
    END;  
           
    BEGIN --Inserindo registros a ser enviado para todas as configurações validas
      --Enviar preço
      INSERT INTO PCECOMMERCEUNILEVERFILA (CODFILIAL, TABELA, ID, DTINCLUSAO, OBSERVACAO)
           SELECT PCECOMMERCEUNILEVER.CODFILIAL
                , 'SELECTPRECO'
                , PCPRODUT.ROWID
                , SYSDATE
                ,vsOBSERVACAO
             FROM PCECOMMERCEUNILEVER
                , PCPRODUT
            WHERE PCPRODUT.CODPROD = :NEW.CODPROD
              AND PCPRODUT.USAECOMMERCEUNILEVER = 'S'
              AND PCECOMMERCEUNILEVER.CODFILIAL = :NEW.CODFILIAL;
    EXCEPTION
    WHEN DUP_VAL_ON_INDEX THEN
      NULL;
    END;
  END IF;  
END;
