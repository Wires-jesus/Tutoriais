CREATE OR REPLACE FUNCTION WMS_RETORNATAMANHOSCAIXAS(PNUMCAR PCCARREG.NUMCAR%TYPE,
                                  PNUMPED IN NUMBER DEFAULT 0)
  RETURN SYS_REFCURSOR
AS
  TBL_RC SYS_REFCURSOR;
  VNUMPED PCPEDC.NUMPED%TYPE;
  VNUMCAR PCPEDC.NUMCAR%TYPE;
BEGIN

  /* Verifica quantidade de caixas por tamnho por carregamento ou pedido
  Data da ultima alteração: 08/06/2025 */

  VNUMPED    := NVL(PNUMPED, 0);
  VNUMCAR    := NVL(PNUMCAR, 0);

  IF VNUMCAR > 0 THEN
    OPEN TBL_RC FOR
      SELECT C.TAMANHO, COUNT(C.TAMANHO) QTDE
        FROM PCWMSVINCULOCAIXA V
       INNER JOIN PCWMSCAIXA C ON V.CODCAIXA = C.CODCAIXA
       WHERE V.NUMCAR = VNUMCAR
         AND V.DTESTORNO IS NULL
       GROUP BY C.TAMANHO;
     RETURN TBL_RC;
  ELSE
    OPEN TBL_RC FOR
      SELECT C.TAMANHO, COUNT(C.TAMANHO) QTDE
        FROM PCWMSVINCULOCAIXA V
       INNER JOIN PCWMSCAIXA C ON V.CODCAIXA = C.CODCAIXA
       WHERE V.NUMPED = VNUMPED
         AND V.DTESTORNO IS NULL
       GROUP BY C.TAMANHO;
    RETURN TBL_RC;
  END IF;



END;