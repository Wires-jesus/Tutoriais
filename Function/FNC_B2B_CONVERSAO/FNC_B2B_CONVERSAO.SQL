CREATE OR REPLACE FUNCTION FNC_B2B_CONVERSAO(P_CODPROD NUMBER, 
                                             P_CODFILIAL VARCHAR2,
                                             P_TIPOCONVERSAO VARCHAR2, 
                                             P_FATORCONVERSAO NUMBER) RETURN NUMBER 
IS
  vFator       NUMBER;
  vCodAuxiliar PCECOMMERCEB2BPRODUTO.CODAUXILIAR%TYPE;
  vTipo        PCECOMMERCEB2BPRODUTO.TIPOCONVERSAO%TYPE;
BEGIN
   SELECT TIPOCONVERSAO, 
          FATORCONVERSAO, 
          CODAUXILIAR
   INTO vTipo, 
        vFator, 
        vCodAuxiliar
   FROM PCECOMMERCEB2BPRODUTO
   WHERE CODPROD = P_CODPROD
     AND CODFILIAL = P_CODFILIAL
     AND FATORCONVERSAO = P_FATORCONVERSAO
     AND TIPOCONVERSAO = P_TIPOCONVERSAO;
  
   IF vTipo = 'UN' THEN
      SELECT QTUNIT
      INTO vFator
      FROM PCPRODUT
      WHERE CODPROD = P_CODPROD;
   ELSIF vTipo = 'MT' THEN
      SELECT QTUNITCX * QTUNIT
      INTO vFator
      FROM PCPRODUT
      WHERE CODPROD = P_CODPROD;
   ELSIF vTipo = 'EM' THEN
      SELECT E.QTUNIT
      INTO vFator
      FROM PCEMBALAGEM E
      WHERE E.CODAUXILIAR = vCodAuxiliar
        AND E.CODFILIAL = P_CODFILIAL;
   ELSIF vTipo = 'MU' THEN
      SELECT MULTIPLO * QTUNIT
      INTO vFator
      FROM PCPRODUT 
      WHERE CODPROD = P_CODPROD;
   END IF;    
  
   RETURN(vFator);
END FNC_B2B_CONVERSAO;
