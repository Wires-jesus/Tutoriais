CREATE OR REPLACE FUNCTION WMS_FORMATATEMPODIA(TEMPO NUMBER)
  RETURN VARCHAR2 IS
  HORA    NUMBER;
  MINUTO  NUMBER;
  SEGUNDO NUMBER;
  TEMPOABS NUMBER;
  V_RET   VARCHAR2(20);
BEGIN
 /* ESSA FUNÇÃO RECEBE A UM VALOR DO TIPO NUMBER QUE REPRESENTA UNICAMENTE
  UM DATE OU UM CÁLCULO ENVOLVENDO DATE E FAZ A CONVERSÃO
  PARA HORAS  */
 IF NVL(TEMPO, 0) <> 0 THEN
    TEMPOABS := ABS(TEMPO);
    HORA := TRUNC(TEMPOABS * 24);
    MINUTO := TRUNC(((TEMPOABS * 24) - HORA) * 60);
    SEGUNDO := TRUNC(((((TEMPOABS * 24) - HORA) * 60) - MINUTO) * 60);
    
    V_RET := TO_CHAR(HORA, 'FM9999900') || ':' || TO_CHAR(MINUTO, 'FM00') || ':' || TO_CHAR
(SEGUNDO, 'FM00');
    
    RETURN RPAD(V_RET, 20, ' ');
  ELSE
    RETURN RPAD(' ', 20, ' ');
  END IF;
END;
