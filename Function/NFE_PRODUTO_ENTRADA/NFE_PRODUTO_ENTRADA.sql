    VALOR_ACRESCIMOPF number;
  begin
    VALOR_ACRESCIMOPF := 0;
    if VALOR_TOTAL_ACRESCIMOPF > 0
    then
      select sum(ROUND(PCMOV.VLACRESCIMOPF * QTCONT, 2))
        into VALOR_ACRESCIMOPF
        from PCMOV
       where PCMOV.NUMTRANSENT = P_TRANSACAO
         and PCMOV.QTCONT > 0
         and PCMOV.DTCANCEL is null
         and PCMOV.CODPROD = P_CODPROD
         and PCMOV.NUMSEQ = P_NUMSEQ;
    end if;

    return VALOR_ACRESCIMOPF;

  end;

