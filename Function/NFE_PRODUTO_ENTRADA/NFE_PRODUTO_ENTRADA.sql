CREATE OR REPLACE FUNCTION NFE_PRODUTO_ENTRADA(P_TRANSACAO NUMBER)
  RETURN TABELA_NFE_PRODUTO IS
  
  CURSOR CR_PRODUTOS IS
    SELECT *
    FROM   SQL_NFE_PRODUTO_ENTRADA PRODUTO
    WHERE  PRODUTO.NUM_TRANSACAO = P_TRANSACAO
    ORDER  BY PRODUTO.NUMSEQ;

  CURSOR CR_PRODUTOS_COMPL IS
    SELECT *
      FROM SQL_NFE_PRODUTO_ENTRADA_COMP PRODUTO
     WHERE PRODUTO.NUM_TRANSACAO = P_TRANSACAO;  

  CURSOR CR_PRODUTOS_CIAP is
    select *
      from SQL_NFE_PRODUTO_ENTRADA_CIAP PRODUTO
     where PRODUTO.NUM_TRANSACAO = P_TRANSACAO;

  CURSOR CR_INFO_COMPLEMENTAR IS
    SELECT PCCOMPLEMENTO.COMPLEMENTO1 AS OBS
    FROM   PCCOMPLEMENTO
    WHERE  PCCOMPLEMENTO.NUMTRANS = P_TRANSACAO;

  RETORNO                   TABELA_NFE_PRODUTO;
  BASE_FRETE                NUMBER;
  BASE_OUTROS               NUMBER;
  INFO_ADICIONAL            VARCHAR2(600);
  FINALIDADE_NFE            VARCHAR2(1);
  VALOR_TOTAL_ACRESCIMOPF   NUMBER;
  VALOR_TOTAL               NUMBER;
  MAIOR_VALOR_PRODUTO       NUMBER;
  TIPO_OPERACAO             VARCHAR2(2);
  LISTA_CONTROLENUMSERIE VARCHAR2(4000);
  C_LIMITE CONSTANT PLS_INTEGER := 4000;
  C_SEP    CONSTANT VARCHAR2(2) := ', ';

