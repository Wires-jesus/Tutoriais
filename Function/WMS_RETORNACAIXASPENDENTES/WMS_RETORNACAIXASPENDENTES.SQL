CREATE OR REPLACE FUNCTION WMS_RETORNACAIXASPENDENTES(PNUMCAR PCCARREG.NUMCAR%TYPE,
                                  PNUMPED IN NUMBER DEFAULT 0)
  RETURN SHP_TABLE AS SHP_DATA_OUT SHP_TABLE;
  TBL_RC SYS_REFCURSOR;
AS
  VNUMPED PCPEDC.NUMPED%TYPE;
  VNUMCAR PCPEDC.NUMCAR%TYPE;  
BEGIN

  /* Verifica quantidade de caixas por tamanho por carregamento ou pedido 
  Data da ultima alteração: 08/06/2025 */
  
  VNUMPED    := NVL(PNUMPED, 0);
  VNUMCAR    := NVL(PNUMCAR, 0);    
  
  IF VNUMCAR > 0 THEN
    OPEN TBL_RC FOR
      SELECT C.TAMANHO, COUNT(C.TAMANHO) QTDE
        FROM PCWMSVINCULOCAIXA V
       INNER JOIN PCWMSCAIXA C ON V.CODCAIXA = C.CODCAIXA
       WHERE V.NUMCAR = VNUMCAR
         AND V.DTESTORNO IS NULL
       GROUP BY C.TAMANHO;
        
       FETCH TBL_RC
          INTO SHP_DATA_OUT;
          RETURN SHP_DATA_OUT;
  ELSE
    OPEN TBL_RC FOR
      SELECT C.TAMANHO, COUNT(C.TAMANHO) QTDE
        FROM PCWMSVINCULOCAIXA V
       INNER JOIN PCWMSCAIXA C ON V.CODCAIXA = C.CODCAIXA
       WHERE V.NUMPED = VNUMPED     
         AND V.DTESTORNO IS NULL
       GROUP BY C.TAMANHO;
       
       FETCH TBL_RC
          INTO SHP_DATA_OUT;
          RETURN SHP_DATA_OUT;
  END IF;
  
  

END;
