CREATE OR REPLACE FUNCTION WMS_RETORNACAIXASPENDENTES(PNUMCAR PCCARREG.NUMCAR%TYPE,
                                  PNUMPED IN NUMBER DEFAULT 0)
  RETURN INTEGER IS
  VCOUNT INTEGER;
  VNUMPED PCPEDC.NUMPED%TYPE;
  VNUMCAR PCPEDC.NUMCAR%TYPE;  
BEGIN

  /* Verifica quantidade de caixas pendentes de retorno 
  Data da ultima alteração: 08/06/2025 */
  
  VNUMPED    := NVL(PNUMPED, 0);
  VNUMCAR    := NVL(PNUMCAR, 0);    
  
  IF VNUMCAR > 0 THEN
    SELECT COUNT(V.CODCAIXA) INTO VCOUNT
      FROM PCWMSVINCULOCAIXA V
     WHERE V.NUMCAR = VNUMCAR
       AND NVL(V.RETORNADO, 'N') = 'N';
  ELSE
    SELECT COUNT(V.CODCAIXA) INTO VCOUNT
      FROM PCWMSVINCULOCAIXA V
     WHERE V.NUMPED = VNUMPED
       AND NVL(V.RETORNADO, 'N') = 'N';
  END IF;
  
  RETURN VCOUNT;
  
  

END;