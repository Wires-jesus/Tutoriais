CREATE OR REPLACE FUNCTION FNC_ECOMMERCEUNILEVER_CODUSUR(CODCLI_DO_PEDIDO NUMBER, CODFORNEC_ITEM_PEDIDO NUMBER, vFilial VARCHAR2)
RETURN PCUSUARI.CODUSUR%TYPE
IS
 VCODUSUR1 PCUSUARI.CODUSUR%TYPE;
 VCODUSUR2 PCUSUARI.CODUSUR%TYPE;
 VCODUSUR3 PCUSUARI.CODUSUR%TYPE;
 VCODUSURCLI PCUSUARI.CODUSUR%TYPE;
 VCODUSURECOMMERCLIENT PCCLIENT.CODUSURECOMMERCE%TYPE;

 VCODUSURECOMMERC PCUSUARI.CODUSUR%TYPE;

BEGIN

  BEGIN
    SELECT NVL(C.CODUSURECOMMERCE,0) AS CODUSUR
      INTO VCODUSURECOMMERCLIENT
      FROM PCCLIENT C, PCUSUARI U, PCUSURFORNEC UF, PCFORNEC F
     WHERE 1=1
      AND NVL(C.CODUSUR1,0) > 0
      AND U.CODUSUR = C.CODUSURECOMMERCE
      AND U.CODUSUR = UF.CODUSUR
      AND F.CODFORNEC = UF.CODFORNEC
      AND C.CODCLI = CODCLI_DO_PEDIDO
      AND UF.CODFORNEC = CODFORNEC_ITEM_PEDIDO;
  EXCEPTION
    WHEN OTHERS THEN
      VCODUSUR1 := 0;
  END;

  BEGIN
    SELECT NVL(C.CODUSUR1,0) AS CODUSUR
      INTO VCODUSUR1
      FROM PCCLIENT C, PCUSUARI U, PCUSURFORNEC UF, PCFORNEC F
     WHERE 1=1
      AND NVL(C.CODUSUR1,0) > 0
      AND U.CODUSUR = C.CODUSUR1
      AND U.CODUSUR = UF.CODUSUR
      AND F.CODFORNEC = UF.CODFORNEC
      AND C.CODCLI = CODCLI_DO_PEDIDO
      AND UF.CODFORNEC = CODFORNEC_ITEM_PEDIDO;
  EXCEPTION
    WHEN OTHERS THEN
      VCODUSUR1 := 0;
  END;

  BEGIN

    SELECT NVL(C.CODUSUR2,0) AS CODUSUR
      INTO VCODUSUR2
      FROM PCCLIENT C, PCUSUARI U, PCUSURFORNEC UF, PCFORNEC F
     WHERE 1=1
      AND NVL(C.CODUSUR2,0) > 0
      AND U.CODUSUR = C.CODUSUR2
      AND U.CODUSUR = UF.CODUSUR
      AND F.CODFORNEC = UF.CODFORNEC
      AND C.CODCLI = CODCLI_DO_PEDIDO
      AND UF.CODFORNEC = CODFORNEC_ITEM_PEDIDO;
  EXCEPTION
    WHEN OTHERS THEN
      VCODUSUR2 := 0;
  END;

  BEGIN
    SELECT NVL(C.CODUSUR3,0) AS CODUSUR
      INTO VCODUSUR3
      FROM PCCLIENT C, PCUSUARI U, PCUSURFORNEC UF, PCFORNEC F
     WHERE 1=1
      AND NVL(C.CODUSUR3,0) > 0
      AND U.CODUSUR = C.CODUSUR3
      AND U.CODUSUR = UF.CODUSUR
      AND F.CODFORNEC = UF.CODFORNEC
      AND C.CODCLI = CODCLI_DO_PEDIDO
      AND UF.CODFORNEC = CODFORNEC_ITEM_PEDIDO;
  EXCEPTION
    WHEN OTHERS THEN
      VCODUSUR3 := 0;
  END;

  BEGIN

    SELECT MIN(NVL(UC.CODUSUR,0)) AS CODUSUR
      INTO VCODUSURCLI
      FROM PCUSURCLI UC, PCCLIENT C ,PCUSUARI U, PCUSURFORNEC UF, PCFORNEC  F
     WHERE 1=1
      AND NVL(UC.CODUSUR,0) > 0
      AND NVL(UF.CODFORNEC,0) > 0
      AND UC.CODCLI = C.CODCLI
      AND UC.CODUSUR = U.CODUSUR
      AND U.CODUSUR = UF.CODUSUR
      AND F.CODFORNEC = UF.CODFORNEC
      AND C.CODCLI = CODCLI_DO_PEDIDO
      AND UF.CODFORNEC = CODFORNEC_ITEM_PEDIDO;

  EXCEPTION
    WHEN OTHERS THEN
      VCODUSURCLI := 0;
  END;

  BEGIN
  SELECT NVL(CODUSUR,0) AS CODUSUR
    INTO VCODUSURECOMMERC
    FROM PCECOMMERCEUNILEVER
    WHERE CODFILIAL = VFILIAL;
  EXCEPTION
    WHEN OTHERS THEN
      VCODUSURECOMMERC := 0;
  END;

  IF VCODUSURECOMMERCLIENT > 0 THEN
    RETURN VCODUSURECOMMERCLIENT;
  ELSE IF VCODUSUR1 > 0  THEN
    RETURN VCODUSUR1;
  ELSE IF VCODUSUR2 > 0   THEN
    RETURN VCODUSUR2;
  ELSE IF VCODUSUR3 > 0  THEN
    RETURN VCODUSUR3;
  ELSE IF VCODUSURCLI > 0 THEN
    RETURN VCODUSURCLI;
  ELSE IF VCODUSURECOMMERC > 0 THEN
    RETURN VCODUSURECOMMERC;
  END IF;
  END IF;
  END IF;
  END IF;
  END IF;
  END IF;

  RETURN 0;
END;
