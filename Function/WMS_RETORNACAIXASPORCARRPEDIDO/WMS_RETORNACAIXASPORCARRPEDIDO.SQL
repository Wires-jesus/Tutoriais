CREATE OR REPLACE FUNCTION WMS_RETORNACAIXASPORCARRPEDIDO(PNUMCAR PCCARREG.NUMCAR%TYPE,
                                  PNUMPED IN NUMBER DEFAULT 0)
  RETURN INTEGER IS
  VCOUNT INTEGER;
  VNUMPED PCPEDC.NUMPED%TYPE;
  VNUMCAR PCPEDC.NUMCAR%TYPE;  
BEGIN

  /* Verifica quantidade de caixas por carregamento ou pedido 
  Data da ultima alteração: 08/06/2025 */
  
  VNUMPED    := NVL(PNUMPED, 0);
  VNUMCAR    := NVL(PNUMCAR, 0);    
  
  IF VNUMCAR > 0 THEN
    SELECT COUNT(CODCAIXA) INTO VCOUNT
      FROM PCWMSVINCULOCAIXA
     WHERE NUMCAR = VNUMCAR;
  ELSE
    SELECT COUNT(CODCAIXA) INTO VCOUNT
      FROM PCWMSVINCULOCAIXA
     WHERE NUMPED = VNUMPED;
  END IF;
  
  RETURN VCOUNT;
  

END;
