CREATE OR REPLACE FUNCTION FNC_ECOMMERCEUNILEVER_CODAUX(pCODPROD NUMBER, pQTUNIT NUMBER,
   pQTUNITCX NUMBER, pEMBVENDAECOMMERCEUNILEVER VARCHAR2, pCODAUXILIAR VARCHAR2, pMULTIPLO NUMBER, 
   pCODFILIAL VARCHAR2, pCODFORNEC NUMBER, pENVIAROBS VARCHAR2, pCODAUXEMBALAGEM NUMBER) RETURN VARCHAR2
IS
   vsVALOR VARCHAR2(1);
   vsCODAUXILIAR VARCHAR2(30);
   vsOBSERVACAO VARCHAR2(30);
   vbOBSERVACAO VARCHAR2(1); 
BEGIN
   vsVALOR := 'N';

   BEGIN
      SELECT NVL(P.VALOR,'N') 
      INTO vsVALOR 
      FROM PCPARAMFILIAL P
          ,PCECOMMERCEUNILEVER E 
      WHERE P.NOME = 'FIL_UTILIZAVENDAPOREMBALAGEM' 
        AND P.CODFILIAL = E.CODFILIAL
        AND P.CODFILIAL = pCODFILIAL;
   EXCEPTION WHEN OTHERS THEN
      vsVALOR := 'N';
   END;  
  
   IF vsVALOR = 'N' THEN
      BEGIN
         SELECT CASE WHEN NVL((SELECT A.CODAUXILIAR 
                               FROM PCECOMMERCEUNILEVERALIENAR A 
                               WHERE A.CODPROD = pCODPROD 
                                 AND A.CODFORNEC = pCODFORNEC 
                                 AND ROWNUM = 1),'') IS NOT NULL THEN 
                      NVL((SELECT A.CODAUXILIAR FROM PCECOMMERCEUNILEVERALIENAR A WHERE A.CODPROD = pCODPROD AND A.CODFORNEC = pCODFORNEC AND ROWNUM = 1),'')
                   WHEN pQTUNIT >= pQTUNITCX THEN 
                      pCODAUXILIAR || 'C' || TO_CHAR(pQTUNIT)
                   WHEN (pEMBVENDAECOMMERCEUNILEVER = 'C') AND (pQTUNITCX > 1) THEN 
                      pCODAUXILIAR || 'C' || TO_CHAR(pQTUNITCX)
                   WHEN (pEMBVENDAECOMMERCEUNILEVER = 'M') AND NVL(PCPRODFILIAL.MULTIPLO, NVL(pMULTIPLO,0)) > 0 THEN 
                      pCODAUXILIAR || 'C' || TO_CHAR(NVL(PCPRODFILIAL.MULTIPLO, NVL(MULTIPLO,0)))
                ELSE 
                   pCODAUXILIAR
                END CODAUXILIAR
         INTO vsCODAUXILIAR
         FROM PCPRODFILIAL, 
              PCECOMMERCEUNILEVER
         WHERE PCECOMMERCEUNILEVER.CODFILIAL = PCPRODFILIAL.CODFILIAL
           AND PCPRODFILIAL.CODPROD = pCODPROD
           AND ROWNUM = 1;
      EXCEPTION WHEN OTHERS THEN
         vsCODAUXILIAR := '';
      END;
   ELSE
      BEGIN
         SELECT CASE WHEN PCEMBALAGEM.QTUNIT > 1 THEN pCODAUXILIAR || 'C' || TO_CHAR(PCEMBALAGEM.QTUNIT) ELSE pCODAUXILIAR END CODAUXILIAR
         INTO vsCODAUXILIAR
         FROM PCPRODFILIAL, 
              PCECOMMERCEUNILEVER, 
              PCEMBALAGEM
         WHERE PCPRODFILIAL.CODPROD = PCEMBALAGEM.CODPROD
           AND PCPRODFILIAL.CODFILIAL = PCEMBALAGEM.CODFILIAL
           AND PCECOMMERCEUNILEVER.CODFILIAL = PCPRODFILIAL.CODFILIAL
           AND PCEMBALAGEM.CODPROD = pCODPROD
           AND PCEMBALAGEM.CODFILIAL = pCODFILIAL
           AND NVL(PCEMBALAGEM.USAECOMMERCEUNILEVER,'N') = 'S'
           AND ROWNUM = 1;
      EXCEPTION WHEN OTHERS THEN
         vsCODAUXILIAR := '';
      END;
   END IF;

   vbOBSERVACAO := pENVIAROBS;
   vsOBSERVACAO := NULL;
   
   IF vbOBSERVACAO = 'S' THEN
      vsOBSERVACAO := vsCODAUXILIAR;
   END IF;

   RETURN vsOBSERVACAO;  
END;
