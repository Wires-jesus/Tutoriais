*/

CREATE or REPLACE VIEW VW_INT_C5_PRECOAPARTIR AS (
  SELECT 
    1 SEQPRECOAPARTIR, 
    'CLIENTE ATACADO' DESCRICAO,
    'S' ATIVO  
  FROM DUAL

  UNION ALL

  SELECT
    CODFIDELIDADE SEQPRECOAPARTIR,
    'CLIENTE FIDELIDADE ' || CODFIDELIDADE DESCRICAO,
    CASE
      WHEN DTEXCLUSAO IS NULL AND (trunc(sysdate) BETWEEN DTINICIAL and DTFINAL) THEN
        'S'
      ELSE
        'N'
    END ATIVO
  FROM
    PCDESCONTOFIDELIDADE T
    LEFT JOIN MONITORPDVMIDDLE.TB_PRECOAPARTIR TP
    ON (TP.SEQPRECOAPARTIR = T.CODFIDELIDADE),
    VW_INT_C5_OBTER_FILIAIS_C5 F,
    (
      SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
      FROM PCCONTROLECONSINCO S
      WHERE (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_PRECOAPARTIR')
    ) DATAPADRAO
  WHERE
    ( 
      (TP.ATIVO = 'S' AND (T.DTEXCLUSAO IS NOT NULL or (trunc(sysdate) BETWEEN DTINICIAL and DTFINAL)))
      OR (T.DTEXCLUSAO IS NULL) 
    )
    AND T.CODFILIAL = F.CODFILIAL
    AND T.DTINICIAL <= TRUNC(sysdate)
    AND NVL(T.CODCLI, 0) <> 0
    AND NVL(T.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO) >= DATAPADRAO.ULTIMAEXECUCAO
)

