CREATE OR REPLACE VIEW VW_INT_C5_PRECOAPARTIRPERIODO AS (
  SELECT
    1 SEQPRECOAPARTIR,
    SEQPRODUTO,
    PRECO,
    NULL PERCDESCONTO,
    1 QTDE
  FROM
    MONITORPDVMIDDLE.TB_PRODPRECO
  WHERE INSTR(IDREF, 'A') > 0 
    AND PRECO > 0
    AND PROMOCAO = 'N'

  UNION ALL

  SELECT
    T.CODFIDELIDADE SEQPRECOAPARTIR ,
    DEPARA.SEQPRODUTO,
    NULL PRECO,
    T.PERCDESCONTO,
    1 QTDE,
    CASE
        WHEN T.DTEXCLUSAO IS NULL AND (trunc(sysdate) BETWEEN T.DTINICIAL and T.DTFINAL) THEN
          'S'
        ELSE
          'N'
      END ATIVO
  FROM 
    PCDESCONTOFIDELIDADE T
  INNER JOIN PCPRODUT 
  ON (
    PCPRODUT.CODSEC = T.CODSECAO 
    OR PCPRODUT.CODEPTO = T.CODEPTO 
    OR PCPRODUT.CODFORNEC = T.CODFORNEC 
    OR PCPRODUT.CODPROD = T.CODPROD 
    OR PCPRODUT.CODCATEGORIA = T.CODCATEGORIA 
  )
  INNER JOIN PCDEPARAPRODC5 DEPARA
    ON DEPARA.CODPROD = PCPRODUT.CODPROD
  LEFT JOIN MONITORPDVMIDDLE.TB_PRECOAPARTIRPERIODO TP
    ON (TP.SEQPRECOAPARTIR = T.CODFIDELIDADE),
  VW_INT_C5_OBTER_FILIAIS_C5 F,
  (
    SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
      FROM PCCONTROLECONSINCO S
      WHERE (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_PRECOAPARTIRPRODUTO')
  ) DATAPADRAO
  WHERE
  ( 
      (TP.ATIVO = 'S' AND (T.DTEXCLUSAO IS NOT NULL or (trunc(sysdate) BETWEEN DTINICIAL AND DTFINAL)))
      OR (T.DTEXCLUSAO IS NULL) 
    )
  AND F.CODFILIAL = T.CODFILIAL 
  AND T.DTINICIAL <= TRUNC(SYSDATE)
  AND NVL(T.CODCLI, 0) <> 0
  AND NVL(T.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO) >= DATAPADRAO.ULTIMAEXECUCAO
)