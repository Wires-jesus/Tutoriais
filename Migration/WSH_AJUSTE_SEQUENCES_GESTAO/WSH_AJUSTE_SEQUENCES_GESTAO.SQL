DECLARE
  VSCRIPT            VARCHAR2(4000);
  SEQUENCE_GF_EXISTE INTEGER := 0;
  SEQUENCE_GR_EXISTE INTEGER := 0;
  SEQUENCE_GD_EXISTE INTEGER := 0;
  SEQUENCE_GT_EXISTE INTEGER := 0;

BEGIN
  
  BEGIN
    SELECT 1
      INTO SEQUENCE_GF_EXISTE
      FROM USER_SEQUENCES
     WHERE UPPER(SEQUENCE_NAME) = 'DFSEQ_PCINTEGRACAOGESTAOFLUXO';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      SEQUENCE_GF_EXISTE := 0;
  END;

  BEGIN
    SELECT 1
      INTO SEQUENCE_GR_EXISTE
      FROM USER_SEQUENCES
     WHERE UPPER(SEQUENCE_NAME) = 'DFSEQ_PCINTEGRACAOGESTAORECURS';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      SEQUENCE_GR_EXISTE := 0;
  END;

  BEGIN
    SELECT 1
      INTO SEQUENCE_GD_EXISTE
      FROM USER_SEQUENCES
     WHERE UPPER(SEQUENCE_NAME) = 'DFSEQ_PCINTEGRACAOGESTAODETALH';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      SEQUENCE_GD_EXISTE := 0;
  END;

  BEGIN
    SELECT 1
      INTO SEQUENCE_GT_EXISTE
      FROM USER_SEQUENCES
     WHERE UPPER(SEQUENCE_NAME) = 'DFSEQ_PCINTEGRACAOGESTAOTEMPO';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      SEQUENCE_GT_EXISTE := 0;
  END;

  IF SEQUENCE_GF_EXISTE > 0 THEN
    VSCRIPT := 'ALTER SEQUENCE DFSEQ_PCINTEGRACAOGESTAOFLUXO MAXVALUE 9223372036854775807 CYCLE';
    EXECUTE IMMEDIATE VSCRIPT;
  END IF;

  IF SEQUENCE_GR_EXISTE > 0 THEN
    VSCRIPT := 'ALTER SEQUENCE DFSEQ_PCINTEGRACAOGESTAORECURS MAXVALUE 9223372036854775807 CYCLE';
    EXECUTE IMMEDIATE VSCRIPT;
  END IF;

  IF SEQUENCE_GD_EXISTE > 0 THEN
    VSCRIPT := 'ALTER SEQUENCE DFSEQ_PCINTEGRACAOGESTAODETALH MAXVALUE 9223372036854775807 CYCLE';
    EXECUTE IMMEDIATE VSCRIPT;
  END IF;

  IF SEQUENCE_GT_EXISTE > 0 THEN
    VSCRIPT := 'ALTER SEQUENCE DFSEQ_PCINTEGRACAOGESTAOTEMPO MAXVALUE 9223372036854775807 CYCLE';
    EXECUTE IMMEDIATE VSCRIPT;
  END IF;

END;
