DECLARE
  V_EXISTE VARCHAR2(100);
BEGIN
  BEGIN
    SELECT T.TYPE_NAME
      INTO V_EXISTE
      FROM ALL_TYPES T
     WHERE T.TYPE_NAME = 'TABELA_FATURAMENTO'
       AND OWNER = SYS_CONTEXT('USERENV', 'SESSION_USER');
  EXCEPTION
    WHEN OTHERS THEN
      V_EXISTE := NULL;
  END;
  
  IF NOT V_EXISTE IS NULL THEN
    EXECUTE IMMEDIATE 'DROP TYPE TABELA_FATURAMENTO';
  END IF;
END;
