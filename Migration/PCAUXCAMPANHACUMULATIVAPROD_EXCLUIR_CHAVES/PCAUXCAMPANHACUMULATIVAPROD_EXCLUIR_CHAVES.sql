    EXECUTE IMMEDIATE 'ALTER TABLE PCAUXCAMPANHACUMULATIVAPROD DROP CONSTRAINT PCAUXCAMPANHACUMULATIVAP_FK1';
  END IF;
  
  -- Verifica se a chave estrangeira PCAUXCAMPANHACUMULATIVAP_FK2 existe
