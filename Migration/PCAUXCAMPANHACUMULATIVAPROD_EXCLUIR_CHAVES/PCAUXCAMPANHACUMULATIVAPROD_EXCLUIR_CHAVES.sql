  V_CONTADOR NUMBER;
BEGIN
  -- Verifica se a chave estrangeira PCAUXCAMPANHACUMULATIVAP_FK1 existe
  SELECT COUNT(*)
    INTO V_CONTADOR 
    FROM USER_CONSTRAINTS 
   WHERE CONSTRAINT_NAME = 'PCAUXCAMPANHACUMULATIVAP_FK1' AND CONSTRAINT_TYPE = 'R';
  
