    INTO V_CONTADOR 
    FROM USER_CONSTRAINTS 
   WHERE CONSTRAINT_NAME = 'PCAUXCAMPANHACUMULATIVAP_FK2' AND CONSTRAINT_TYPE = 'R';
  
