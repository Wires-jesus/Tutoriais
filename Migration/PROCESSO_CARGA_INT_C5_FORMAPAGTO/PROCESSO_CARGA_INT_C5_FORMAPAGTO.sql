CREATE OR REPLACE VIEW VW_INT_C5_FORMAPAGTOBINCARTAO AS
(
  SELECT F.CODFINALIZADORA NROFORMAPAGTO, 
         F.NUMBINCARTAO NROBINCARTAOTEF,
         'S' ATIVO
  FROM   PCFINALIZADORA F,
         (select min(s.ultimaexecucao) ultimaexecucao
          from pccontroleconsinco s
		  where (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_FORMAPAGTOEMPRESA')
		  or    (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_FORMAPAGTO')
		  or    (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_FORMAPAGTOBINCARTAO')
         ) D
  WHERE F.NUMBINCARTAO IS NOT NULL 
  AND NVL(F.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO
  AND F.DTINATIVACAO IS NULL
)

