DECLARE
  V_EXISTE ALL_TABLES.TABLE_NAME%TYPE;
BEGIN
  BEGIN
    SELECT TABLE_NAME
      INTO V_EXISTE
      FROM ALL_TABLES
     WHERE TABLE_NAME = 'PCMED_PROMOCAO_NIVEL_DSC_OL'
       AND OWNER = SYS_CONTEXT('USERENV', 'SESSION_USER');
  EXCEPTION
    WHEN OTHERS THEN
      V_EXISTE := NULL;
  END;
  
  IF V_EXISTE IS NULL THEN
    EXECUTE IMMEDIATE 'CREATE GLOBAL TEMPORARY TABLE PCMED_PROMOCAO_NIVEL_DSC_OL
                       (
                         CODPROMOCAOMED             NUMBER(6),
                         CODPROD                    NUMBER(6) NOT NULL,
                         INICIOINTERVALOPROMOCAOMED NUMBER,
                         FIMINTERVALOPROMOCAOMED    NUMBER,
                         NIVEL                      NUMBER
                       )
                       ON COMMIT PRESERVE ROWS';
  END IF;
END;
