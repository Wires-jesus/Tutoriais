CREATE OR REPLACE VIEW VW_INT_C5_PRODPRECO AS
WITH CTE_PRECO AS (
    --Linha de preço varejo
    SELECT
    TO_CHAR(E.CODAUXILIAR) IDREF,
    DEPARA.SEQPRODUTO SEQPRODUTO,
    --E.CODFILIAL NROEMPRESA,
    C5.CODFILIALINTEGRACAO NROEMPRESA,
    NVL(E.QTUNIT, 1) QTDEMBALAGEM,
    1 NROSEGMENTO,
    'N' PROMOCAO,

    (CASE
      WHEN E.TIPOMERC IN ('CB', 'KT') THEN
           0.00
      ELSE LEAST(E.PVENDA, 9999999.999)
    END)  PRECO,

    0 PRECONORMAL,
    'S' ATIVO
  FROM VW_INT_C5_OBTER_FILIAIS_C5 C5,
       VW_INT_C5_EMBPROD_MAT e
  JOIN PCDEPARAPRODC5 DEPARA
    ON DEPARA.CODPROD = E.CODPROD
       AND DEPARA.ATIVO = 'S'
       AND (DEPARA.CODAUXILIAR = E.CODAUXILIAR OR DEPARA.CODAUXILIAR = 0)
       AND E.QTUNIT <> E.QTMINIMAATACADO
   WHERE C5.CODFILIAL = E.CODFILIAL

  UNION ALL

  --LINHA DE PREÇO ATACADO
  SELECT
    CASE 
      WHEN (NVL(E.QTMINIMAATACADO, 0) >= 1) AND (NVL(E.PVENDAATAC, 0) <= 0) THEN
           TO_CHAR(E.CODAUXILIAR) || 'V' /*SETAR QUE A LINHA DE ATACADO RECEBEU "V" INDICANDO QUE PREÇO DE ATACADO RECEBEU O PRECO DE VAREJO*/
      ELSE     
           TO_CHAR(E.CODAUXILIAR) || 'A' 
    END  IDREF,
    p.SEQPRODUTO SEQPRODUTO,
    --E.CODFILIAL NROEMPRESA,
    C5.CODFILIALINTEGRACAO NROEMPRESA,
    NVL(E.QTMINIMAATACADO, 1) QTDEMBALAGEM,
    1 NROSEGMENTO,
    'N' PROMOCAO,

    /*(CASE
      WHEN E.TIPOMERC IN ('CB', 'KT') THEN
           0.01
      ELSE LEAST(ROUNDABNT((E.PVENDAATAC / E.QTUNIT) * E.QTMINIMAATACADO, 3), 9999999.999)
    END)  PRECO,*/
    (CASE
      WHEN E.TIPOMERC IN ('CB', 'KT') THEN
           0.00
      WHEN (NVL(E.QTMINIMAATACADO, 0) >= 1) AND (NVL(E.PVENDAATAC, 0) <= 0) THEN /*CASO EXISTA GATILHO MAS NAO TENHA PRECO DE ATACADO, SETAR O PREÇO DE VAREJO*/
           LEAST(ROUNDABNT((E.PVENDA / E.QTUNIT) * E.QTMINIMAATACADO, 3), 9999999.999)
      ELSE LEAST(ROUNDABNT((E.PVENDAATAC / E.QTUNIT) * E.QTMINIMAATACADO, 3), 9999999.999)
    END)  PRECO,

    0 PRECONORMAL,
    'S' ATIVO
  FROM VW_INT_C5_OBTER_FILIAIS_C5 C5,
       VW_INT_C5_EMBPROD_MAT E
    JOIN PCDEPARAPRODC5 P ON E.CODPROD = P.CODPROD-- AND P.ATIVO = 'S'
    WHERE
        (
            (E.CODAUXILIAR = P.CODAUXILIAR AND P.ATIVO = 'S')
            OR (P.CODAUXILIAR = 0 AND P.ATIVO = 'S' AND E.QTUNIT = (SELECT MIN(QTUNIT) FROM VW_INT_C5_EMBPROD_MAT EMB
                                                                    WHERE E.CODFILIAL = EMB.CODFILIAL
                                                                    AND E.CODPROD = EMB.CODPROD)
                                                                    )
        )
    AND E.QTMINIMAATACADO >= 1
    AND C5.CODFILIAL = E.CODFILIAL
)
SELECT "IDREF","SEQPRODUTO","NROEMPRESA","QTDEMBALAGEM","NROSEGMENTO","PROMOCAO","PRECO","PRECONORMAL","ATIVO" 
FROM CTE_PRECO

