AND   LT.CODFILIAL = F.CODIGO
AND   PROD.CODPROD = P.CODPROD
AND   PROD.CODPROD = LT.CODPROD
AND   LT.DTVALIDADE IS NOT NULL
AND   NVL(LT.DTALTERC5, DTPADRAO.ULTIMAEXECUCAO) >= DTPADRAO.ULTIMAEXECUCAO
