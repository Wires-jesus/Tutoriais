CREATE OR REPLACE VIEW VW_INT_C5_EMBFAMILIA AS
WITH CTE_EMBFAMILIA AS (
SELECT DISTINCT DEPARA.SEQFAMILIA,
       E.CODPRODPRINC, 
       NVL(E.QTUNIT, 1) QTDEMBALAGEM, 
       NVL(MAX(E.UNIDADE), '1') EMBALAGEM, 
       LEAST(NVL(MAX(E.pesobruto), 0), 9999.999) AS PESOBRUTO,
       LEAST(NVL(MAX(E.pesoliq), 0), 9999.999) AS PESOLIQUIDO,
       'N' AS PESOAFERIDO,
       'S' AS ATIVO,
       E.CODPROD IDREF
FROM VW_INT_C5_EMBPROD_MAT E,
     PCDEPARAPRODC5 DEPARA,
     VW_INT_C5_OBTER_FILIAIS_C5 C5
WHERE E.CODFILIAL = C5.CODFILIAL
AND   E.CODPRODPRINC = DEPARA.CODPROD
AND   DEPARA.ATIVO = 'S'
AND   (DEPARA.CODAUXILIAR = E.CODAUXILIAR OR DEPARA.CODAUXILIAR = 0)
AND   E.CODPROD <> E.CODPRODPRINC
AND   NOT EXISTS(SELECT 1
                 FROM MONITORPDVMIDDLE.TB_FAMEMBALAGEM EMB
                 WHERE EMB.SEQFAMILIA = DEPARA.SEQFAMILIA
                 AND    NVL(E.QTUNIT, 1) = EMB.QTDEMBALAGEM)
GROUP BY DEPARA.SEQFAMILIA, E.QTUNIT, E.CODPRODPRINC, E.CODPROD
)
SELECT DISTINCT
  SEQFAMILIA,
  QTDEMBALAGEM,
  EMBALAGEM,
  PESOBRUTO,
  PESOLIQUIDO,
  PESOAFERIDO,
  ATIVO
FROM CTE_EMBFAMILIA
WHERE NOT EXISTS (SELECT 1
                  FROM MONITORPDVMIDDLE.TB_FAMEMBALAGEM EMB
                  WHERE EMB.SEQFAMILIA = SEQFAMILIA
                  AND   EMB.QTDEMBALAGEM = QTDEMBALAGEM)

