CREATE OR REPLACE VIEW VW_INT_C5_PRODPRECO AS
WITH CTE_PRECO AS (
    --Linha de preço varejo
    SELECT
    E.CODAUXILIAR IDREF,
    DEPARA.SEQPRODUTO SEQPRODUTO,
    E.CODFILIAL NROEMPRESA,
    NVL(E.QTUNIT, 1) QTDEMBALAGEM,
    1 NROSEGMENTO,
    'N' PROMOCAO,

    (CASE
      WHEN E.TIPOMERC IN ('CB', 'KT') THEN
           0.01
      ELSE LEAST(E.PVENDA, 9999999.999)
    END)  PRECO,

    0 PRECONORMAL,
    'S' ATIVO
  FROM VW_INT_C5_EMBPROD e
  JOIN PCDEPARAPRODC5 DEPARA
    ON DEPARA.CODPROD = E.CODPROD
   AND DEPARA.ATIVO = 'S'
   AND (DEPARA.CODAUXILIAR = E.CODAUXILIAR OR DEPARA.CODAUXILIAR = 0)
   AND E.QTUNIT <> E.QTMINIMAATACADO

  UNION ALL

  --LINHA DE PREÇO ATACADO
  SELECT
    E.CODAUXILIAR IDREF,
    p.SEQPRODUTO SEQPRODUTO,
    E.CODFILIAL NROEMPRESA,
    NVL(E.QTMINIMAATACADO, 1) QTDEMBALAGEM,
    1 NROSEGMENTO,
    'N' PROMOCAO,

    (CASE
      WHEN E.TIPOMERC IN ('CB', 'KT') THEN
           0.01
      ELSE LEAST(ROUNDABNT((E.PVENDAATAC / E.QTUNIT) * E.QTMINIMAATACADO, 3), 9999999.999)
    END)  PRECO,

    0 PRECONORMAL,
    'S' ATIVO
  FROM VW_INT_C5_EMBPROD E
    JOIN PCDEPARAPRODC5 P ON E.CODPROD = P.CODPROD-- AND P.ATIVO = 'S'
    WHERE
        (
            (E.CODAUXILIAR = P.CODAUXILIAR AND P.ATIVO = 'S')
            OR (P.CODAUXILIAR = 0 AND P.ATIVO = 'S' AND E.QTUNIT = (SELECT MIN(QTUNIT) FROM VW_INT_C5_EMBPROD EMB
                                                                    WHERE E.CODFILIAL = EMB.CODFILIAL
                                                                    AND E.CODPROD = EMB.CODPROD)
                                                                    )
        )
        AND E.QTMINIMAATACADO >= 1
)
SELECT "IDREF","SEQPRODUTO","NROEMPRESA","QTDEMBALAGEM","NROSEGMENTO","PROMOCAO","PRECO","PRECONORMAL","ATIVO" 
FROM CTE_PRECO

