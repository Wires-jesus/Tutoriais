CREATE OR REPLACE VIEW VW_INT_C5_PRODCOMPOSTO AS
(

SELECT
  (SELECT X.SEQPRODUTO FROM PCDEPARAPRODC5 X WHERE X.CODPROD = CESTA.CODPRODACAB AND X.ATIVO = 'S' AND ROWNUM = 1)SEQPRODCOMPOSTO,
  CESTA.SEQPRODUTO,
  CESTA.QTDEMBALAGEM,
  CESTA.QUANTIDADE,
  
  (CASE
       WHEN (TRUNC(SYSDATE) BETWEEN C.DTINICIO AND C.DTFIM) AND (NVL(I.PRECOFIXO,0) > 0) THEN
            I.PRECOFIXO
       WHEN (TRUNC(SYSDATE) BETWEEN C.DTINICIO AND C.DTFIM) AND (NVL(I.PERCDESC,0) > 0) THEN
            CESTA.PRECO - (ABS(CESTA.PRECO * I.PERCDESC / 100))
       ELSE CESTA.PRECO
  END)PRECO,
  
  CESTA.ATIVO,
  CESTA.CODPRODACAB IDREF
FROM(/*SELECT BASE, SEM A VALIDAÇÃO COM A PRECOCESTAC E PRECOCESTA I QUE NEM SEMPRE ESTARÃO POPULADAS*/
     SELECT DISTINCT
       F.CODPRODACAB CODPRODACAB,
       (SELECT X.CODAUXILIAR FROM PCEMBALAGEM X WHERE X.CODPROD = F.CODPRODACAB AND X.CODFILIAL = F.CODFILIAL AND ROWNUM = 1) CODAUXILIAR_PRODACAB,
       E.CODFILIALINTEGRACAO NROEMPRESA,
	   F.CODFILIAL CODFILIAL,
       P.SEQPRODUTO,
       F.CODAUXILIARMP CODAUXILIAR,
       F.CODPRODMP,
       (CASE
         WHEN FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('FIL_PRECOPOREMBALAGEM', F.CODFILIAL, 'N') = 'S' THEN
              COLUNA_PRECO(BUSCAPRECOS(F.CODFILIAL, 0, F.CODAUXILIARMP, TRUNC(SYSDATE)),'PVENDA')
         ELSE COLUNA_PRECO(BUSCAPRECOS(F.CODFILIAL, FERRAMENTAS.F_BUSCARPARAMETRO_NUM('NUMREGIAOPADRAOVAREJO', F.CODFILIAL, 1), F.CODAUXILIARMP, TRUNC(SYSDATE)),'PVENDA1')
       END) PRECO,
       E.qtunit QTDEMBALAGEM,
       F.QTPRODMP QUANTIDADE,
       E.ATIVO
     FROM VW_INT_C5_EMBPROD E,
          PCFORMPROD F,
          PCDEPARAPRODC5 P
     WHERE E.codprod = F.CODPRODMP
     AND   E.codauxiliar = F.CODAUXILIARMP
     AND   ((P.CODPROD = E.codprod) AND (P.CODAUXILIAR = 0) OR
            (P.CODPROD = E.codprod) AND (P.CODAUXILIAR = E.codauxiliar)
           )  
           
     AND   ((P.CODPROD = F.codprodMP) AND (P.CODAUXILIAR = 0) OR
            (P.CODPROD = F.codprodMP) AND (P.CODAUXILIAR = F.codauxiliarMP)
           )
           
     
     AND   P.ATIVO = 'S'
     AND   F.CODFILIAL IS NOT NULL
     AND   NVL(F.CODFILIAL, E.CODFILIAL) = E.CODFILIAL
     
     /*TRATATIVA PARA NÃO DEIXAR DESCER A CESTA CASO UM DOS ITENS ESTEJA COM PREÇO ZERADO*/
     AND   NOT EXISTS (SELECT EMB_PRECO.CODAUXILIAR
                       FROM VW_INT_C5_EMBPROD EMB_PRECO,
                            PCFORMPROD FP
                       WHERE EMB_PRECO.pvenda <= 0
                       AND EMB_PRECO.codprod = FP.CODPRODMP
                       AND EMB_PRECO.codauxiliar = FP.CODAUXILIARMP
                       AND EMB_PRECO.codfilial = FP.CODFILIAL
                       AND FP.CODFILIAL = F.CODFILIAL
                       AND FP.CODPRODACAB = F.CODPRODACAB
                       AND NVL(FP.CODFILIAL, EMB_PRECO.CODFILIAL) = EMB_PRECO.CODFILIAL
                      )
     
     /*TRATATIVA PARA NÃO DEIXAR DESCER A CESTA CASO UM DOS ITENS ESTEJA INATIVO*/
     AND   NOT EXISTS(SELECT EMB.CODAUXILIAR
                      FROM PCEMBALAGEM EMB, -- ANALISAR PELA PCEMBALAGEM PQ A VIEW EMBPROD NAO TRAZ REGISTROS INATIVADOS
                           PCFORMPROD FP,
                           PCPRODUT PR,
                           PCPRODFILIAL PRODFILIAL
                      WHERE EMB.CODPROD = FP.CODPRODMP
                      AND   EMB.CODAUXILIAR = FP.CODAUXILIARMP
                      AND   EMB.CODPROD = PR.CODPROD
                      AND   NVL(FP.CODFILIAL, EMB.CODFILIAL) = EMB.CODFILIAL
                      AND   FP.CODPRODACAB = F.CODPRODACAB
                      AND   EMB.CODPROD = PRODFILIAL.CODPROD
                      AND   EMB.CODFILIAL = PRODFILIAL.CODFILIAL
                      AND   FP.CODPRODMP = PRODFILIAL.CODPROD
                      AND   FP.CODFILIAL = PRODFILIAL.CODFILIAL
                      AND   FP.CODPRODACAB = PRODFILIAL.CODPROD
                      AND   PR.CODPROD = PRODFILIAL.CODPROD
                      AND   ((DTINATIVO IS NOT NULL) OR (PR.DTEXCLUSAO IS NOT NULL) OR (NVL(PRODFILIAL.PROIBIDAVENDA, 'N') = 'N'))
                     )
    ) CESTA,
    --PCPRECOCESTAC C,
    (SELECT * FROM PCPRECOCESTAC WHERE TRUNC(SYSDATE) BETWEEN DTINICIO AND DTFIM) C,
    PCPRECOCESTAI I,
    PCDEPARAPRODC5 EMBC5
WHERE CESTA.CODPRODACAB = I.CODPRODACAB(+) 
--AND   NVL(C.CODPRECOCESTA, 1) = NVL(I.CODPRECOCESTA, 1)
AND   I.CODPRECOCESTA = C.CODPRECOCESTA(+)
AND   CESTA.CODPRODMP = NVL(I.CODPRODMP, CESTA.CODPRODMP)
AND   CESTA.CODFILIAL = NVL(I.CODFILIAL, CESTA.CODFILIAL)
--AND  EMBC5.CODAUXILIAR = CESTA.CODAUXILIAR_PRODACAB/*Clausula para eliminar cestas que estao com cadastro inconsistentes*/
AND  ((EMBC5.CODPROD = CESTA.CODPRODACAB) AND (EMBC5.CODAUXILIAR = 0) OR
      (EMBC5.CODPROD = CESTA.CODPRODACAB) AND (EMBC5.CODAUXILIAR = CESTA.CODAUXILIAR_PRODACAB)
     )
AND   EMBC5.ATIVO = 'S'     
)
