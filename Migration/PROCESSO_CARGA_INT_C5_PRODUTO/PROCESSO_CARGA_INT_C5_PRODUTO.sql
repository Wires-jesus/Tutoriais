CREATE OR REPLACE VIEW VW_INT_C5_PRODUTO AS
(
SELECT
    DEPARA.SEQPRODUTO,
    
    NVL((CASE
         /*DEFINICAO DO SEQFAMILIA DOS PRODUTOS DESMEMBRADOS*/
         WHEN ORIGEM = 'D' THEN
              DEPARA.SEQFAMILIA
         /*DEFINICAO DO SEQFAMILIA DE PRODUTO PAI*/
         WHEN TBPROD.CODPROD = TBPROD.CODPRODPRINC THEN
              DEPARA.SEQFAMILIA
         /*Se o produto principal não existir nas filiais de integração, então seta o seqfamilia do filho*/
         WHEN (SELECT COUNT(*)
               FROM PCPRODFILIAL PF
               WHERE PF.CODFILIAL IN (SELECT C5.CODFILIAL FROM VW_INT_C5_OBTER_FILIAIS_C5 C5)
               AND  TBPROD.CODPRODPRINC = PF.CODPROD
              ) = 0 THEN
              DEPARA.SEQFAMILIA
         /*DEFINICAO DO SEQFAMILIA DE PRODUTO FILHO, DE ACORDO COM A TRIBUTACAO DO PRODUTO PAI*/     
         WHEN /*SE O PRODUTO É FILHO...*/
              (TBPROD.CODPROD <> TBPROD.CODPRODPRINC) AND 
              
              /*E SE A TRIBUTACAO DO PRODUTO FILHO É DIFERENTE DA TRIBUTACAO DO PRODUTO PAI, ENTÃO SETA O SEQFAMILIA DO FILHO*/
              (
                (SELECT F.NROTRIBUTACAO 
                 FROM VW_INT_C5_FAMDIV_MAT F 
                 WHERE F.CODPROD = TBPROD.CODPROD 
                 AND ROWNUM = 1
                ) 
                
                <>
              
                NVL((SELECT F.NROTRIBUTACAO 
                     FROM VW_INT_C5_FAMDIV_MAT F
                     WHERE F.CODPROD = TBPROD.CODPRODPRINC
                     AND ROWNUM = 1), (SELECT F.NROTRIBUTACAO
                                       FROM MONITORPDVMIDDLE.TB_FAMDIVISAO F,
                                            PCDEPARAPRODC5 D
                                       WHERE F.SEQFAMILIA = D.SEQFAMILIA
                                       AND   D.ATIVO = 'S'
                                       AND   D.CODPROD = TBPROD.CODPRODPRINC
                                       AND   D.CODAUXILIAR = 0
                                       AND ROWNUM = 1)
                    )
              ) 
              THEN 
                DEPARA.SEQFAMILIA
         ELSE /*SE A TRIBUTACAO PRODUTO FILHO É IGUAL A TRIBUTACAO DO PRODUTO PAI, SETA O SEQFAMILIA DO PRODUTO PAI*/ 
             NVL((SELECT PP.SEQFAMILIA
             FROM PCPRODUT P, PCDEPARAPRODC5 PP, MONITORPDVMIDDLE.TB_FAMILIA F
             WHERE P.CODPROD = PP.CODPROD
             AND F.SEQFAMILIA = PP.SEQFAMILIA
             AND  F.ATIVO = 'S'
             AND F.IDREF = P.CODPROD
             AND PP.CODAUXILIAR = 0
             AND TBPROD.CODPRODPRINC = PP.CODPROD
             AND PP.ATIVO = 'S'), DEPARA.SEQFAMILIA)
        END
        ), DEPARA.SEQPRODUTO) SEQFAMILIA,
           
    TBPROD.*
FROM (
      SELECT 
           MIN(PROD.CODPROD) CODPROD,
           (CASE
             WHEN MIN(PROD.CODAUXILIAR) IS NULL THEN
                  0
             ELSE MIN(PROD.CODAUXILIAR)
           END) CODAUXILIAR,
           MIN(PROD.ORIGEM) ORIGEM,
           MIN(PROD.CODPRODUTO) CODPRODUTO,
           MIN(PROD.desccompleta) desccompleta,
           MIN(PROD.descreduzida) descreduzida,
           MIN(PROD.produtocomposto) produtocomposto,
           MIN(PROD.QTDDIAVALIDADE) QTDDIAVALIDADE,
           MIN(PROD.codanp) codanp,
           MIN(PROD.descanp_prod) descanp_prod,
           MIN(PROD.percglp) percglp,
           MIN(PROD.percgnn) percgnn,
           MIN(PROD.percgni) percgni,
           MIN(PROD.CODPRODPRINC)CODPRODPRINC,
           MIN(PROD.ATIVO) ATIVO
      FROM( 
            SELECT DISTINCT
                   p.codprod CODPROD,
                   NULL CODAUXILIAR,
                   MIN(P.CODAUXILIAR) IDREF, /*NECESSÁRIO TRAZER O CODAUXILIAR PARA SER UTILIZADO NO GROUP BY*/
                   'E' ORIGEM,
                   p.codprod codproduto,
                   CASE
				     WHEN (SELECT VALOR FROM PCPARAMETROS2651 WHERE NOME = 'UTILIZAR_FNC_REMOVE_CHAR_ESP_V2') = 'S' THEN
					   fnc_remove_char_esp_v2(p.descricao)
					 ELSE
					   fnc_remove_char_esp(p.descricao)
				   END as desccompleta,
                   SUBSTR(
				     CASE
					   WHEN (SELECT VALOR FROM PCPARAMETROS2651 WHERE NOME = 'UTILIZAR_FNC_REMOVE_CHAR_ESP_V2') = 'S' THEN
						 fnc_remove_char_esp_v2(p.descricao)
					   ELSE
						 fnc_remove_char_esp(p.descricao)
					  END,
					  1,
					  24
				   ) as descreduzida,
                   --'N' produtocomposto,
                   (CASE
                      WHEN MAX(P.TIPOMERC) IN ('CB', 'KT') THEN
                           'S'
                      ELSE 'N'     
                   END) PRODUTOCOMPOSTO,
                   0 QTDDIAVALIDADE,
                   MAX(nvl(P.anp, 0)) codanp,
                   MAX(P.descanp) descanp_prod,
                   MAX(nvl(P.pglp, 0)) percglp,
                   MAX(nvl(P.pgnn, 0)) percgnn,
                   MAX(nvl(P.pgni, 0)) percgni,
                   MIN(P.CODPRODPRINC) CODPRODPRINC,
                   'S' ATIVO
            FROM VW_INT_C5_EMBPROD_MAT p
            GROUP BY p.codprod, p.descricao

            UNION ALL

            /* SELECT CUSTOMIZADO PARA TRAZER EMBALAGENS COM O MESMO QTUNIT DESMEMBRADAS */
            SELECT
                   p.codprod CODPROD,
                   p.codauxiliar CODAUXILIAR,
                   p.codauxiliar IDREF, /*NECESSÁRIO TRAZER O CODAUXILIAR PARA SER UTILIZADO NO GROUP BY*/
                   'D' ORIGEM,
                   p.codprod codproduto,
                   CASE
					 WHEN (SELECT VALOR FROM PCPARAMETROS2651 WHERE NOME = 'UTILIZAR_FNC_REMOVE_CHAR_ESP_V2') = 'S' THEN
					   fnc_remove_char_esp_v2(NVL(p.descricaoreduzida, p.descricao))
					 ELSE
					   fnc_remove_char_esp(NVL(p.descricaoreduzida, p.descricao))
				   END as desccompleta,
                   SUBSTR(
				     CASE
					   WHEN (SELECT VALOR FROM PCPARAMETROS2651 WHERE NOME = 'UTILIZAR_FNC_REMOVE_CHAR_ESP_V2') = 'S' THEN
						 fnc_remove_char_esp_v2(NVL(p.descricaoreduzida, p.descricao))
					   ELSE
						 fnc_remove_char_esp(NVL(p.descricaoreduzida, p.descricao))
					 END,
					 1,
					 24
				   ) as descreduzida,
                   'N' produtocomposto,
                   0 QTDDIAVALIDADE,
                   nvl(p.anp, 0) codanp,
                   p.descanp descanp_prod,
                   nvl(P.pglp, 0) percglp,
                   nvl(P.pgnn, 0) percgnn,
                   nvl(P.pgni, 0) percgni,                   
                   NULL CODPRODPRINC,
                   'S' ATIVO

            FROM VW_INT_C5_EMB_DESMEMBRADAS p
          ) PROD /*TABELA VIRTUAL CRIADA PARA LISTAR REGISTROS DA VIEW EMBPROD E VW_INT_C5_EMB_DESMEMBRADAS*/
      GROUP BY PROD.IDREF /*ORDERNAÇÃO DEVE SER PELO IDREF PARA AGRUPAR O RESULTADO O UNION ALL DA TABELA VIRTUAL "PROD"*/
      ) TBPROD, /*TABELA VIRTUAL COM O RESULTADO FINAL DO AGRUPAMENTO, SEM CODAUXILIAR DUPLICADO*/
      PCDEPARAPRODC5 DEPARA
WHERE TBPROD.CODPROD = DEPARA.CODPROD
AND   TBPROD.CODAUXILIAR = DEPARA.CODAUXILIAR
)

