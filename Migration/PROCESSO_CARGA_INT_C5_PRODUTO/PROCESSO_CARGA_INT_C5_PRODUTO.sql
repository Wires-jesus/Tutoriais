        WHERE E.CODPROD = F.CODPRODMP
        AND E.CODAUXILIAR = F.CODAUXILIARMP 
        AND E.CODFILIAL = F.CODFILIAL
       ), 1) QTDEMBALAGEM,
       
       F.QTPRODMP QUANTIDADE,
       0 PRECO,
       'S'ATIVO,
       F.CODPRODACAB IDREF
FROM MONITORPDVMIDDLE.TB_PRODUTO TP,
     PCFORMPROD F,
     PCPRODUT PROD,
     PCDEPARAPRODC5 P,
     VW_INT_C5_OBTER_FILIAIS_C5 C5
WHERE F.CODFILIAL IS NOT NULL
AND   (TP.CODPRODUTO = F.CODPRODACAB or TP.CODPRODUTO = F.CODPRODMP)
AND   TP.CODPRODUTO = PROD.CODPROD
AND   TP.PRODUTOCOMPOSTO = 'S'
AND   PROD.CODPROD = F.CODPRODACAB
AND   PROD.TIPOMERC IN ('CB', 'KT')
AND   C5.CODFILIAL = F.CODFILIAL           
AND   P.ATIVO = 'S'
AND   EXISTS(SELECT SEQPRODUTO FROM MONITORPDVMIDDLE.TB_PRODUTO WHERE SEQPRODUTO = P.SEQPRODUTO)
AND   ((P.CODPROD = F.codprodMP) AND (P.CODAUXILIAR = 0) OR
       (P.CODPROD = F.codprodMP) AND (P.CODAUXILIAR = F.codauxiliarMP)
      )
           
/*TRATATIVA PARA NÃO DESCER CESSTA QUE NÃO POSSUI VINCULO NA PCDEPARAPRODC5*/
AND EXISTS(SELECT 1
           FROM PCDEPARAPRODC5 PROD
           WHERE F.CODPRODACAB = PROD.CODPROD
           AND PROD.ATIVO = 'S'
          )    

/*TRATATIVA PARA NÃO DEIXAR DESCER A CESTA CASO UM DOS ITENS ESTEJA COM PREÇO ZERADO*/
AND   NOT EXISTS (SELECT EMB_PRECO.CODAUXILIAR
                  FROM VW_INT_C5_EMBPROD_MAT EMB_PRECO,
                       PCFORMPROD FP
                  WHERE EMB_PRECO.pvenda <= 0
                  AND EMB_PRECO.codprod = FP.CODPRODMP
                  AND EMB_PRECO.codauxiliar = FP.CODAUXILIARMP
                  AND EMB_PRECO.codfilial = FP.CODFILIAL
                  AND FP.CODFILIAL = F.CODFILIAL
                  AND FP.CODPRODACAB = F.CODPRODACAB
                  AND NVL(FP.CODFILIAL, EMB_PRECO.CODFILIAL) = EMB_PRECO.CODFILIAL
                 )
     
/*TRATATIVA PARA NÃO DEIXAR DESCER A CESTA CASO UM DOS ITENS ESTEJA INATIVO*/
AND   NOT EXISTS(SELECT EMB.CODAUXILIAR
                 FROM PCEMBALAGEM EMB, -- ANALISAR PELA PCEMBALAGEM PQ A VIEW EMBPROD NAO TRAZ REGISTROS INATIVADOS
                      PCFORMPROD FP,
                      PCPRODUT PR,
                      PCPRODFILIAL PRODFILIAL
                 WHERE EMB.CODPROD = FP.CODPRODMP
                 AND   EMB.CODAUXILIAR = FP.CODAUXILIARMP
                 AND   EMB.CODPROD = PR.CODPROD
                 AND   NVL(FP.CODFILIAL, EMB.CODFILIAL) = EMB.CODFILIAL
                 AND   FP.CODPRODACAB = F.CODPRODACAB
                 AND   EMB.CODPROD = PRODFILIAL.CODPROD
                 AND   EMB.CODFILIAL = PRODFILIAL.CODFILIAL
                 AND   FP.CODPRODMP = PRODFILIAL.CODPROD
                 AND   FP.CODFILIAL = PRODFILIAL.CODFILIAL
                 AND   FP.CODPRODACAB = PRODFILIAL.CODPROD
                 AND   PR.CODPROD = PRODFILIAL.CODPROD
                 AND   ((DTINATIVO IS NOT NULL) OR (PR.DTEXCLUSAO IS NOT NULL) OR (NVL(PRODFILIAL.PROIBIDAVENDA, 'N') = 'N'))
                )
)

