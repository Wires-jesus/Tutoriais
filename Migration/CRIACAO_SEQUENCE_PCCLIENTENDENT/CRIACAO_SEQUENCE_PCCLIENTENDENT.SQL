DECLARE
  V_CONTADOR INTEGER;
  V_NOME_SEQUENCIA VARCHAR2(20) := 'DFSEQ_PCCLIENTENDENT';
  V_PROXIMO_ID PCCLIENTENDENT.CODENDENTCLI%TYPE;
BEGIN
  SELECT COUNT(*) 
  INTO V_CONTADOR
  FROM USER_SEQUENCES
  WHERE SEQUENCE_NAME = UPPER(V_NOME_SEQUENCIA);

  IF V_CONTADOR = 0 THEN
    SELECT MAX(C.CODENDENTCLI) + 1
    INTO V_PROXIMO_ID
    FROM PCCLIENTENDENT C;

    EXECUTE IMMEDIATE '
      CREATE SEQUENCE ' || V_NOME_SEQUENCIA || '
        START WITH '|| V_PROXIMO_ID || '
        INCREMENT BY 1
        NOCACHE
        NOCYCLE';

    DBMS_OUTPUT.PUT_LINE('Sequence ' || V_NOME_SEQUENCIA || ' criada com sucesso.');
  ELSE
    DBMS_OUTPUT.PUT_LINE('Sequence ' || V_NOME_SEQUENCIA || ' já existe.');
  END IF;
END;