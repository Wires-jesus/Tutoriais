CREATE OR REPLACE VIEW VW_INT_C5_CLI_CONV AS
(
SELECT DISTINCT
       C.CODCLI SEQPESSOAPORTADOR,
       C.CODCLI NROCARTAO,
       C.CODCLI SEQPESSOATITULAR,
       F.CODFINALIZADORA NROFORMAPAGTO,
       TRUNC(SYSDATE) + 100000 DTAVALIDADE,
       CASE
         WHEN C.DTEXCLUSAO IS NULL THEN
              'S'
         ELSE 'N'
       END ATIVO
FROM PCCLIENT C, MONITORPDVMIDDLE.TB_CLIENTE TC,
     (SELECT DISTINCT * FROM PCFINALIZADORA FIN, VW_INT_C5_OBTER_FILIAIS_C5 C5 WHERE FIN.CODFILIAL = C5.CODFILIAL) F,
     (select s.ultimaexecucao from pccontroleconsinco s where upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_CLIENTECARTAO') DTPADRAO
WHERE ((F.ESPECIE = 'CNV') OR ((F.ESPECIE = 'O') AND (F.CODCOB = 'CONV')))
AND F.dtinativacao IS NULL
AND C.CODCLI = TC.SEQPESSOA
AND C.CODCLI NOT IN(1,2,3)

AND GREATEST(NVL(C.dtalterc5, DTPADRAO.ULTIMAEXECUCAO),
             NVL(F.dtalterc5, DTPADRAO.ULTIMAEXECUCAO)
            ) >= DTPADRAO.ULTIMAEXECUCAO
)

\

CREATE OR REPLACE VIEW VW_INT_C5_CLI_CONV_PERIODO AS
(
SELECT DISTINCT
       F.CODFINALIZADORA NROFORMAPAGTO,
       F.CODFINALIZADORA SEQPERIODO,
       TRUNC(SYSDATE)  DTAINICIO,
       CASE WHEN (NVL(P.PRAZO1, 0) > 0) AND (P.FORMAPARCELAMENTO = 'C') THEN TRUNC(SYSDATE) + NVL(P.PRAZO1, 0) ELSE TRUNC(SYSDATE) + 30 END DTAFIM,
       CASE WHEN (NVL(P.PRAZO1, 0) > 0) AND (P.FORMAPARCELAMENTO = 'C')THEN TRUNC(SYSDATE) + NVL(P.PRAZO1, 0) ELSE TRUNC(SYSDATE) + 30 END DTAVENCTO,
       'S' ATIVO
FROM (SELECT DISTINCT * FROM PCFINALIZADORA FIN, VW_INT_C5_OBTER_FILIAIS_C5 C5 WHERE FIN.CODFILIAL = C5.CODFILIAL) F, PCPLPAG P
WHERE ((F.ESPECIE = 'CNV') OR ((F.ESPECIE = 'O') AND (F.CODCOB = 'CONV'))) 
AND  F.DTINATIVACAO IS NULL   
AND F.CODPLPAG = P.CODPLPAG 
)

\

CREATE OR REPLACE VIEW VW_INT_C5_CARTAOPRESENTE AS
(
SELECT G.NUMGIFTCARD AS NROCARTAO,
       G.VALOR AS VALOR,
       'G' AS STATUS,
       CASE
         WHEN ((G.STATUS not in('A', 'D')) OR (G.DTINATIVO IS NOT NULL) OR
              (TRUNC(SYSDATE) >= G.DTVALIDADE)) THEN
          'N'
         ELSE
          'S'
       END ATIVO
  FROM PCCADGIFTCARD G,
       (SELECT S.ULTIMAEXECUCAO
          FROM PCCONTROLECONSINCO S
         WHERE UPPER(S.OBJETOREFERENCIA) =
               'PKG_SINC_PDV_CONSINCO.CARREGA_TB_CARTAOPRESENTE') D
 WHERE NVL(G.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO
 AND LENGTH(G.NUMGIFTCARD) <= 15
)