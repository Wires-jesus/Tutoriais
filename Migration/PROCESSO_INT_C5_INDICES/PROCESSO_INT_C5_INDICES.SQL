DECLARE
  vContIdx NUMBER;
  vEXEC    VARCHAR2(4000);
BEGIN
  vContIdx := 0;
  vEXEC    := '';
  BEGIN
    SELECT COUNT(1)
      INTO vContIdx
      FROM ALL_INDEXES I
     WHERE I.TABLE_NAME = 'PCPRODFILIAL'
       AND I.INDEX_NAME = 'PCPRODFILIAL_C5_IDX01';
  END;

  IF (vContIdx = 0) THEN
    vEXEC := 'CREATE INDEX PCPRODFILIAL_C5_IDX01 ON PCPRODFILIAL (CODPROD,CODFILIAL,NVL(PROIBIDAVENDA,''N''))';
    EXECUTE IMMEDIATE vEXEC;
  END IF;

  vContIdx := 0;
  vEXEC    := '';
  BEGIN
    SELECT COUNT(1)
      INTO vContIdx
      FROM ALL_INDEXES I
     WHERE I.TABLE_NAME = 'PCEMBALAGEM'
       AND I.INDEX_NAME = 'PCEMBALAGEM_C5_IDX01';
  END;

  IF (vContIdx = 0) THEN
    vEXEC := 'CREATE INDEX PCEMBALAGEM_C5_IDX01 ON PCEMBALAGEM (CODPROD,CODFILIAL,NVL(ENVIAFRENTECAIXA,''S''),DTINATIVO,CODAUXILIAR,DTALTERC5)';
    EXECUTE IMMEDIATE vEXEC;
  END IF;

  vContIdx := 0;
  vEXEC    := '';
  BEGIN
    SELECT COUNT(1)
      INTO vContIdx
      FROM ALL_INDEXES I
     WHERE I.TABLE_NAME = 'PCPRODUT'
       AND I.INDEX_NAME = 'PCPRODUT_C5_IDX01';
  END;

  IF (vContIdx = 0) THEN
    vEXEC := 'CREATE INDEX PCPRODUT_C5_IDX01 ON PCPRODUT (CODPROD,CODMARCA,NVL(REVENDA, ''S''),NVL(TIPOMERC, ''L''),DTEXCLUSAO,NBM,DTALTERC5)';
    EXECUTE IMMEDIATE vEXEC;
  END IF;

  vContIdx := 0;
  vEXEC    := '';
  BEGIN
    SELECT COUNT(1)
      INTO vContIdx
      FROM ALL_INDEXES I
     WHERE I.TABLE_NAME = 'PCCESTPRODUTO'
       AND I.INDEX_NAME = 'PCCESTPRODUTO_IDX1';
  END;

  IF (vContIdx > 0) THEN
    vEXEC := 'DROP INDEX PCCESTPRODUTO_IDX1';
    EXECUTE IMMEDIATE vEXEC;
  END IF;


  vContIdx := 0;
  vEXEC    := '';
  BEGIN
    SELECT COUNT(1)
      INTO vContIdx
      FROM ALL_INDEXES I
     WHERE I.TABLE_NAME = 'PCCESTPRODUTO'
       AND I.INDEX_NAME = 'PCCESTPRODUTO_C5_IDX01';
  END;

  IF (vContIdx = 0) THEN
    vEXEC := 'CREATE INDEX PCCESTPRODUTO_C5_IDX01 ON PCCESTPRODUTO (CODPROD ASC)';
    EXECUTE IMMEDIATE vEXEC;
  END IF;

END;