CREATE OR REPLACE VIEW VW_INT_C5_CCTCENARIOIMPOSTO AS 
WITH BASECALCULO AS (
  SELECT
    CEN.SEQCENARIO,
    CEN.SEQIMPOSTO,
    CEN.DESCRICAO AS TIPO_IMPOSTO,
    CEN.ATIVO,
    CEN.IDREF,
    T.CST,
    T.CCLASSTRIB,
    CASE 
      WHEN CEN.DESCRICAO = 'IS'  THEN NVL(T.PERC_IS, 0)
      WHEN CEN.DESCRICAO = 'CBS' THEN NVL(T.PERC_CBS, 0)
      WHEN CEN.DESCRICAO = 'IBS' THEN 
          CASE T.TIPO_LOCAL_CONSUMO 
            WHEN 'G' THEN NVL(T.PERC_IBS_UF, 0)
            WHEN 'M' THEN NVL(T.PERC_IBS_MUN, 0)
            ELSE 0 
          END
      ELSE 0 
    END AS VALOR_PERALIQ,
    CASE 
      WHEN CEN.DESCRICAO = 'IS'  THEN NVL(T.PERC_IS, 0)
      WHEN CEN.DESCRICAO = 'CBS' THEN NVL(T.PERC_CBS, 0)
      WHEN CEN.DESCRICAO = 'IBS' THEN 
        CASE T.TIPO_LOCAL_CONSUMO 
          WHEN 'G' THEN NVL(T.PERC_IBS_UF, 0)
          WHEN 'M' THEN NVL(T.PERC_IBS_MUN, 0)
          WHEN 'M' THEN NVL(T.PERC_RED_IBS_MUN, 0)
          ELSE 0 
        END
      ELSE 0 
    END AS VALOR_PERALIQRED,
    CASE 
        WHEN CEN.DESCRICAO = 'IBS' AND T.TIPO_LOCAL_CONSUMO = 'M' THEN NVL(T.PERC_IBS_MUN, 0)
        ELSE 0 
    END AS VALOR_PERALIQMUN
  FROM MONITORPDVMIDDLE.TB_CCTCENARIO CEN
  JOIN PCTRIBUTACAO T ON CEN.IDREF = T.CODIGO_TRIBUTACAO
)
SELECT
  NVL(CI.SEQCENARIOIMPOSTO, 0) AS SEQCENARIOIMPOSTO,
  BASE.SEQCENARIO,
  BASE.SEQIMPOSTO,
  CCTC_CST.SEQCODIGOTRIBUTARIO AS SEQCODTRIBCST,
  CCTC_CC.SEQCODIGOTRIBUTARIO AS SEQCODTRIBCCLASTRIB,
  BASE.VALOR_PERALIQ AS PERALIQ,
  BASE.VALOR_PERALIQRED AS PERALIQRED,
  BASE.VALOR_PERALIQMUN AS PERALIQMUN,
  1 AS SEQFORMULA,
  BASE.ATIVO,
  BASE.IDREF || '-' || BASE.TIPO_IMPOSTO AS IDREF,
  CCTC_CST.CODIGO AS CODIGOCST,
  CCTC_CC.CODIGO AS CODIGOCCLASSTRIB,
  CCTC_CST.INDISENTO AS INDISENTO
FROM BASECALCULO BASE
LEFT JOIN MONITORPDVMIDDLE.TB_CCTCENARIOIMPOSTO CI 
       ON (CI.SEQIMPOSTO = BASE.SEQIMPOSTO AND CI.SEQCENARIO = BASE.SEQCENARIO)
LEFT JOIN MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO CCTC_CST 
       ON (CCTC_CST.INDTIPOCODIGO = 1 AND CCTC_CST.CODIGO = BASE.CST)
LEFT JOIN MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO CCTC_CC 
       ON (CCTC_CC.INDTIPOCODIGO = 2 AND CCTC_CC.CODIGO = BASE.CCLASSTRIB)

