CREATE OR REPLACE VIEW VW_INT_C5_FAMDIVISAO AS
(
 SELECT
    PRODTRIB."SEQFAMILIA", PRODTRIB."CODPROD", PRODTRIB."NROTRIBUTACAO",PRODTRIB."NRODIVISAO",PRODTRIB."IDREF",PRODTRIB."ATIVO", PRODTRIB."CODORIGEMTRIB"
 FROM
     (SELECT DISTINCT
        R.CODPROD,
        T.SEQFAMILIA,
        R.CODST nrotributacao,
        0 codorigemtrib,
        D.NRODIVISAO,
        D.NUMREGIAO IDREF,
        'S' ativo

      FROM MONITORPDVMIDDLE.TB_FAMILIA TF,
           PCDEPARAPRODC5 T,
           MONITORPDVMIDDLE.TB_TRIBUTACAOUF F,
           (SELECT PR.CODPROD, PR.CODST, PR.NUMREGIAO
            FROM PCTABPR PR,
                 (select s.ultimaexecucao from pccontroleconsinco s where upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_FAMDIVISAO') DTPADRAO
            WHERE NVL(PR.DTALTERC5, DTPADRAO.ULTIMAEXECUCAO) >= DTPADRAO.ULTIMAEXECUCAO 
            AND  PR.NUMREGIAO IN (SELECT VALOR
                                   FROM PCPARAMFILIAL
                                   WHERE NOME = 'NUMREGIAOPADRAOVAREJO'
                                   AND CODFILIAL IN (SELECT CODFILIAL FROM VW_INT_C5_OBTER_FILIAIS_C5)
                                   AND VALOR <> '99'
                                   AND VALOR IS NOT NULL)
            UNION
            SELECT PR2.CODPROD, PR2.CODST, PR2.NUMREGIAO
            FROM PCTABPR PR2,
                 MONITORPDVMIDDLE.TB_FAMILIA FAM,
                 PCDEPARAREGIAOC5 REG
            WHERE TO_CHAR(PR2.CODPROD) = FAM.IDREF
            AND   FAM.ATIVO = 'S'
            AND   REG.NUMREGIAO = PR2.NUMREGIAO
            AND   PR2.NUMREGIAO IN (SELECT VALOR
                                   FROM PCPARAMFILIAL
                                   WHERE NOME = 'NUMREGIAOPADRAOVAREJO'
                                   AND CODFILIAL IN (SELECT CODFILIAL FROM VW_INT_C5_OBTER_FILIAIS_C5)
                                   AND VALOR <> '99'
                                   AND VALOR IS NOT NULL)
            AND NOT EXISTS (SELECT 1
                            FROM MONITORPDVMIDDLE.TB_FAMDIVISAO DIV
                            WHERE DIV.NROTRIBUTACAO = PR2.CODST
                            AND   DIV.SEQFAMILIA = FAM.SEQFAMILIA
                            AND   DIV.NRODIVISAO = REG.NRODIVISAO
                           )

           )R,
          PCCONSOLIDATRIBUTACAO C,
          PCDEPARAREGIAOC5 D
      WHERE T.CODPROD = R.CODPROD
      AND   TF.IDREF = TO_CHAR(R.CODPROD)
      AND   TF.IDREF = TO_CHAR(T.CODPROD)
      AND   T.ATIVO = 'S'
	  AND   T.SEQFAMILIA = TF.SEQFAMILIA
	  AND   TF.ATIVO = 'S'
      AND   R.CODST = C.CODST
      AND   R.CODST = F.NROTRIBUTACAO
      AND   C.CODST = F.NROTRIBUTACAO
      AND   R.NUMREGIAO = C.NUMREGIAO
      AND   R.NUMREGIAO = D.NUMREGIAO
      AND   C.NUMREGIAO = D.NUMREGIAO
      AND   R.CODST IS NOT NULL
      AND FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('CON_USATRIBUTACAOPORUF', '99', 'N') <> 'S'
      GROUP BY T.SEQFAMILIA, R.CODPROD, R.CODST, D.NRODIVISAO, D.NUMREGIAO

      UNION ALL
      
      SELECT DISTINCT
        TB.CODPROD,
        T.SEQFAMILIA,
        TB.CODST nrotributacao,
        0 codorigemtrib,
        C5.CODFILIALINTEGRACAO NRODIVISAO,
         C5.CODFILIALINTEGRACAO IDREF,
        'S' ativo
      FROM MONITORPDVMIDDLE.TB_FAMILIA TF,
           PCDEPARAPRODC5 T,
           MONITORPDVMIDDLE.TB_TRIBUTACAOUF F,
           MONITORPDVMIDDLE.TB_EMPRESA E,
           PCFILIAL FIL,
           (SELECT TABTRIB.CODPROD, TABTRIB.CODST, TABTRIB.UFDESTINO, TABTRIB.CODFILIALNF
            FROM PCTABTRIB TABTRIB,
                 (select s.ultimaexecucao from pccontroleconsinco s where upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_FAMDIVISAO') DTPADRAO
            WHERE NVL(TABTRIB.DTALTERC5, DTPADRAO.ULTIMAEXECUCAO) >= DTPADRAO.ULTIMAEXECUCAO
            UNION
            SELECT TABTRIB2.CODPROD, TABTRIB2.CODST, TABTRIB2.UFDESTINO, TABTRIB2.CODFILIALNF
            FROM PCTABTRIB TABTRIB2,
                 MONITORPDVMIDDLE.TB_FAMILIA FAM
            WHERE TO_CHAR(TABTRIB2.CODPROD) = FAM.IDREF
            AND   FAM.ATIVO = 'S'
            AND NOT EXISTS (SELECT 1
                            FROM MONITORPDVMIDDLE.TB_FAMDIVISAO DIV
                            WHERE DIV.NROTRIBUTACAO = TABTRIB2.CODST
                            AND   DIV.SEQFAMILIA = FAM.SEQFAMILIA
                           )

           )TB,
           PCCONSOLIDATRIBUTACAO C,
           VW_INT_C5_OBTER_FILIAIS_C5 c5,
           (select s.ultimaexecucao from pccontroleconsinco s where upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_FAMDIVISAO') DTPADRAO
      
      WHERE T.CODPROD = TB.CODPROD
      AND   TF.IDREF = TO_CHAR(TB.CODPROD)
      AND   TF.IDREF = TO_CHAR(T.CODPROD)
      AND   T.ATIVO = 'S'
	  AND   T.SEQFAMILIA = TF.SEQFAMILIA
	  AND   TF.ATIVO = 'S'
      AND   F.NROTRIBUTACAO = TB.CODST
      AND   F.UFORIGEM = TB.UFDESTINO
      AND   C5.CODFILIAL = TB.CODFILIALNF
      AND   C.CODST = TB.CODST
      AND   C.NUMREGIAO = C5.CODFILIALINTEGRACAO
      AND   C.UFORIGEM = TB.UFDESTINO
      AND   C5.CODFILIAL = TB.CODFILIALNF
      AND   FIL.UF = TB.UFDESTINO
      AND   FIL.CODIGO = TB.CODFILIALNF
      AND   F.NROTRIBUTACAO = C.CODST
      AND   F.UFORIGEM = C.UFORIGEM
      AND   E.NROEMPRESA = C.NUMREGIAO
      AND   E.NROEMPRESA = C5.CODFILIALINTEGRACAO
      AND   FIL.CODIGO  = C5.CODFILIAL
      AND   TB.CODST IS NOT NULL
      AND FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('CON_USATRIBUTACAOPORUF', '99', 'N') = 'S'
      GROUP BY T.SEQFAMILIA, TB.CODPROD, TB.CODST, C5.CODFILIALINTEGRACAO
      )PRODTRIB    
  )

