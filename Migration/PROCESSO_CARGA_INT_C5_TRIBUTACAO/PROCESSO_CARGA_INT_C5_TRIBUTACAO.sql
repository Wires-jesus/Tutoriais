    B.SEQCENARIO,
    NVL(I.PRODUTO, '0') AS PRODUTO, 
    NVL(I.NCM, '0') AS NCM,
    B.FAMILIA,
    B.CGO,
    B.ORIGEM,
    B.DESTINO,
    B.INDTIPOENTIDADEORIGEM,
    B.INDTIPOENTIDADEDESTINO,
    B.ATIVO,
    B.NBS,
    B.IDREF
FROM 
	BASE_UNICA B
LEFT JOIN PRODUTOS_E_NCMS I 
    ON B.SEQCENARIO = I.SEQCENARIO
ORDER BY
	I.SEQCENARIO,
	PRODUTO,
	NCM



