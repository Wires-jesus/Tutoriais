CREATE OR REPLACE VIEW VW_INT_C5_CCTALIQUOTA AS 
(
  SELECT 
    (SELECT IMP.SEQIMPOSTO FROM MONITORPDVMIDDLE.TB_CCTIMPOSTO IMP WHERE IMP.DESCRICAO = CEN.DESCRICAO) AS SEQIMPOSTO,
    TRIB.DTINICIO_VIGENCIA AS DTAINICIALVALIDADE,
    TRIB.DTFIM_VIGENCIA AS DTAFINALVALIDADE,
    NVL(CASE
      WHEN CEN.DESCRICAO = 'CBS' THEN TRIB.PERC_CBS
      WHEN CEN.DESCRICAO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'M' THEN TRIB.PERC_IBS_MUN
      WHEN CEN.DESCRICAO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'G' THEN TRIB.PERC_IBS_UF
      WHEN CEN.DESCRICAO = 'IS' THEN TRIB.PERC_IS
      ELSE 0
    END, 0) AS PERALIQ,
    CASE
      WHEN CEN.DESCRICAO IN ('CBS', 'IS') THEN ''
      WHEN CEN.DESCRICAO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'M' THEN (SELECT C.UF FROM PCCIDADE C WHERE C.CODIBGE = TRIB.LOCAL_CONSUMO_MUNICIPIO) 
      WHEN CEN.DESCRICAO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'G' THEN TRIB.LOCAL_CONSUMO_GERAL
      ELSE NULL
    END AS UF,
    CASE
      WHEN CEN.DESCRICAO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'M' THEN TRIB.LOCAL_CONSUMO_MUNICIPIO 
      ELSE NULL
    END AS CODIBGE,
    CEN.ATIVO,
    CEN.IDREF AS IDREF
  FROM MONITORPDVMIDDLE.TB_CCTCENARIO CEN,
      PCTRIBUTACAO TRIB,
      (SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
          FROM PCCONTROLECONSINCO S
        WHERE ATIVO = 'A'
          AND (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_CCTALIQUOTA')
      ) D
  WHERE CEN.IDREF = TRIB.CODIGO_TRIBUTACAO
    AND (SYSDATE >= TRIB.DTINICIO_VIGENCIA AND SYSDATE <= TRIB.DTFIM_VIGENCIA)
    AND NVL(TRIB.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO
)

