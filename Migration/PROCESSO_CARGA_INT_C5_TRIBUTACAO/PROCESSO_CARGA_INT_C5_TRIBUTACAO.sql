CREATE OR REPLACE VIEW VW_INT_C5_CCTCODIGOTRIBUTARIO AS 
(
  SELECT 
    *
  FROM (
    SELECT
      MIN(NVL((SELECT SEQCODIGOTRIBUTARIO
              FROM MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO
          WHERE CODIGO = CODTRIB.CODIGO
          AND ROWNUM = 1), 0)) SEQCODIGOTRIBUTARIO,
      CODTRIB.CODIGO,
      CODTRIB.DESCRICAO,
      CODTRIB.CODIGOPAI,
      CODTRIB.INDTIPOCODIGO,
      CODTRIB.DTAINICIALVALIDADE,
      CODTRIB.DTAFINALVALIDADE,
      CODTRIB.PERALIQREDCBS,
      CODTRIB.PERALIQREDIBS,
      CODTRIB.ATIVO,
      CODTRIB.IDREF,
      ROW_NUMBER() OVER (
        PARTITION BY CODTRIB.CODIGO
        ORDER BY CODTRIB.DTAFINALVALIDADE ASC
      ) AS RN
    FROM (
      SELECT
        T.CST AS CODIGO,
        'CST ' || T.CST AS DESCRICAO,
        1 AS INDTIPOCODIGO,
        T.CCLASSTRIB AS CODIGOPAI,
        CEN.DTAINICIALVALIDADE,
        CEN.DTAFINALVALIDADE,
        NVL(T.PERC_RED_CBS, 0) AS PERALIQREDCBS,
        CASE
          WHEN T.TIPO_LOCAL_CONSUMO = 'G' THEN NVL(T.PERC_RED_IBS_UF, 0)
          WHEN T.TIPO_LOCAL_CONSUMO = 'M' THEN NVL(T.PERC_RED_IBS_MUN, 0)
          ELSE 0
        END AS PERALIQREDIBS,
        CEN.ATIVO,
        CEN.IDREF IDREF
      FROM PCTRIBUTACAO T
      INNER JOIN MONITORPDVMIDDLE.TB_CCTCENARIO CEN ON (CEN.IDREF = T.CODIGO_TRIBUTACAO)
      WHERE (SYSDATE >= CEN.DTAINICIALVALIDADE AND SYSDATE <= CEN.DTAFINALVALIDADE)
      
      UNION ALL
      
      SELECT
        T.CCLASSTRIB AS CODIGO,
        'CCLASSTRIB ' || T.CCLASSTRIB AS DESCRICAO,
        2 AS INDTIPOCODIGO,
        T.CCLASSTRIB AS CODIGOPAI,
        CEN.DTAINICIALVALIDADE,
        CEN.DTAFINALVALIDADE,
        NVL(T.PERC_RED_CBS, 0) AS PERALIQREDCBS,
        CASE
          WHEN T.TIPO_LOCAL_CONSUMO = 'G' THEN NVL(T.PERC_RED_IBS_UF, 0)
          WHEN T.TIPO_LOCAL_CONSUMO = 'M' THEN NVL(T.PERC_RED_IBS_MUN, 0)
          ELSE 0
        END AS PERALIQREDIBS,
        CEN.ATIVO,
        CEN.IDREF IDREF
      FROM PCTRIBUTACAO T
      INNER JOIN MONITORPDVMIDDLE.TB_CCTCENARIO CEN ON (CEN.IDREF = T.CODIGO_TRIBUTACAO)
      WHERE (SYSDATE >= CEN.DTAINICIALVALIDADE AND SYSDATE <= CEN.DTAFINALVALIDADE)
    ) CODTRIB
    GROUP BY CODTRIB.CODIGO, CODTRIB.DESCRICAO, CODTRIB.CODIGOPAI, CODTRIB.INDTIPOCODIGO, 
            CODTRIB.DTAINICIALVALIDADE, CODTRIB.DTAFINALVALIDADE, CODTRIB.PERALIQREDCBS, 
            CODTRIB.PERALIQREDIBS, CODTRIB.ATIVO, CODTRIB.IDREF
  )
  WHERE RN = 1
)

