CREATE OR REPLACE VIEW VW_INT_C5_CCTCODIGOTRIBUTARIO AS 
WITH CTEPCTRIBUTACAO AS (
    SELECT 
        T.CST,
        T.CCLASSTRIB,
        T.PERC_RED_CBS,
        T.TIPO_LOCAL_CONSUMO,
        T.PERC_RED_IBS_UF,
        T.PERC_RED_IBS_MUN,
        CEN.DTAINICIALVALIDADE,
        CEN.DTAFINALVALIDADE,
        CEN.ATIVO,
        CEN.IDREF,
        (SELECT DECODE(MAX(PC.TRIBUTACAOGIBSCBS), 'Sim', 'N', 'Não', 'S') 
          FROM PCCSTTRIBUTACAOIBSCBS PC 
          WHERE PC.CODSITUACAOTRIBUTARIA = T.CST) AS INDISENTO
    FROM PCTRIBUTACAO T
    INNER JOIN MONITORPDVMIDDLE.TB_CCTCENARIO CEN ON (CEN.IDREF = T.CODIGO_TRIBUTACAO)
    WHERE SYSDATE BETWEEN CEN.DTAINICIALVALIDADE AND CEN.DTAFINALVALIDADE
),
CTECSTCCLASSTRIB AS (
    SELECT 
        CST AS CODIGO, 'CST ' || CST AS DESCRICAO, 1 AS INDTIPOCODIGO, 
        CCLASSTRIB AS CODIGOPAI, INDISENTO, DTAINICIALVALIDADE, DTAFINALVALIDADE,
        PERC_RED_CBS, TIPO_LOCAL_CONSUMO, PERC_RED_IBS_UF, PERC_RED_IBS_MUN, ATIVO, IDREF
    FROM CTEPCTRIBUTACAO
    UNION ALL
    SELECT 
        CCLASSTRIB AS CODIGO, 'CCLASSTRIB ' || CCLASSTRIB AS DESCRICAO, 2 AS INDTIPOCODIGO, 
        CCLASSTRIB AS CODIGOPAI, INDISENTO, DTAINICIALVALIDADE, DTAFINALVALIDADE,
        PERC_RED_CBS, TIPO_LOCAL_CONSUMO, PERC_RED_IBS_UF, PERC_RED_IBS_MUN, ATIVO, IDREF
    FROM CTEPCTRIBUTACAO
),
CTEROWNUMBER AS (
    SELECT
        MIN(NVL((SELECT SEQCODIGOTRIBUTARIO 
                  FROM MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO 
                  WHERE CODIGO = U.CODIGO AND ROWNUM = 1), 0)) AS SEQCODIGOTRIBUTARIO,
        U.CODIGO,
        U.DESCRICAO,
        U.CODIGOPAI,
        U.INDTIPOCODIGO,
        U.INDISENTO,
        U.DTAINICIALVALIDADE,
        U.DTAFINALVALIDADE,
        NVL(U.PERC_RED_CBS, 0) AS PERALIQREDCBS,
        CASE
            WHEN U.TIPO_LOCAL_CONSUMO = 'G' THEN NVL(U.PERC_RED_IBS_UF, 0)
            WHEN U.TIPO_LOCAL_CONSUMO = 'M' THEN NVL(U.PERC_RED_IBS_MUN, 0)
            ELSE 0
        END AS PERALIQREDIBS,
        U.ATIVO,
        U.IDREF,
        ROW_NUMBER() OVER (PARTITION BY U.CODIGO ORDER BY U.DTAFINALVALIDADE ASC) AS RN
    FROM CTECSTCCLASSTRIB U
    GROUP BY 
        U.CODIGO, U.DESCRICAO, U.CODIGOPAI, U.INDTIPOCODIGO, U.INDISENTO,
        U.DTAINICIALVALIDADE, U.DTAFINALVALIDADE, U.PERC_RED_CBS, 
        U.TIPO_LOCAL_CONSUMO, U.PERC_RED_IBS_UF, U.PERC_RED_IBS_MUN, U.ATIVO, U.IDREF
)
SELECT * FROM CTEROWNUMBER 
WHERE RN = 1

