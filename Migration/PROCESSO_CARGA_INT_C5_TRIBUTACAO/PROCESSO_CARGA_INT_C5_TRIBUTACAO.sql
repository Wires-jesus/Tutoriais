CREATE OR REPLACE VIEW VW_INT_C5_CCTCENARIOITEM AS 
WITH BASE_UNICA AS (
    SELECT
        SEQCENARIO,
        MAX(CASE WHEN IDENTIFICADOR = 'FAMILIA' THEN VALOR END) AS FAMILIA,
        MAX(CASE WHEN IDENTIFICADOR = 'CGO'     THEN VALOR END) AS CGO,
        MAX(CASE WHEN IDENTIFICADOR = 'ORIGEM'  THEN VALOR END) AS ORIGEM,
        MAX(CASE WHEN IDENTIFICADOR = 'DESTINO' THEN VALOR END) AS DESTINO,
        MAX(CASE WHEN IDENTIFICADOR = 'NBS'     THEN VALOR END) AS NBS,
        MIN(INDTIPOENTIDADE) AS INDTIPOENTIDADEORIGEM, 
        MIN(INDTIPOENTIDADE) AS INDTIPOENTIDADEDESTINO,
        MIN(ATIVO) AS ATIVO,
		MIN(SEQCENARIOCONDICAOITEM) AS IDREF
    FROM 
		MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
    GROUP BY 
		SEQCENARIO
),

PRODUTOS_E_NCMS AS (
    SELECT DISTINCT 
        SEQCENARIO, 
        VALOR AS PRODUTO, 
        '0'   AS NCM
    FROM 
		MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
    WHERE IDENTIFICADOR = 'PRODUTO'
      AND VALOR <> '0' 

    UNION ALL

    SELECT DISTINCT 
        SEQCENARIO, 
        '0'   AS PRODUTO, 
        VALOR AS NCM
    FROM 
		MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
    WHERE IDENTIFICADOR = 'NCM'
      AND VALOR <> '0'
)

