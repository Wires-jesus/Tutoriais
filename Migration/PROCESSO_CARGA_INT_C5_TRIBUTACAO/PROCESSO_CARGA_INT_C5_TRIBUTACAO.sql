CREATE OR REPLACE VIEW VW_INT_C5_FAMDIVISAO AS
WITH 
  cte_param_data AS (
    SELECT /*+ MATERIALIZE */ s.ultimaexecucao 
    FROM pccontroleconsinco s 
    WHERE upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_FAMDIVISAO'
  ),
  cte_filiais_validas AS (
    SELECT /*+ MATERIALIZE */ CODFILIAL, CODFILIALINTEGRACAO FROM VW_INT_C5_OBTER_FILIAIS_C5
  ),
  cte_regioes_validas AS (
    SELECT /*+ MATERIALIZE */ VALOR 
    FROM PCPARAMFILIAL
    WHERE NOME = 'NUMREGIAOPADRAOVAREJO'
      AND CODFILIAL IN (SELECT CODFILIAL FROM cte_filiais_validas)
      AND VALOR <> '99'
      AND VALOR IS NOT NULL
  ),
  cte_pcdeparaprodc5 AS (
    SELECT SEQFAMILIA, SEQPRODUTO, CODPROD, CODAUXILIAR, ATIVO FROM PCDEPARAPRODC5
  ),
  cte_base_tabpr AS (
    SELECT DISTINCT 
           PR.CODPROD, 
           PR.CODST, 
           PR.NUMREGIAO,
           REG.NRODIVISAO
    FROM PCTABPR PR
    JOIN cte_regioes_validas REG_VAL ON PR.NUMREGIAO = REG_VAL.VALOR
    JOIN MONITORPDVMIDDLE.TB_FAMILIA FAM ON PR.CODPROD = FAM.IDREF AND FAM.ATIVO = 'S'
    JOIN PCDEPARAREGIAOC5 REG ON REG.NUMREGIAO = PR.NUMREGIAO
    CROSS JOIN cte_param_data DTPADRAO
    LEFT JOIN MONITORPDVMIDDLE.TB_FAMDIVISAO DIV 
           ON DIV.NROTRIBUTACAO = PR.CODST
          AND DIV.SEQFAMILIA = FAM.SEQFAMILIA
          AND DIV.NRODIVISAO = REG.NRODIVISAO
    WHERE (
       NVL(PR.DTALTERC5, DTPADRAO.ULTIMAEXECUCAO) >= DTPADRAO.ULTIMAEXECUCAO
       OR
       DIV.SEQFAMILIA IS NULL
    )
  ),
  cte_base_tabtrib AS (
    SELECT DISTINCT 
           TAB.CODPROD, 
           TAB.CODST, 
           TAB.UFDESTINO, 
           TAB.CODFILIALNF
    FROM PCTABTRIB TAB
    JOIN MONITORPDVMIDDLE.TB_FAMILIA FAM ON TAB.CODPROD = FAM.IDREF AND FAM.ATIVO = 'S'
    CROSS JOIN cte_param_data DTPADRAO
    LEFT JOIN MONITORPDVMIDDLE.TB_FAMDIVISAO DIV 
           ON DIV.NROTRIBUTACAO = TAB.CODST
          AND DIV.SEQFAMILIA = FAM.SEQFAMILIA
    WHERE (
       NVL(TAB.DTALTERC5, DTPADRAO.ULTIMAEXECUCAO) >= DTPADRAO.ULTIMAEXECUCAO
       OR
       DIV.SEQFAMILIA IS NULL
    )
  )
  
SELECT
    PRODTRIB."SEQFAMILIA", 
    PRODTRIB."CODPROD", 
    PRODTRIB."NROTRIBUTACAO",
    PRODTRIB."NRODIVISAO",
    PRODTRIB."IDREF",
    PRODTRIB."ATIVO", 
    PRODTRIB."CODORIGEMTRIB"
FROM ( 
  SELECT 
      T.SEQFAMILIA,
      R.CODPROD,
      R.CODST AS nrotributacao,
      0 AS codorigemtrib,
      D.NRODIVISAO,
      D.NUMREGIAO AS IDREF,
      'S' AS ativo
  FROM cte_base_tabpr R
  JOIN cte_pcdeparaprodc5 T ON T.CODPROD = R.CODPROD
  JOIN MONITORPDVMIDDLE.TB_TRIBUTACAOUF F ON F.NROTRIBUTACAO = R.CODST
  JOIN PCCONSOLIDATRIBUTACAO C ON C.CODST = R.CODST AND C.NUMREGIAO = R.NUMREGIAO
  JOIN PCDEPARAREGIAOC5 D ON D.NUMREGIAO = R.NUMREGIAO
  WHERE T.ATIVO = 'S'
    AND R.CODST IS NOT NULL
    AND FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('CON_USATRIBUTACAOPORUF', '99', 'N') <> 'S'
  GROUP BY T.SEQFAMILIA, R.CODPROD, R.CODST, D.NRODIVISAO, D.NUMREGIAO

  UNION ALL
  
  SELECT 
      T.SEQFAMILIA,
      TB.CODPROD,
      TB.CODST AS NROTRIBUTACAO,
      0 AS CODORIGEMTRIB,
      C5.CODFILIALINTEGRACAO AS NRODIVISAO,
      C5.CODFILIALINTEGRACAO AS IDREF,
      'S' AS ATIVO
  FROM cte_base_tabtrib TB
  JOIN cte_pcdeparaprodc5 T ON T.CODPROD = TB.CODPROD
  JOIN MONITORPDVMIDDLE.TB_TRIBUTACAOUF F ON F.NROTRIBUTACAO = TB.CODST 
                                          AND F.UFORIGEM = TB.UFDESTINO
  JOIN cte_filiais_validas C5 ON C5.CODFILIAL = TB.CODFILIALNF
  JOIN PCCONSOLIDATRIBUTACAO C ON C.CODST = TB.CODST 
                              AND C.NUMREGIAO = C5.CODFILIALINTEGRACAO 
                              AND C.UFORIGEM = TB.UFDESTINO
                              AND C.CODST = F.NROTRIBUTACAO 
                              AND C.UFORIGEM = F.UFORIGEM
  INNER JOIN PCFILIAL FIL ON FIL.CODIGO = TB.CODFILIALNF 
                          AND FIL.UF = TB.UFDESTINO 
                          AND FIL.CODIGO = C5.CODFILIAL
  INNER JOIN MONITORPDVMIDDLE.TB_EMPRESA E ON E.NROEMPRESA = C.NUMREGIAO 
                                          AND E.NROEMPRESA = C5.CODFILIALINTEGRACAO
  WHERE T.ATIVO = 'S'
    AND TB.CODST IS NOT NULL
    AND FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('CON_USATRIBUTACAOPORUF', '99', 'N') = 'S'
  GROUP BY T.SEQFAMILIA, TB.CODPROD, TB.CODST, C5.CODFILIALINTEGRACAO
) PRODTRIB

