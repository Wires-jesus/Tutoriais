CREATE OR REPLACE VIEW VW_INT_C5_CCTCENARIOITEM AS 
WITH BASE_DADOS AS (
    SELECT 
        SEQCENARIO,
        IDENTIFICADOR,
        VALOR,
        ATIVO,
        INDTIPOENTIDADE,
        MAX(CASE WHEN IDENTIFICADOR = 'FAMILIA' THEN VALOR END) OVER (PARTITION BY SEQCENARIO) AS FAMILIA,
        MAX(CASE WHEN IDENTIFICADOR = 'CGO'     THEN VALOR END) OVER (PARTITION BY SEQCENARIO) AS CGO,
        MAX(CASE WHEN IDENTIFICADOR = 'ORIGEM'  THEN VALOR END) OVER (PARTITION BY SEQCENARIO) AS ORIGEM,
        MAX(CASE WHEN IDENTIFICADOR = 'DESTINO' THEN VALOR END) OVER (PARTITION BY SEQCENARIO) AS DESTINO,
        MAX(CASE WHEN IDENTIFICADOR = 'NBS'     THEN VALOR END) OVER (PARTITION BY SEQCENARIO) AS NBS,
        MAX(CASE 
                WHEN IDENTIFICADOR IN ('PRODUTO', 'NCM') 
                AND VALOR != '0' 
                AND ATIVO = 'S' THEN 1 
                ELSE 0 
            END) OVER (PARTITION BY SEQCENARIO) AS TEM_ATIVO_NUM
    FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
),
NORMALIZACAO_DADOS AS (
    SELECT 
        SEQCENARIO,
        CASE WHEN IDENTIFICADOR = 'PRODUTO' THEN VALOR ELSE '0' END AS PRODUTO,
        CASE WHEN IDENTIFICADOR = 'NCM'     THEN VALOR ELSE '0' END AS NCM,
        FAMILIA, CGO, ORIGEM, DESTINO, NBS, INDTIPOENTIDADE, ATIVO, TEM_ATIVO_NUM
    FROM BASE_DADOS
    WHERE IDENTIFICADOR IN ('PRODUTO', 'NCM')
)
SELECT 
    SEQCENARIO,
    PRODUTO,
    NCM,
    FAMILIA,
    CGO,
    ORIGEM,
    DESTINO,
    MIN(INDTIPOENTIDADE) AS INDTIPOENTIDADEORIGEM,
    MIN(INDTIPOENTIDADE) AS INDTIPOENTIDADEDESTINO,
    CASE 
        WHEN PRODUTO = '0' AND NCM = '0' THEN 
            CASE WHEN MAX(TEM_ATIVO_NUM) = 1 THEN 'N' ELSE 'S' END
        ELSE MIN(ATIVO) 
    END AS ATIVO,
    NBS
FROM NORMALIZACAO_DADOS
GROUP BY 
    SEQCENARIO, PRODUTO, NCM, FAMILIA, CGO, ORIGEM, DESTINO, NBS
ORDER BY 
    SEQCENARIO, PRODUTO DESC, NCM DESC