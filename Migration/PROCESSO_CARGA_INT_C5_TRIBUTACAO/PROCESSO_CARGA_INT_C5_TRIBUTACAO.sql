CREATE OR REPLACE VIEW VW_INT_C5_CCTCENCONDITEM AS 
WITH BASE_DADOS AS (
    SELECT 
        CC.SEQCENARIOCONDICAO,
        CC.SEQCENARIO,
        CC.SEQCONDICAO,
        CC.ATIVO AS CC_ATIVO,
        DPTRIB.CODIGO_TRIBUTACAO AS IDREF,
        CASE CC.SEQCONDICAO
            WHEN 1 THEN TO_CHAR(DEPARA.SEQPRODUTO)
            WHEN 3 THEN TO_CHAR(NCM_F.NCM)
            WHEN 8 THEN 
                CASE WHEN T.TIPO_LOCAL_CONSUMO = 'G' 
                     THEN TO_CHAR(NVL(T.LOCAL_CONSUMO_GERAL, 'BR'))
                     ELSE TO_CHAR(T.LOCAL_CONSUMO_MUNICIPIO)
                END
            ELSE '0'
        END AS VALOR_ESPECIFICO,
        DECODE(CC.SEQCONDICAO, 1,'PRODUTO', 2,'FAMILIA', 3,'NCM', 4,'CFOP', 5,'CGO', 6,'ORIGEM', 7,'DESTINO', 8,'UFORIGEM', 9,'UFDESTINO', 10,'NBS') AS IDENTIFICADOR,
        CASE 
            WHEN CC.SEQCONDICAO = 1 THEN CASE WHEN CC.ATIVO = 'S' AND DEPARA.ATIVO = 'S' AND P_FILTRO.DTEXCLUSAO IS NULL THEN 'S' ELSE 'N' END
            WHEN CC.SEQCONDICAO = 3 THEN CASE WHEN CC.ATIVO = 'S' AND NCM_F.DTEXCLUSAO IS NULL THEN 'S' ELSE 'N' END
            
            ELSE CC.ATIVO
        END AS ATIVO_ESPECIFICO
    FROM VW_INT_C5_CCTCENARIOCONDICAO CC
    JOIN PCDEPARATRIBUTACAOC5 DPTRIB ON DPTRIB.SEQCENARIO = CC.SEQCENARIO
    JOIN PCTRIBUTACAO T ON DPTRIB.CODIGO_TRIBUTACAO = T.CODIGO_TRIBUTACAO
    LEFT JOIN PCTRIBUTACAO_FILTRO_PRODUTO P_FILTRO ON P_FILTRO.CODIGO_TRIBUTACAO = DPTRIB.CODIGO_TRIBUTACAO AND CC.SEQCONDICAO = 1
    LEFT JOIN PCDEPARAPRODC5 DEPARA ON DEPARA.CODPROD = P_FILTRO.CODPROD AND CC.SEQCONDICAO = 1
    LEFT JOIN PCTRIBUTACAO_FILTRO_NCM NCM_F ON NCM_F.CODIGO_TRIBUTACAO = DPTRIB.CODIGO_TRIBUTACAO AND CC.SEQCONDICAO = 3
    WHERE CC.SEQCONDICAO BETWEEN 1 AND 10
),
UNIFICACAO_LINHAS AS (
    SELECT SEQCENARIOCONDICAO, SEQCENARIO, SEQCONDICAO, IDREF, VALOR_ESPECIFICO AS VALOR, IDENTIFICADOR, ATIVO_ESPECIFICO AS ATIVO
    FROM BASE_DADOS
    WHERE VALOR_ESPECIFICO IS NOT NULL 

    UNION ALL

    SELECT DISTINCT 
        SEQCENARIOCONDICAO, SEQCENARIO, SEQCONDICAO, IDREF, '0' AS VALOR, IDENTIFICADOR,
        CASE 
            WHEN MAX(ATIVO_ESPECIFICO) OVER (PARTITION BY SEQCENARIOCONDICAO, IDENTIFICADOR) = 'S' THEN 'N'
            ELSE 'S' 
        END AS ATIVO
    FROM BASE_DADOS
    WHERE SEQCONDICAO IN (1, 3) 
      AND VALOR_ESPECIFICO <> '0'  
)
SELECT 
    NVL((SELECT MIN(I.SEQCENARIOCONDICAOITEM)
         FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM I
         WHERE I.SEQCENARIOCONDICAO = U.SEQCENARIOCONDICAO
           AND I.VALOR = U.VALOR
           AND I.IDENTIFICADOR = U.IDENTIFICADOR
           AND ROWNUM = 1
        ), 0) AS SEQCENARIOCONDICAOITEM,
    U.SEQCENARIOCONDICAO,
    U.VALOR,
    'N' AS INDTIPOIDENTIDADE,
    U.IDENTIFICADOR,
    U.SEQCENARIO,
    U.ATIVO,
    U.IDREF
FROM UNIFICACAO_LINHAS U
ORDER BY U.SEQCENARIO, U.SEQCENARIOCONDICAO

