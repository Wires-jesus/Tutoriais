CREATE OR REPLACE VIEW VW_INT_C5_DESC2048FIDELIDADE AS
(
SELECT DISTINCT
     NVL((SELECT M.SEQREGRA
            FROM MONITORPDVMIDDLE.TB_REGRAINCENTIVO M
           WHERE M.IDREF = P.CODFILIAL||2048||P.CODFIDELIDADE
             AND ROWNUM = 1), 0) SEQREGRA,
     C5.CODFILIALINTEGRACAO NROEMPRESA,
     'DESCONTO FIDELIDADE' REGRA,
     3 AS SEQTIPOCREDITO,
     (CASE
        WHEN (P.DTFINAL < TRUNC(SYSDATE) OR P.DTEXCLUSAO IS NOT NULL) THEN
             'N'
        ELSE 'S'
     END) ATIVO,
     'G' TIPOREGRA,
     'S' CUMULATIVO,
     1 NROSEGMENTO,
     P.DTINICIAL DTAHORINICIO,
     P.DTFINAL DTAHORFIM,
     P.CODCLI SEQPESSOA,
     P.PERCDESCONTO PERCDESCONTO,
     CASE WHEN (FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('UTILIZACASHBACK', P.CODFILIAL, 'N') = 'S') AND (FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('GATILHOGERACAOCASHBACK', P.CODFILIAL, 'C') = 'P')
        THEN NVL(P.PRECOCASHBACK, 0)
        ELSE 0
     END PRECO,
     P.CODFILIAL||2048||P.CODFIDELIDADE IDREF,
     P.CODFIDELIDADE
FROM PCDESCONTOFIDELIDADE P,
     (SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
      FROM PCCONTROLECONSINCO S
      WHERE (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRAPRODUTO')
         or (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRAEMPRESA')
         or (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRASEGMENTO')
     ) DATAPADRAO,
     VW_INT_C5_OBTER_FILIAIS_C5 C5
WHERE 0 = 0
AND   P.DTINICIAL IS NOT NULL
AND   P.DTFINAL IS NOT NULL
AND   NVL(P.CODFILIAL, '99') <> '99'
AND   P.CODFILIAL = C5.CODFILIAL
AND   TRUNC(SYSDATE) BETWEEN P.DTINICIAL AND P.DTFINAL
AND   GREATEST(NVL(P.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO), DATAPADRAO.ULTIMAEXECUCAO) >= DATAPADRAO.ULTIMAEXECUCAO
)

