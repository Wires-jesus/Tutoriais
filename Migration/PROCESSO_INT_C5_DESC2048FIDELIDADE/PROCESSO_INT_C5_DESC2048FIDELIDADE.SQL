CREATE OR REPLACE VIEW VW_INT_C5_DESC2048PRODUTO AS
(

SELECT
   PRODUTO.SEQREGRA, 
   PRODUTO.REGRA, 
   PRODUTO.NROEMPRESA,
   PRODUTO.CODPROD, 
   DEPARA.SEQPRODUTO,
   E.QTUNIT QTDEMBALAGEM,
   PRODUTO.ORIGEM, 
   PRODUTO.SEQTIPOCREDITO, 
   PRODUTO.ATIVO, 
   PRODUTO.TIPOREGRA, 
   PRODUTO.CUMULATIVO,  
   PRODUTO.NROSEGMENTO,
   PRODUTO.DTAHORINICIO,
   PRODUTO.DTAHORFIM,  
   PRODUTO.PERCDESCONTO,
   PRODUTO.PRECO,
   PRODUTO.IDREF
FROM
    (
      SELECT DISTINCT
           NVL((SELECT M.SEQREGRA
                    FROM MONITORPDVMIDDLE.TB_REGRAINCENTIVO M
                    WHERE M.IDREF = P.CODFILIAL||2048||P.CODFIDELIDADE
                    AND   ROWNUM = 1), 0) SEQREGRA,
           'DESCONTO FIDELIDADE' REGRA,
           PCPRODUT.CODPROD,
           CASE
             WHEN (P.CODFORNEC = PCPRODUT.CODFORNEC) THEN
                'FORNCEDOR'
             WHEN (P.CODPROD = PCPRODUT.CODPROD) THEN
                'PRODUTO'
             WHEN ((P.CODEPTO IS NOT NULL) OR (P.CODSEC IS NOT NULL) OR (P.CODCATEGORIA IS NOT NULL) OR (P.CODSUBCATEGORIA IS NOT NULL)) THEN
                'CLASSIFICACAO MERCADOLOGICA'
             ELSE NULL
           END ORIGEM,
           C5.CODFILIALINTEGRACAO NROEMPRESA,
           C5.CODFILIAL,
           3 AS SEQTIPOCREDITO,
           (CASE
              WHEN (P.DTFINAL < TRUNC(SYSDATE) OR P.DTEXCLUSAO IS NOT NULL) THEN
                   'N'
              ELSE 'S'
           END) ATIVO,
           'G' TIPOREGRA,
           'S' CUMULATIVO,
           1 NROSEGMENTO,
           P.DTINICIAL DTAHORINICIO,
           P.DTFINAL DTAHORFIM,           
           CASE WHEN (P.CODFIDELIDADE IN (SELECT F.CODFIDELIDADE FROM PCBINCARTAOFIDELIDADE F GROUP BY F.CODFIDELIDADE))
               THEN 0
               --ELSE P.PERCDESCONTO
               ELSE 
                  CASE 
                     WHEN (NOT EXISTS (SELECT F.CODFIDELIDADE
                                         FROM PCDESCONTOFIDELIDADE F,
                                              PCGRUPOFIDELIDADEDESCONTO G 
                                        WHERE F.CODFIDELIDADE = G.CODFIDELIDADE
                                          AND F.CODFIDELIDADE = P.CODFIDELIDADE)) THEN P.PERCDESCONTO
                     ELSE 0
                  END
           END PERCDESCONTO,
         CASE WHEN (FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('UTILIZACASHBACK', P.CODFILIAL, 'N') = 'S') AND (FERRAMENTAS.F_BUSCARPARAMETRO_ALFA('GATILHOGERACAOCASHBACK', P.CODFILIAL, 'C') = 'P')
              THEN NVL(P.PRECOCASHBACK, 0)
          ELSE 0
         END PRECO,
         P.CODFILIAL||2048||P.CODFIDELIDADE IDREF
      FROM PCDESCONTOFIDELIDADE P,
           PCPRODUT ,
           (SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
            FROM PCCONTROLECONSINCO S
            WHERE (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRAPRODUTO')
               or (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRAEMPRESA')
               or (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRASEGMENTO')
           ) DATAPADRAO, VW_INT_C5_OBTER_FILIAIS_C5 C5
      WHERE P.CODFILIAL = C5.CODFILIAL

      AND   NVL(P.CODFILIAL, '99') <> '99'
      AND   P.DTINICIAL IS NOT NULL
      AND   P.DTFINAL IS NOT NULL
      AND   TRUNC(SYSDATE) BETWEEN P.DTINICIAL AND P.DTFINAL
      AND   GREATEST(NVL(P.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO)) >= DATAPADRAO.ULTIMAEXECUCAO
      AND   ((((P.CODFORNEC = PCPRODUT.CODFORNEC))) OR--FORNECEDOR
             (((P.CODPROD = PCPRODUT.CODPROD))) OR--PRODUTO
             (
              ((P.CODEPTO IS NOT NULL) OR (P.CODSEC IS NOT NULL) OR (P.CODCATEGORIA IS NOT NULL) OR (P.CODSUBCATEGORIA IS NOT NULL))

               AND

               ((P.CODSUBCATEGORIA IS NOT NULL) AND (PCPRODUT.CODSUBCATEGORIA = P.CODSUBCATEGORIA) OR
               (P.CODSUBCATEGORIA IS NULL) AND (P.CODCATEGORIA IS NOT NULL) AND (PCPRODUT.CODCATEGORIA = P.CODCATEGORIA) OR
               (P.CODSUBCATEGORIA IS NULL) AND (P.CODCATEGORIA IS NULL) AND (P.CODSEC IS NOT NULL) AND(PCPRODUT.CODSEC = P.CODSEC) OR
               (P.CODSUBCATEGORIA IS NULL) AND (P.CODCATEGORIA IS NULL) AND (P.CODSEC IS NULL) AND (P.CODEPTO IS NOT NULL) AND (PCPRODUT.CODEPTO = P.CODEPTO)
               )
             )--CLASSIFICACAO MERCADOLOGICA

            )
      AND   (NVL(P.CODCLI, 0) <> 0
             OR EXISTS (SELECT 1 FROM PCGRUPOFIDELIDADEDESCONTO D WHERE D.CODFIDELIDADE = P.CODFIDELIDADE )
             OR NVL(P.CODPROD, 0) <> 0
             OR EXISTS (SELECT 1
                      FROM PCBINCARTAO C,
                           PCBINCARTAOFIDELIDADE B
                     WHERE C.CODBINCARTAO  = B.CODBINCARTAO
                       AND B.CODFIDELIDADE = P.CODFIDELIDADE))
    ) PRODUTO,
      PCEMBALAGEM E,
      PCDEPARAPRODC5 DEPARA
WHERE DEPARA.CODPROD = PRODUTO.CODPROD
AND   DEPARA.CODPROD = E.CODPROD
AND   DEPARA.ATIVO = 'S'
AND   TO_CHAR(PRODUTO.CODFILIAL) = E.CODFILIAL
)

