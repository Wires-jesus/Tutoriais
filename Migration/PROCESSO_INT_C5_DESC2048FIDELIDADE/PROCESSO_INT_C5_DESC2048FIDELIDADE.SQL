CREATE OR REPLACE VIEW VW_INT_C5_DESC2048GRUPOPESSOA AS 
(
SELECT DISTINCT
       NVL((SELECT M.SEQREGRA
             FROM MONITORPDVMIDDLE.TB_REGRAINCENTIVO M
            WHERE M.IDREF = P.CODFILIAL || 2048 || P.CODFIDELIDADE
              AND ROWNUM = 1),
           0) SEQREGRA,
       D.CODGRUPOFIDELIDADE SEQGRUPOPESSOA,
       (CASE
         WHEN (P.DTFINAL < TRUNC(SYSDATE) OR P.DTEXCLUSAO IS NOT NULL) THEN
          'N'
         ELSE
          'S'
       END) ATIVO,
       P.CODFILIAL || 2048 || P.CODFIDELIDADE IDREF
  FROM PCGRUPOFIDELIDADEDESCONTO          D,
       PCDESCONTOFIDELIDADE               P,
       VW_INT_C5_OBTER_FILIAIS_C5         F,
       MONITORPDVMIDDLE.TB_REGRAINCENTIVO R
 WHERE D.CODFIDELIDADE = P.CODFIDELIDADE
   AND F.CODFILIAL = P.CODFILIAL
   AND P.DTINICIAL IS NOT NULL
   AND P.DTFINAL IS NOT NULL
   AND R.IDREF = (P.CODFILIAL || 2048 || P.CODFIDELIDADE)
   AND TRUNC(SYSDATE) BETWEEN P.DTINICIAL AND P.DTFINAL
)

