CREATE OR REPLACE VIEW VW_INT_C5_BRINDE_ITEM_GRUPO AS 
(
  SELECT
    SEQCOMBO,
    ROW_NUMBER() OVER(PARTITION BY SEQCOMBO ORDER BY SEQCOMBO, MAX(SEQITEM)) SEQGRUPO,
    SUM(QTDEGRUPO) QTDE,
    CASE WHEN MAX(SEQITEM) = 1
    THEN
      'ITEM PROMOCIONAL'
    ELSE
      'ITENS GATILHO FAMILIA ' || CODPRODPRINC
    END GRUPO,
    MAX(ATIVO) ATIVO,
    CODPRODPRINC IDREF
  FROM VW_INT_C5_BRINDE_ITENS
  WHERE PRODUTOPRINC = 'S'
  AND   SEQUENCIA = 1
  GROUP BY SEQCOMBO, CODPRODPRINC
)

