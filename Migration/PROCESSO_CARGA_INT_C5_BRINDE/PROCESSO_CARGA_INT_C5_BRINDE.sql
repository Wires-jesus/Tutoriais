CREATE OR REPLACE VIEW VW_INT_C5_BRINDE_CABECALHO_AUT AS
(
  SELECT
    PCPROMC.CODIGO SEQPROMSURPRESA,
    PCPROMC.CODFILIAL NROEMPRESA,
    PCPROMC.DESCRICAO,
    TO_DATE(PCPROMC.DTINICIO || ' 00:00:01','DD-MM-YY HH24:MI:SS','NLS_DATE_LANGUAGE = AMERICAN') DTAHORINICIO,
    TO_DATE(PCPROMC.DTFIM || ' 23:59:59','DD-MM-YY HH24:MI:SS','NLS_DATE_LANGUAGE = AMERICAN') DTAHORFIM,
    'C' TIPOSURPRESA,
    (
     CASE
       WHEN TRUNC(SYSDATE) BETWEEN DTINICIO AND DTFIM
          AND DTINATIVACAO IS NULL THEN 'S'
       ELSE 'N'
     END
    ) ATIVO
  FROM
    PCPROMC

  INNER JOIN PCDEPARAEMBALAGENSC5
  ON (PCDEPARAEMBALAGENSC5.CODAUXILIAR = PCPROMC.CODAUXILIAR)

  INNER JOIN PCPRODUT
  ON (PCPRODUT.CODPROD = PCPROMC.CODPROD),
  (
    SELECT min(s.ultimaexecucao) ultimaexecucao
    FROM pccontroleconsinco s
    WHERE upper(s.objetoreferencia) IN (
      'PKG_SINC_PDV_CONSINCO.CARREGA_TB_PROMSURPRESA',
      'PKG_SINC_PDV_CONSINCO.CARREGA_TB_PROMSURPRESAEMPRESA',
      'PKG_SINC_PDV_CONSINCO.CARREGA_TB_PROMSURPRESAPERIODO',
      'PKG_SINC_PDV_CONSINCO.CARREGA_TB_PROMSURPRESAITEM'
    )
  ) D, VW_INT_C5_OBTER_FILIAIS_C5 C5
  WHERE
    TRUNC(SYSDATE) BETWEEN DTINICIO AND DTFIM
    AND NVL(PCPRODUT.TIPOMERC, 'L') = 'L'
    AND NVL(PCPROMC.dtalterc5, D.ultimaexecucao) >= D.ultimaexecucao
    AND PCPROMC.TIPODESCONTOBRINDE = 'B'
    AND PCPROMC.CODFILIAL = C5.CODFILIAL
)


