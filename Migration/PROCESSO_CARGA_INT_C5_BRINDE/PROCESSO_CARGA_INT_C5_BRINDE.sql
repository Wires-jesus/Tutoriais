  ON (PCPROMI.CODAUXILIAR = PCDEPARAEMBALAGENSC5.CODAUXILIAR)
  
  INNER JOIN PCPRODUT PRODUTOITEM
  ON (PRODUTOITEM.CODPROD = PCPROMI.CODPROD)

  INNER JOIN PCEMBALAGEM EMBALAGEMITEM
  ON (EMBALAGEMITEM.CODAUXILIAR = PCPROMI.CODAUXILIAR AND EMBALAGEMITEM.CODFILIAL = PCPROMC.CODFILIAL)
  
