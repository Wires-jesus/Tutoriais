CREATE OR REPLACE VIEW VW_INT_C5_BRINDE_ITENS AS (
  SELECT 
    DENSE_RANK() OVER (PARTITION BY P.SEQCOMBO ORDER BY P.IDREF, P.TIPOITEM) SEQGRUPO,
    P.*
  FROM 
  (
    --ITENS GATILHO
    SELECT 
      PCPROMI.CODIGO SEQCOMBO,
      CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN PRODUTO_FAMILIA.CODPROD ELSE PCPROMI.CODPROD END SEQITEM,
      CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN PRODUTO_MIDDLE.SEQFAMILIA ELSE null END SEQFAMILIA, 
      CASE WHEN PCPROMC.PRODUTOPRINC = 'N' THEN DEPARA.SEQPRODUTO ELSE null END SEQPRODUTO,
      'N' TIPOITEM,
      (
        CASE
          WHEN TRUNC(SYSDATE) BETWEEN TRUNC(PCPROMC.DTINICIO) AND TRUNC(PCPROMC.DTFIM) AND PCPROMC.DTINATIVACAO IS NULL
          THEN
              'S'
          ELSE
              'N'
        END
      ) ATIVO,
      CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN 1 ELSE PCPROMI.QT END QTDE,
      CASE WHEN (PCPROMC.PRODUTOPRINC = 'S') AND (PRODUTO_FAMILIA.CODPROD = PCPROMI.CODPROD AND PCPROMI.CODAUXILIAR = EMBITEM.CODAUXILIAR) THEN PCPROMI.QT ELSE 0 END QTDEGRUPO,
      0 PRECO,
      0 PERCDESCONTO,
      PRODUTO_FAMILIA.CODPRODPRINC,
      PCPROMC.PRODUTOPRINC,
      EMBITEM.QTUNIT QTDEMBALAGEM,
      PCPROMI.CODAUXILIAR IDREF
    FROM 
      PCPROMI ,
      PCPROMC ,
      PCPRODUT PRODUTO_ITEM,
      PCPRODUT PRODUTO_FAMILIA,
      PCDEPARAPRODC5 DEPARA,
      PCEMBALAGEM EMBITEM,
      MONITORPDVMIDDLE.TB_PRODUTO PRODUTO_MIDDLE,
      (
          SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
          FROM PCCONTROLECONSINCO S
          WHERE UPPER(S.OBJETOREFERENCIA) IN (
            'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBO',
            'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOEMPRESA',
            'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOITEM',
            'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOGRUPO'
          )
        ) D, 
      VW_INT_C5_OBTER_FILIAIS_C5 FILIAL
    WHERE 
      PCPROMI.CODPROD = PRODUTO_ITEM.CODPROD
      
      AND PCPROMC.CODIGO = PCPROMI.CODIGO
      AND PCPROMC.CODFILIAL = FILIAL.CODFILIAL
      AND PCPROMC.TIPODESCONTOBRINDE = 'D'

      AND (
        (PCPROMC.PRODUTOPRINC = 'S'
          AND PRODUTO_FAMILIA.CODPRODPRINC = PRODUTO_ITEM.CODPRODPRINC
          AND (EMBITEM.CODAUXILIAR = DEPARA.CODAUXILIAR OR DEPARA.CODAUXILIAR = 0))
        OR 
        (PCPROMC.PRODUTOPRINC = 'N'
          AND PRODUTO_FAMILIA.CODPROD = PRODUTO_ITEM.CODPROD
          AND EMBITEM.CODAUXILIAR = PCPROMI.CODAUXILIAR
        )
      )
      AND DEPARA.CODPROD = PRODUTO_FAMILIA.CODPROD
      AND DEPARA.ATIVO = 'S'
      
        
      AND EMBITEM.CODPROD = DEPARA.CODPROD
      AND EMBITEM.CODFILIAL = PCPROMC.CODFILIAL
      AND PRODUTO_MIDDLE.SEQPRODUTO = DEPARA.SEQPRODUTO 
      AND TRUNC(SYSDATE) BETWEEN TRUNC(DTINICIO) AND TRUNC(DTFIM)
      AND (NVL(PCPROMI.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO OR NVL(PCPROMC.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO)

     UNION ALL

    -- ITEM REFERENTE AO BRINDE
      SELECT
        PCPROMC.CODIGO SEQCOMBO,
        1 SEQITEM, --PALIATIVO
        --PCDEPARAEMBALAGENSC5.SEQPRODUTO,
        NULL SEQFAMILIA,
        PCDEPARAPRODC5.SEQPRODUTO,/*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
        'P' TIPOITEM,

        (
        CASE
          WHEN TRUNC(SYSDATE) BETWEEN TRUNC(DTINICIO) AND TRUNC(DTFIM) AND DTINATIVACAO IS NULL
          THEN
              'S'
          ELSE
              'N'
        END
        ) ATIVO,
        CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN 1 ELSE PCPROMC.QTBRINDE END QTDE,
        CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN PCPROMC.QTBRINDE ELSE NULL END QTDEGRUPO,
        (
          CASE
            WHEN PCPROMC.DESCONTOPARCIAL = 100 THEN 0.01
            ELSE 0
          END
        ) PRECO,
        (
          CASE
            WHEN PCPROMC.DESCONTOPARCIAL = 100 THEN 99.99
            ELSE PCPROMC.DESCONTOPARCIAL
          END
        ) PERCDESCONTO,
        NULL CODPRODPRINC,
        PCPROMC.PRODUTOPRINC,
      EMBITEM.QTUNIT QTDEMBALAGEM,
        PCPROMC.CODAUXILIAR IDREF
      FROM
        PCPROMC,
        PCPRODUT,
        PCEMBALAGEM EMBITEM,
        --PCDEPARAEMBALAGENSC5,
        PCDEPARAPRODC5,
      (
        SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
        FROM PCCONTROLECONSINCO S
        WHERE UPPER(S.OBJETOREFERENCIA) IN (
          'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBO',
          'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOEMPRESA',
          'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOITEM',
          'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOGRUPO'
        )
      ) D, VW_INT_C5_OBTER_FILIAIS_C5 C5

      WHERE
        PCPRODUT.CODPROD = PCPROMC.CODPROD
      AND EMBITEM.CODPROD = PCPRODUT.CODPROD
        AND EMBITEM.CODFILIAL = C5.CODFILIAL
        AND PCPROMC.CODAUXILIAR = EMBITEM.CODAUXILIAR
        --AND PCPROMC.CODAUXILIAR = PCDEPARAEMBALAGENSC5.CODAUXILIAR
        AND ((PCPROMC.CODAUXILIAR = PCDEPARAPRODC5.CODAUXILIAR/*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
        AND PCPROMC.CODPROD = PCDEPARAPRODC5.CODPROD /*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
      AND PCDEPARAPRODC5.ATIVO = 'S')
      OR (PCDEPARAPRODC5.CODAUXILIAR = 0
        AND PCPROMC.CODPROD = PCDEPARAPRODC5.CODPROD 
      AND PCDEPARAPRODC5.ATIVO = 'S'))
    
      AND TRUNC(SYSDATE) BETWEEN TRUNC(DTINICIO) AND TRUNC(DTFIM)
      AND NVL(PCPRODUT.TIPOMERC, 'L') = 'L'
      AND NVL(PCPROMC.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO
      AND PCPROMC.TIPODESCONTOBRINDE = 'D'
      AND PCPROMC.CODFILIAL = C5.CODFILIAL
  ) P 
)

