    TRUNC(SYSDATE) BETWEEN DTINICIO AND DTFIM
    AND NVL(PRODUTOCABECALHO.TIPOMERC, 'L') = 'L'
    AND NVL(PRODUTOITEM.TIPOMERC, 'L') = 'L'
    AND (NVL(PCPROMI.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO 
        OR NVL(PCPROMC.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO
        OR NVL(EMBALAGEMITEM.DTALTERC5, D.ultimaexecucao) >= D.ultimaexecucao)
    AND PCPROMC.TIPODESCONTOBRINDE = 'B'
    AND PCPROMC.CODFILIAL = C5.CODFILIAL

