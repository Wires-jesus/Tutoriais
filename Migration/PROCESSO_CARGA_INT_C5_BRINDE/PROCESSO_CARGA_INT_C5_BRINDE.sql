CREATE OR REPLACE VIEW VW_INT_C5_BRINDE_ITENS AS
(
--ITENS REFERENTES A CAMPANHA DE BRINDE
SELECT
  ROW_NUMBER() OVER(partition by COMBOITENS.SEQCOMBO,  COMBOITENS.SEQITEM  order BY COMBOITENS.EXISTEPCPROMI DESC) SEQUENCIA,
  COMBOITENS."SEQCOMBO",COMBOITENS."SEQITEM",COMBOITENS."IDREF",COMBOITENS."SEQPRODUTO",COMBOITENS."SEQFAMILIA",COMBOITENS."EXISTEPCPROMI",COMBOITENS."TIPOITEM",COMBOITENS."ATIVO",COMBOITENS."QTDE",COMBOITENS."QTDEGRUPO",COMBOITENS."PRECO",COMBOITENS."PERCDESCONTO",COMBOITENS."CODPRODPRINC",COMBOITENS."PRODUTOPRINC", COMBOITENS."QTDEMBALAGEM"

FROM
  (
  SELECT
    PCPROMC.CODIGO SEQCOMBO,
    CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN PRODUTOFAMILIA.CODPROD ELSE PCPROMI.CODPROD END SEQITEM,
    PCPROMI.CODAUXILIAR IDREF,
    --CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN NULL ELSE PCDEPARAEMBALAGENSC5.SEQPRODUTO END SEQPRODUTO,
    CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN NULL ELSE PCDEPARAPRODC5.SEQPRODUTO END SEQPRODUTO,/*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
    CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN PRODUTOFAMILIA.CODPROD ELSE NULL END SEQFAMILIA,
    CASE WHEN (PCPROMC.PRODUTOPRINC = 'S') AND (PRODUTOFAMILIA.CODPROD = PCPROMI.CODPROD) THEN 'S' ELSE 'N' END EXISTEPCPROMI,
    'N' TIPOITEM,
    (
     CASE
       WHEN TRUNC(SYSDATE) BETWEEN TRUNC(PCPROMC.DTINICIO) AND TRUNC(PCPROMC.DTFIM) AND PCPROMC.DTINATIVACAO IS NULL
       THEN
          'S'
       ELSE
          'N'
     END
    ) ATIVO,
    CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN 1 ELSE PCPROMI.QT END QTDE,
    CASE WHEN (PCPROMC.PRODUTOPRINC = 'S') AND (PRODUTOFAMILIA.CODPROD = PCPROMI.CODPROD) THEN PCPROMI.QT ELSE 0 END QTDEGRUPO,
    0 PRECO,
    0 PERCDESCONTO,
    PRODUTOFAMILIA.CODPRODPRINC,
    PCPROMC.PRODUTOPRINC,
	EMBITEM.QTUNIT QTDEMBALAGEM
  FROM
    PCPROMI,
    PCPRODUT PRODUTOITEM,
	  PCEMBALAGEM EMBITEM,
    --PCDEPARAEMBALAGENSC5,
    PCDEPARAPRODC5,
    PCPROMC,
    PCPRODUT PRODUTOFAMILIA,
    PCPRODUT PRODUTOCABECALHO,
    --PCDEPARAEMBALAGENSC5 PCDEPARAEMBALAGENSC5CABECALHO,
    PCDEPARAPRODC5 PCDEPARAEMBALAGENSC5CABECALHO,
    MONITORPDVMIDDLE.TB_FAMILIA FAM,
    MONITORPDVMIDDLE.TB_FAMILIA FAMCABECALHO,
    (
      SELECT min(s.ultimaexecucao) ultimaexecucao
      FROM pccontroleconsinco s
      WHERE upper(s.objetoreferencia) IN (
        'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBO',
        'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOEMPRESA',
        'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOITEM',
        'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOGRUPO'
      )
    ) D, 
    VW_INT_C5_OBTER_FILIAIS_C5 C5

  WHERE
    PRODUTOITEM.CODPROD = PCPROMI.CODPROD
    --AND PCPROMI.CODAUXILIAR = PCDEPARAEMBALAGENSC5.CODAUXILIAR
    
    AND PCPROMI.CODAUXILIAR = PCDEPARAPRODC5.CODAUXILIAR /*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
    AND PCPROMI.CODPROD = PCDEPARAPRODC5.CODPROD/*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
    
    AND PCPROMI.CODIGO = PCPROMC.CODIGO
	  AND PCPROMI.CODAUXILIAR = EMBITEM.CODAUXILIAR
    AND EMBITEM.CODPROD = PRODUTOITEM.CODPROD
    AND EMBITEM.CODFILIAL = C5.CODFILIAL
    AND PRODUTOITEM.CODPRODPRINC = PRODUTOFAMILIA.CODPRODPRINC(+)
    AND PRODUTOCABECALHO.CODPROD = PCPROMC.CODPROD
    AND PCDEPARAEMBALAGENSC5CABECALHO.CODAUXILIAR = PCPROMC.CODAUXILIAR
    AND PCDEPARAEMBALAGENSC5CABECALHO.CODPROD = PCPROMC.CODPROD /*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
    AND FAM.SEQFAMILIA = PRODUTOITEM.CODPROD 
    AND FAMCABECALHO.SEQFAMILIA = PRODUTOCABECALHO.CODPROD
    AND FAM.SEQFAMILIA = PCPROMI.CODPROD
    AND FAMCABECALHO.SEQFAMILIA = PCPROMC.CODPROD
    AND FAMCABECALHO.ATIVO = 'S'
    AND FAM.ATIVO = 'S'
    AND TRUNC(SYSDATE) BETWEEN TRUNC(DTINICIO) AND TRUNC(DTFIM)
    AND (NVL(PCPROMI.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO OR NVL(PCPROMC.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO)
    AND PCPROMC.TIPODESCONTOBRINDE = 'D'
    AND PCPROMC.CODFILIAL = C5.CODFILIAL
  )COMBOITENS

  UNION ALL

-- ITEM REFERENTE AO BRINDE
  SELECT
    1 SEQUENCIA,
    PCPROMC.CODIGO SEQCOMBO,
    1 SEQITEM, --PALIATIVO
    PCPROMC.CODAUXILIAR idref,
    --PCDEPARAEMBALAGENSC5.SEQPRODUTO,
    PCDEPARAPRODC5.SEQPRODUTO,/*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
    NULL SEQFAMILIA,
    'N' EXISTEPCPROMI,
    'P' TIPOITEM,

    (
     CASE
       WHEN TRUNC(SYSDATE) BETWEEN TRUNC(DTINICIO) AND TRUNC(DTFIM) AND DTINATIVACAO IS NULL
       THEN
          'S'
       ELSE
          'N'
     END
    ) ATIVO,
    CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN 1 ELSE PCPROMC.QTBRINDE END QTDE,
    CASE WHEN PCPROMC.PRODUTOPRINC = 'S' THEN PCPROMC.QTBRINDE ELSE NULL END QTDEGRUPO,
    (
      CASE
        WHEN PCPROMC.DESCONTOPARCIAL = 100 THEN 0.01
        ELSE 0
      END
    ) PRECO,
    (
      CASE
        WHEN PCPROMC.DESCONTOPARCIAL = 100 THEN 99.99
        ELSE PCPROMC.DESCONTOPARCIAL
      END
    ) PERCDESCONTO,
    NULL CODPRODPRINC,
    PCPROMC.PRODUTOPRINC,
	EMBITEM.QTUNIT QTDEMBALAGEM
  FROM
    PCPROMC,
    PCPRODUT,
	  PCEMBALAGEM EMBITEM,
    --PCDEPARAEMBALAGENSC5,
    PCDEPARAPRODC5,
  (
    SELECT min(s.ultimaexecucao) ultimaexecucao
    FROM pccontroleconsinco s
    WHERE upper(s.objetoreferencia) IN (
      'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBO',
      'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOEMPRESA',
      'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOITEM',
      'PKG_SINC_PDV_CONSINCO.CARREGA_TB_COMBOGRUPO'
    )
  ) D, VW_INT_C5_OBTER_FILIAIS_C5 C5

  WHERE
     PCPRODUT.CODPROD = PCPROMC.CODPROD
	AND EMBITEM.CODPROD = PCPRODUT.CODPROD
    AND EMBITEM.CODFILIAL = C5.CODFILIAL
    AND PCPROMC.CODAUXILIAR = EMBITEM.CODAUXILIAR
    --AND PCPROMC.CODAUXILIAR = PCDEPARAEMBALAGENSC5.CODAUXILIAR
    AND PCPROMC.CODAUXILIAR = PCDEPARAPRODC5.CODAUXILIAR/*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
    AND PCPROMC.CODPROD = PCDEPARAPRODC5.CODPROD/*REFATORACAO SEQFAMILIA/SEQPRODUTO*/
    AND TRUNC(SYSDATE) BETWEEN TRUNC(DTINICIO) AND TRUNC(DTFIM)
    AND NVL(PCPRODUT.TIPOMERC, 'L') = 'L'
    AND NVL(PCPROMC.dtalterc5, D.ultimaexecucao) >= D.ultimaexecucao
    AND PCPROMC.TIPODESCONTOBRINDE = 'D'
    AND PCPROMC.CODFILIAL = C5.CODFILIAL
)

