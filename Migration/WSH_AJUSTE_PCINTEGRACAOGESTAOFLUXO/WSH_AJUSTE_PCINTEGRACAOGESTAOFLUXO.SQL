DECLARE
  VSCRIPT               VARCHAR2(4000);
  EXISTE_SEQUENCE       INTEGER := 0;
  EXISTE_GESTAOFLUXO    INTEGER := 0;
  EXISTE_GESTAORECURSO  INTEGER := 0;
  EXISTE_GESTAODETALHES INTEGER := 0;
  EXISTE_GESTAOTEMPO    INTEGER := 0;
BEGIN

  BEGIN
    SELECT 1
      INTO EXISTE_GESTAOFLUXO
      FROM USER_TABLES T
     WHERE T.TABLE_NAME = 'PCINTEGRACAOGESTAOFLUXO';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      EXISTE_GESTAOFLUXO := 0;
  END;

  BEGIN
    SELECT 1
      INTO EXISTE_GESTAORECURSO
      FROM USER_TABLES T
     WHERE T.TABLE_NAME = 'PCINTEGRACAOGESTAORECURSO';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      EXISTE_GESTAORECURSO := 0;
  END;

  BEGIN
    SELECT 1
      INTO EXISTE_GESTAODETALHES
      FROM USER_TABLES T
     WHERE T.TABLE_NAME = 'PCINTEGRACAOGESTAODETALHES';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      EXISTE_GESTAODETALHES := 0;
  END;

   BEGIN
    SELECT 1
      INTO EXISTE_GESTAOTEMPO
      FROM USER_TABLES T
     WHERE T.TABLE_NAME = 'PCINTEGRACAOGESTAOTEMPO';
  EXCEPTION
    WHEN NO_DATA_FOUND THEN
      EXISTE_GESTAOTEMPO := 0;
  END;

  IF EXISTE_GESTAOTEMPO > 0 THEN
    vscript := 'DELETE FROM PCINTEGRACAOGESTAOTEMPO';
    execute immediate vscript;
  END IF;

  IF EXISTE_GESTAODETALHES > 0 THEN
    vscript := 'DELETE FROM PCINTEGRACAOGESTAODETALHES';
    execute immediate vscript;
  END IF;

  IF EXISTE_GESTAORECURSO > 0 THEN
    vscript := 'DELETE FROM PCINTEGRACAOGESTAORECURSO';
    execute immediate vscript;
  END IF;

  IF EXISTE_GESTAOFLUXO > 0 THEN
    vscript := 'DELETE FROM PCINTEGRACAOGESTAOFLUXO';
    execute immediate vscript;
  END IF;

  SELECT 1
    INTO EXISTE_SEQUENCE
    FROM USER_SEQUENCES S
   WHERE S.sequence_name = 'DFSEQ_PCINTEGRACAOGESTAOFLUXO';

  IF EXISTE_SEQUENCE > 0 THEN
    vscript := 'DROP SEQUENCE DFSEQ_PCINTEGRACAOGESTAOFLUXO ';
    execute immediate vscript;
    VSCRIPT := 'CREATE SEQUENCE  DFSEQ_PCINTEGRACAOGESTAOFLUXO minvalue 1 maxvalue  9223372036854775807 start with 1 increment by 1 nocache cycle';
    execute immediate vscript;
  END IF;

END;