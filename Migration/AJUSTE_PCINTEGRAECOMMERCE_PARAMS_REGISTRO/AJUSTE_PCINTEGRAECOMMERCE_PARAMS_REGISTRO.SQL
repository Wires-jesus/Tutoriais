DECLARE

  V_EXISTE_CONFIG NUMBER;
  V_PROXIMO_ID    PCINTEGRAECOMMERCE_PARAMS.ID%TYPE;

BEGIN

  FOR PARAMETROS IN (SELECT P.CODIGO, P.CODLAYOUT, P.CODFILIAL
                       FROM PCINTEGRAECOMMERCE_PARAMS P
                      GROUP BY P.CODIGO, P.CODLAYOUT, P.CODFILIAL
                     ORDER BY P.CODIGO, P.CODLAYOUT, P.CODFILIAL) LOOP

    BEGIN

      SELECT COUNT(1)
        INTO V_EXISTE_CONFIG
        FROM PCINTEGRAECOMMERCE_PARAMS P
       WHERE P.NOMECAMPO = 'CONSIDERAESTOQUEPRODMASTER'
         AND P.CODIGO = PARAMETROS.CODIGO
         AND P.CODLAYOUT = PARAMETROS.CODLAYOUT
         AND NVL(P.CODFILIAL, ' ') = NVL(PARAMETROS.CODFILIAL, ' ');

    EXCEPTION

      WHEN OTHERS THEN
        V_EXISTE_CONFIG := 0;

    END;

    IF V_EXISTE_CONFIG = 0 THEN

      V_PROXIMO_ID := DFSEQ_PCINTEGRAECOMMERCE_PARAM.NEXTVAL;

      EXECUTE IMMEDIATE
        'INSERT INTO PCINTEGRAECOMMERCE_PARAMS (
          ID,
          CODIGO,
          CODLAYOUT,
          CODFILIAL,
          TIPO,
          NOMECAMPO,
          VALOR)
        VALUES (:1, :2, :3, :4, :5, :6, :7)'
      USING
        V_PROXIMO_ID,
        PARAMETROS.CODIGO,
        PARAMETROS.CODLAYOUT,
        PARAMETROS.CODFILIAL,
        'CONFIGURACOES',
        'CONSIDERAESTOQUEPRODMASTER',
        'N';

    END IF;

  END LOOP;

END;
