      'S'
    ELSE
      'N'
    END ATIVO,
    N.CODIGO_TRIBUTACAO IDREF
  FROM VW_INT_C5_CCTCENARIOCONDICAO CC,
       PCDEPARATRIBUTACAOC5 DPTRIB,
       PCTRIBUTACAO_FILTRO_PRODUTO N,
       PCDEPARAPRODC5 DEPARA
  WHERE CC.SEQCENARIO = DPTRIB.SEQCENARIO
    AND DPTRIB.CODIGO_TRIBUTACAO = N.CODIGO_TRIBUTACAO
    AND DEPARA.CODPROD = N.CODPROD
    AND DEPARA.ATIVO = 'S'
    AND CC.SEQCONDICAO = 1

  UNION ALL

  SELECT
    NVL((SELECT SEQCENARIOCONDICAOITEM
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
        WHERE SEQCENARIOCONDICAO = CC.SEQCENARIOCONDICAO
        AND ROWNUM = 1), 0) SEQCENARIOCONDICAOITEM,
    CC.SEQCENARIOCONDICAO,
    TO_CHAR(N.NCM) VALOR,
    'P' INDTIPOIDENTIDADE,
    'NCM' IDENTIFICADOR,
    CC.SEQCENARIO,
    CASE WHEN N.DTINATIVACAO IS NULL
    THEN
      'S'
    ELSE
      'N'
    END ATIVO,
    N.CODIGO_TRIBUTACAO IDREF
  FROM VW_INT_C5_CCTCENARIOCONDICAO CC,
       PCDEPARATRIBUTACAOC5 DPTRIB,
       PCTRIBUTACAO_FILTRO_NCM N
  WHERE CC.SEQCENARIO = DPTRIB.SEQCENARIO
    AND DPTRIB.CODIGO_TRIBUTACAO = N.CODIGO_TRIBUTACAO
    AND CC.SEQCONDICAO = 3

  UNION ALL

  SELECT
    NVL((SELECT SEQCENARIOCONDICAOITEM
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
        WHERE SEQCENARIOCONDICAO = CC.SEQCENARIOCONDICAO
        AND ROWNUM = 1), 0) SEQCENARIOCONDICAOITEM,
    CC.SEQCENARIOCONDICAO,
    CASE WHEN T.TIPO_LOCAL_CONSUMO = 'G' 
      THEN TO_CHAR(NVL(T.LOCAL_CONSUMO_GERAL, 'BR'))
      ELSE TO_CHAR(T.LOCAL_CONSUMO_MUNICIPIO)
    END VALOR,
    'P' INDTIPOIDENTIDADE,
    'UFORIGEM' IDENTIFICADOR,
    CC.SEQCENARIO,
    CC.ATIVO,
    T.CODIGO_TRIBUTACAO IDREF
  FROM VW_INT_C5_CCTCENARIOCONDICAO CC,
       PCDEPARATRIBUTACAOC5 DPTRIB,
       PCTRIBUTACAO T
  WHERE CC.SEQCENARIO = DPTRIB.SEQCENARIO
    AND DPTRIB.CODIGO_TRIBUTACAO = T.CODIGO_TRIBUTACAO
    AND CC.SEQCONDICAO = 8
