CREATE OR REPLACE VIEW VW_INT_C5_CCTCENARIOCONDICAO AS
(
  SELECT 
    MIN(NVL((SELECT SEQCENARIOCONDICAO
          FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAO
      WHERE SEQCENARIO = DEPARA.SEQCENARIO
      AND SEQCONDICAO = CN.SEQCONDICAO
      AND ROWNUM = 1), 0)) SEQCENARIOCONDICAO,
    DEPARA.SEQCENARIO,
    CN.SEQCONDICAO,
    MIN(CN.PESOBUSCA) PONTOSBUSCA,
    CASE WHEN MIN(TP.DTINATIVACAO) IS NULL
    THEN
      'S'
    ELSE
      'N'
    END ATIVO
  FROM PCDEPARATRIBUTACAOC5 DEPARA,
       PCTRIBUTACAO_FILTRO_PRODUTO TP,
       VW_INT_C5_CCTCONDICAO CN
  WHERE DEPARA.CODIGO_TRIBUTACAO = TP.CODIGO_TRIBUTACAO
    AND CN.SEQCONDICAO = 1
  GROUP BY DEPARA.SEQCENARIO, CN.SEQCONDICAO    
  
  UNION ALL
  
  SELECT
    MIN(NVL((SELECT SEQCENARIOCONDICAO
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAO
        WHERE SEQCENARIO = DEPARA.SEQCENARIO
        AND SEQCONDICAO = CN.SEQCONDICAO
        AND ROWNUM = 1), 0)) SEQCENARIOCONDICAO,
    DEPARA.SEQCENARIO,
    CN.SEQCONDICAO,
    MIN(CN.PESOBUSCA) PONTOSBUSCA,   
    CASE WHEN MIN(N.DTINATIVACAO) IS NULL
    THEN
      'S'
    ELSE
      'N'
    END ATIVO
  FROM PCDEPARATRIBUTACAOC5 DEPARA,
       PCTRIBUTACAO_FILTRO_NCM N,
       VW_INT_C5_CCTCONDICAO CN
  WHERE DEPARA.CODIGO_TRIBUTACAO = N.CODIGO_TRIBUTACAO
    AND CN.SEQCONDICAO = 3
  GROUP BY DEPARA.SEQCENARIO, CN.SEQCONDICAO  

  UNION ALL
  
  SELECT 
    NVL((SELECT SEQCENARIOCONDICAO
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAO
        WHERE SEQCENARIO = DEPARA.SEQCENARIO
        AND SEQCONDICAO = CN.SEQCONDICAO
        AND ROWNUM = 1), 0) SEQCENARIOCONDICAO,
    DEPARA.SEQCENARIO,
    CN.SEQCONDICAO,
    CN.PESOBUSCA PONTOSBUSCA,
    DEPARA.ATIVO
  FROM PCDEPARATRIBUTACAOC5 DEPARA,
       PCTRIBUTACAO T,
       VW_INT_C5_CCTCONDICAO CN
  WHERE 1 = 1
    AND DEPARA.CODIGO_TRIBUTACAO = T.CODIGO_TRIBUTACAO
    AND CN.SEQCONDICAO = 8  
)

