CREATE OR REPLACE VIEW VW_INT_C5_CCTCODIGOTRIBUTARIO AS 
(
  SELECT
    MIN(NVL((SELECT SEQCODIGOTRIBUTARIO
            FROM MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO
        WHERE CODIGO = CODTRIB.CODIGO
        AND ROWNUM = 1), 0)) SEQCODIGOTRIBUTARIO,
    CODTRIB.CODIGO,
    CODTRIB.DESCRICAO,
    CODTRIB.INDTIPOCODIGO,
    CODTRIB.DTAINICIALVALIDADE,
    CODTRIB.DTAFINALVALIDADE ,
    CODTRIB.ATIVO,
    CODTRIB.IDREF
  FROM (
    SELECT
      T.CST AS CODIGO,
      'CST ' || T.CST AS DESCRICAO,
      1 AS INDTIPOCODIGO,
      NVL(T.DTINICIO_VIGENCIA, SYSDATE) AS DTAINICIALVALIDADE,
      NVL(T.DTFIM_VIGENCIA, SYSDATE + 1) AS DTAFINALVALIDADE,
      CASE 
        WHEN T.DTINATIVACAO IS NULL THEN 'S' ELSE 'N'
      END AS ATIVO,
      T.CODIGO_TRIBUTACAO IDREF
    FROM PCTRIBUTACAO T
    
    UNION ALL
    
    SELECT
      T.CCLASSTRIB AS CODIGO,
      'CCLASSTRIB ' || T.CCLASSTRIB AS DESCRICAO,
      2 AS INDTIPOCODIGO,
      NVL(T.DTINICIO_VIGENCIA, SYSDATE) AS DTAINICIALVALIDADE,
      NVL(T.DTFIM_VIGENCIA, SYSDATE + 1) AS DTAFINALVALIDADE,
      CASE 
        WHEN T.DTINATIVACAO IS NULL THEN 'S' ELSE 'N'
      END AS ATIVO,
      T.CODIGO_TRIBUTACAO IDREF
    FROM PCTRIBUTACAO T
  ) CODTRIB
  GROUP BY CODTRIB.CODIGO, CODTRIB.DESCRICAO, CODTRIB.INDTIPOCODIGO, CODTRIB.DTAINICIALVALIDADE, CODTRIB.DTAFINALVALIDADE, CODTRIB.ATIVO, CODTRIB.IDREF
)

