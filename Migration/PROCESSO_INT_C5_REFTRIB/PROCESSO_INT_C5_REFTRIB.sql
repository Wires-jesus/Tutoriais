CREATE OR REPLACE VIEW VW_INT_C5_CCTCENCONDITEM AS 
(
  SELECT
    NVL((SELECT SEQCENARIOCONDICAOITEM
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
        WHERE SEQCENARIOCONDICAO = CC.SEQCENARIOCONDICAO
        AND ROWNUM = 1), 0) SEQCENARIOCONDICAOITEM,
    CC.SEQCENARIOCONDICAO,
    TO_CHAR(DEPARA.SEQPRODUTO) VALOR,
    'P' INDTIPOIDENTIDADE,
    'PRODUTO' IDENTIFICADOR,
    CC.SEQCENARIO,
    CASE WHEN N.DTINATIVACAO IS NULL
    THEN
      'S'
    ELSE
      'N'
    END ATIVO
  FROM VW_INT_C5_CCTCENARIOCONDICAO CC,
      PCTRIBUTACAO_FILTRO_PRODUTO N,
      PCDEPARAPRODC5 DEPARA
  WHERE CC.SEQCENARIO = N.CODIGO_TRIBUTACAO
    AND DEPARA.CODPROD = N.CODPROD
    AND DEPARA.ATIVO = 'S'
    AND CC.SEQCONDICAO = 1
      
  UNION ALL 

  SELECT
    NVL((SELECT SEQCENARIOCONDICAOITEM
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
        WHERE SEQCENARIOCONDICAO = CC.SEQCENARIOCONDICAO
        AND ROWNUM = 1), 0) SEQCENARIOCONDICAOITEM,
    CC.SEQCENARIOCONDICAO,
    TO_CHAR(N.NCM) VALOR,
    'P' INDTIPOIDENTIDADE,
    'NCM' IDENTIFICADOR,
    CC.SEQCENARIO,
        CASE WHEN N.DTINATIVACAO IS NULL
    THEN
      'S'
    ELSE
      'N'
    END ATIVO
  FROM VW_INT_C5_CCTCENARIOCONDICAO CC,
      PCTRIBUTACAO_FILTRO_NCM N
  WHERE CC.SEQCENARIO = N.CODIGO_TRIBUTACAO
    AND CC.SEQCONDICAO = 3
      
  UNION ALL

  SELECT
    NVL((SELECT SEQCENARIOCONDICAOITEM
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOCONDICAOITEM
        WHERE SEQCENARIOCONDICAO = CC.SEQCENARIOCONDICAO
        AND ROWNUM = 1), 0) SEQCENARIOCONDICAOITEM,
    CC.SEQCENARIOCONDICAO,
    TO_CHAR(T.LOCAL_CONSUMO) VALOR,
    'P' INDTIPOIDENTIDADE,
    'UFORIGEM' IDENTIFICADOR,
    CC.SEQCENARIO,
    CC.ATIVO
  FROM VW_INT_C5_CCTCENARIOCONDICAO CC,
      PCTRIBUTACAO T
  WHERE CC.SEQCENARIO = T.CODIGO_TRIBUTACAO
    AND CC.SEQCONDICAO = 8
)

