CREATE OR REPLACE VIEW VW_INT_C5_CCTCENARIOIMPOSTOMUN AS
(
 SELECT 
    NVL((SELECT SEQCENARIOIMPOSTOMUNICIPIO
      FROM MONITORPDVMIDDLE.TB_CCTCENARIOIMPOSTOMUN
      WHERE SEQCENARIOIMPOSTO = DEPARA.SEQCENARIO
        AND CODIBGE = TRIB.CODIBGE
        AND ROWNUM = 1), 0) SEQCENARIOIMPOSTOMUNICIPIO,
    (SELECT CCTC.SEQCENARIOIMPOSTO FROM MONITORPDVMIDDLE.TB_CCTCENARIOIMPOSTO CCTC WHERE CCTC.SEQCENARIO = DEPARA.SEQCENARIO) SEQCENARIOIMPOSTO,
    TRIB.CODIBGE CODIBGE,
    TRIB.NOMECIDADE CIDADE,
    TRIB.UF UF,
    CASE 
      WHEN DEPARA.TIPO_IMPOSTO = 'CBS' THEN TRIB.PERC_CBS
      WHEN DEPARA.TIPO_IMPOSTO = 'IBS' 
      THEN
        CASE WHEN (TRIB.TIPO_LOCAL_CONSUMO = 'G')
          THEN TRIB.PERC_IBS_UF
          ELSE TRIB.PERC_IBS_MUN				
        END
      ELSE TRIB.PERC_IS
    END PERALIQ,
    CASE 
      WHEN DEPARA.TIPO_IMPOSTO = 'CBS' THEN TRIB.PERC_RED_CBS
      WHEN DEPARA.TIPO_IMPOSTO = 'IBS' 
      THEN
        CASE WHEN (TRIB.TIPO_LOCAL_CONSUMO = 'G')
          THEN TRIB.PERC_RED_IBS_UF
          ELSE TRIB.PERC_RED_IBS_MUN				
        END
      ELSE 0
    END PERALIQRED,	
    '1' AS SEQFORMULA,
    TRIB.ATIVO,
    TRIB.CODIGO_TRIBUTACAO IDREF
  FROM (
    SELECT
      T.CODIGO_TRIBUTACAO,
      C.CODIBGE,
      C.NOMECIDADE,
      C.UF,
      T.TIPO_LOCAL_CONSUMO,
      T.PERC_CBS,
      T.PERC_RED_CBS,
      T.PERC_IBS_UF,
      T.PERC_RED_IBS_UF,
      T.PERC_IBS_MUN,
      T.PERC_RED_IBS_MUN,
      T.PERC_IS,
      CASE WHEN T.DTINATIVACAO IS NULL
        THEN 'S'
        ELSE 'N'
      END AS ATIVO
    FROM PCTRIBUTACAO T
    JOIN PCCIDADE C
      ON 1 = 1
    WHERE
      T.TIPO_LOCAL_CONSUMO = 'G'
      AND T.LOCAL_CONSUMO_GERAL = 'BR'
    
    UNION ALL
    
    SELECT
      T.CODIGO_TRIBUTACAO,
      C.CODIBGE,
      C.NOMECIDADE,
      C.UF,
      T.TIPO_LOCAL_CONSUMO,
      T.PERC_CBS,
      T.PERC_RED_CBS,
      T.PERC_IBS_UF,
      T.PERC_RED_IBS_UF,
      T.PERC_IBS_MUN,
      T.PERC_RED_IBS_MUN,
      T.PERC_IS,
      CASE WHEN T.DTINATIVACAO IS NULL
        THEN 'S'
        ELSE 'N'
      END AS ATIVO
    FROM PCTRIBUTACAO T
    JOIN PCCIDADE C
      ON C.UF = T.LOCAL_CONSUMO_GERAL
    WHERE
      T.TIPO_LOCAL_CONSUMO = 'G'
      AND T.LOCAL_CONSUMO_GERAL <> 'BR'
    
    UNION ALL
    
    SELECT
      T.CODIGO_TRIBUTACAO,
      C.CODIBGE,
      C.NOMECIDADE,
      C.UF,
      T.TIPO_LOCAL_CONSUMO,
      T.PERC_CBS,
      T.PERC_RED_CBS,
      T.PERC_IBS_UF,
      T.PERC_RED_IBS_UF,
      T.PERC_IBS_MUN,
      T.PERC_RED_IBS_MUN,
      T.PERC_IS,
      CASE WHEN T.DTINATIVACAO IS NULL
        THEN 'S'
        ELSE 'N'
      END AS ATIVO
    FROM PCTRIBUTACAO T
    JOIN PCCIDADE C
      ON C.CODIBGE = T.LOCAL_CONSUMO_MUNICIPIO
    WHERE
      T.TIPO_LOCAL_CONSUMO = 'M'
  ) TRIB,
  PCDEPARATRIBUTACAOC5 DEPARA
  WHERE DEPARA.CODIGO_TRIBUTACAO  = TRIB.CODIGO_TRIBUTACAO
)