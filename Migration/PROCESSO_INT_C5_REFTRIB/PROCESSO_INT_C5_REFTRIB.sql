CREATE OR REPLACE VIEW VW_INT_C5_CCTCENARIOIMPOSTO AS 
(
  SELECT
    NVL((SELECT SEQCENARIOIMPOSTO
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOIMPOSTO
        WHERE SEQIMPOSTO = CCTI.SEQIMPOSTO
        AND ROWNUM = 1), 0) SEQCENARIOIMPOSTO,
    DPTRIB.SEQCENARIO,
    CCTI.SEQIMPOSTO,
    (SELECT CCTC.SEQCODIGOTRIBUTARIO FROM MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO CCTC WHERE TO_CHAR(T.CODIGO_TRIBUTACAO) = CCTC.IDREF AND CCTC.INDTIPOCODIGO = 1) SEQCODTRIBCST,
    (SELECT CCTC.SEQCODIGOTRIBUTARIO FROM MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO CCTC WHERE TO_CHAR(T.CODIGO_TRIBUTACAO) = CCTC.IDREF AND CCTC.INDTIPOCODIGO = 2) SEQCODTRIBCCLASTRIB,
    CASE
      WHEN DPTRIB.TIPO_IMPOSTO = 'IS' THEN
           T.PERC_IS
      WHEN DPTRIB.TIPO_IMPOSTO = 'CBS' THEN
           T.PERC_CBS
      WHEN T.PERC_IBS_MUN IS NOT NULL THEN
           T.PERC_IBS_MUN     
      WHEN  T.PERC_IBS_UF IS NOT NULL THEN
           T.PERC_IBS_UF     
      ELSE NULL
    END PERALIQ,
    
    CASE
      WHEN DPTRIB.TIPO_IMPOSTO = 'CBS' THEN
           T.PERC_RED_CBS
      WHEN T.PERC_IBS_MUN IS NOT NULL THEN
           T.PERC_RED_IBS_MUN     
      WHEN  T.PERC_IBS_UF IS NOT NULL THEN
           T.PERC_RED_IBS_UF     
      ELSE NULL
    END PERALIQRED,
    
    1 SEQFORMULA,
    DPTRIB.ATIVO,
    T.CODIGO_TRIBUTACAO||'-'||CCTI.DESCRICAO IDREF
  FROM PCDEPARATRIBUTACAOC5 DPTRIB,
       PCTRIBUTACAO T,
       VW_INT_C5_CCTIMPOSTO CCTI       
  WHERE DPTRIB.CODIGO_TRIBUTACAO = T.CODIGO_TRIBUTACAO
  AND   DPTRIB.TIPO_IMPOSTO = CCTI.DESCRICAO
)

