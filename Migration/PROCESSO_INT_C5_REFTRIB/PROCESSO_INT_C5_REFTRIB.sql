CREATE OR REPLACE VIEW VW_INT_C5_CCTCONFIGURACAO AS
(
  SELECT 
    1 AS SEQCONFIGURACAO, 
    NVL((SELECT 
            CASE 
              WHEN VALOR = 'S' THEN 'P'
              ELSE 'H'
            END
         FROM PCPARAMFILIAL WHERE NOME = 'USAREFORMAPDVSUPERMERCADO'), 'H') AS INDTIPOAMBIENTE, 
    'S' AS ATIVO 
  FROM PCCONSUM
)

\

CREATE OR REPLACE VIEW VW_INT_C5_CCTALIQUOTA AS 
(
  SELECT 
    (SELECT IMP.SEQIMPOSTO FROM MONITORPDVMIDDLE.TB_CCTIMPOSTO IMP WHERE IMP.DESCRICAO = DEPARA.TIPO_IMPOSTO) AS SEQIMPOSTO,
    NVL(TRIB.DTINICIO_VIGENCIA, TRUNC(SYSDATE)) AS DTAINICIALVALIDADE,
    NVL(TRIB.DTFIM_VIGENCIA, TRUNC(SYSDATE))  AS DTAFINALVALIDADE,
    CASE
      WHEN DEPARA.TIPO_IMPOSTO = 'CBS' THEN TRIB.PERC_CBS
      WHEN DEPARA.TIPO_IMPOSTO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'M' THEN TRIB.PERC_IBS_MUN
      WHEN DEPARA.TIPO_IMPOSTO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'G' THEN TRIB.PERC_IBS_UF
      WHEN DEPARA.TIPO_IMPOSTO = 'IS' THEN TRIB.PERC_IS
      ELSE 0
    END AS PERALIQ,
    CASE
      WHEN DEPARA.TIPO_IMPOSTO IN ('CBS', 'IS') THEN ''
      WHEN DEPARA.TIPO_IMPOSTO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'M' THEN (SELECT C.UF FROM PCCIDADE C WHERE C.CODIBGE = TRIB.LOCAL_CONSUMO_MUNICIPIO) 
      WHEN DEPARA.TIPO_IMPOSTO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'G' THEN TRIB.LOCAL_CONSUMO_GERAL
      ELSE NULL
    END AS UF,
    CASE
      WHEN DEPARA.TIPO_IMPOSTO = 'IBS' AND TRIB.TIPO_LOCAL_CONSUMO = 'M' THEN TRIB.LOCAL_CONSUMO_MUNICIPIO 
      ELSE NULL
    END AS CODIBGE,
    CASE
      WHEN (TRUNC(SYSDATE) >= NVL(TRIB.DTINICIO_VIGENCIA, TRUNC(SYSDATE))) AND (TRUNC(SYSDATE) <= NVL(TRIB.DTFIM_VIGENCIA, TRUNC(SYSDATE))) AND (DEPARA.ATIVO = 'S') THEN 'S'
      ELSE 'N'
    END AS ATIVO,
    DEPARA.CODIGO_TRIBUTACAO AS IDREF
  FROM PCDEPARATRIBUTACAOC5 DEPARA,
      PCTRIBUTACAO TRIB,
      (SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
          FROM PCCONTROLECONSINCO S
         WHERE ATIVO = 'A'
           AND (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_CCTALIQUOTA')
      ) D
  WHERE DEPARA.CODIGO_TRIBUTACAO = TRIB.CODIGO_TRIBUTACAO
    AND NVL(TRIB.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO
)