CREATE OR REPLACE VIEW VW_INT_C5_CCTCENARIO AS (
  SELECT 
    T.CODIGO_TRIBUTACAO SEQCENARIO,
    T.TIPO_IMPOSTO DESCRICAO,
    T.DESCRICAO_TRIBUTACAO ORIENTACAO,
    NVL(T.DTINICIO_VIGENCIA, SYSDATE) DTAINICIALVALIDADE,
    NVL(T.DTFIM_VIGENCIA, SYSDATE + 1) DTAFINALVALIDADE,
    0 TOTALPONTOS,
    CASE WHEN T.DTINATIVACAO IS NULL
    THEN
      'S'
    ELSE
      'N'
    END ATIVO,
    T.DTALTERC5
  FROM PCTRIBUTACAO T,
       (SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
          FROM PCCONTROLECONSINCO S
         WHERE ATIVO = 'A'
		       AND ((UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_CCTCENARIO') 
			      OR (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_CCTCENARIOCONDICAO'))
        ) D
  WHERE NVL(T.DTALTERC5, D.ULTIMAEXECUCAO) >= D.ULTIMAEXECUCAO
)

