CREATE OR REPLACE VIEW VW_INT_C5_DEPARATRIBUTACAO AS (
  SELECT 
    DEPARA.SEQCENARIO,
    TRIBUTACAO.CODIGO_TRIBUTACAO,
    TRIBUTACAO.TIPO_IMPOSTO_EXIBIR AS TIPO_IMPOSTO,
    CASE 
      WHEN TRIBUTACAO.DTINATIVACAO IS NULL THEN 'S' 
      ELSE 'N' 
    END AS ATIVO
  FROM (
    SELECT 
      CODIGO_TRIBUTACAO,
      'CBS' AS TIPO_IMPOSTO_EXIBIR,
      DTINATIVACAO
    FROM PCTRIBUTACAO
    WHERE TIPO_IMPOSTO = 'CBSIBS'

    UNION ALL

    SELECT 
      CODIGO_TRIBUTACAO,
      'IBS' AS TIPO_IMPOSTO_EXIBIR,
      DTINATIVACAO
    FROM PCTRIBUTACAO
    WHERE TIPO_IMPOSTO = 'CBSIBS'

    UNION ALL

    SELECT 
      CODIGO_TRIBUTACAO,
      'IS' AS TIPO_IMPOSTO_EXIBIR,
      DTINATIVACAO
    FROM PCTRIBUTACAO
    WHERE TIPO_IMPOSTO = 'IS') TRIBUTACAO,
    PCDEPARATRIBUTACAOC5 DEPARA
  WHERE TRIBUTACAO.CODIGO_TRIBUTACAO = DEPARA.CODIGO_TRIBUTACAO(+)
    AND TRIBUTACAO.TIPO_IMPOSTO_EXIBIR = DEPARA.TIPO_IMPOSTO(+)
)

