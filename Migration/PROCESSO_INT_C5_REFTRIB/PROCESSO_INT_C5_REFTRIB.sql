    NVL((SELECT SEQCENARIOIMPOSTO
        FROM MONITORPDVMIDDLE.TB_CCTCENARIOIMPOSTO
        WHERE SEQIMPOSTO = CCTI.SEQIMPOSTO
          AND SEQCENARIO = DPTRIB.SEQCENARIO        
        AND ROWNUM = 1), 0) SEQCENARIOIMPOSTO,
    DPTRIB.SEQCENARIO,
    CCTI.SEQIMPOSTO,
    (SELECT CCTC.SEQCODIGOTRIBUTARIO FROM MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO CCTC WHERE TO_CHAR(T.CODIGO_TRIBUTACAO) = CCTC.IDREF AND CCTC.INDTIPOCODIGO = 1 AND CCTC.CODIGO = T.CST) SEQCODTRIBCST,
    (SELECT CCTC.SEQCODIGOTRIBUTARIO FROM MONITORPDVMIDDLE.TB_CCTCODIGOTRIBUTARIO CCTC WHERE TO_CHAR(T.CODIGO_TRIBUTACAO) = CCTC.IDREF AND CCTC.INDTIPOCODIGO = 2 AND CCTC.CODIGO = T.CCLASSTRIB) SEQCODTRIBCCLASTRIB,
    CASE
      WHEN DPTRIB.TIPO_IMPOSTO = 'IS' THEN
           NVL(T.PERC_IS, 0)
      WHEN DPTRIB.TIPO_IMPOSTO = 'CBS' THEN
           NVL(T.PERC_CBS, 0)
      WHEN DPTRIB.TIPO_IMPOSTO = 'IBS' AND T.TIPO_LOCAL_CONSUMO = 'G' THEN
           NVL(T.PERC_IBS_UF, 0)
      WHEN DPTRIB.TIPO_IMPOSTO = 'IBS' AND T.TIPO_LOCAL_CONSUMO = 'M' THEN
           NVL(T.PERC_IBS_MUN, 0)     
      ELSE 0
    END PERALIQ,
    
    CASE
      WHEN DPTRIB.TIPO_IMPOSTO = 'CBS' THEN
           NVL(T.PERC_RED_CBS, 0)
      WHEN DPTRIB.TIPO_IMPOSTO = 'IBS' AND T.TIPO_LOCAL_CONSUMO = 'G' THEN
           NVL(T.PERC_RED_IBS_UF, 0)
      WHEN DPTRIB.TIPO_IMPOSTO = 'IBS' AND T.TIPO_LOCAL_CONSUMO = 'M' THEN
           NVL(T.PERC_RED_IBS_MUN, 0)     
      ELSE 0
    END PERALIQRED,
    
    1 SEQFORMULA,
    DPTRIB.ATIVO,
    T.CODIGO_TRIBUTACAO||'-'||CCTI.DESCRICAO IDREF
  FROM PCDEPARATRIBUTACAOC5 DPTRIB,
       PCTRIBUTACAO T,
       VW_INT_C5_CCTIMPOSTO CCTI       
  WHERE DPTRIB.CODIGO_TRIBUTACAO = T.CODIGO_TRIBUTACAO
  AND   DPTRIB.TIPO_IMPOSTO = CCTI.DESCRICAO
)

