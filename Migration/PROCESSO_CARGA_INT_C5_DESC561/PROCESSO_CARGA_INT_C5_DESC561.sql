CREATE OR REPLACE VIEW VW_INT_C5_DESC561FAMILIA AS
(SELECT 
     P.CODFILIAL||561||P.CODDESCONTO SEQREGRA,
     'DESCONTO POR PRODUTO' REGRA,
     3 AS SEQTIPOCREDITO,
     (CASE
        WHEN (L.CODDESCONTO IS NOT NULL) OR (P.DTFIM < TRUNC(SYSDATE)) THEN
             'N'
        ELSE 'S'
     END) ATIVO,
     
     'G' TIPOREGRA,
     'S' CUMULATIVO,
     P.DTINICIO DTAHORINICIO,
     P.DTFIM DTAHORFIM,
     P.CODPROD SEQFAMILIA,
     '1' QTDEMBALAGEM,
     P.PERCDESC PERCDESCONTO,
     0  PRECO,
     P.CODDESCONTO IDREF
FROM PCDESCONTO P,
     PCDESCONTOLOG L,
     monitorpdvmiddle.tb_familia f,
     (SELECT S.ULTIMAEXECUCAO
      FROM PCCONTROLECONSINCO S
      WHERE UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRAFAMILIA') DATAPADRAO
WHERE P.CODPROD = f.SEQFAMILIA
AND   P.CODDESCONTO = L.CODDESCONTO(+) 
AND   P.DTINICIO IS NOT NULL
AND   P.DTFIM IS NOT NULL
AND   P.ORIGEMPED IN('O', 'A') 
AND   P.TIPO = 'C'
AND   NVL(P.APLICADESCONTO, 'N') = 'S'
AND   NVL(P.CODFILIAL, '99') <> '99'
AND   REGEXP_LIKE(P.CODFILIAL, '^[[:digit:]]+$')
AND   TRUNC(SYSDATE) BETWEEN P.DTINICIO AND P.DTFIM
AND   ((P.CODPROD IS NOT NULL) OR (P.CODPROD IS NOT NULL AND P.CODCLI IS NOT NULL))  
AND   P.CODAUXILIAR IS NULL
AND   P.CODEPTO IS NULL
AND   P.CODSEC IS NULL
AND   P.CODCATEGORIA IS NULL
AND   P.SUBCATEGORIA IS NULL
AND   P.CODMARCA IS NULL
AND   P.CODFORNEC IS NULL
AND   NVL(P.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO) >= DATAPADRAO.ULTIMAEXECUCAO)

\

