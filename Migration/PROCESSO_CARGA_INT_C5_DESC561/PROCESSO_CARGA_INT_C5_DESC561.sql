CREATE OR REPLACE VIEW VW_INT_C5_DESC561PRODUTO AS
(
SELECT DISTINCT
     NVL((SELECT M.SEQREGRA
              FROM MONITORPDVMIDDLE.TB_REGRAINCENTIVO M
              WHERE M.IDREF = P.CODFILIAL||561||P.CODDESCONTO
              AND   ROWNUM = 1), 0) SEQREGRA,

     C5.CODFILIALINTEGRACAO NROEMPRESA,
     'DESCONTO POR PRODUTO' REGRA,
     3 AS SEQTIPOCREDITO,
     (CASE
        WHEN (L.CODDESCONTO IS NOT NULL) OR (P.DTFIM < TRUNC(SYSDATE)) THEN
             'N'
        ELSE 'S'
     END) ATIVO,

     'G' TIPOREGRA,
     'S' CUMULATIVO,
     P.DTINICIO DTAHORINICIO,
     P.DTFIM DTAHORFIM,
     --DEPARA.SEQFAMILIA,
     DEPARA.SEQPRODUTO,
     --'1' QTDEMBALAGEM,
     E.QTUNIT QTDEMBALAGEM,
     P.PERCDESC PERCDESCONTO,
     0  PRECO,
     P.CODFILIAL||561||P.CODDESCONTO IDREF
FROM PCDESCONTO P,
     PCDESCONTOLOG L,
     PCEMBALAGEM E,
     PCDEPARAPRODC5 DEPARA,
     (SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
      FROM PCCONTROLECONSINCO S
      WHERE (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRAPRODUTO')
         or (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRAEMPRESA')
         or (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRASEGMENTO')
     ) DATAPADRAO, VW_INT_C5_OBTER_FILIAIS_C5 C5
WHERE DEPARA.CODPROD = P.CODPROD
AND DEPARA.ATIVO = 'S'
AND   P.CODFILIAL = C5.CODFILIAL
AND   P.CODDESCONTO = L.CODDESCONTO(+)
AND   P.CODPROD = E.CODPROD
AND   DEPARA.CODPROD = E.CODPROD
AND   P.CODFILIAL = E.CODFILIAL
AND   P.DTINICIO IS NOT NULL
AND   P.DTFIM IS NOT NULL
AND   P.ORIGEMPED IN('O', 'A')
AND   P.TIPO = 'C'
AND   NVL(P.APLICADESCONTO, 'N') = 'S'
AND   NVL(P.CODFILIAL, '99') <> '99'
AND   TRUNC(SYSDATE) BETWEEN P.DTINICIO AND P.DTFIM
AND   ((/*POLITICA DE DESCONTO POR PRODUTO*/
        ((P.CODPROD IS NOT NULL) OR 
         (P.CODPROD IS NOT NULL AND P.CODCLI IS NOT NULL)
        ) AND (P.CODAUXILIAR IS NULL)

        OR
        /*POLITICA DE DESCONTO POR EMBALAGEM*/
        ((P.CODPROD IS NULL) AND (P.CODAUXILIAR IS NOT NULL))

       ) 
      )

AND   P.CODEPTO IS NULL
AND   P.CODSEC IS NULL
AND   P.CODCATEGORIA IS NULL
AND   P.SUBCATEGORIA IS NULL
AND   P.CODMARCA IS NULL
AND   P.CODFORNEC IS NULL
--AND   NVL(P.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO) >= DATAPADRAO.ULTIMAEXECUCAO)
AND   GREATEST(NVL(P.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO),
                     NVL(E.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO)) >= DATAPADRAO.ULTIMAEXECUCAO)

\

CREATE OR REPLACE VIEW VW_INT_C5_DESC561CLIENTE AS
(
SELECT
     --P.CODFILIAL||561||P.CODDESCONTO SEQREGRA,
     NVL((SELECT M.SEQREGRA
              FROM MONITORPDVMIDDLE.TB_REGRAINCENTIVO M
              WHERE M.IDREF = P.CODFILIAL||561||P.CODDESCONTO
              AND   ROWNUM = 1), 0) SEQREGRA,
     C5.CODFILIALINTEGRACAO NROEMPRESA,
     'DESCONTO POR CLIENTE' REGRA,
     3 AS SEQTIPOCREDITO,
     (CASE
        WHEN (L.CODDESCONTO IS NOT NULL) OR (P.DTFIM < TRUNC(SYSDATE)) THEN
             'N'
        ELSE 'S'
     END) ATIVO,

     'G' TIPOREGRA,
     'S' CUMULATIVO,
     P.DTINICIO DTAHORINICIO,
     P.DTFIM DTAHORFIM,
     P.CODCLI SEQPESSOA,
     P.PERCDESC PERCDESCONTO,
     0  PRECO,
     --P.CODDESCONTO IDREF
     P.CODFILIAL||561||P.CODDESCONTO IDREF
FROM PCDESCONTO P,
     PCDESCONTOLOG L,
     MONITORPDVMIDDLE.TB_CLIENTE CLI,
     (SELECT MIN(S.ULTIMAEXECUCAO) ULTIMAEXECUCAO
      FROM PCCONTROLECONSINCO S
      WHERE (UPPER(S.OBJETOREFERENCIA) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRACLIENTE')
         or (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRAEMPRESA')
         or (upper(s.objetoreferencia) = 'PKG_SINC_PDV_CONSINCO.CARREGA_TB_REGRASEGMENTO')
      ) DATAPADRAO, VW_INT_C5_OBTER_FILIAIS_C5 C5
WHERE CLI.SEQPESSOA = P.CODCLI
AND   P.CODDESCONTO = L.CODDESCONTO(+)
AND   P.DTINICIO IS NOT NULL
AND   P.CODFILIAL = C5.CODFILIAL
AND   P.DTFIM IS NOT NULL
AND   P.ORIGEMPED IN('O', 'A')
AND   P.TIPO = 'C'
AND   NVL(P.APLICADESCONTO, 'N') = 'S'
AND   NVL(P.CODFILIAL, '99') <> '99'
AND   TRUNC(SYSDATE) BETWEEN P.DTINICIO AND P.DTFIM
AND   P.CODCLI IS NOT NULL
AND   P.CODPROD IS NULL
AND   P.CODAUXILIAR IS NULL
AND   P.CODEPTO IS NULL
AND   P.CODSEC IS NULL
AND   P.CODCATEGORIA IS NULL
AND   P.SUBCATEGORIA IS NULL
AND   P.CODMARCA IS NULL
AND   P.CODFORNEC IS NULL
AND   NVL(P.DTALTERC5, DATAPADRAO.ULTIMAEXECUCAO) >= DATAPADRAO.ULTIMAEXECUCAO
)

